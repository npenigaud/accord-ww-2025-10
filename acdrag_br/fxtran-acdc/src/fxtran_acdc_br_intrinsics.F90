ELEMENTAL REAL (KIND=JPRM) FUNCTION FXTRAN_ACDC_BR_ATAN2_RM (PA, PB)
!$ACC ROUTINE SEQ

REAL (KIND=JPRM), INTENT(IN) :: PA, PB

REAL (KIND=JPRD) , PARAMETER :: PI = 3.1415926535897932384626433832795_JPRD

IF (PB > 0.0) THEN 
  FXTRAN_ACDC_BR_ATAN2_RM = FXTRAN_ACDC_BR_ATAN(PA/PB)
ELSE IF ((PB < 0.0_JPRM) .AND. (PA >= 0.0_JPRM)) THEN
  FXTRAN_ACDC_BR_ATAN2_RM = FXTRAN_ACDC_BR_ATAN(PA/PB) + PI
ELSE IF ((PB < 0.0_JPRM) .AND. (PA < 0.0_JPRM)) THEN
  FXTRAN_ACDC_BR_ATAN2_RM = FXTRAN_ACDC_BR_ATAN(PA/PB) - PI
ELSE IF ((PB == 0.0_JPRM) .AND. (PA > 0.0_JPRM)) THEN
  FXTRAN_ACDC_BR_ATAN2_RM = PI / 2.0_JPRM 
ELSE IF ((PB == 0.0_JPRM) .AND. (PA < 0.0_JPRM)) THEN
  FXTRAN_ACDC_BR_ATAN2_RM = 0.0_JPRM - (PI / 2.0_JPRM)
ELSE IF ((PB == 0.0_JPRM) .AND. (PA == 0.0_JPRM)) THEN
  FXTRAN_ACDC_BR_ATAN2_RM = 0.0_JPRM
END IF

END FUNCTION FXTRAN_ACDC_BR_ATAN2_RM

ELEMENTAL REAL (KIND=JPRM) FUNCTION FXTRAN_ACDC_BR_POWRR_RM(PVAL, PPOW)
!$ACC ROUTINE SEQ
REAL (KIND=JPRM), INTENT(IN) :: PVAL, PPOW
