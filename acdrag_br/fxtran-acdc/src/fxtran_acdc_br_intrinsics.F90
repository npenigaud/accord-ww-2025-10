  FXTRAN_ACDC_BR_ATAN2_RD = FXTRAN_ACDC_BR_ATAN(PA/PB) + PI
ELSE IF ((PB < 0.0_JPRD) .AND. (PA < 0.0_JPRD)) THEN
  FXTRAN_ACDC_BR_ATAN2_RD = FXTRAN_ACDC_BR_ATAN(PA/PB) - PI
ELSE IF ((PB == 0.0_JPRD) .AND. (PA > 0.0_JPRD)) THEN
  FXTRAN_ACDC_BR_ATAN2_RD = PI / 2.0_JPRD 
ELSE IF ((PB == 0.0_JPRD) .AND. (PA < 0.0_JPRD)) THEN
  FXTRAN_ACDC_BR_ATAN2_RD = 0.0_JPRD - (PI / 2.0_JPRD)
ELSE IF ((PB == 0.0_JPRD) .AND. (PA == 0.0_JPRD)) THEN
  FXTRAN_ACDC_BR_ATAN2_RD = 0.0_JPRD
END IF

