SUBROUTINE APL_ARPEGE_INIT_OUTLINE_002 (YDCPG_BNDS, YDCPG_OPTS, YDCST&
&, YDMF_PHYS_BASE_STATE, YDMODEL, PPOID, PPRODTH_CVPP, PIPOI)
USE CPG_OPTS_TYPE_MOD,ONLY:CPG_BNDS_TYPE, CPG_OPTS_TYPE
USE PARKIND1,ONLY:JPIM, JPRB
USE MF_PHYS_BASE_STATE_TYPE_MOD,ONLY:MF_PHYS_BASE_STATE_TYPE
USE TYPE_MODEL,ONLY:MODEL
USE YOMCST,ONLY:TCST
USE YOMHOOK,ONLY:LHOOK, JPHOOK, DR_HOOK

IMPLICIT NONE

TYPE (CPG_BNDS_TYPE), INTENT (IN)::YDCPG_BNDS
TYPE (CPG_OPTS_TYPE), INTENT (IN)::YDCPG_OPTS
TYPE (TCST), INTENT (IN)::YDCST
TYPE (MF_PHYS_BASE_STATE_TYPE), INTENT (IN)::YDMF_PHYS_BASE_STATE
TYPE (MODEL), INTENT (IN)::YDMODEL
REAL (KIND=JPRB), INTENT (OUT)::PPOID (YDCPG_OPTS%KLON, YDCPG_OPTS%KFLEVG)
REAL (KIND=JPRB), INTENT (OUT)::PPRODTH_CVPP (YDCPG_OPTS%KLON, 0:YDCPG_OPTS%KFLEVG)
REAL (KIND=JPRB), INTENT (INOUT)::PIPOI (YDCPG_OPTS%KLON, YDCPG_OPTS%KFLEVG)
INTEGER (KIND=JPIM)::JLEV
INTEGER (KIND=JPIM)::JLON
REAL (KIND=JPRB)::ZGDT
REAL (KIND=JPRB)::ZGDTI
REAL (KIND=JPHOOK)::ZHOOK_HANDLE
IF (LHOOK) CALL DR_HOOK ('APL_ARPEGE_INIT_OUTLINE_002', 0, ZHOOK_HANDLE)
PPRODTH_CVPP (:,:)=0.0_JPRB
ZGDT=YDCST%RG*YDMODEL%YRML_PHY_MF%YRPHY2%TSPHY
ZGDTI=1.0_JPRB/ZGDT

DO JLEV=YDCPG_OPTS%KTDIA, YDCPG_OPTS%KFLEVG

  DO JLON=YDCPG_BNDS%KIDIA, YDCPG_BNDS%KFDIA
    PPOID (JLON, JLEV)=YDMF_PHYS_BASE_STATE%YCPG_PHY%XYB%DELP (JLON, JLEV)*ZGDTI
    PIPOI (JLON, JLEV)=YDMF_PHYS_BASE_STATE%YCPG_PHY%XYB%RDELP (JLON, JLEV)*ZGDT
  ENDDO

ENDDO

IF (LHOOK) CALL DR_HOOK ('APL_ARPEGE_INIT_OUTLINE_002', 1, ZHOOK_HANDLE)
ENDSUBROUTINE
