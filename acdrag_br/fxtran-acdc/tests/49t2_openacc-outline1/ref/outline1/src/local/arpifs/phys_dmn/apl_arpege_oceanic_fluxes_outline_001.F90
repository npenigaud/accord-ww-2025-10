SUBROUTINE APL_ARPEGE_OCEANIC_FLUXES_OUTLINE_001 (YDCPG_BNDS&
&, YDCPG_OPTS, PCEROV, PCHROV, PFLU_CH, PCE)
USE CPG_OPTS_TYPE_MOD,ONLY:CPG_BNDS_TYPE, CPG_OPTS_TYPE
USE PARKIND1,ONLY:JPIM, JPRB
USE YOMHOOK,ONLY:LHOOK, JPHOOK, DR_HOOK

IMPLICIT NONE

TYPE (CPG_BNDS_TYPE), INTENT (IN)::YDCPG_BNDS
TYPE (CPG_OPTS_TYPE), INTENT (IN)::YDCPG_OPTS
REAL (KIND=JPRB), INTENT (INOUT)::PCEROV (YDCPG_OPTS%KLON)
REAL (KIND=JPRB), INTENT (INOUT)::PCHROV (YDCPG_OPTS%KLON)
REAL (KIND=JPRB), INTENT (INOUT)::PFLU_CH (YDCPG_OPTS%KLON)
REAL (KIND=JPRB), INTENT (INOUT)::PCE (YDCPG_OPTS%KLON)
INTEGER (KIND=JPIM)::JLON
REAL (KIND=JPHOOK)::ZHOOK_HANDLE
IF (LHOOK) CALL DR_HOOK ('APL_ARPEGE_OCEANIC_FLUXES_OUTLINE_001', 0, ZHOOK_HANDLE)

DO JLON=YDCPG_BNDS%KIDIA, YDCPG_BNDS%KFDIA
  PCE (JLON)=PFLU_CH (JLON)
  PCEROV (JLON)=PCHROV (JLON)
ENDDO

IF (LHOOK) CALL DR_HOOK ('APL_ARPEGE_OCEANIC_FLUXES_OUTLINE_001', 1, ZHOOK_HANDLE)
ENDSUBROUTINE
