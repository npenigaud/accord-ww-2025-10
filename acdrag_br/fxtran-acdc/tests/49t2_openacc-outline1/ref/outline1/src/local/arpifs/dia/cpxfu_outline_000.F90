SUBROUTINE CPXFU_OUTLINE_000 (YDCPG_BNDS, YDCPG_OPTS, YDXFU, LDGST, LDGST2, PXNUCLS&
&, PXNVCLS, PXUCLS, PXUGST, PXVCLS, PXVGST, PNUMEAN, PNVMEAN, PUMEAN, PVMEAN)
USE CPG_OPTS_TYPE_MOD,ONLY:CPG_BNDS_TYPE, CPG_OPTS_TYPE
USE PARKIND1,ONLY:JPIM, JPRB
USE YOMXFU,ONLY:TXFU
USE YOMHOOK,ONLY:LHOOK, JPHOOK, DR_HOOK

IMPLICIT NONE

TYPE (CPG_BNDS_TYPE), INTENT (IN)::YDCPG_BNDS
TYPE (CPG_OPTS_TYPE), INTENT (IN)::YDCPG_OPTS
TYPE (TXFU), INTENT (INOUT)::YDXFU
LOGICAL, INTENT (INOUT)::LDGST (YDCPG_OPTS%KLON)
LOGICAL, INTENT (INOUT)::LDGST2 (YDCPG_OPTS%KLON)
REAL (KIND=JPRB), INTENT (IN)::PXNUCLS (YDCPG_OPTS%KLON)
REAL (KIND=JPRB), INTENT (IN)::PXNVCLS (YDCPG_OPTS%KLON)
REAL (KIND=JPRB), INTENT (IN)::PXUCLS (YDCPG_OPTS%KLON)
REAL (KIND=JPRB), INTENT (IN)::PXUGST (YDCPG_OPTS%KLON)
REAL (KIND=JPRB), INTENT (IN)::PXVCLS (YDCPG_OPTS%KLON)
REAL (KIND=JPRB), INTENT (IN)::PXVGST (YDCPG_OPTS%KLON)
REAL (KIND=JPRB), INTENT (INOUT)::PNUMEAN (YDCPG_OPTS%KLON)
REAL (KIND=JPRB), INTENT (INOUT)::PNVMEAN (YDCPG_OPTS%KLON)
REAL (KIND=JPRB), INTENT (INOUT)::PUMEAN (YDCPG_OPTS%KLON)
REAL (KIND=JPRB), INTENT (INOUT)::PVMEAN (YDCPG_OPTS%KLON)
#include "meanwind_xfu.intfb.h"
INTEGER (KIND=JPIM)::JLON
REAL (KIND=JPRB)::ZGST
REAL (KIND=JPRB)::ZGST2
REAL (KIND=JPHOOK)::ZHOOK_HANDLE
IF (LHOOK) CALL DR_HOOK ('CPXFU_OUTLINE_000', 0, ZHOOK_HANDLE)

IF (YDXFU%LXXGST) THEN

  DO JLON=YDCPG_BNDS%KIDIA, YDCPG_BNDS%KFDIA

    IF (YDXFU%LRESET_GST) THEN
      ZGST=-1.0_JPRB
    ELSE
      ZGST=YDXFU%UGST (JLON)**2+YDXFU%VGST (JLON)**2
    ENDIF

    LDGST (JLON)=PXUGST (JLON)**2+PXVGST (JLON)**2>ZGST
  ENDDO

ENDIF


IF (YDXFU%LXXGST2) THEN

  DO JLON=YDCPG_BNDS%KIDIA, YDCPG_BNDS%KFDIA

    IF (YDXFU%LRESET_GST2) THEN
      ZGST2=-1.0_JPRB
    ELSE
      ZGST2=YDXFU%UGST2 (JLON)**2+YDXFU%VGST2 (JLON)**2
    ENDIF

    LDGST2 (JLON)=PXUGST (JLON)**2+PXVGST (JLON)**2>ZGST2
  ENDDO

ENDIF


IF (YDXFU%LXMWINDCLS.AND.YDXFU%NMEANSTEPS/=0) THEN

  IF (YDXFU%LXCLS) THEN
    CALL MEANWIND_XFU (YDCPG_OPTS%KLON, YDCPG_BNDS%KIDIA, YDCPG_BNDS%KFDIA, YDXFU%NMEANSTEPS&
    &, YDXFU%UCLS, YDXFU%VCLS, PXUCLS, PXVCLS, YDXFU%RMWINDCALC, PUMEAN, PVMEAN)
  ENDIF


  IF (YDXFU%LXNUVCLS) THEN
    CALL MEANWIND_XFU (YDCPG_OPTS%KLON, YDCPG_BNDS%KIDIA, YDCPG_BNDS%KFDIA, YDXFU%NMEANSTEPS&
    &, YDXFU%NUCLS, YDXFU%NVCLS, PXNUCLS, PXNVCLS, YDXFU%RMNWINDCALC, PNUMEAN, PNVMEAN)
  ENDIF

ELSEIF (YDXFU%LXMWINDCLS) THEN
  PUMEAN=0._JPRB
  PVMEAN=0._JPRB
  PNUMEAN=0._JPRB
  PNVMEAN=0._JPRB
ENDIF

IF (LHOOK) CALL DR_HOOK ('CPXFU_OUTLINE_000', 1, ZHOOK_HANDLE)
ENDSUBROUTINE
