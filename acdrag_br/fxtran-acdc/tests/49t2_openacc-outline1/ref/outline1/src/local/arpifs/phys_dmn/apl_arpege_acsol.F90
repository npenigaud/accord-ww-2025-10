SUBROUTINE APL_ARPEGE_ACSOL (LDHMT, YDCPG_BNDS, YDCPG_OPTS, YDMF_PHYS, YDMF_PHYS_BASE_STATE, YDMF_PHYS_SURF&
&, YDMODEL, PC3, PCN, PDSA_C1, PDSA_C2, PNEIJG, PNEIJV, PWFC, PWPMX, PWSEQ, PWSMX, PWWILT)
USE CPG_OPTS_TYPE_MOD,ONLY:CPG_BNDS_TYPE, CPG_OPTS_TYPE
USE PARKIND1,ONLY:JPIM, JPRB
USE MF_PHYS_BASE_STATE_TYPE_MOD,ONLY:MF_PHYS_BASE_STATE_TYPE
USE MF_PHYS_SURFACE_TYPE_MOD,ONLY:MF_PHYS_SURF_TYPE
USE MF_PHYS_TYPE_MOD,ONLY:MF_PHYS_TYPE
USE TYPE_MODEL,ONLY:MODEL
USE YOMHOOK,ONLY:LHOOK, JPHOOK, DR_HOOK

IMPLICIT NONE

LOGICAL, INTENT (INOUT)::LDHMT
TYPE (CPG_BNDS_TYPE), INTENT (IN)::YDCPG_BNDS
TYPE (CPG_OPTS_TYPE), INTENT (IN)::YDCPG_OPTS
TYPE (MF_PHYS_TYPE), INTENT (INOUT)::YDMF_PHYS
TYPE (MF_PHYS_BASE_STATE_TYPE), INTENT (IN)::YDMF_PHYS_BASE_STATE
TYPE (MF_PHYS_SURF_TYPE), INTENT (INOUT)::YDMF_PHYS_SURF
TYPE (MODEL), INTENT (IN)::YDMODEL
REAL (KIND=JPRB), INTENT (INOUT)::PC3 (YDCPG_OPTS%KLON)
REAL (KIND=JPRB), INTENT (INOUT)::PCN (YDCPG_OPTS%KLON)
REAL (KIND=JPRB), INTENT (INOUT)::PDSA_C1 (YDCPG_OPTS%KLON)
REAL (KIND=JPRB), INTENT (INOUT)::PDSA_C2 (YDCPG_OPTS%KLON)
REAL (KIND=JPRB), INTENT (INOUT)::PNEIJG (YDCPG_OPTS%KLON)
REAL (KIND=JPRB), INTENT (INOUT)::PNEIJV (YDCPG_OPTS%KLON)
REAL (KIND=JPRB), INTENT (INOUT)::PWFC (YDCPG_OPTS%KLON)
REAL (KIND=JPRB), INTENT (INOUT)::PWPMX (YDCPG_OPTS%KLON)
REAL (KIND=JPRB), INTENT (INOUT)::PWSEQ (YDCPG_OPTS%KLON)
REAL (KIND=JPRB), INTENT (INOUT)::PWSMX (YDCPG_OPTS%KLON)
REAL (KIND=JPRB), INTENT (INOUT)::PWWILT (YDCPG_OPTS%KLON)
#include "acsol.intfb.h"
INTEGER (KIND=JPIM)::JLON
REAL (KIND=JPRB)::ZCG (YDCPG_OPTS%KLON)
REAL (KIND=JPHOOK)::ZHOOK_HANDLE
IF (LHOOK) CALL DR_HOOK ('APL_ARPEGE_ACSOL', 0, ZHOOK_HANDLE)

IF (YDMODEL%YRML_PHY_MF%YRPHY%LSOLV) THEN
  LDHMT=.FALSE.
  CALL ACSOL (YDCPG_OPTS%YRCLI, YDMODEL%YRCST, YDMODEL%YRML_PHY_MF%YRPHY, YDMODEL%YRML_PHY_MF%YRPHY1&
  &, YDCPG_BNDS%KIDIA, YDCPG_BNDS%KFDIA, YDCPG_OPTS%KLON, YDMF_PHYS_SURF%GSD_VV%PARG, YDMF_PHYS_SURF&
  &%GSD_VV%PD2, YDMF_PHYS_SURF%GSD_VF%PZ0F, YDMF_PHYS_SURF%GSD_VV%PZ0H, YDMF_PHYS_SURF%GSD_VF%PZ0RLF&
  &, YDMF_PHYS_SURF%GSD_VF%PLSM, YDMF_PHYS_SURF%GSD_VV%PIVEG, YDMF_PHYS_SURF%GSD_VV%PLAI, YDMF_PHYS_BASE_STATE&
  &%YGSP_SG%A (:, 1), YDMF_PHYS_BASE_STATE%YGSP_SG%R (:, 1), YDMF_PHYS_SURF%GSD_VV%PSAB, YDMF_PHYS_BASE_STATE&
  &%YGSP_SG%F (:, 1), YDMF_PHYS_BASE_STATE%YGSP_RR%T, YDMF_PHYS_SURF%GSD_VF%PVEG, YDMF_PHYS_BASE_STATE&
  &%YGSP_SB%Q (:, 1), YDMF_PHYS_BASE_STATE%YGSP_SB%TL (:, 1), YDMF_PHYS_BASE_STATE%YGSP_RR%W, YDMF_PHYS_BASE_STATE&
  &%YGSP_RR%IC, LDHMT, PDSA_C1, PDSA_C2, PC3, ZCG, PCN, YDMF_PHYS%OUT%CT, PNEIJG, PNEIJV, PWFC, PWPMX&
  &, PWSEQ, PWSMX, PWWILT)
ELSE

  DO JLON=YDCPG_BNDS%KIDIA, YDCPG_BNDS%KFDIA
    YDMF_PHYS%OUT%CT (JLON)=YDMODEL%YRML_PHY_MF%YRPHY1%HSOL/(1.0_JPRB+YDMODEL%YRML_PHY_MF%YRPHY1&
    &%HSOLIWR*(YDMF_PHYS_BASE_STATE%YGSP_RR%W (JLON)+YDMF_PHYS_BASE_STATE%YGSP_SB%Q (JLON, 1))/(YDMODEL&
    &%YRML_PHY_MF%YRPHY1%WSMX+YDMODEL%YRML_PHY_MF%YRPHY1%WPMX)*EXP (-0.5_JPRB*(YDMODEL%YRML_PHY_MF&
    &%YRPHY1%HSOLIT0*(YDMF_PHYS_BASE_STATE%YGSP_RR%T (JLON)-YDMODEL%YRCST%RTT))**2))
  ENDDO

ENDIF

IF (LHOOK) CALL DR_HOOK ('APL_ARPEGE_ACSOL', 1, ZHOOK_HANDLE)
ENDSUBROUTINE
