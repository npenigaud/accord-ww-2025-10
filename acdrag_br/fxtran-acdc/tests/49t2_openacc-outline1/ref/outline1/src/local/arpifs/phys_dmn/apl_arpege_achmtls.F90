SUBROUTINE APL_ARPEGE_ACHMTLS (LDCLS, YDCPG_BNDS, YDCPG_MISC, YDCPG_OPTS&
&, YDMF_PHYS, YDMF_PHYS_BASE_STATE, YDMODEL, PRVMD, PDPHIT, PDSA_CPS, PDSA_LHS&
&, PFLU_CD, PFLU_CDN, PGWDCS, PMRIPP, PNBVNO, PPCLS, PRTI)
USE CPG_OPTS_TYPE_MOD,ONLY:CPG_BNDS_TYPE, CPG_OPTS_TYPE
USE CPG_TYPE_MOD,ONLY:CPG_MISC_TYPE, CPG_DYN_TYPE, CPG_GPAR_TYPE, CPG_PHY_TYPE
USE PARKIND1,ONLY:JPIM, JPRB
USE MF_PHYS_BASE_STATE_TYPE_MOD,ONLY:MF_PHYS_BASE_STATE_TYPE
USE MF_PHYS_TYPE_MOD,ONLY:MF_PHYS_TYPE
USE TYPE_MODEL,ONLY:MODEL
USE YOMHOOK,ONLY:LHOOK, JPHOOK, DR_HOOK

IMPLICIT NONE

LOGICAL, INTENT (INOUT)::LDCLS
TYPE (CPG_BNDS_TYPE), INTENT (IN)::YDCPG_BNDS
TYPE (CPG_MISC_TYPE), INTENT (INOUT)::YDCPG_MISC
TYPE (CPG_OPTS_TYPE), INTENT (IN)::YDCPG_OPTS
TYPE (MF_PHYS_TYPE), INTENT (INOUT)::YDMF_PHYS
TYPE (MF_PHYS_BASE_STATE_TYPE), INTENT (IN)::YDMF_PHYS_BASE_STATE
TYPE (MODEL), INTENT (IN)::YDMODEL
REAL (KIND=JPRB), INTENT (INOUT)::PRVMD
REAL (KIND=JPRB), INTENT (INOUT)::PDPHIT (YDCPG_OPTS%KLON)
REAL (KIND=JPRB), INTENT (INOUT)::PDSA_CPS (YDCPG_OPTS%KLON)
REAL (KIND=JPRB), INTENT (INOUT)::PDSA_LHS (YDCPG_OPTS%KLON)
REAL (KIND=JPRB), INTENT (INOUT)::PFLU_CD (YDCPG_OPTS%KLON)
REAL (KIND=JPRB), INTENT (INOUT)::PFLU_CDN (YDCPG_OPTS%KLON)
REAL (KIND=JPRB), INTENT (INOUT)::PGWDCS (YDCPG_OPTS%KLON)
REAL (KIND=JPRB), INTENT (INOUT)::PMRIPP (YDCPG_OPTS%KLON, 0:YDCPG_OPTS%KFLEVG)
REAL (KIND=JPRB), INTENT (INOUT)::PNBVNO (YDCPG_OPTS%KLON, 0:YDCPG_OPTS%KFLEVG)
REAL (KIND=JPRB), INTENT (INOUT)::PPCLS (YDCPG_OPTS%KLON)
REAL (KIND=JPRB), INTENT (INOUT)::PRTI (YDCPG_OPTS%KLON)
#include "achmtls.intfb.h"
INTEGER (KIND=JPIM)::JLON
REAL (KIND=JPRB)::ZDPHI (YDCPG_OPTS%KLON)
REAL (KIND=JPRB)::ZPRS (YDCPG_OPTS%KLON)
REAL (KIND=JPHOOK)::ZHOOK_HANDLE
IF (LHOOK) CALL DR_HOOK ('APL_ARPEGE_ACHMTLS', 0, ZHOOK_HANDLE)
CALL ACHMTLS (YDMODEL%YRCST, YDMODEL%YRML_PHY_MF, YDCPG_BNDS%KIDIA, YDCPG_BNDS%KFDIA, YDCPG_OPTS&
&%KLON, YDCPG_OPTS%KFLEVG, YDMF_PHYS_BASE_STATE%YCPG_DYN%PHI, YDMF_PHYS_BASE_STATE%YCPG_DYN%PHIF&
&, YDMF_PHYS_BASE_STATE%YCPG_PHY%PREHYD, YDMF_PHYS_BASE_STATE%YCPG_PHY%PREHYDF, YDMF_PHYS_BASE_STATE&
&%YCPG_DYN%RCP%R, YDMF_PHYS_BASE_STATE%T, YDMF_PHYS_BASE_STATE%U, YDMF_PHYS_BASE_STATE%V, YDMF_PHYS_BASE_STATE&
&%YGSP_RR%T, YDCPG_MISC%QS, PDPHIT, YDMF_PHYS%OUT%GZ0, YDMF_PHYS%OUT%GZ0H, LDCLS, PNBVNO, PMRIPP&
&, PDSA_CPS, PGWDCS, PDSA_LHS, PPCLS, PFLU_CD, PFLU_CDN)

DO JLON=YDCPG_BNDS%KIDIA, YDCPG_BNDS%KFDIA
  ZDPHI (JLON)=YDMF_PHYS_BASE_STATE%YCPG_DYN%PHIF (JLON, YDCPG_OPTS%KFLEVG&
  &)-YDMF_PHYS_BASE_STATE%YCPG_DYN%PHI (JLON, YDCPG_OPTS%KFLEVG)
  ZPRS (JLON)=YDMODEL%YRCST%RD+PRVMD*YDCPG_MISC%QS (JLON)
  PRTI (JLON)=2.0_JPRB/(YDMF_PHYS_BASE_STATE%YCPG_DYN%RCP%R (JLON, YDCPG_OPTS&
  &%KFLEVG)*YDMF_PHYS_BASE_STATE%T (JLON, YDCPG_OPTS%KFLEVG)+YDMODEL%YRCST%RKAPPA&
  &*ZDPHI (JLON)+ZPRS (JLON)*YDMF_PHYS_BASE_STATE%YGSP_RR%T (JLON))
ENDDO

IF (LHOOK) CALL DR_HOOK ('APL_ARPEGE_ACHMTLS', 1, ZHOOK_HANDLE)
ENDSUBROUTINE
