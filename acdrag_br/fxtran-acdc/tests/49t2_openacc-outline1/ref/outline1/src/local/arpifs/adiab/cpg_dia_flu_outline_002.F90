SUBROUTINE CPG_DIA_FLU_OUTLINE_002 (YDCPG_BNDS, YDCPG_OPTS,&
& YDVARS, PSTRCU, PSTRCV, PSTRDU, PSTRDV, PSTRTU, PSTRTV)
