REAL (KIND=JPRB), INTENT (INOUT)::PSTRTV (YDCPG_OPTS%KLON, 0:YDCPG_OPTS%KFLEVG)
INTEGER (KIND=JPIM)::JLEV
REAL (KIND=JPHOOK)::ZHOOK_HANDLE
IF (LHOOK) CALL DR_HOOK ('CPG_DIA_FLU_OUTLINE_002', 0, ZHOOK_HANDLE)

DO JLEV=0, YDCPG_OPTS%KFLEVG
  PSTRCU (YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA, JLEV)=PSTRCU (YDCPG_BNDS%KIDIA:YDCPG_BNDS&
  &%KFDIA, JLEV)/YDVARS%GEOMETRY%GM%P (YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA)
  PSTRCV (YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA, JLEV)=PSTRCV (YDCPG_BNDS%KIDIA:YDCPG_BNDS&
  &%KFDIA, JLEV)/YDVARS%GEOMETRY%GM%P (YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA)
  PSTRDU (YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA, JLEV)=PSTRDU (YDCPG_BNDS%KIDIA:YDCPG_BNDS&
  &%KFDIA, JLEV)/YDVARS%GEOMETRY%GM%P (YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA)
  PSTRDV (YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA, JLEV)=PSTRDV (YDCPG_BNDS%KIDIA:YDCPG_BNDS&
  &%KFDIA, JLEV)/YDVARS%GEOMETRY%GM%P (YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA)
  PSTRTU (YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA, JLEV)=PSTRTU (YDCPG_BNDS%KIDIA:YDCPG_BNDS&
  &%KFDIA, JLEV)/YDVARS%GEOMETRY%GM%P (YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA)
  PSTRTV (YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA, JLEV)=PSTRTV (YDCPG_BNDS%KIDIA:YDCPG_BNDS&
  &%KFDIA, JLEV)/YDVARS%GEOMETRY%GM%P (YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA)
ENDDO

IF (LHOOK) CALL DR_HOOK ('CPG_DIA_FLU_OUTLINE_002', 1, ZHOOK_HANDLE)
ENDSUBROUTINE
