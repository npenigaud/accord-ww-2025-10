SUBROUTINE LAPINEB_LRDBBC (YDCPG_BNDS, YDCPG_SL2, YDGEOMETRY, YDVARS&
&, PDBBC9, PDPHI9, PGWS9, POROGL_SR, POROGM_SR, PUST1, PVST1)
USE CPG_OPTS_TYPE_MOD,ONLY:CPG_OPTS_TYPE, CPG_BNDS_TYPE
USE CPG_SL2_TYPE_MOD,ONLY:CPG_SL2_TYPE
USE FIELD_VARIABLES_MOD,ONLY:FIELD_VARIABLES
USE GEOMETRY_MOD,ONLY:GEOMETRY
USE PARKIND1,ONLY:JPIM, JPRB
USE YOMHOOK,ONLY:LHOOK, JPHOOK, DR_HOOK

IMPLICIT NONE

TYPE (CPG_BNDS_TYPE), INTENT (IN)::YDCPG_BNDS
TYPE (CPG_SL2_TYPE), INTENT (IN)::YDCPG_SL2
TYPE (GEOMETRY), INTENT (IN)::YDGEOMETRY
TYPE (FIELD_VARIABLES), INTENT (INOUT)::YDVARS
REAL (KIND=JPRB), INTENT (INOUT)::PDBBC9 (YDGEOMETRY%YRDIM%NPROMA)
REAL (KIND=JPRB), INTENT (INOUT)::PDPHI9 (YDGEOMETRY%YRDIM%NPROMA)
REAL (KIND=JPRB), INTENT (INOUT)::PGWS9 (YDGEOMETRY%YRDIM%NPROMA)
REAL (KIND=JPRB), INTENT (INOUT)::POROGL_SR (YDGEOMETRY%YRDIM%NPROMA)
REAL (KIND=JPRB), INTENT (INOUT)::POROGM_SR (YDGEOMETRY%YRDIM%NPROMA)
REAL (KIND=JPRB), INTENT (INOUT)::PUST1 (YDGEOMETRY%YRDIM%NPROMA)
REAL (KIND=JPRB), INTENT (INOUT)::PVST1 (YDGEOMETRY%YRDIM%NPROMA)
INTEGER (KIND=JPIM)::JROF
REAL (KIND=JPHOOK)::ZHOOK_HANDLE
IF (LHOOK) CALL DR_HOOK ('LAPINEB_LRDBBC', 0, ZHOOK_HANDLE)

DO JROF=YDCPG_BNDS%KIDIA, YDCPG_BNDS%KFDIA
  YDVARS%SVD%T1 (JROF, YDGEOMETRY%YRDIMV%NFLEVG)=YDVARS%SVD%T1 (JROF, YDGEOMETRY%YRDIMV%NFLEVG&
  &)-0.5_JPRB*(PDPHI9 (JROF)+YDCPG_SL2%DPHI1 (JROF))*(PUST1 (JROF)*POROGL_SR (JROF)+PVST1&
  & (JROF)*POROGM_SR (JROF)-PGWS9 (JROF))-(PDBBC9 (JROF)+YDCPG_SL2%DBBC1 (JROF))
ENDDO

IF (LHOOK) CALL DR_HOOK ('LAPINEB_LRDBBC', 1, ZHOOK_HANDLE)
ENDSUBROUTINE
