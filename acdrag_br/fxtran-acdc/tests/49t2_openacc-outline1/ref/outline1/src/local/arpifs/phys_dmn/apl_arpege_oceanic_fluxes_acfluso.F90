SUBROUTINE APL_ARPEGE_OCEANIC_FLUXES_ACFLUSO (LDHMT, YDCPG_BNDS, YDCPG_OPTS, YDMF_PHYS&
&, YDMF_PHYS_BASE_STATE, YDMF_PHYS_SURF, YDMODEL, PCDROV, PCEROV, PCHROV, PDPHIT&
&, PDPHIV, PDSA_RS, PFLU_CD, PFLU_CDN, PFLU_CH, PFLU_QSATS, PCE)
USE CPG_OPTS_TYPE_MOD,ONLY:CPG_BNDS_TYPE, CPG_OPTS_TYPE
USE PARKIND1,ONLY:JPIM, JPRB
USE MF_PHYS_BASE_STATE_TYPE_MOD,ONLY:MF_PHYS_BASE_STATE_TYPE
USE MF_PHYS_SURFACE_TYPE_MOD,ONLY:MF_PHYS_SURF_TYPE
USE MF_PHYS_TYPE_MOD,ONLY:MF_PHYS_TYPE
USE TYPE_MODEL,ONLY:MODEL
USE YOMHOOK,ONLY:LHOOK, JPHOOK, DR_HOOK

IMPLICIT NONE

LOGICAL, INTENT (IN)::LDHMT
TYPE (CPG_BNDS_TYPE), INTENT (IN)::YDCPG_BNDS
TYPE (CPG_OPTS_TYPE), INTENT (IN)::YDCPG_OPTS
TYPE (MF_PHYS_TYPE), INTENT (INOUT)::YDMF_PHYS
TYPE (MF_PHYS_BASE_STATE_TYPE), INTENT (IN)::YDMF_PHYS_BASE_STATE
TYPE (MF_PHYS_SURF_TYPE), INTENT (INOUT)::YDMF_PHYS_SURF
TYPE (MODEL), INTENT (IN)::YDMODEL
REAL (KIND=JPRB), INTENT (INOUT)::PCDROV (YDCPG_OPTS%KLON)
REAL (KIND=JPRB), INTENT (INOUT)::PCEROV (YDCPG_OPTS%KLON)
REAL (KIND=JPRB), INTENT (INOUT)::PCHROV (YDCPG_OPTS%KLON)
REAL (KIND=JPRB), INTENT (IN)::PDPHIT (YDCPG_OPTS%KLON)
REAL (KIND=JPRB), INTENT (IN)::PDPHIV (YDCPG_OPTS%KLON)
REAL (KIND=JPRB), INTENT (IN)::PDSA_RS (YDCPG_OPTS%KLON)
REAL (KIND=JPRB), INTENT (INOUT)::PFLU_CD (YDCPG_OPTS%KLON)
REAL (KIND=JPRB), INTENT (INOUT)::PFLU_CDN (YDCPG_OPTS%KLON)
REAL (KIND=JPRB), INTENT (INOUT)::PFLU_CH (YDCPG_OPTS%KLON)
REAL (KIND=JPRB), INTENT (IN)::PFLU_QSATS (YDCPG_OPTS%KLON)
REAL (KIND=JPRB), INTENT (INOUT)::PCE (YDCPG_OPTS%KLON)
#include "acfluso.intfb.h"
INTEGER (KIND=JPIM)::JLON
REAL (KIND=JPRB)::ZCRTI (YDCPG_OPTS%KLON)
REAL (KIND=JPHOOK)::ZHOOK_HANDLE
IF (LHOOK) CALL DR_HOOK ('APL_ARPEGE_OCEANIC_FLUXES_ACFLUSO', 0, ZHOOK_HANDLE)

DO JLON=YDCPG_BNDS%KIDIA, YDCPG_BNDS%KFDIA
  ZCRTI (JLON)=1.0_JPRB/(YDMF_PHYS_BASE_STATE%YGSP_RR%T (JLON)*PDSA_RS (JLON))
ENDDO

CALL ACFLUSO (YDMODEL%YRCST, YDMODEL%YRML_PHY_MF, YDCPG_BNDS%KIDIA, YDCPG_BNDS%KFDIA, YDCPG_OPTS%KLON&
&, YDCPG_OPTS%KFLEVG, YDMF_PHYS_BASE_STATE%YCPG_DYN%PHI, YDMF_PHYS_BASE_STATE%YCPG_DYN%PHIF, YDMF_PHYS_BASE_STATE&
&%YCPG_PHY%PREHYD, YDMF_PHYS_BASE_STATE%YCPG_PHY%PREHYDF, YDMF_PHYS_BASE_STATE%Q, YDMF_PHYS_BASE_STATE&
&%T, YDMF_PHYS_BASE_STATE%U, YDMF_PHYS_BASE_STATE%V, PDPHIT, PDPHIV, YDMF_PHYS%OUT%GZ0, YDMF_PHYS_SURF&
&%GSD_VF%PLSM, PFLU_QSATS, ZCRTI, YDMF_PHYS_BASE_STATE%YGSP_RR%T, LDHMT, PFLU_CD, PFLU_CDN, PCDROV,&
& PCE, PCEROV, PFLU_CH, PCHROV, YDMF_PHYS%OUT%QCLS, YDMF_PHYS%OUT%RHCLS, YDMF_PHYS%OUT%TCLS, YDMF_PHYS&
&%OUT%UCLS, YDMF_PHYS%OUT%VCLS, YDMF_PHYS%OUT%UGST, YDMF_PHYS%OUT%VGST)
IF (LHOOK) CALL DR_HOOK ('APL_ARPEGE_OCEANIC_FLUXES_ACFLUSO', 1, ZHOOK_HANDLE)
ENDSUBROUTINE
