SUBROUTINE LAPINEB_ZVWEI (YDCPG_BNDS, YDGEOMETRY, PVWEI)
USE CPG_OPTS_TYPE_MOD,ONLY:CPG_OPTS_TYPE, CPG_BNDS_TYPE
USE GEOMETRY_MOD,ONLY:GEOMETRY
USE PARKIND1,ONLY:JPIM, JPRB
USE YOMHOOK,ONLY:LHOOK, JPHOOK, DR_HOOK

IMPLICIT NONE

TYPE (CPG_BNDS_TYPE), INTENT (IN)::YDCPG_BNDS
TYPE (GEOMETRY), INTENT (IN)::YDGEOMETRY
REAL (KIND=JPRB), INTENT (INOUT)::PVWEI (YDGEOMETRY%YRDIM%NPROMA, YDGEOMETRY%YRDIMV%NFLEVG)
INTEGER (KIND=JPIM)::JLEV
REAL (KIND=JPHOOK)::ZHOOK_HANDLE
IF (LHOOK) CALL DR_HOOK ('LAPINEB_ZVWEI', 0, ZHOOK_HANDLE)

DO JLEV=1, YDGEOMETRY%YRDIMV%NFLEVG
  PVWEI (YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA, JLEV)=YDGEOMETRY%YRVERT_GEOM%YRVAB%VDELB (JLEV)
ENDDO

IF (LHOOK) CALL DR_HOOK ('LAPINEB_ZVWEI', 1, ZHOOK_HANDLE)
ENDSUBROUTINE
