SUBROUTINE LATTEX_LRHS_CURV (YDCPG_BNDS, YDCPG_SL1, YDGEOMETRY, YDML_DYN, YDVARS)
USE CPG_OPTS_TYPE_MOD,ONLY:CPG_BNDS_TYPE, CPG_OPTS_TYPE
USE CPG_SL1_TYPE_MOD,ONLY:CPG_SL1B_TYPE
USE FIELD_VARIABLES_MOD,ONLY:FIELD_VARIABLES
USE GEOMETRY_MOD,ONLY:GEOMETRY
USE PARKIND1,ONLY:JPIM, JPRB
USE MODEL_DYNAMICS_MOD,ONLY:MODEL_DYNAMICS_TYPE
USE YOMHOOK,ONLY:LHOOK, JPHOOK, DR_HOOK

IMPLICIT NONE

TYPE (CPG_BNDS_TYPE), INTENT (IN)::YDCPG_BNDS
TYPE (CPG_SL1B_TYPE), INTENT (INOUT)::YDCPG_SL1
TYPE (GEOMETRY), INTENT (IN)::YDGEOMETRY
TYPE (MODEL_DYNAMICS_TYPE), INTENT (IN)::YDML_DYN
TYPE (FIELD_VARIABLES), INTENT (INOUT)::YDVARS
INTEGER (KIND=JPIM)::JLEV
REAL (KIND=JPRB)::ZUCOMP (YDGEOMETRY%YRDIM%NPROMA, YDGEOMETRY%YRDIMV%NFLEVG)
REAL (KIND=JPHOOK)::ZHOOK_HANDLE
IF (LHOOK) CALL DR_HOOK ('LATTEX_LRHS_CURV', 0, ZHOOK_HANDLE)

DO JLEV=1, YDGEOMETRY%YRDIMV%NFLEVG
  ZUCOMP (YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA, JLEV)=YDCPG_SL1&
  &%U9%P (YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA, JLEV)
  YDCPG_SL1%Z9%P (YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA, JLEV)=YDCPG_SL1%V9%P (YDCPG_BNDS%KIDIA&
  &:YDCPG_BNDS%KFDIA, JLEV)*YDVARS%GEOMETRY%GSQM2%P (YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA)
  YDCPG_SL1%U9%P (YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA, JLEV)=-YDCPG_SL1%U9%P (YDCPG_BNDS%KIDIA&
  &:YDCPG_BNDS%KFDIA, JLEV)*YDVARS%GEOMETRY%GESLO%P (YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA)-YDCPG_SL1&
  &%V9%P (YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA, JLEV)*YDVARS%GEOMETRY%GECLO%P (YDCPG_BNDS%KIDIA&
  &:YDCPG_BNDS%KFDIA)*YDVARS%GEOMETRY%GEMU%P (YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA)
  YDCPG_SL1%V9%P (YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA, JLEV)=ZUCOMP (YDCPG_BNDS%KIDIA:YDCPG_BNDS&
  &%KFDIA, JLEV)*YDVARS%GEOMETRY%GECLO%P (YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA)-YDCPG_SL1%V9&
  &%P (YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA, JLEV)*YDVARS%GEOMETRY%GESLO%P (YDCPG_BNDS%KIDIA&
  &:YDCPG_BNDS%KFDIA)*YDVARS%GEOMETRY%GEMU%P (YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA)
ENDDO


IF ((YDML_DYN%YRDYN%NWLAG>=3).OR.(YDML_DYN%YRDYN%NSPLTHOI/=0)) THEN

  DO JLEV=1, YDGEOMETRY%YRDIMV%NFLEVG
    ZUCOMP (YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA, JLEV)=YDCPG_SL1&
    &%U0%P (YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA, JLEV)
    YDCPG_SL1%Z0%P (YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA, JLEV)=YDCPG_SL1%V0%P (YDCPG_BNDS%KIDIA&
    &:YDCPG_BNDS%KFDIA, JLEV)*YDVARS%GEOMETRY%GSQM2%P (YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA)
    YDCPG_SL1%U0%P (YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA, JLEV)=-YDCPG_SL1%U0%P (YDCPG_BNDS%KIDIA&
    &:YDCPG_BNDS%KFDIA, JLEV)*YDVARS%GEOMETRY%GESLO%P (YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA)-YDCPG_SL1&
    &%V0%P (YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA, JLEV)*YDVARS%GEOMETRY%GECLO%P (YDCPG_BNDS%KIDIA&
    &:YDCPG_BNDS%KFDIA)*YDVARS%GEOMETRY%GEMU%P (YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA)
    YDCPG_SL1%V0%P (YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA, JLEV)=ZUCOMP (YDCPG_BNDS%KIDIA:YDCPG_BNDS&
    &%KFDIA, JLEV)*YDVARS%GEOMETRY%GECLO%P (YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA)-YDCPG_SL1%V0&
    &%P (YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA, JLEV)*YDVARS%GEOMETRY%GESLO%P (YDCPG_BNDS%KIDIA&
    &:YDCPG_BNDS%KFDIA)*YDVARS%GEOMETRY%GEMU%P (YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA)
  ENDDO

ENDIF

IF (LHOOK) CALL DR_HOOK ('LATTEX_LRHS_CURV', 1, ZHOOK_HANDLE)
ENDSUBROUTINE
