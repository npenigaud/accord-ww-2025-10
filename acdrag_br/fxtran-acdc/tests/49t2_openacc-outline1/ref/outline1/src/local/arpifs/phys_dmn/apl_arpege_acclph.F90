SUBROUTINE APL_ARPEGE_ACCLPH (YDCPG_BNDS, YDCPG_MISC, YDCPG_OPTS, YDMF_PHYS&
&, YDMF_PHYS_BASE_STATE, YDMODEL, KMOC_CLPH, PBLH, PCP, PQV)
USE CPG_OPTS_TYPE_MOD,ONLY:CPG_BNDS_TYPE, CPG_OPTS_TYPE
USE CPG_TYPE_MOD,ONLY:CPG_MISC_TYPE, CPG_DYN_TYPE, CPG_GPAR_TYPE, CPG_PHY_TYPE
USE PARKIND1,ONLY:JPIM, JPRB
USE MF_PHYS_BASE_STATE_TYPE_MOD,ONLY:MF_PHYS_BASE_STATE_TYPE
USE MF_PHYS_TYPE_MOD,ONLY:MF_PHYS_TYPE
USE TYPE_MODEL,ONLY:MODEL
USE YOMHOOK,ONLY:LHOOK, JPHOOK, DR_HOOK

IMPLICIT NONE

TYPE (CPG_BNDS_TYPE), INTENT (IN)::YDCPG_BNDS
TYPE (CPG_MISC_TYPE), INTENT (INOUT)::YDCPG_MISC
TYPE (CPG_OPTS_TYPE), INTENT (IN)::YDCPG_OPTS
TYPE (MF_PHYS_TYPE), INTENT (INOUT)::YDMF_PHYS
TYPE (MF_PHYS_BASE_STATE_TYPE), INTENT (IN)::YDMF_PHYS_BASE_STATE
TYPE (MODEL), INTENT (IN)::YDMODEL
INTEGER (KIND=JPIM), INTENT (INOUT)::KMOC_CLPH (YDCPG_OPTS%KLON)
REAL (KIND=JPRB), INTENT (INOUT)::PBLH (YDCPG_OPTS%KLON)
REAL (KIND=JPRB), INTENT (INOUT)::PCP (YDCPG_OPTS%KLON, YDCPG_OPTS%KFLEVG)
REAL (KIND=JPRB), INTENT (INOUT)::PQV (YDCPG_OPTS%KLON, YDCPG_OPTS%KFLEVG)
#include "acclph.intfb.h"
INTEGER (KIND=JPIM)::JLEV
INTEGER (KIND=JPIM)::JLON
REAL (KIND=JPRB)::ZTHETAV (YDCPG_OPTS%KLON, YDCPG_OPTS%KFLEVG)
REAL (KIND=JPRB)::ZTHETAVS (YDCPG_OPTS%KLON)
REAL (KIND=JPRB)::ZUGST (YDCPG_OPTS%KLON)
REAL (KIND=JPRB)::ZVGST (YDCPG_OPTS%KLON)
REAL (KIND=JPHOOK)::ZHOOK_HANDLE
IF (LHOOK) CALL DR_HOOK ('APL_ARPEGE_ACCLPH', 0, ZHOOK_HANDLE)

DO JLEV=1, YDCPG_OPTS%KFLEVG

  DO JLON=YDCPG_BNDS%KIDIA, YDCPG_BNDS%KFDIA
    PCP (JLON, JLEV)=YDMF_PHYS_BASE_STATE%YCPG_DYN%RCP%CP (JLON, JLEV)
  ENDDO

ENDDO


DO JLEV=YDCPG_OPTS%KTDIA, YDCPG_OPTS%KFLEVG

  DO JLON=YDCPG_BNDS%KIDIA, YDCPG_BNDS%KFDIA
    ZTHETAV (JLON, JLEV)=YDMF_PHYS_BASE_STATE%T (JLON, JLEV)*(1.0_JPRB+YDMODEL%YRCST%RETV*PQV (JLON, JLEV&
    &))*(YDMODEL%YRCST%RATM/YDMF_PHYS_BASE_STATE%YCPG_PHY%PREHYDF (JLON, JLEV))**YDMODEL%YRCST%RKAPPA
  ENDDO

ENDDO


DO JLON=YDCPG_BNDS%KIDIA, YDCPG_BNDS%KFDIA
  ZTHETAVS (JLON)=YDMF_PHYS_BASE_STATE%YGSP_RR%T (JLON)*(1.0_JPRB+YDMODEL%YRCST&
  &%RETV*YDCPG_MISC%QS (JLON))*(YDMODEL%YRCST%RATM/YDMF_PHYS_BASE_STATE%YCPG_PHY&
  &%PREHYD (JLON, YDCPG_OPTS%KFLEVG))**YDMODEL%YRCST%RKAPPA
ENDDO

CALL ACCLPH (YDMODEL%YRCST, YDMODEL%YRML_PHY_MF%YRPHY0, YDMODEL%YRML_PHY_MF%YRPHY2, YDCPG_BNDS%KIDIA&
&, YDCPG_BNDS%KFDIA, YDCPG_OPTS%KLON, YDCPG_OPTS%KTDIA, YDCPG_OPTS%KFLEVG, ZTHETAV, YDMF_PHYS_BASE_STATE&
&%YCPG_DYN%PHI, YDMF_PHYS_BASE_STATE%YCPG_DYN%PHIF, YDMF_PHYS_BASE_STATE%U, YDMF_PHYS_BASE_STATE&
&%V, ZTHETAVS, KMOC_CLPH, YDMF_PHYS%OUT%CLPH, YDMF_PHYS%OUT%VEIN, ZUGST, ZVGST)

DO JLON=YDCPG_BNDS%KIDIA, YDCPG_BNDS%KFDIA
  PBLH (JLON)=YDMF_PHYS%OUT%CLPH (JLON)
ENDDO

IF (LHOOK) CALL DR_HOOK ('APL_ARPEGE_ACCLPH', 1, ZHOOK_HANDLE)
ENDSUBROUTINE
