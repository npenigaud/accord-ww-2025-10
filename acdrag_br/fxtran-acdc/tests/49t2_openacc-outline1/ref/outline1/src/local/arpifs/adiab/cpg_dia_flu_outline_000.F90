SUBROUTINE CPG_DIA_FLU_OUTLINE_000 (YDCPG_BNDS, YDCPG_OPTS, YDMF_PHYS_OUT&
&, PSTRCU, PSTRCV, PSTRDU, PSTRDV, PSTRTU, PSTRTV)
USE CPG_OPTS_TYPE_MOD,ONLY:CPG_BNDS_TYPE, CPG_OPTS_TYPE
USE PARKIND1,ONLY:JPIM, JPRB
USE MF_PHYS_TYPE_MOD,ONLY:MF_PHYS_OUT_TYPE
USE YOMHOOK,ONLY:LHOOK, JPHOOK, DR_HOOK

IMPLICIT NONE

TYPE (CPG_BNDS_TYPE), INTENT (IN)::YDCPG_BNDS
TYPE (CPG_OPTS_TYPE), INTENT (IN)::YDCPG_OPTS
TYPE (MF_PHYS_OUT_TYPE), INTENT (IN)::YDMF_PHYS_OUT
REAL (KIND=JPRB), INTENT (INOUT)::PSTRCU (YDCPG_OPTS%KLON, 0:YDCPG_OPTS%KFLEVG)
REAL (KIND=JPRB), INTENT (INOUT)::PSTRCV (YDCPG_OPTS%KLON, 0:YDCPG_OPTS%KFLEVG)
REAL (KIND=JPRB), INTENT (INOUT)::PSTRDU (YDCPG_OPTS%KLON, 0:YDCPG_OPTS%KFLEVG)
REAL (KIND=JPRB), INTENT (INOUT)::PSTRDV (YDCPG_OPTS%KLON, 0:YDCPG_OPTS%KFLEVG)
REAL (KIND=JPRB), INTENT (INOUT)::PSTRTU (YDCPG_OPTS%KLON, 0:YDCPG_OPTS%KFLEVG)
REAL (KIND=JPRB), INTENT (INOUT)::PSTRTV (YDCPG_OPTS%KLON, 0:YDCPG_OPTS%KFLEVG)
INTEGER (KIND=JPIM)::JLEV
REAL (KIND=JPHOOK)::ZHOOK_HANDLE
IF (LHOOK) CALL DR_HOOK ('CPG_DIA_FLU_OUTLINE_000', 0, ZHOOK_HANDLE)

DO JLEV=0, YDCPG_OPTS%KFLEVG
  PSTRCU (YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA, JLEV)=YDMF_PHYS_OUT&
  &%STRCU (YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA, JLEV)
  PSTRCV (YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA, JLEV)=YDMF_PHYS_OUT&
  &%STRCV (YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA, JLEV)
  PSTRDU (YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA, JLEV)=YDMF_PHYS_OUT&
  &%STRDU (YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA, JLEV)
  PSTRDV (YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA, JLEV)=YDMF_PHYS_OUT&
  &%STRDV (YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA, JLEV)
  PSTRTU (YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA, JLEV)=YDMF_PHYS_OUT&
  &%STRTU (YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA, JLEV)
  PSTRTV (YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA, JLEV)=YDMF_PHYS_OUT&
  &%STRTV (YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA, JLEV)
ENDDO

IF (LHOOK) CALL DR_HOOK ('CPG_DIA_FLU_OUTLINE_000', 1, ZHOOK_HANDLE)
ENDSUBROUTINE
