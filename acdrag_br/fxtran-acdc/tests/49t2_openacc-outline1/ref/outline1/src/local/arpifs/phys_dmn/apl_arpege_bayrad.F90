&, YDVARS%RCONV%T1, YDVARS%SCONV%T1, YDMODEL)
IF (LHOOK) CALL DR_HOOK ('APL_ARPEGE_BAYRAD', 1, ZHOOK_HANDLE)
ENDSUBROUTINE
