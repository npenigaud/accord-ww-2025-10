SUBROUTINE GPTF1_YDVARS_LTWOTL (YDVARS)
USE FIELD_VARIABLES_MOD,ONLY:FIELD_VARIABLES
USE YOMHOOK,ONLY:LHOOK, JPHOOK, DR_HOOK

IMPLICIT NONE

TYPE (FIELD_VARIABLES), INTENT (INOUT)::YDVARS
REAL (KIND=JPHOOK)::ZHOOK_HANDLE
IF (LHOOK) CALL DR_HOOK ('GPTF1_YDVARS_LTWOTL', 0, ZHOOK_HANDLE)
YDVARS%U%T9=YDVARS%U%T0
YDVARS%V%T9=YDVARS%V%T0
IF (LHOOK) CALL DR_HOOK ('GPTF1_YDVARS_LTWOTL', 1, ZHOOK_HANDLE)
ENDSUBROUTINE
