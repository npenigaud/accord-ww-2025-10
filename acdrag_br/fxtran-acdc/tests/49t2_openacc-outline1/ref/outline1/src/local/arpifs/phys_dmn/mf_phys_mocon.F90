SUBROUTINE MF_PHYS_MOCON (YDCPG_BNDS, YDCPG_OPTS, PRDG_LCVQ&
&, PMOC_CLPH, YDMF_PHYS, YDMF_PHYS_BASE_STATE)
USE YOMHOOK,ONLY:LHOOK, DR_HOOK, JPHOOK
USE PARKIND1,ONLY:JPIM, JPRB
USE CPG_OPTS_TYPE_MOD,ONLY:CPG_BNDS_TYPE, CPG_OPTS_TYPE
USE MF_PHYS_TYPE_MOD,ONLY:MF_PHYS_TYPE
USE MF_PHYS_BASE_STATE_TYPE_MOD,ONLY:MF_PHYS_BASE_STATE_TYPE

IMPLICIT NONE


TYPE (CPG_BNDS_TYPE), INTENT (IN)::YDCPG_BNDS
TYPE (CPG_OPTS_TYPE), INTENT (IN)::YDCPG_OPTS
REAL (KIND=JPRB), INTENT (IN)::PRDG_LCVQ (YDCPG_OPTS%KLON, 1:YDCPG_OPTS%KFLEVG)
INTEGER (KIND=JPIM), INTENT (IN)::PMOC_CLPH (YDCPG_OPTS%KLON)
TYPE (MF_PHYS_TYPE), INTENT (INOUT)::YDMF_PHYS
TYPE (MF_PHYS_BASE_STATE_TYPE), INTENT (IN)::YDMF_PHYS_BASE_STATE


REAL (KIND=JPHOOK)::ZHOOK_HANDLE
#include "mf_phys_mocon_ppwetpoint.intfb.h"
IF (LHOOK) CALL DR_HOOK ('MF_PHYS_MOCON', 0, ZHOOK_HANDLE)
CALL MF_PHYS_MOCON_PPWETPOINT (YDCPG_BNDS, YDCPG_OPTS, YDMF_PHYS&
&, YDMF_PHYS_BASE_STATE, PMOC_CLPH, PRDG_LCVQ)
IF (LHOOK) CALL DR_HOOK ('MF_PHYS_MOCON', 1, ZHOOK_HANDLE)
ENDSUBROUTINE MF_PHYS_MOCON
