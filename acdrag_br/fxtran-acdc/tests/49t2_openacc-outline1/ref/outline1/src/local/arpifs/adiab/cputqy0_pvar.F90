SUBROUTINE CPUTQY0_PVAR (PDT, YDCPG_BNDS, YDCPG_DIMS_VAR, YDCPG_OPTS, PTND, PVAR)
USE CPG_OPTS_TYPE_MOD,ONLY:CPG_BNDS_TYPE, CPG_DIM_VAR_TYPE, CPG_OPTS_TYPE
USE PARKIND1,ONLY:JPRB, JPIM
USE YOMHOOK,ONLY:LHOOK, JPHOOK, DR_HOOK

IMPLICIT NONE

REAL (KIND=JPRB), INTENT (IN)::PDT
TYPE (CPG_BNDS_TYPE), INTENT (IN)::YDCPG_BNDS
TYPE (CPG_DIM_VAR_TYPE), INTENT (IN)::YDCPG_DIMS_VAR
TYPE (CPG_OPTS_TYPE), INTENT (IN)::YDCPG_OPTS
REAL (KIND=JPRB), INTENT (IN)::PTND (YDCPG_OPTS%KLON, YDCPG_OPTS%KFLEVG)
REAL (KIND=JPRB), INTENT (INOUT)::PVAR (YDCPG_OPTS%KLON&
&, YDCPG_DIMS_VAR%ILEVT1I:YDCPG_DIMS_VAR%ILEVT1F)
INTEGER (KIND=JPIM)::JLEV
INTEGER (KIND=JPIM)::JLON
REAL (KIND=JPHOOK)::ZHOOK_HANDLE
IF (LHOOK) CALL DR_HOOK ('CPUTQY0_PVAR', 0, ZHOOK_HANDLE)

DO JLEV=1, YDCPG_OPTS%KFLEVG

  DO JLON=YDCPG_BNDS%KIDIA, YDCPG_BNDS%KFDIA
    PVAR (JLON, JLEV)=PVAR (JLON, JLEV)+PDT*PTND (JLON, JLEV)
  ENDDO

ENDDO

IF (LHOOK) CALL DR_HOOK ('CPUTQY0_PVAR', 1, ZHOOK_HANDLE)
ENDSUBROUTINE
