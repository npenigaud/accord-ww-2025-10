SUBROUTINE LAPINEB_LACONE (YDA_PCCO, YDCPG_BNDS, YDCPG_GMV9, YDCPG_OPTS&
&, YDGEOMETRY, YDMODEL, YDVARS, PDSTRET, PP, PQ)
USE CPG_OPTS_TYPE_MOD,ONLY:CPG_OPTS_TYPE, CPG_BNDS_TYPE
USE CPG_GMV_TYPE_MOD,ONLY:CPG_GMV_TYPE
USE FIELD_ARRAY_MODULE,ONLY:FIELD_3IM_ARRAY, FIELD_4IM_ARRAY, FIELD_3RB_ARRAY, FIELD_4RB_ARRAY
USE FIELD_VARIABLES_MOD,ONLY:FIELD_VARIABLES
USE GEOMETRY_MOD,ONLY:GEOMETRY
USE PARKIND1,ONLY:JPIM, JPRB
USE TYPE_MODEL,ONLY:MODEL
USE YOMHOOK,ONLY:LHOOK, JPHOOK, DR_HOOK

IMPLICIT NONE

TYPE (FIELD_4RB_ARRAY), INTENT (IN)::YDA_PCCO
TYPE (CPG_BNDS_TYPE), INTENT (IN)::YDCPG_BNDS
TYPE (CPG_GMV_TYPE), INTENT (INOUT)::YDCPG_GMV9
TYPE (CPG_OPTS_TYPE), INTENT (IN)::YDCPG_OPTS
TYPE (GEOMETRY), INTENT (IN)::YDGEOMETRY
TYPE (MODEL), INTENT (IN)::YDMODEL
TYPE (FIELD_VARIABLES), INTENT (INOUT)::YDVARS
REAL (KIND=JPRB), INTENT (INOUT)::PDSTRET
REAL (KIND=JPRB), INTENT (INOUT)::PP (YDGEOMETRY%YRDIM%NPROMA, YDGEOMETRY%YRDIMV%NFLEVG)
REAL (KIND=JPRB), INTENT (INOUT)::PQ (YDGEOMETRY%YRDIM%NPROMA, YDGEOMETRY%YRDIMV%NFLEVG)
#include "lacone.intfb.h"
#include "rotuv.intfb.h"
INTEGER (KIND=JPIM)::JLEV
INTEGER (KIND=JPIM)::JROF
REAL (KIND=JPHOOK)::ZHOOK_HANDLE
IF (LHOOK) CALL DR_HOOK ('LAPINEB_LACONE', 0, ZHOOK_HANDLE)

IF (YDCPG_OPTS%LRPLANE) THEN
  YDCPG_GMV9%U%P (YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA, 1:YDGEOMETRY%YRDIMV%NFLEVG)=YDA_PCCO%P (YDCPG_BNDS&
  &%KIDIA:YDCPG_BNDS%KFDIA, 1:YDGEOMETRY%YRDIMV%NFLEVG, YDMODEL%YRML_DYN%YYTCCO%M_RLON)
  YDCPG_GMV9%V%P (YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA, 1:YDGEOMETRY%YRDIMV%NFLEVG)=YDA_PCCO%P (YDCPG_BNDS&
  &%KIDIA:YDCPG_BNDS%KFDIA, 1:YDGEOMETRY%YRDIMV%NFLEVG, YDMODEL%YRML_DYN%YYTCCO%M_RLAT)
ELSE
  CALL LACONE (YDGEOMETRY%YRDIM%NPROMA, YDCPG_BNDS%KIDIA, YDCPG_BNDS%KFDIA, YDGEOMETRY%YRDIMV&
  &%NFLEVG, YDGEOMETRY%YRGEM%NSTTYP, YDCPG_OPTS%N2DINI, YDMODEL%YRML_DYN%YRDYN%YRSWE, YDMODEL&
  &%YRCST%ROMEGA, PDSTRET, YDGEOMETRY%YRGEM%RC2M1, YDGEOMETRY%YRGEM%RC2P1, YDGEOMETRY%YRGEM%RMUCEN&
  &, YDMODEL%YRCST%RA, YDMODEL%YRCST%RPI, YDA_PCCO%P (:,:, YDMODEL%YRML_DYN%YYTCCO%M_RLON), YDA_PCCO&
  &%P (:,:, YDMODEL%YRML_DYN%YYTCCO%M_RLAT), YDCPG_GMV9%U%P, YDCPG_GMV9%V%P)
ENDIF


IF (YDMODEL%YRML_DYN%YRDYN%LRHS_CURV) THEN

  DO JLEV=1, YDGEOMETRY%YRDIMV%NFLEVG

    DO JROF=YDCPG_BNDS%KIDIA, YDCPG_BNDS%KFDIA
      YDVARS%U%T1 (JROF, JLEV)=YDVARS%U%T1 (JROF, JLEV)+YDCPG_GMV9%U%P (JROF, JLEV)
      YDVARS%V%T1 (JROF, JLEV)=YDVARS%V%T1 (JROF, JLEV)+YDCPG_GMV9%V%P (JROF, JLEV)
    ENDDO

  ENDDO

ELSE
  CALL ROTUV (YDGEOMETRY%YRDIMV%NFLEVG, YDGEOMETRY%YRDIM%NPROMA, YDCPG_BNDS%KIDIA, YDCPG_BNDS%KFDIA,&
  & YDCPG_GMV9%U%P, YDCPG_GMV9%V%P, PP, PQ, YDVARS%U%T1, YDVARS%V%T1, LDIR=.FALSE., LDADD=.TRUE.)
ENDIF


IF (.NOT.YDMODEL%YRML_DYN%YRDYN%L2TLFF.AND.YDMODEL%YRML_DYN%YRDYNA&
  &%LPC_CHEAP.AND.(YDMODEL%YRML_DYN%YRDYN%NCURRENT_ITER==0)) THEN

  IF (YDMODEL%YRML_DYN%YRDYN%LRHS_CURV) THEN

    DO JLEV=1, YDGEOMETRY%YRDIMV%NFLEVG

      DO JROF=YDCPG_BNDS%KIDIA, YDCPG_BNDS%KFDIA
        YDVARS%PCF_U%PC_PH (JROF, JLEV)=YDVARS%PCF_U%PC_PH (JROF, JLEV)+YDCPG_GMV9%U%P (JROF, JLEV)
        YDVARS%PCF_V%PC_PH (JROF, JLEV)=YDVARS%PCF_V%PC_PH (JROF, JLEV)+YDCPG_GMV9%V%P (JROF, JLEV)
      ENDDO

    ENDDO

  ELSE
    CALL ROTUV (YDGEOMETRY%YRDIMV%NFLEVG, YDGEOMETRY%YRDIM%NPROMA, YDCPG_BNDS&
    &%KIDIA, YDCPG_BNDS%KFDIA, YDCPG_GMV9%U%P, YDCPG_GMV9%V%P, PP, PQ, YDVARS&
    &%PCF_U%PC_PH, YDVARS%PCF_V%PC_PH, LDIR=.FALSE., LDADD=.TRUE.)
  ENDIF

ENDIF

IF (LHOOK) CALL DR_HOOK ('LAPINEB_LACONE', 1, ZHOOK_HANDLE)
ENDSUBROUTINE
