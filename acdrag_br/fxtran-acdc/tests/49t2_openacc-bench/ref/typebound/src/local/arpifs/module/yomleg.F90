MODULE YOMLEG

!$ACDC methods 


USE PARKIND1 , ONLY : JPRB

IMPLICIT NONE

SAVE

!     ------------------------------------------------------------------
!*    Description of Legendre polynomials

! 1/ Gaussian grid and geometry:
! RW          : weights of the gaussian quadrature
! RMU         : mu              sin(theta)
! R1MU2       : 1.-MU*MU        cos(theta)**2
! R1MUI       : 1./R1MU2      1/cos(theta)**2
! R1MUA       : 1./R1MU2/RA   1/(a*cos(theta)**2)
! RSQM2       : SQRT(R1MU2)     cos(theta)
! R1QM2       : 1./SQRT(R1MU2) 1/cos(theta)
! RACTHE      : 1./SQRT(R1MU2) 1/(a*cos(theta))
! RLATIG      : arcsin(mu)      theta  GLOBAL VIEW
! RLATI       : arcsin(mu)      theta

TYPE TCSGLEG
  REAL(KIND=JPRB), ALLOCATABLE :: RW(:)
  REAL(KIND=JPRB), ALLOCATABLE :: RMU(:)
  REAL(KIND=JPRB), ALLOCATABLE :: R1MU2(:)
  REAL(KIND=JPRB), ALLOCATABLE :: R1MUI(:)
  REAL(KIND=JPRB), ALLOCATABLE :: R1MUA(:)
  REAL(KIND=JPRB), ALLOCATABLE :: RSQM2(:)
  REAL(KIND=JPRB), ALLOCATABLE :: R1QM2(:)
  REAL(KIND=JPRB), ALLOCATABLE :: RACTHE(:)
  REAL(KIND=JPRB), ALLOCATABLE :: RLATIG(:)
  REAL(KIND=JPRB), ALLOCATABLE :: RLATI(:)
CONTAINS
PROCEDURE :: ACDC_COPY => ACDC_COPY_TCSGLEG
PROCEDURE :: ACDC_CRC64 => ACDC_CRC64_TCSGLEG
PROCEDURE :: ACDC_HOST => ACDC_HOST_TCSGLEG
PROCEDURE :: ACDC_LOAD => ACDC_LOAD_TCSGLEG
PROCEDURE :: ACDC_SAVE => ACDC_SAVE_TCSGLEG
PROCEDURE :: ACDC_SIZE => ACDC_SIZE_TCSGLEG
PROCEDURE :: ACDC_WIPE => ACDC_WIPE_TCSGLEG
END TYPE TCSGLEG


INTERFACE

MODULE SUBROUTINE ACDC_COPY_TCSGLEG (SELF, LDCREATED, LDFIELDAPI)

IMPLICIT NONE
CLASS (TCSGLEG), INTENT (IN), TARGET :: SELF
LOGICAL, OPTIONAL, INTENT (IN) :: LDCREATED, LDFIELDAPI
END SUBROUTINE

MODULE SUBROUTINE ACDC_CRC64_TCSGLEG (SELF, KLUN, CDPATH)
USE FXTRAN_ACDC_CRC64_INTRINSIC_MOD, ONLY : FCRC64 => FXTRAN_ACDC_CRC64
IMPLICIT NONE
CLASS (TCSGLEG), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
CHARACTER(LEN=*), INTENT (IN) :: CDPATH
END SUBROUTINE

MODULE SUBROUTINE ACDC_HOST_TCSGLEG (SELF)

IMPLICIT NONE
CLASS (TCSGLEG), TARGET :: SELF
END SUBROUTINE

MODULE SUBROUTINE ACDC_LOAD_TCSGLEG (SELF, KLUN)
USE PARKIND1, ONLY : JPRD
IMPLICIT NONE
CLASS (TCSGLEG), INTENT (OUT), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
END SUBROUTINE

MODULE SUBROUTINE ACDC_SAVE_TCSGLEG (SELF, KLUN)

IMPLICIT NONE
CLASS (TCSGLEG), INTENT (IN), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
END SUBROUTINE

MODULE FUNCTION ACDC_SIZE_TCSGLEG (SELF, CDPATH, LDPRINT) RESULT (KSIZE)

IMPLICIT NONE
CLASS (TCSGLEG),     INTENT (IN), TARGET :: SELF
CHARACTER(LEN=*), INTENT (IN), OPTIONAL :: CDPATH
LOGICAL,          INTENT (IN), OPTIONAL :: LDPRINT
INTEGER*8 :: KSIZE
END FUNCTION

MODULE SUBROUTINE ACDC_WIPE_TCSGLEG (SELF, LDDELETED, LDFIELDAPI)

IMPLICIT NONE
CLASS (TCSGLEG), INTENT (IN), TARGET :: SELF
LOGICAL, OPTIONAL, INTENT (IN) :: LDDELETED, LDFIELDAPI
END SUBROUTINE


END INTERFACE

END MODULE YOMLEG
