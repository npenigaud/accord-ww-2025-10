IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF
LF_F = ASSOCIATED (SELF%F_F)
IF (LF_F .AND. LLFIELDAPI) THEN
  CALL WIPE_FIELD_3RB (SELF%F_F)
ENDIF

LF_A = ASSOCIATED (SELF%F_A)
IF (LF_A .AND. LLFIELDAPI) THEN
  CALL WIPE_FIELD_3RB (SELF%F_A)
ENDIF

LF_R = ASSOCIATED (SELF%F_R)
IF (LF_R .AND. LLFIELDAPI) THEN
  CALL WIPE_FIELD_3RB (SELF%F_R)
ENDIF

LLDELETED = .FALSE.
IF (PRESENT (LDDELETED)) THEN
  LLDELETED = LDDELETED
ENDIF
IF (.NOT. LLDELETED) THEN
  !$ACC EXIT DATA DELETE (SELF)
ENDIF
END SUBROUTINE

