SUBMODULE (YOERADGHG) YOERADGHG_TRADGHG_SIZE_SMOD

IMPLICIT NONE

CONTAINS

MODULE FUNCTION ACDC_SIZE_TRADGHG (SELF, CDPATH, LDPRINT) RESULT (KSIZE)

IMPLICIT NONE
CLASS (TRADGHG),     INTENT (IN), TARGET :: SELF
CHARACTER(LEN=*), INTENT (IN), OPTIONAL :: CDPATH
LOGICAL,          INTENT (IN), OPTIONAL :: LDPRINT
INTEGER*8 :: KSIZE
INTEGER*8 :: ISIZE, JSIZE
LOGICAL :: LLPRINT
CHARACTER(LEN=128) :: CLPATH
LOGICAL :: LMASS_CCL4, LMASS_CFC11, LMASS_CFC12, LMASS_CH4, LMASS_CO2, LMASS_HCFC22, LMASS_N2O, LMASS_NO2, LMASS_O3, LPRESSURE_HL
LOGICAL :: LSINLAT
LLPRINT = .FALSE.
IF (PRESENT (LDPRINT)) THEN
  LLPRINT = LDPRINT
ENDIF
CLPATH=''
IF (PRESENT (CDPATH)) THEN
  CLPATH = CDPATH
ENDIF
KSIZE = 0
LSINLAT = ALLOCATED (SELF%SINLAT)
IF (LSINLAT) THEN
  ISIZE = KIND (SELF%SINLAT) * SIZE (SELF%SINLAT)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%SINLAT'
  ENDIF
  KSIZE = KSIZE + ISIZE
ENDIF
LPRESSURE_HL = ALLOCATED (SELF%PRESSURE_HL)
IF (LPRESSURE_HL) THEN
  ISIZE = KIND (SELF%PRESSURE_HL) * SIZE (SELF%PRESSURE_HL)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%PRESSURE_HL'
  ENDIF
  KSIZE = KSIZE + ISIZE
ENDIF
LMASS_CH4 = ALLOCATED (SELF%MASS_CH4)
IF (LMASS_CH4) THEN
  ISIZE = KIND (SELF%MASS_CH4) * SIZE (SELF%MASS_CH4)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%MASS_CH4'
  ENDIF
  KSIZE = KSIZE + ISIZE
ENDIF
LMASS_CO2 = ALLOCATED (SELF%MASS_CO2)
IF (LMASS_CO2) THEN
  ISIZE = KIND (SELF%MASS_CO2) * SIZE (SELF%MASS_CO2)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%MASS_CO2'
  ENDIF
  KSIZE = KSIZE + ISIZE
ENDIF
LMASS_N2O = ALLOCATED (SELF%MASS_N2O)
IF (LMASS_N2O) THEN
  ISIZE = KIND (SELF%MASS_N2O) * SIZE (SELF%MASS_N2O)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%MASS_N2O'
  ENDIF
  KSIZE = KSIZE + ISIZE
ENDIF
LMASS_NO2 = ALLOCATED (SELF%MASS_NO2)
IF (LMASS_NO2) THEN
  ISIZE = KIND (SELF%MASS_NO2) * SIZE (SELF%MASS_NO2)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%MASS_NO2'
  ENDIF
  KSIZE = KSIZE + ISIZE
ENDIF
LMASS_CFC11 = ALLOCATED (SELF%MASS_CFC11)
IF (LMASS_CFC11) THEN
  ISIZE = KIND (SELF%MASS_CFC11) * SIZE (SELF%MASS_CFC11)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%MASS_CFC11'
  ENDIF
  KSIZE = KSIZE + ISIZE
ENDIF
LMASS_CFC12 = ALLOCATED (SELF%MASS_CFC12)
IF (LMASS_CFC12) THEN
  ISIZE = KIND (SELF%MASS_CFC12) * SIZE (SELF%MASS_CFC12)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%MASS_CFC12'
  ENDIF
  KSIZE = KSIZE + ISIZE
ENDIF
LMASS_CCL4 = ALLOCATED (SELF%MASS_CCL4)
IF (LMASS_CCL4) THEN
  ISIZE = KIND (SELF%MASS_CCL4) * SIZE (SELF%MASS_CCL4)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%MASS_CCL4'
  ENDIF
  KSIZE = KSIZE + ISIZE
ENDIF
LMASS_HCFC22 = ALLOCATED (SELF%MASS_HCFC22)
IF (LMASS_HCFC22) THEN
  ISIZE = KIND (SELF%MASS_HCFC22) * SIZE (SELF%MASS_HCFC22)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%MASS_HCFC22'
  ENDIF
  KSIZE = KSIZE + ISIZE
ENDIF
LMASS_O3 = ALLOCATED (SELF%MASS_O3)
IF (LMASS_O3) THEN
  ISIZE = KIND (SELF%MASS_O3) * SIZE (SELF%MASS_O3)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%MASS_O3'
  ENDIF
  KSIZE = KSIZE + ISIZE
ENDIF
ISIZE = KIND (SELF%NLATITUDE)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%NLATITUDE'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%NPRESSURE)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%NPRESSURE'
ENDIF
KSIZE = KSIZE + ISIZE
END FUNCTION

