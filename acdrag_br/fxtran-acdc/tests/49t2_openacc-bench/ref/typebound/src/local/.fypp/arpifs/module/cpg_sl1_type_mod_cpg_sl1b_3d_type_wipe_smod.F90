SUBMODULE (CPG_SL1_TYPE_MOD) CPG_SL1_TYPE_MOD_CPG_SL1B_3D_TYPE_WIPE_SMOD

IMPLICIT NONE

CONTAINS

MODULE SUBROUTINE ACDC_WIPE_CPG_SL1B_3D_TYPE (SELF, LDDELETED, LDFIELDAPI)
USE FIELD_UTIL_MODULE
IMPLICIT NONE
CLASS (CPG_SL1B_3D_TYPE), INTENT (IN), TARGET :: SELF
LOGICAL, OPTIONAL, INTENT (IN) :: LDDELETED, LDFIELDAPI
LOGICAL :: LLDELETED
LOGICAL :: LLFIELDAPI
LOGICAL :: LF_P, LF_P_F, LF_P_P, LF_P_SP, LF_P_SPF
LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF
LF_P = ASSOCIATED (SELF%F_P)
IF (LF_P .AND. LLFIELDAPI) THEN
  CALL WIPE_FIELD_3RB (SELF%F_P)
ENDIF

LF_P_F = ASSOCIATED (SELF%F_P_F)
IF (LF_P_F .AND. LLFIELDAPI) THEN
  CALL WIPE_FIELD_3RB (SELF%F_P_F)
ENDIF

LF_P_SPF = ASSOCIATED (SELF%F_P_SPF)
IF (LF_P_SPF .AND. LLFIELDAPI) THEN
  CALL WIPE_FIELD_3RB (SELF%F_P_SPF)
ENDIF

LF_P_SP = ASSOCIATED (SELF%F_P_SP)
IF (LF_P_SP .AND. LLFIELDAPI) THEN
  CALL WIPE_FIELD_3RB (SELF%F_P_SP)
ENDIF

LF_P_P = ASSOCIATED (SELF%F_P_P)
IF (LF_P_P .AND. LLFIELDAPI) THEN
  CALL WIPE_FIELD_3RB (SELF%F_P_P)
ENDIF

CALL SELF%YCOMP%ACDC_WIPE (LDDELETED=.TRUE., LDFIELDAPI=LDFIELDAPI)

LLDELETED = .FALSE.
IF (PRESENT (LDDELETED)) THEN
  LLDELETED = LDDELETED
ENDIF
IF (.NOT. LLDELETED) THEN
  !$ACC EXIT DATA DELETE (SELF)
ENDIF
END SUBROUTINE

END SUBMODULE
