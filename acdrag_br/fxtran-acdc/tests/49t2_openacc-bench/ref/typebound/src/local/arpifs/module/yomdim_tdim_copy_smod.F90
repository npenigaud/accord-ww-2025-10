MODULE SUBROUTINE ACDC_COPY_TDIM (SELF, LDCREATED, LDFIELDAPI)

IMPLICIT NONE
CLASS (TDIM), INTENT (IN), TARGET :: SELF
LOGICAL, OPTIONAL, INTENT (IN) :: LDCREATED, LDFIELDAPI
LOGICAL :: LLCREATED
LOGICAL :: LLFIELDAPI
LOGICAL :: LNDLUNL, LNDLUXL
LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF
LLCREATED = .FALSE.
IF (PRESENT (LDCREATED)) THEN
  LLCREATED = LDCREATED
ENDIF
IF (.NOT. LLCREATED) THEN
  !$ACC ENTER DATA CREATE (SELF)
  !$ACC UPDATE DEVICE (SELF)
ENDIF

LNDLUNL = ALLOCATED (SELF%NDLUNL)
IF (LNDLUNL) THEN
  !$ACC ENTER DATA CREATE (SELF%NDLUNL)
  !$ACC UPDATE DEVICE (SELF%NDLUNL)
  !$ACC ENTER DATA ATTACH (SELF%NDLUNL)
ENDIF

LNDLUXL = ALLOCATED (SELF%NDLUXL)
IF (LNDLUXL) THEN
  !$ACC ENTER DATA CREATE (SELF%NDLUXL)
  !$ACC UPDATE DEVICE (SELF%NDLUXL)
  !$ACC ENTER DATA ATTACH (SELF%NDLUXL)
ENDIF

END SUBROUTINE

END SUBMODULE
