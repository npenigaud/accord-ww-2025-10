SUBMODULE (YOEAERLID) YOEAERLID_TEAERLID_LOAD_SMOD

IMPLICIT NONE

CONTAINS

MODULE SUBROUTINE ACDC_LOAD_TEAERLID (SELF, KLUN)
USE PARKIND1, ONLY : JPRD
IMPLICIT NONE
CLASS (TEAERLID), INTENT (OUT), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
REAL(KIND=JPRD) :: ZTMP0
REAL(KIND=JPRD), ALLOCATABLE :: ZTMP1 (:)
REAL(KIND=JPRD), ALLOCATABLE :: ZTMP2 (:,:)
READ (KLUN) SELF%NAELID
READ (KLUN) SELF%NWLID
READ (KLUN) SELF%NAERLISI
READ (KLUN) SELF%JWLID
ALLOCATE (ZTMP1(LBOUND(SELF%RWLID,1):UBOUND(SELF%RWLID,1)))
READ (KLUN) ZTMP1
SELF%RWLID = ZTMP1
DEALLOCATE (ZTMP1)
ALLOCATE (ZTMP1(LBOUND(SELF%RLICO2,1):UBOUND(SELF%RLICO2,1)))
READ (KLUN) ZTMP1
SELF%RLICO2 = ZTMP1
DEALLOCATE (ZTMP1)
ALLOCATE (ZTMP1(LBOUND(SELF%RLIO2,1):UBOUND(SELF%RLIO2,1)))
READ (KLUN) ZTMP1
SELF%RLIO2 = ZTMP1
DEALLOCATE (ZTMP1)
ALLOCATE (ZTMP2(LBOUND(SELF%RLINO2,1):UBOUND(SELF%RLINO2,1), LBOUND(SELF%RLINO2,2):UBOUND(SELF%RLINO2,2)))
READ (KLUN) ZTMP2
SELF%RLINO2 = ZTMP2
DEALLOCATE (ZTMP2)
ALLOCATE (ZTMP2(LBOUND(SELF%RLIO3,1):UBOUND(SELF%RLIO3,1), LBOUND(SELF%RLIO3,2):UBOUND(SELF%RLIO3,2)))
READ (KLUN) ZTMP2
SELF%RLIO3 = ZTMP2
DEALLOCATE (ZTMP2)
READ (KLUN) ZTMP0
SELF%RLICLS = ZTMP0
READ (KLUN) ZTMP0
SELF%RLIDELT = ZTMP0
READ (KLUN) ZTMP0
SELF%RLINS = ZTMP0
READ (KLUN) ZTMP0
SELF%RLIPREF = ZTMP0
READ (KLUN) ZTMP0
SELF%RLITREF = ZTMP0
READ (KLUN) ZTMP0
SELF%RLIT0 = ZTMP0
READ (KLUN) ZTMP0
SELF%RLIDDZ = ZTMP0
END SUBROUTINE

END SUBMODULE
