SUBMODULE (YOEAERVOL) YOEAERVOL_TEAERVOL_LOAD_SMOD

IMPLICIT NONE

CONTAINS

MODULE SUBROUTINE ACDC_LOAD_TEAERVOL (SELF, KLUN)
USE PARKIND1, ONLY : JPRD
IMPLICIT NONE
CLASS (TEAERVOL), INTENT (OUT), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
REAL(KIND=JPRD) :: ZTMP0
REAL(KIND=JPRD), ALLOCATABLE :: ZTMP1 (:)
REAL(KIND=JPRD), ALLOCATABLE :: ZTMP2 (:,:)
READ (KLUN) SELF%NAERVOLC
READ (KLUN) SELF%NAERVOLE
READ (KLUN) SELF%NINTERPT
READ (KLUN) SELF%NTVOLC
READ (KLUN) SELF%NVOLERUP
READ (KLUN) SELF%NVOLHOMO
READ (KLUN) SELF%NVOLOPTP
READ (KLUN) SELF%NVOLERUZ
READ (KLUN) SELF%NVOLDATS
READ (KLUN) SELF%NVOLDATE
ALLOCATE (ZTMP2(LBOUND(SELF%RAERVOLC,1):UBOUND(SELF%RAERVOLC,1), LBOUND(SELF%RAERVOLC,2):UBOUND(SELF%RAERVOLC,2)))
READ (KLUN) ZTMP2
SELF%RAERVOLC = ZTMP2
DEALLOCATE (ZTMP2)
ALLOCATE (ZTMP2(LBOUND(SELF%RAERVOLE,1):UBOUND(SELF%RAERVOLE,1), LBOUND(SELF%RAERVOLE,2):UBOUND(SELF%RAERVOLE,2)))
READ (KLUN) ZTMP2
SELF%RAERVOLE = ZTMP2
DEALLOCATE (ZTMP2)
ALLOCATE (ZTMP1(LBOUND(SELF%RVOLERUZ,1):UBOUND(SELF%RVOLERUZ,1)))
READ (KLUN) ZTMP1
SELF%RVOLERUZ = ZTMP1
DEALLOCATE (ZTMP1)
READ (KLUN) ZTMP0
SELF%RVSEDVOL = ZTMP0
READ (KLUN) ZTMP0
SELF%RVDEPVOL = ZTMP0
READ (KLUN) ZTMP0
SELF%RWDEPVOL = ZTMP0
END SUBROUTINE

END SUBMODULE
