MODULE YOECND

!$ACDC methods 


USE PARKIND1  ,ONLY : JPRB

IMPLICIT NONE

SAVE

!     -----------------------------------------------------------------

TYPE :: TECND
REAL(KIND=JPRB) :: REPFLM
REAL(KIND=JPRB) :: REPQMI
!----------------------------------------------------------------------------
CONTAINS
  PROCEDURE, PASS :: PRINT => PRINT_CONFIGURATION 
PROCEDURE :: ACDC_COPY => ACDC_COPY_TECND
PROCEDURE :: ACDC_CRC64 => ACDC_CRC64_TECND
PROCEDURE :: ACDC_HOST => ACDC_HOST_TECND
PROCEDURE :: ACDC_LOAD => ACDC_LOAD_TECND
PROCEDURE :: ACDC_SAVE => ACDC_SAVE_TECND
PROCEDURE :: ACDC_SIZE => ACDC_SIZE_TECND
PROCEDURE :: ACDC_WIPE => ACDC_WIPE_TECND
END TYPE TECND
!============================================================================

!!TYPE(TECND), POINTER :: YRECND => NULL()

!     -----------------------------------------------------------------
!*    CONTROL PARAMETERS FOR MOIST PROCESSES

! REPFLM :  Minimum flux to avoid zero division in ice proportion
!           computations
! REPQMI :  Minimum specific humidity (security within QNEGAT)
!     -----------------------------------------------------------------

INTERFACE

MODULE SUBROUTINE ACDC_COPY_TECND (SELF, LDCREATED, LDFIELDAPI)

IMPLICIT NONE
CLASS (TECND), INTENT (IN), TARGET :: SELF
LOGICAL, OPTIONAL, INTENT (IN) :: LDCREATED, LDFIELDAPI
END SUBROUTINE

MODULE SUBROUTINE ACDC_CRC64_TECND (SELF, KLUN, CDPATH)
USE FXTRAN_ACDC_CRC64_INTRINSIC_MOD, ONLY : FCRC64 => FXTRAN_ACDC_CRC64
IMPLICIT NONE
CLASS (TECND), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
CHARACTER(LEN=*), INTENT (IN) :: CDPATH
END SUBROUTINE

MODULE SUBROUTINE ACDC_HOST_TECND (SELF)

IMPLICIT NONE
CLASS (TECND), TARGET :: SELF
END SUBROUTINE

MODULE SUBROUTINE ACDC_LOAD_TECND (SELF, KLUN)
USE PARKIND1, ONLY : JPRD
IMPLICIT NONE
CLASS (TECND), INTENT (OUT), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
END SUBROUTINE

MODULE SUBROUTINE ACDC_SAVE_TECND (SELF, KLUN)

IMPLICIT NONE
CLASS (TECND), INTENT (IN), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
END SUBROUTINE

MODULE FUNCTION ACDC_SIZE_TECND (SELF, CDPATH, LDPRINT) RESULT (KSIZE)

IMPLICIT NONE
CLASS (TECND),     INTENT (IN), TARGET :: SELF
CHARACTER(LEN=*), INTENT (IN), OPTIONAL :: CDPATH
LOGICAL,          INTENT (IN), OPTIONAL :: LDPRINT
INTEGER*8 :: KSIZE
END FUNCTION

MODULE SUBROUTINE ACDC_WIPE_TECND (SELF, LDDELETED, LDFIELDAPI)

IMPLICIT NONE
CLASS (TECND), INTENT (IN), TARGET :: SELF
LOGICAL, OPTIONAL, INTENT (IN) :: LDDELETED, LDFIELDAPI
END SUBROUTINE


END INTERFACE

CONTAINS

SUBROUTINE PRINT_CONFIGURATION(SELF, KDEPTH, KOUTNO)
  IMPLICIT NONE
  CLASS(TECND), INTENT(IN) :: SELF
  INTEGER     , INTENT(IN) :: KDEPTH
  INTEGER     , INTENT(IN) :: KOUTNO

  INTEGER :: IDEPTHLOC

  IDEPTHLOC = KDEPTH+2
  
  WRITE(KOUTNO,*) REPEAT(' ',KDEPTH   ) // 'model%yrml_phy_ec%yrecnd : '
  WRITE(KOUTNO,*) REPEAT(' ',IDEPTHLOC) // 'REPFLM = ',SELF%REPFLM
  WRITE(KOUTNO,*) REPEAT(' ',IDEPTHLOC) // 'REPQMI = ',SELF%REPQMI
 
END SUBROUTINE PRINT_CONFIGURATION

END MODULE YOECND
