SUBMODULE (TYPE_MODEL) TYPE_MODEL_MODEL_SAVE_SMOD

IMPLICIT NONE

CONTAINS

MODULE SUBROUTINE ACDC_SAVE_MODEL (SELF, KLUN)

IMPLICIT NONE
CLASS (MODEL), INTENT (IN), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
LOGICAL :: LYRCST
WRITE (KLUN) SELF%LINEAR_MODEL
CALL SELF%YRML_GCONF%ACDC_SAVE (KLUN)
CALL SELF%YRML_AOC%ACDC_SAVE (KLUN)
CALL SELF%YRML_DYN%ACDC_SAVE (KLUN)
CALL SELF%YRML_PHY_G%ACDC_SAVE (KLUN)
CALL SELF%YRML_PHY_EC%ACDC_SAVE (KLUN)
CALL SELF%YRML_PHY_SLIN%ACDC_SAVE (KLUN)
CALL SELF%YRML_PHY_AER%ACDC_SAVE (KLUN)
CALL SELF%YRML_PHY_RAD%ACDC_SAVE (KLUN)
CALL SELF%YRML_PHY_STOCH%ACDC_SAVE (KLUN)
CALL SELF%YRML_PHY_MF%ACDC_SAVE (KLUN)
CALL SELF%YRML_CHEM%ACDC_SAVE (KLUN)
CALL SELF%YRML_DIAG%ACDC_SAVE (KLUN)
CALL SELF%YREWCOU%ACDC_SAVE (KLUN)
LYRCST = ASSOCIATED (SELF%YRCST)
WRITE (KLUN) LYRCST
IF (LYRCST) THEN
  CALL SELF%YRCST%ACDC_SAVE (KLUN)
ENDIF
CALL SELF%YRML_LBC%ACDC_SAVE (KLUN)
WRITE (KLUN) SELF%MOBJECT_ID
WRITE (KLUN) SELF%COBJECT_ID
END SUBROUTINE

END SUBMODULE
