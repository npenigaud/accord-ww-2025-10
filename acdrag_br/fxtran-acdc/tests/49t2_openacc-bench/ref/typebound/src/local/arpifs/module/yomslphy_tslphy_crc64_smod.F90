MODULE SUBROUTINE ACDC_CRC64_TSLPHY (SELF, KLUN, CDPATH)
USE FXTRAN_ACDC_CRC64_INTRINSIC_MOD, ONLY : FCRC64 => FXTRAN_ACDC_CRC64
IMPLICIT NONE
CLASS (TSLPHY), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
CHARACTER(LEN=*), INTENT (IN) :: CDPATH
CHARACTER(LEN=128) :: CLIND
LOGICAL :: LRSLWX, LSAVTEND, LSAVTEND5
WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (SELF%RSLWX), CDPATH//'%RSLWX'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (SELF%NVTEND), CDPATH//'%NVTEND'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (SELF%SAVTEND), CDPATH//'%SAVTEND'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (SELF%SAVTEND5), CDPATH//'%SAVTEND5'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (SELF%MU_SAVTEND), CDPATH//'%MU_SAVTEND'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (SELF%MV_SAVTEND), CDPATH//'%MV_SAVTEND'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (SELF%MT_SAVTEND), CDPATH//'%MT_SAVTEND'

END SUBROUTINE

