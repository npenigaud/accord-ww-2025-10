SUBMODULE (YOEAERC) YOEAERC_TEAERC_TEGEN_SAVE_SMOD

IMPLICIT NONE

CONTAINS

MODULE SUBROUTINE ACDC_SAVE_TEAERC_TEGEN (SELF, KLUN)

IMPLICIT NONE
CLASS (TEAERC_TEGEN), INTENT (IN), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
LOGICAL :: LRAERBC, LRAEROR, LRAERSD, LRAERSS, LRAERSU, LRAERV, LRLATV
WRITE (KLUN) SELF%REPAER
LRAERBC = ASSOCIATED (SELF%RAERBC)
WRITE (KLUN) LRAERBC
IF (LRAERBC) THEN
  WRITE (KLUN) LBOUND (SELF%RAERBC)
  WRITE (KLUN) UBOUND (SELF%RAERBC)
  WRITE (KLUN) SELF%RAERBC
ENDIF
LRAEROR = ASSOCIATED (SELF%RAEROR)
WRITE (KLUN) LRAEROR
IF (LRAEROR) THEN
  WRITE (KLUN) LBOUND (SELF%RAEROR)
  WRITE (KLUN) UBOUND (SELF%RAEROR)
  WRITE (KLUN) SELF%RAEROR
ENDIF
LRAERSD = ASSOCIATED (SELF%RAERSD)
WRITE (KLUN) LRAERSD
IF (LRAERSD) THEN
  WRITE (KLUN) LBOUND (SELF%RAERSD)
  WRITE (KLUN) UBOUND (SELF%RAERSD)
  WRITE (KLUN) SELF%RAERSD
ENDIF
LRAERSS = ASSOCIATED (SELF%RAERSS)
WRITE (KLUN) LRAERSS
IF (LRAERSS) THEN
  WRITE (KLUN) LBOUND (SELF%RAERSS)
  WRITE (KLUN) UBOUND (SELF%RAERSS)
  WRITE (KLUN) SELF%RAERSS
ENDIF
LRAERSU = ASSOCIATED (SELF%RAERSU)
WRITE (KLUN) LRAERSU
IF (LRAERSU) THEN
  WRITE (KLUN) LBOUND (SELF%RAERSU)
  WRITE (KLUN) UBOUND (SELF%RAERSU)
  WRITE (KLUN) SELF%RAERSU
ENDIF
LRAERV = ASSOCIATED (SELF%RAERV)
WRITE (KLUN) LRAERV
IF (LRAERV) THEN
  WRITE (KLUN) LBOUND (SELF%RAERV)
  WRITE (KLUN) UBOUND (SELF%RAERV)
  WRITE (KLUN) SELF%RAERV
ENDIF
LRLATV = ASSOCIATED (SELF%RLATV)
WRITE (KLUN) LRLATV
IF (LRLATV) THEN
  WRITE (KLUN) LBOUND (SELF%RLATV)
  WRITE (KLUN) UBOUND (SELF%RLATV)
  WRITE (KLUN) SELF%RLATV
ENDIF
WRITE (KLUN) SELF%RTAEBC
WRITE (KLUN) SELF%RTAEOR
WRITE (KLUN) SELF%RTAESD
WRITE (KLUN) SELF%RTAESS
WRITE (KLUN) SELF%RTAESU
WRITE (KLUN) SELF%RTAEVO
WRITE (KLUN) SELF%ISYEAR
WRITE (KLUN) SELF%IEYEAR
WRITE (KLUN) SELF%IS_INITIALISED
END SUBROUTINE

END SUBMODULE
