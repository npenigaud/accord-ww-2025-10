SUBMODULE (YOMCOMPO) YOMCOMPO_TCOMPO_CRC64_SMOD

IMPLICIT NONE

CONTAINS

MODULE SUBROUTINE ACDC_CRC64_TCOMPO (SELF, KLUN, CDPATH)
USE FXTRAN_ACDC_CRC64_INTRINSIC_MOD, ONLY : FCRC64 => FXTRAN_ACDC_CRC64
IMPLICIT NONE
CLASS (TCOMPO), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
CHARACTER(LEN=*), INTENT (IN) :: CDPATH
CHARACTER(LEN=128) :: CLIND
INTEGER :: J1
LOGICAL :: LYEMIS2DAUX_DESC, LYEMIS2D_DESC, LYEMIS3D_DESC
WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (SELF%LCHEM_DIA), CDPATH//'%LCHEM_DIA'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (SELF%RCHEM_DIA_PERIOD), CDPATH//'%RCHEM_DIA_PERIOD'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (SELF%LCHEM_DDFLX), CDPATH//'%LCHEM_DDFLX'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (SELF%LCOMPO_DDFLX_DIR), CDPATH//'%LCOMPO_DDFLX_DIR'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (SELF%LCHEM_TROPO), CDPATH//'%LCHEM_TROPO'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (SELF%LAEREQSAM4CLIM), CDPATH//'%LAEREQSAM4CLIM'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (SELF%LAEROSFC), CDPATH//'%LAEROSFC'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (SELF%LCOMPO_DCDD), CDPATH//'%LCOMPO_DCDD'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (SELF%LAERNITRATE), CDPATH//'%LAERNITRATE'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (SELF%LAERRESUSPENSION), CDPATH//'%LAERRESUSPENSION'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (SELF%LAERSOA), CDPATH//'%LAERSOA'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (SELF%LAERSOA_COUPLED), CDPATH//'%LAERSOA_COUPLED'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (SELF%KGHG_CHEMTEND_CH4), CDPATH//'%KGHG_CHEMTEND_CH4'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (SELF%LAERNUCL), CDPATH//'%LAERNUCL'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (SELF%AERO_SCHEME), CDPATH//'%AERO_SCHEME'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (SELF%NEMIS2D_DESC), CDPATH//'%NEMIS2D_DESC'

LYEMIS2D_DESC = ALLOCATED (SELF%YEMIS2D_DESC)
IF (LYEMIS2D_DESC) THEN
  DO J1 = LBOUND (SELF%YEMIS2D_DESC, 1), UBOUND (SELF%YEMIS2D_DESC, 1)
    WRITE (CLIND, '("(",I0,")")') J1
    CALL SELF%YEMIS2D_DESC (J1)%ACDC_CRC64 (KLUN, CDPATH//'%YEMIS2D_DESC'//TRIM(CLIND))
  ENDDO
ENDIF

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (SELF%NEMIS3D_DESC), CDPATH//'%NEMIS3D_DESC'

LYEMIS3D_DESC = ALLOCATED (SELF%YEMIS3D_DESC)
IF (LYEMIS3D_DESC) THEN
  DO J1 = LBOUND (SELF%YEMIS3D_DESC, 1), UBOUND (SELF%YEMIS3D_DESC, 1)
    WRITE (CLIND, '("(",I0,")")') J1
    CALL SELF%YEMIS3D_DESC (J1)%ACDC_CRC64 (KLUN, CDPATH//'%YEMIS3D_DESC'//TRIM(CLIND))
  ENDDO
ENDIF

LYEMIS2DAUX_DESC = ALLOCATED (SELF%YEMIS2DAUX_DESC)
IF (LYEMIS2DAUX_DESC) THEN
  DO J1 = LBOUND (SELF%YEMIS2DAUX_DESC, 1), UBOUND (SELF%YEMIS2DAUX_DESC, 1)
    WRITE (CLIND, '("(",I0,")")') J1
    CALL SELF%YEMIS2DAUX_DESC (J1)%ACDC_CRC64 (KLUN, CDPATH//'%YEMIS2DAUX_DESC'//TRIM(CLIND))
  ENDDO
ENDIF

END SUBROUTINE

END SUBMODULE
