SUBMODULE (YEMLBC_GEO) YEMLBC_GEO_TELBC_GEO_SAVE_SMOD

IMPLICIT NONE

CONTAINS

MODULE SUBROUTINE ACDC_SAVE_TELBC_GEO (SELF, KLUN)

IMPLICIT NONE
CLASS (TELBC_GEO), INTENT (IN), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
LOGICAL :: LMPTRCPLBLK, LNIND_LEN, LNIND_LIST
LNIND_LIST = ALLOCATED (SELF%NIND_LIST)
WRITE (KLUN) LNIND_LIST
IF (LNIND_LIST) THEN
  WRITE (KLUN) LBOUND (SELF%NIND_LIST)
  WRITE (KLUN) UBOUND (SELF%NIND_LIST)
  WRITE (KLUN) SELF%NIND_LIST
ENDIF
LNIND_LEN = ALLOCATED (SELF%NIND_LEN)
WRITE (KLUN) LNIND_LEN
IF (LNIND_LEN) THEN
  WRITE (KLUN) LBOUND (SELF%NIND_LEN)
  WRITE (KLUN) UBOUND (SELF%NIND_LEN)
  WRITE (KLUN) SELF%NIND_LEN
ENDIF
WRITE (KLUN) SELF%NCPLBLKS
LMPTRCPLBLK = ALLOCATED (SELF%MPTRCPLBLK)
WRITE (KLUN) LMPTRCPLBLK
IF (LMPTRCPLBLK) THEN
  WRITE (KLUN) LBOUND (SELF%MPTRCPLBLK)
  WRITE (KLUN) UBOUND (SELF%MPTRCPLBLK)
  WRITE (KLUN) SELF%MPTRCPLBLK
ENDIF
END SUBROUTINE

END SUBMODULE
