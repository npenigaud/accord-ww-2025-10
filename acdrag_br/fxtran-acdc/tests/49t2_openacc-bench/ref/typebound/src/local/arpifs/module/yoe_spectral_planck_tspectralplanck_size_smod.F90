SUBMODULE (YOE_SPECTRAL_PLANCK) YOE_SPECTRAL_PLANCK_TSPECTRALPLANCK_SIZE_SMOD

IMPLICIT NONE

CONTAINS

MODULE FUNCTION ACDC_SIZE_TSPECTRALPLANCK (SELF, CDPATH, LDPRINT) RESULT (KSIZE)

IMPLICIT NONE
CLASS (TSPECTRALPLANCK),     INTENT (IN), TARGET :: SELF
CHARACTER(LEN=*), INTENT (IN), OPTIONAL :: CDPATH
LOGICAL,          INTENT (IN), OPTIONAL :: LDPRINT
INTEGER*8 :: KSIZE
INTEGER*8 :: ISIZE, JSIZE
LOGICAL :: LLPRINT
CHARACTER(LEN=128) :: CLPATH
LOGICAL :: LINTERVAL_MAP, LPLANCK_LUT, LWAVLEN_BOUND
LLPRINT = .FALSE.
IF (PRESENT (LDPRINT)) THEN
  LLPRINT = LDPRINT
ENDIF
CLPATH=''
IF (PRESENT (CDPATH)) THEN
  CLPATH = CDPATH
ENDIF
KSIZE = 0
ISIZE = KIND (SELF%NINTERVALS)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%NINTERVALS'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%NTEMPS)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%NTEMPS'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%TEMP1)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%TEMP1'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%DTEMP)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%DTEMP'
ENDIF
KSIZE = KSIZE + ISIZE
LPLANCK_LUT = ALLOCATED (SELF%PLANCK_LUT)
IF (LPLANCK_LUT) THEN
  ISIZE = KIND (SELF%PLANCK_LUT) * SIZE (SELF%PLANCK_LUT)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%PLANCK_LUT'
  ENDIF
  KSIZE = KSIZE + ISIZE
ENDIF
LWAVLEN_BOUND = ALLOCATED (SELF%WAVLEN_BOUND)
IF (LWAVLEN_BOUND) THEN
  ISIZE = KIND (SELF%WAVLEN_BOUND) * SIZE (SELF%WAVLEN_BOUND)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%WAVLEN_BOUND'
  ENDIF
  KSIZE = KSIZE + ISIZE
ENDIF
LINTERVAL_MAP = ALLOCATED (SELF%INTERVAL_MAP)
IF (LINTERVAL_MAP) THEN
  ISIZE = KIND (SELF%INTERVAL_MAP) * SIZE (SELF%INTERVAL_MAP)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%INTERVAL_MAP'
  ENDIF
  KSIZE = KSIZE + ISIZE
ENDIF
END FUNCTION

END SUBMODULE
