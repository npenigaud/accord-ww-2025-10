SUBMODULE (YOECLD) YOECLD_TECLD_SAVE_SMOD

IMPLICIT NONE

CONTAINS

MODULE SUBROUTINE ACDC_SAVE_TECLD (SELF, KLUN)

IMPLICIT NONE
CLASS (TECLD), INTENT (IN), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
LOGICAL :: LCETA
LCETA = ALLOCATED (SELF%CETA)
WRITE (KLUN) LCETA
IF (LCETA) THEN
  WRITE (KLUN) LBOUND (SELF%CETA)
  WRITE (KLUN) UBOUND (SELF%CETA)
  WRITE (KLUN) SELF%CETA
ENDIF
WRITE (KLUN) SELF%RANVA
WRITE (KLUN) SELF%RANVB
WRITE (KLUN) SELF%RANVH
WRITE (KLUN) SELF%RCCA
WRITE (KLUN) SELF%RCCB
WRITE (KLUN) SELF%RCCC
WRITE (KLUN) SELF%RCFCT
WRITE (KLUN) SELF%RCLWMR
WRITE (KLUN) SELF%RCSCAL
WRITE (KLUN) SELF%RDECORR_CF
WRITE (KLUN) SELF%RDECORR_CW
WRITE (KLUN) SELF%RETAHB
WRITE (KLUN) SELF%RETAMB
WRITE (KLUN) SELF%RLOIA
WRITE (KLUN) SELF%RLOIB
WRITE (KLUN) SELF%RLOIC
WRITE (KLUN) SELF%RLOID
WRITE (KLUN) SELF%RLONIA
WRITE (KLUN) SELF%RLONIB
WRITE (KLUN) SELF%RRHH
WRITE (KLUN) SELF%RRHL
WRITE (KLUN) SELF%RGAMMAS
WRITE (KLUN) SELF%REPSCR
WRITE (KLUN) SELF%REPSEC
WRITE (KLUN) SELF%LOMEGA
END SUBROUTINE

END SUBMODULE
