SUBMODULE (TYPE_ECV) TYPE_ECV_TECV_CONFIG_HOST_SMOD

IMPLICIT NONE

CONTAINS

MODULE SUBROUTINE ACDC_HOST_TECV_CONFIG (SELF)

IMPLICIT NONE
CLASS (TECV_CONFIG), TARGET :: SELF
INTEGER :: J1
LOGICAL :: LYRCONFIG

LYRCONFIG = ALLOCATED (SELF%YRCONFIG)
IF (LYRCONFIG) THEN
  DO J1 = LBOUND (SELF%YRCONFIG, 1), UBOUND (SELF%YRCONFIG, 1)
    CALL SELF%YRCONFIG (J1)%ACDC_HOST ()
  ENDDO
ENDIF

END SUBROUTINE

END SUBMODULE
