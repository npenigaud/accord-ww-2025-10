MODULE MODEL_ATMOS_OCEAN_COUPLING_MOD

!$ACDC methods 

  USE YOMMCC, ONLY : TMCC
  USE YOMCOM, ONLY : TCOM
  USE YOMCOU, ONLY : TCOU
  IMPLICIT NONE

  TYPE MODEL_ATMOS_OCEAN_COUPLING_TYPE

    TYPE(TMCC)  :: YRMCC
    TYPE(TCOM)  :: YRCOM
    TYPE(TCOU)  :: YRCOU

    CONTAINS

    PROCEDURE, PASS :: PRINT => PRINT_CONFIGURATION 
    
  PROCEDURE :: ACDC_COPY => ACDC_COPY_MODEL_ATMOS_OCEAN_COUPLING_TYPE
PROCEDURE :: ACDC_CRC64 => ACDC_CRC64_MODEL_ATMOS_OCEAN_COUPLING_TYPE
PROCEDURE :: ACDC_HOST => ACDC_HOST_MODEL_ATMOS_OCEAN_COUPLING_TYPE
