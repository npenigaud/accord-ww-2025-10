PROCEDURE :: ACDC_SAVE => ACDC_SAVE_MODEL_ATMOS_OCEAN_COUPLING_TYPE
PROCEDURE :: ACDC_SIZE => ACDC_SIZE_MODEL_ATMOS_OCEAN_COUPLING_TYPE
PROCEDURE :: ACDC_WIPE => ACDC_WIPE_MODEL_ATMOS_OCEAN_COUPLING_TYPE
END TYPE MODEL_ATMOS_OCEAN_COUPLING_TYPE

  !---------------------------------------------------------------------

  INTERFACE

