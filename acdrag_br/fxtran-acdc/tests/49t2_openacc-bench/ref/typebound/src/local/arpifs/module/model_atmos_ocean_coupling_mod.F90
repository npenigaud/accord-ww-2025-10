INTEGER, INTENT (IN) :: KLUN
CHARACTER(LEN=*), INTENT (IN) :: CDPATH
END SUBROUTINE

MODULE SUBROUTINE ACDC_HOST_MODEL_ATMOS_OCEAN_COUPLING_TYPE (SELF)

IMPLICIT NONE
CLASS (MODEL_ATMOS_OCEAN_COUPLING_TYPE), TARGET :: SELF
END SUBROUTINE

MODULE SUBROUTINE ACDC_LOAD_MODEL_ATMOS_OCEAN_COUPLING_TYPE (SELF, KLUN)

