SUBMODULE (TYPE_GEOMETRY) TYPE_GEOMETRY_GEOMETRY_CRC64_SMOD

IMPLICIT NONE

CONTAINS

MODULE SUBROUTINE ACDC_CRC64_GEOMETRY (SELF, KLUN, CDPATH)
USE FXTRAN_ACDC_CRC64_INTRINSIC_MOD, ONLY : FCRC64 => FXTRAN_ACDC_CRC64
IMPLICIT NONE
CLASS (GEOMETRY), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
CHARACTER(LEN=*), INTENT (IN) :: CDPATH
CHARACTER(LEN=128) :: CLIND
LOGICAL :: LYRELBC_GEO
WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (SELF%LNHX_GEOM), CDPATH//'%LNHX_GEOM'

CLIND = ''
CALL SELF%YRVERT_GEOM%ACDC_CRC64 (KLUN, CDPATH//'%YRVERT_GEOM'//TRIM(CLIND))

CLIND = ''
CALL SELF%YRSTA%ACDC_CRC64 (KLUN, CDPATH//'%YRSTA'//TRIM(CLIND))

CLIND = ''
CALL SELF%YRLAP%ACDC_CRC64 (KLUN, CDPATH//'%YRLAP'//TRIM(CLIND))

CLIND = ''
CALL SELF%YRCSGLEG%ACDC_CRC64 (KLUN, CDPATH//'%YRCSGLEG'//TRIM(CLIND))

CLIND = ''
CALL SELF%YRDIM%ACDC_CRC64 (KLUN, CDPATH//'%YRDIM'//TRIM(CLIND))

CLIND = ''
CALL SELF%YRDIMV%ACDC_CRC64 (KLUN, CDPATH//'%YRDIMV'//TRIM(CLIND))

CLIND = ''
CALL SELF%YRMP%ACDC_CRC64 (KLUN, CDPATH//'%YRMP'//TRIM(CLIND))

CLIND = ''
CALL SELF%YRGEM%ACDC_CRC64 (KLUN, CDPATH//'%YRGEM'//TRIM(CLIND))

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (SELF%AD_GEOM), CDPATH//'%AD_GEOM'

CLIND = ''
CALL SELF%YROROG_B%ACDC_CRC64 (KLUN, CDPATH//'%YROROG_B'//TRIM(CLIND))

CLIND = ''
CALL SELF%YSPGEOM%ACDC_CRC64 (KLUN, CDPATH//'%YSPGEOM'//TRIM(CLIND))

CLIND = ''
CALL SELF%YVABIO%ACDC_CRC64 (KLUN, CDPATH//'%YVABIO'//TRIM(CLIND))

CLIND = ''
CALL SELF%YREDIM%ACDC_CRC64 (KLUN, CDPATH//'%YREDIM'//TRIM(CLIND))

CLIND = ''
CALL SELF%YREGEO%ACDC_CRC64 (KLUN, CDPATH//'%YREGEO'//TRIM(CLIND))

CLIND = ''
CALL SELF%YREMP%ACDC_CRC64 (KLUN, CDPATH//'%YREMP'//TRIM(CLIND))

CLIND = ''
CALL SELF%YRELAP%ACDC_CRC64 (KLUN, CDPATH//'%YRELAP'//TRIM(CLIND))

CLIND = ''
CALL SELF%YREGSL%ACDC_CRC64 (KLUN, CDPATH//'%YREGSL'//TRIM(CLIND))

LYRELBC_GEO = ASSOCIATED (SELF%YRELBC_GEO)
IF (LYRELBC_GEO) THEN
  CLIND = ''
  CALL SELF%YRELBC_GEO%ACDC_CRC64 (KLUN, CDPATH//'%YRELBC_GEO'//TRIM(CLIND))
ENDIF

END SUBROUTINE

END SUBMODULE
