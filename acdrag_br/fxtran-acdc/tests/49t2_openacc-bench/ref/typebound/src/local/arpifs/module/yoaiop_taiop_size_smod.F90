    WRITE (*, *) TRIM (CLPATH)//'%RSWASS'
  ENDIF
  KSIZE = KSIZE + ISIZE
ENDIF
LRLWASS = ALLOCATED (SELF%RLWASS)
IF (LRLWASS) THEN
  ISIZE = KIND (SELF%RLWASS) * SIZE (SELF%RLWASS)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%RLWASS'
  ENDIF
  KSIZE = KSIZE + ISIZE
