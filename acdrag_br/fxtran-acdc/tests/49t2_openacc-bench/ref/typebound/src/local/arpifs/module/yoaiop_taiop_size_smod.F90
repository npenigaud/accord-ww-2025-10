IF (LRSWAME) THEN
  ISIZE = KIND (SELF%RSWAME) * SIZE (SELF%RSWAME)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%RSWAME'
