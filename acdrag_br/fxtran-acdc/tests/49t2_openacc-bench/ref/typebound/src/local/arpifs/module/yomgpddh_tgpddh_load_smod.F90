IF (LGMVTNDHD_DDH) THEN
  READ (KLUN) IL4
  READ (KLUN) IU4
  ALLOCATE (SELF%GMVTNDHD_DDH (IL4(1):IU4(1), IL4(2):IU4(2), IL4(3):IU4(3), IL4(4):IU4(4)))
