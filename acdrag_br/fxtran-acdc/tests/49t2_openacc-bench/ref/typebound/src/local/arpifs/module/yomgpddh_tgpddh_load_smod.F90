MODULE SUBROUTINE ACDC_LOAD_TGPDDH (SELF, KLUN)
USE PARKIND1, ONLY : JPRD
IMPLICIT NONE
CLASS (TGPDDH), INTENT (OUT), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
INTEGER :: IL4(4), IU4(4)
LOGICAL :: LGFLTNDHD_DDH, LGFLTNDSL_DDH, LGMVTNDHD_DDH, LGMVTNDSI_DDH, LGMVTNDSL_DDH
REAL(KIND=JPRD), ALLOCATABLE :: ZTMP4 (:,:,:,:)
