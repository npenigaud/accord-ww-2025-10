CONTAINS

MODULE SUBROUTINE ACDC_COPY_CPG_TNDSI_DDH_TYPE (SELF, LDCREATED, LDFIELDAPI)

IMPLICIT NONE
CLASS (CPG_TNDSI_DDH_TYPE), INTENT (IN), TARGET :: SELF
LOGICAL, OPTIONAL, INTENT (IN) :: LDCREATED, LDFIELDAPI
LOGICAL :: LLCREATED
LOGICAL :: LLFIELDAPI
LOGICAL :: LZDATA_GMV, LZVIEW_GMV
LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF
LLCREATED = .FALSE.
IF (PRESENT (LDCREATED)) THEN
  LLCREATED = LDCREATED
ENDIF
IF (.NOT. LLCREATED) THEN
  !$ACC ENTER DATA CREATE (SELF)
  !$ACC UPDATE DEVICE (SELF)
ENDIF
LZDATA_GMV = ASSOCIATED (SELF%ZDATA_GMV)
IF (LZDATA_GMV) THEN
  !$ACC ENTER DATA CREATE (SELF%ZDATA_GMV)
  !$ACC UPDATE DEVICE (SELF%ZDATA_GMV)
  !$ACC ENTER DATA ATTACH (SELF%ZDATA_GMV)
ENDIF

LZVIEW_GMV = ASSOCIATED (SELF%ZVIEW_GMV)
IF (LZVIEW_GMV) THEN
  !$ACC ENTER DATA CREATE (SELF%ZVIEW_GMV)
  !$ACC UPDATE DEVICE (SELF%ZVIEW_GMV)
  !$ACC ENTER DATA ATTACH (SELF%ZVIEW_GMV)
ENDIF

CALL SELF%U%ACDC_COPY (LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL SELF%V%ACDC_COPY (LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL SELF%T%ACDC_COPY (LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL SELF%PD%ACDC_COPY (LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL SELF%VD%ACDC_COPY (LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

END SUBROUTINE

END SUBMODULE
