SUBMODULE (CPG_SL1_TYPE_MOD) CPG_SL1_TYPE_MOD_CPG_SL1B_3D_TYPE_COPY_SMOD

IMPLICIT NONE

CONTAINS

MODULE SUBROUTINE ACDC_COPY_CPG_SL1B_3D_TYPE (SELF, LDCREATED, LDFIELDAPI)
USE FIELD_UTIL_MODULE
IMPLICIT NONE
CLASS (CPG_SL1B_3D_TYPE), INTENT (IN), TARGET :: SELF
LOGICAL, OPTIONAL, INTENT (IN) :: LDCREATED, LDFIELDAPI
LOGICAL :: LLCREATED
LOGICAL :: LLFIELDAPI
LOGICAL :: LF_P, LF_P_F, LF_P_P, LF_P_SP, LF_P_SPF
LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF
LLCREATED = .FALSE.
IF (PRESENT (LDCREATED)) THEN
  LLCREATED = LDCREATED
ENDIF
IF (.NOT. LLCREATED) THEN
  !$ACC ENTER DATA CREATE (SELF)
  !$ACC UPDATE DEVICE (SELF)
ENDIF
LF_P = ASSOCIATED (SELF%F_P)
IF (LF_P .AND. LLFIELDAPI) THEN
  CALL COPY_FIELD_3RB (SELF%F_P)
  !$ACC ENTER DATA ATTACH (SELF%F_P)
ENDIF

LF_P_F = ASSOCIATED (SELF%F_P_F)
IF (LF_P_F .AND. LLFIELDAPI) THEN
  CALL COPY_FIELD_3RB (SELF%F_P_F)
  !$ACC ENTER DATA ATTACH (SELF%F_P_F)
ENDIF

LF_P_SPF = ASSOCIATED (SELF%F_P_SPF)
IF (LF_P_SPF .AND. LLFIELDAPI) THEN
  CALL COPY_FIELD_3RB (SELF%F_P_SPF)
  !$ACC ENTER DATA ATTACH (SELF%F_P_SPF)
ENDIF

LF_P_SP = ASSOCIATED (SELF%F_P_SP)
IF (LF_P_SP .AND. LLFIELDAPI) THEN
  CALL COPY_FIELD_3RB (SELF%F_P_SP)
  !$ACC ENTER DATA ATTACH (SELF%F_P_SP)
ENDIF

LF_P_P = ASSOCIATED (SELF%F_P_P)
IF (LF_P_P .AND. LLFIELDAPI) THEN
  CALL COPY_FIELD_3RB (SELF%F_P_P)
  !$ACC ENTER DATA ATTACH (SELF%F_P_P)
ENDIF

CALL SELF%YCOMP%ACDC_COPY (LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

END SUBROUTINE

END SUBMODULE
