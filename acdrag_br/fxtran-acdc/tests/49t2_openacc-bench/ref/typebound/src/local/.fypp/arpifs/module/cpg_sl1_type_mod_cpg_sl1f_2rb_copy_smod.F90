SUBMODULE (CPG_SL1_TYPE_MOD) CPG_SL1_TYPE_MOD_CPG_SL1F_2RB_COPY_SMOD

IMPLICIT NONE

CONTAINS

MODULE SUBROUTINE ACDC_COPY_CPG_SL1F_2RB (SELF, LDCREATED, LDFIELDAPI)
USE FIELD_UTIL_MODULE
IMPLICIT NONE
CLASS (CPG_SL1F_2RB), INTENT (IN), TARGET :: SELF
LOGICAL, OPTIONAL, INTENT (IN) :: LDCREATED, LDFIELDAPI
LOGICAL :: LLCREATED
LOGICAL :: LLFIELDAPI
LOGICAL :: LF_P
LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF
LLCREATED = .FALSE.
IF (PRESENT (LDCREATED)) THEN
  LLCREATED = LDCREATED
ENDIF
IF (.NOT. LLCREATED) THEN
  !$ACC ENTER DATA CREATE (SELF)
  !$ACC UPDATE DEVICE (SELF)
ENDIF
LF_P = ASSOCIATED (SELF%F_P)
IF (LF_P .AND. LLFIELDAPI) THEN
  CALL COPY_FIELD_2RB (SELF%F_P)
  !$ACC ENTER DATA ATTACH (SELF%F_P)
ENDIF

END SUBROUTINE

END SUBMODULE
