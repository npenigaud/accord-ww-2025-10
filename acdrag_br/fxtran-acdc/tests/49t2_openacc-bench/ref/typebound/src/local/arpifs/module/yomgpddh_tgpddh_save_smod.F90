SUBMODULE (YOMGPDDH) YOMGPDDH_TGPDDH_SAVE_SMOD

IMPLICIT NONE

CONTAINS

MODULE SUBROUTINE ACDC_SAVE_TGPDDH (SELF, KLUN)

IMPLICIT NONE
CLASS (TGPDDH), INTENT (IN), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
LOGICAL :: LGFLTNDHD_DDH, LGFLTNDSL_DDH, LGMVTNDHD_DDH, LGMVTNDSI_DDH, LGMVTNDSL_DDH
LGMVTNDHD_DDH = ALLOCATED (SELF%GMVTNDHD_DDH)
WRITE (KLUN) LGMVTNDHD_DDH
IF (LGMVTNDHD_DDH) THEN
  WRITE (KLUN) LBOUND (SELF%GMVTNDHD_DDH)
  WRITE (KLUN) UBOUND (SELF%GMVTNDHD_DDH)
  WRITE (KLUN) SELF%GMVTNDHD_DDH
ENDIF
LGFLTNDHD_DDH = ALLOCATED (SELF%GFLTNDHD_DDH)
WRITE (KLUN) LGFLTNDHD_DDH
IF (LGFLTNDHD_DDH) THEN
  WRITE (KLUN) LBOUND (SELF%GFLTNDHD_DDH)
  WRITE (KLUN) UBOUND (SELF%GFLTNDHD_DDH)
  WRITE (KLUN) SELF%GFLTNDHD_DDH
ENDIF
LGMVTNDSI_DDH = ALLOCATED (SELF%GMVTNDSI_DDH)
WRITE (KLUN) LGMVTNDSI_DDH
IF (LGMVTNDSI_DDH) THEN
  WRITE (KLUN) LBOUND (SELF%GMVTNDSI_DDH)
  WRITE (KLUN) UBOUND (SELF%GMVTNDSI_DDH)
  WRITE (KLUN) SELF%GMVTNDSI_DDH
ENDIF
LGMVTNDSL_DDH = ALLOCATED (SELF%GMVTNDSL_DDH)
WRITE (KLUN) LGMVTNDSL_DDH
IF (LGMVTNDSL_DDH) THEN
  WRITE (KLUN) LBOUND (SELF%GMVTNDSL_DDH)
  WRITE (KLUN) UBOUND (SELF%GMVTNDSL_DDH)
  WRITE (KLUN) SELF%GMVTNDSL_DDH
ENDIF
LGFLTNDSL_DDH = ALLOCATED (SELF%GFLTNDSL_DDH)
WRITE (KLUN) LGFLTNDSL_DDH
IF (LGFLTNDSL_DDH) THEN
  WRITE (KLUN) LBOUND (SELF%GFLTNDSL_DDH)
  WRITE (KLUN) UBOUND (SELF%GFLTNDSL_DDH)
  WRITE (KLUN) SELF%GFLTNDSL_DDH
ENDIF
END SUBROUTINE

END SUBMODULE
