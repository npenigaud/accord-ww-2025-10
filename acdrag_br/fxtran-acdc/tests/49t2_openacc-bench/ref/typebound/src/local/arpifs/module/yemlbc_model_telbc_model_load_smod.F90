  READ (KLUN) IL3
  READ (KLUN) IU3
  ALLOCATE (SELF%EALFAGT3GMVS (IL3(1):IU3(1), IL3(2):IU3(2), IL3(3):IU3(3)))
  ALLOCATE (ZTMP3(LBOUND(SELF%EALFAGT3GMVS,1):UBOUND(SELF%EALFAGT3GMVS,1), LBOUND(SELF%EALFAGT3GMVS,2):UBOUND(SELF%EALFAGT3GMVS,2), LBOUND(SELF%EALFAGT3GMVS,3):UBOUND(SELF%EALFAGT3GMVS,3)))
