  ALLOCATE (ZTMP3(LBOUND(SELF%EALFAGT3GFL,1):UBOUND(SELF%EALFAGT3GFL,1), LBOUND(SELF%EALFAGT3GFL,2):UBOUND(SELF%EALFAGT3GFL,2), LBOUND(SELF%EALFAGT3GFL,3):UBOUND(SELF%EALFAGT3GFL,3)))
