  READ (KLUN) IL2
  READ (KLUN) IU2
  ALLOCATE (SELF%GMGT3 (IL2(1):IU2(1), IL2(2):IU2(2)))
  ALLOCATE (ZTMP2(LBOUND(SELF%GMGT3,1):UBOUND(SELF%GMGT3,1), LBOUND(SELF%GMGT3,2):UBOUND(SELF%GMGT3,2)))
  READ (KLUN) ZTMP2
  SELF%GMGT3 = ZTMP2
  DEALLOCATE (ZTMP2)
ENDIF
