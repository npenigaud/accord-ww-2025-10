IF (LGMGT4) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (SELF%GMGT4 (IL1(1):IU1(1)))
  ALLOCATE (ZTMP1(LBOUND(SELF%GMGT4,1):UBOUND(SELF%GMGT4,1)))
  READ (KLUN) ZTMP1
  SELF%GMGT4 = ZTMP1
  DEALLOCATE (ZTMP1)
