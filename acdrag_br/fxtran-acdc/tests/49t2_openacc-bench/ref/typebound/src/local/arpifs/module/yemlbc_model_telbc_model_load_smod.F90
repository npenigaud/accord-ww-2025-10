  READ (KLUN) IL2
  READ (KLUN) IU2
  ALLOCATE (SELF%EALFA_TENC (IL2(1):IU2(1), IL2(2):IU2(2)))
  ALLOCATE (ZTMP2(LBOUND(SELF%EALFA_TENC,1):UBOUND(SELF%EALFA_TENC,1), LBOUND(SELF%EALFA_TENC,2):UBOUND(SELF%EALFA_TENC,2)))
