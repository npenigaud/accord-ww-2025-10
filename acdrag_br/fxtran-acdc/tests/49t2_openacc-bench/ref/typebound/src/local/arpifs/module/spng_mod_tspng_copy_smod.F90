MODULE SUBROUTINE ACDC_COPY_TSPNG (SELF, LDCREATED, LDFIELDAPI)

IMPLICIT NONE
CLASS (TSPNG), INTENT (IN), TARGET :: SELF
LOGICAL, OPTIONAL, INTENT (IN) :: LDCREATED, LDFIELDAPI
LOGICAL :: LLCREATED
LOGICAL :: LLFIELDAPI
LOGICAL :: LRSPONGF
LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF
LLCREATED = .FALSE.
IF (PRESENT (LDCREATED)) THEN
  LLCREATED = LDCREATED
ENDIF
IF (.NOT. LLCREATED) THEN
  !$ACC ENTER DATA CREATE (SELF)
  !$ACC UPDATE DEVICE (SELF)
ENDIF

LRSPONGF = ALLOCATED (SELF%RSPONGF)
IF (LRSPONGF) THEN
  !$ACC ENTER DATA CREATE (SELF%RSPONGF)
  !$ACC UPDATE DEVICE (SELF%RSPONGF)
  !$ACC ENTER DATA ATTACH (SELF%RSPONGF)
ENDIF

END SUBROUTINE

