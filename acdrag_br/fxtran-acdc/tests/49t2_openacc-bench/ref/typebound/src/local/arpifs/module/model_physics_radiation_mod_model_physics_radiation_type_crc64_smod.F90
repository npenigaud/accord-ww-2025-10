SUBMODULE (MODEL_PHYSICS_RADIATION_MOD) MODEL_PHYSICS_RADIATION_MOD_MODEL_PHYSICS_RADIATION_TYPE_CRC64_SMOD

IMPLICIT NONE

CONTAINS

MODULE SUBROUTINE ACDC_CRC64_MODEL_PHYSICS_RADIATION_TYPE (SELF, KLUN, CDPATH)
USE FXTRAN_ACDC_CRC64_INTRINSIC_MOD, ONLY : FCRC64 => FXTRAN_ACDC_CRC64
IMPLICIT NONE
CLASS (MODEL_PHYSICS_RADIATION_TYPE), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
CHARACTER(LEN=*), INTENT (IN) :: CDPATH
CHARACTER(LEN=128) :: CLIND
INTEGER :: J1
LOGICAL :: LYRAD_GSGEOM
CLIND = ''
CALL SELF%YRERAD%ACDC_CRC64 (KLUN, CDPATH//'%YRERAD'//TRIM(CLIND))

CLIND = ''
CALL SELF%YRESWRT%ACDC_CRC64 (KLUN, CDPATH//'%YRESWRT'//TRIM(CLIND))

CLIND = ''
CALL SELF%YREOVLP%ACDC_CRC64 (KLUN, CDPATH//'%YREOVLP'//TRIM(CLIND))

CLIND = ''
CALL SELF%YRENEUR%ACDC_CRC64 (KLUN, CDPATH//'%YRENEUR'//TRIM(CLIND))

CLIND = ''
CALL SELF%YRELWRAD%ACDC_CRC64 (KLUN, CDPATH//'%YRELWRAD'//TRIM(CLIND))

CLIND = ''
CALL SELF%YREAERD%ACDC_CRC64 (KLUN, CDPATH//'%YREAERD'//TRIM(CLIND))

CLIND = ''
CALL SELF%YREAERATM%ACDC_CRC64 (KLUN, CDPATH//'%YREAERATM'//TRIM(CLIND))

CLIND = ''
CALL SELF%YREUVRAD%ACDC_CRC64 (KLUN, CDPATH//'%YREUVRAD'//TRIM(CLIND))

CLIND = ''
CALL SELF%YRERDI%ACDC_CRC64 (KLUN, CDPATH//'%YRERDI'//TRIM(CLIND))

CLIND = ''
CALL SELF%YREMCICA%ACDC_CRC64 (KLUN, CDPATH//'%YREMCICA'//TRIM(CLIND))

CLIND = ''
CALL SELF%YRRCOEF%ACDC_CRC64 (KLUN, CDPATH//'%YRRCOEF'//TRIM(CLIND))

CLIND = ''
CALL SELF%YRTRC%ACDC_CRC64 (KLUN, CDPATH//'%YRTRC'//TRIM(CLIND))

CLIND = ''
CALL SELF%RADGRID%ACDC_CRC64 (KLUN, CDPATH//'%RADGRID'//TRIM(CLIND))

LYRAD_GSGEOM = ASSOCIATED (SELF%YRAD_GSGEOM)
IF (LYRAD_GSGEOM) THEN
  DO J1 = LBOUND (SELF%YRAD_GSGEOM, 1), UBOUND (SELF%YRAD_GSGEOM, 1)
    WRITE (CLIND, '("(",I0,")")') J1
    CALL SELF%YRAD_GSGEOM (J1)%ACDC_CRC64 (KLUN, CDPATH//'%YRAD_GSGEOM'//TRIM(CLIND))
  ENDDO
ENDIF

CLIND = ''
CALL SELF%YRERIP%ACDC_CRC64 (KLUN, CDPATH//'%YRERIP'//TRIM(CLIND))

CLIND = ''
CALL SELF%YRADIATION%ACDC_CRC64 (KLUN, CDPATH//'%YRADIATION'//TRIM(CLIND))

CLIND = ''
CALL SELF%YRRI%ACDC_CRC64 (KLUN, CDPATH//'%YRRI'//TRIM(CLIND))

CLIND = ''
CALL SELF%YRRO%ACDC_CRC64 (KLUN, CDPATH//'%YRRO'//TRIM(CLIND))

END SUBROUTINE

END SUBMODULE
