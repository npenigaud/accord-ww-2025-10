SUBMODULE (CPLNG_TYPES_MOD) CPLNG_TYPES_MOD_CPLNG_FLD_TYPE_SAVE_SMOD

IMPLICIT NONE

CONTAINS

MODULE SUBROUTINE ACDC_SAVE_CPLNG_FLD_TYPE (SELF, KLUN)

IMPLICIT NONE
CLASS (CPLNG_FLD_TYPE), INTENT (IN), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
LOGICAL :: LD, LID
LID = ALLOCATED (SELF%ID)
WRITE (KLUN) LID
IF (LID) THEN
  WRITE (KLUN) LBOUND (SELF%ID)
  WRITE (KLUN) UBOUND (SELF%ID)
  WRITE (KLUN) SELF%ID
ENDIF
WRITE (KLUN) SELF%NAME
WRITE (KLUN) SELF%TYPE
WRITE (KLUN) SELF%INOUT
WRITE (KLUN) SELF%STAGE
WRITE (KLUN) SELF%NUM_LVL
WRITE (KLUN) SELF%NUM_CAT
LD = ALLOCATED (SELF%D)
WRITE (KLUN) LD
IF (LD) THEN
  WRITE (KLUN) LBOUND (SELF%D)
  WRITE (KLUN) UBOUND (SELF%D)
  WRITE (KLUN) SELF%D
ENDIF
END SUBROUTINE

END SUBMODULE
