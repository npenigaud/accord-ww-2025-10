WRITE (KLUN) LNDLUXL
IF (LNDLUXL) THEN
  WRITE (KLUN) LBOUND (SELF%NDLUXL)
  WRITE (KLUN) UBOUND (SELF%NDLUXL)
  WRITE (KLUN) SELF%NDLUXL
ENDIF
WRITE (KLUN) SELF%NPROMA
WRITE (KLUN) SELF%NPROMM
WRITE (KLUN) SELF%NPROMM9
WRITE (KLUN) SELF%NPROMNH
WRITE (KLUN) SELF%NGPBLKS
