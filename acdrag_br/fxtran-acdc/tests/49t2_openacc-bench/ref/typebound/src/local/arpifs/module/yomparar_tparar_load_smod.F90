SUBMODULE (YOMPARAR) YOMPARAR_TPARAR_LOAD_SMOD

IMPLICIT NONE

CONTAINS

MODULE SUBROUTINE ACDC_LOAD_TPARAR (SELF, KLUN)
USE PARKIND1, ONLY : JPRD
IMPLICIT NONE
CLASS (TPARAR), INTENT (OUT), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
INTEGER :: IL1(1), IU1(1)
LOGICAL :: LXSW_BANDS
REAL(KIND=JPRD) :: ZTMP0
REAL(KIND=JPRD), ALLOCATABLE :: ZTMP1 (:)
READ (KLUN) SELF%CMICRO
READ (KLUN) SELF%CTURB
CALL SELF%PHYEX%ACDC_LOAD (KLUN)
READ (KLUN) SELF%NRR
READ (KLUN) SELF%NRRL
READ (KLUN) SELF%NRRI
READ (KLUN) SELF%NSV
READ (KLUN) SELF%NSWB_MNH
READ (KLUN) SELF%NGPAR
READ (KLUN) SELF%MINPRR
READ (KLUN) SELF%MACPRR
READ (KLUN) SELF%MINPRS
READ (KLUN) SELF%MACPRS
READ (KLUN) SELF%MINPRG
READ (KLUN) SELF%MACPRG
READ (KLUN) SELF%MALBDIR
READ (KLUN) SELF%MALBSCA
READ (KLUN) SELF%MRAIN
READ (KLUN) SELF%MSNOW
READ (KLUN) SELF%MGZ0
READ (KLUN) SELF%MGZ0H
READ (KLUN) SELF%MVQS
READ (KLUN) SELF%MVTS
READ (KLUN) SELF%MVEMIS
READ (KLUN) SELF%MSWDIR
READ (KLUN) SELF%MSWDIF
READ (KLUN) SELF%MLSM
READ (KLUN) SELF%MCD
READ (KLUN) SELF%MCH
READ (KLUN) LXSW_BANDS
IF (LXSW_BANDS) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (SELF%XSW_BANDS (IL1(1):IU1(1)))
  ALLOCATE (ZTMP1(LBOUND(SELF%XSW_BANDS,1):UBOUND(SELF%XSW_BANDS,1)))
  READ (KLUN) ZTMP1
  SELF%XSW_BANDS = ZTMP1
  DEALLOCATE (ZTMP1)
ENDIF
READ (KLUN) SELF%LOLSMC
READ (KLUN) SELF%LOTOWNC
READ (KLUN) SELF%LKOGAN
READ (KLUN) SELF%LMODICEDEP
READ (KLUN) SELF%LICERAD
READ (KLUN) ZTMP0
SELF%RADGR = ZTMP0
READ (KLUN) ZTMP0
SELF%RADSN = ZTMP0
READ (KLUN) SELF%LLCRIT
READ (KLUN) SELF%LTOTPREC
READ (KLUN) SELF%LTOTPRECL
READ (KLUN) SELF%LDUALMF
READ (KLUN) SELF%LSCAWAREMF
READ (KLUN) ZTMP0
SELF%XVMAXTHP = ZTMP0
READ (KLUN) ZTMP0
SELF%XVMAXTHM = ZTMP0
READ (KLUN) SELF%LSQUALL
READ (KLUN) SELF%NREFROI1
READ (KLUN) SELF%NREFROI2
READ (KLUN) ZTMP0
SELF%VSQUALL = ZTMP0
READ (KLUN) SELF%LAROBU_ENABLE
READ (KLUN) ZTMP0
SELF%XCQVR = ZTMP0
READ (KLUN) ZTMP0
SELF%GQVPLIM = ZTMP0
READ (KLUN) ZTMP0
SELF%GQVTOP = ZTMP0
READ (KLUN) SELF%LQVTOP
READ (KLUN) SELF%LIMIT_TENDQ
END SUBROUTINE

END SUBMODULE
