READ (KLUN) SELF%NAER_BLNUCL
READ (KLUN) SELF%NAERSCAV
READ (KLUN) SELF%ITLISTWAVLCPL
READ (KLUN) ZTMP0
SELF%RGRATE = ZTMP0
READ (KLUN) ZTMP0
