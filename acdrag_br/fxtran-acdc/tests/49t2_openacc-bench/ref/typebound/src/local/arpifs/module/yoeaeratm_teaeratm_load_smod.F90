READ (KLUN) SELF%LAERDIAG2
READ (KLUN) SELF%LAERRRTM
READ (KLUN) SELF%LAERUVP
READ (KLUN) SELF%LAEREXTR
READ (KLUN) SELF%LAERGBUD
READ (KLUN) SELF%LAERPRNT
READ (KLUN) SELF%LAERNITRATE
READ (KLUN) SELF%LAERRADCPL
READ (KLUN) SELF%LSEASALT_RH80
READ (KLUN) SELF%LAERDUSTSOURCE
READ (KLUN) SELF%LAERDUSTSIZEVAR
