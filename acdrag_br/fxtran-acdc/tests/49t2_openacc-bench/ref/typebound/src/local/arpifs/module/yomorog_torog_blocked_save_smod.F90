SUBMODULE (YOMOROG) YOMOROG_TOROG_BLOCKED_SAVE_SMOD

IMPLICIT NONE

CONTAINS

MODULE SUBROUTINE ACDC_SAVE_TOROG_BLOCKED (SELF, KLUN)

IMPLICIT NONE
CLASS (TOROG_BLOCKED), INTENT (IN), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
LOGICAL :: LOROG, LOROGL, LOROGLL, LOROGLM, LOROGM, LOROGMM
LOROG = ALLOCATED (SELF%OROG)
WRITE (KLUN) LOROG
IF (LOROG) THEN
  WRITE (KLUN) LBOUND (SELF%OROG)
  WRITE (KLUN) UBOUND (SELF%OROG)
  WRITE (KLUN) SELF%OROG
ENDIF
LOROGL = ALLOCATED (SELF%OROGL)
WRITE (KLUN) LOROGL
IF (LOROGL) THEN
  WRITE (KLUN) LBOUND (SELF%OROGL)
  WRITE (KLUN) UBOUND (SELF%OROGL)
  WRITE (KLUN) SELF%OROGL
ENDIF
LOROGM = ALLOCATED (SELF%OROGM)
WRITE (KLUN) LOROGM
IF (LOROGM) THEN
  WRITE (KLUN) LBOUND (SELF%OROGM)
  WRITE (KLUN) UBOUND (SELF%OROGM)
  WRITE (KLUN) SELF%OROGM
ENDIF
LOROGLL = ALLOCATED (SELF%OROGLL)
WRITE (KLUN) LOROGLL
IF (LOROGLL) THEN
  WRITE (KLUN) LBOUND (SELF%OROGLL)
  WRITE (KLUN) UBOUND (SELF%OROGLL)
  WRITE (KLUN) SELF%OROGLL
ENDIF
LOROGMM = ALLOCATED (SELF%OROGMM)
WRITE (KLUN) LOROGMM
IF (LOROGMM) THEN
  WRITE (KLUN) LBOUND (SELF%OROGMM)
  WRITE (KLUN) UBOUND (SELF%OROGMM)
  WRITE (KLUN) SELF%OROGMM
ENDIF
LOROGLM = ALLOCATED (SELF%OROGLM)
WRITE (KLUN) LOROGLM
IF (LOROGLM) THEN
  WRITE (KLUN) LBOUND (SELF%OROGLM)
  WRITE (KLUN) UBOUND (SELF%OROGLM)
  WRITE (KLUN) SELF%OROGLM
ENDIF
END SUBROUTINE

END SUBMODULE
