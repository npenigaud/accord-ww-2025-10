WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (SELF%NECVGP_2D), CDPATH//'%NECVGP_2D'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (SELF%NECV_2D), CDPATH//'%NECV_2D'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (SELF%NECVSP_3D), CDPATH//'%NECVSP_3D'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (SELF%NECVGP_3D), CDPATH//'%NECVGP_3D'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (SELF%NECV_3D), CDPATH//'%NECV_3D'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (SELF%NECV_ALL), CDPATH//'%NECV_ALL'

END SUBROUTINE

