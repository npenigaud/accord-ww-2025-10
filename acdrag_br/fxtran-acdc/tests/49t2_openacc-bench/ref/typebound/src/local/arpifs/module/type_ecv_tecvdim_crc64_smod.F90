MODULE SUBROUTINE ACDC_CRC64_TECVDIM (SELF, KLUN, CDPATH)
USE FXTRAN_ACDC_CRC64_INTRINSIC_MOD, ONLY : FCRC64 => FXTRAN_ACDC_CRC64
IMPLICIT NONE
CLASS (TECVDIM), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
CHARACTER(LEN=*), INTENT (IN) :: CDPATH
CHARACTER(LEN=128) :: CLIND
WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (SELF%LECV_1D), CDPATH//'%LECV_1D'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (SELF%LECV_2D), CDPATH//'%LECV_2D'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (SELF%LECV_3D), CDPATH//'%LECV_3D'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (SELF%NECV_1D), CDPATH//'%NECV_1D'

