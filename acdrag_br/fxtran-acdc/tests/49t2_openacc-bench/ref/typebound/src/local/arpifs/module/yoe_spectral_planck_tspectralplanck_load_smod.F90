SUBMODULE (YOE_SPECTRAL_PLANCK) YOE_SPECTRAL_PLANCK_TSPECTRALPLANCK_LOAD_SMOD

IMPLICIT NONE

CONTAINS

MODULE SUBROUTINE ACDC_LOAD_TSPECTRALPLANCK (SELF, KLUN)
USE PARKIND1, ONLY : JPRD
IMPLICIT NONE
CLASS (TSPECTRALPLANCK), INTENT (OUT), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
INTEGER :: IL1(1), IU1(1), IL2(2), IU2(2)
LOGICAL :: LINTERVAL_MAP, LPLANCK_LUT, LWAVLEN_BOUND
REAL(KIND=JPRD) :: ZTMP0
REAL(KIND=JPRD), ALLOCATABLE :: ZTMP1 (:)
REAL(KIND=JPRD), ALLOCATABLE :: ZTMP2 (:,:)
READ (KLUN) SELF%NINTERVALS
READ (KLUN) SELF%NTEMPS
READ (KLUN) ZTMP0
SELF%TEMP1 = ZTMP0
READ (KLUN) ZTMP0
SELF%DTEMP = ZTMP0
READ (KLUN) LPLANCK_LUT
IF (LPLANCK_LUT) THEN
  READ (KLUN) IL2
  READ (KLUN) IU2
  ALLOCATE (SELF%PLANCK_LUT (IL2(1):IU2(1), IL2(2):IU2(2)))
  ALLOCATE (ZTMP2(LBOUND(SELF%PLANCK_LUT,1):UBOUND(SELF%PLANCK_LUT,1), LBOUND(SELF%PLANCK_LUT,2):UBOUND(SELF%PLANCK_LUT,2)))
  READ (KLUN) ZTMP2
  SELF%PLANCK_LUT = ZTMP2
  DEALLOCATE (ZTMP2)
ENDIF
READ (KLUN) LWAVLEN_BOUND
IF (LWAVLEN_BOUND) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (SELF%WAVLEN_BOUND (IL1(1):IU1(1)))
  ALLOCATE (ZTMP1(LBOUND(SELF%WAVLEN_BOUND,1):UBOUND(SELF%WAVLEN_BOUND,1)))
  READ (KLUN) ZTMP1
  SELF%WAVLEN_BOUND = ZTMP1
  DEALLOCATE (ZTMP1)
ENDIF
READ (KLUN) LINTERVAL_MAP
IF (LINTERVAL_MAP) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (SELF%INTERVAL_MAP (IL1(1):IU1(1)))
  READ (KLUN) SELF%INTERVAL_MAP
ENDIF
END SUBROUTINE

END SUBMODULE
