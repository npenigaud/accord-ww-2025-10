SUBMODULE (TYPE_ECV) TYPE_ECV_ECV_CONTAINER_SIZE_SMOD

IMPLICIT NONE

CONTAINS

MODULE FUNCTION ACDC_SIZE_ECV_CONTAINER (SELF, CDPATH, LDPRINT) RESULT (KSIZE)

IMPLICIT NONE
CLASS (ECV_CONTAINER),     INTENT (IN), TARGET :: SELF
CHARACTER(LEN=*), INTENT (IN), OPTIONAL :: CDPATH
LOGICAL,          INTENT (IN), OPTIONAL :: LDPRINT
INTEGER*8 :: KSIZE
INTEGER*8 :: ISIZE, JSIZE
LOGICAL :: LLPRINT
CHARACTER(LEN=128) :: CLPATH
LOGICAL :: LRECV1D, LRGPECV2D, LRGPECV3D, LRLSM, LRSPECV2D, LRSPECV3D
LLPRINT = .FALSE.
IF (PRESENT (LDPRINT)) THEN
  LLPRINT = LDPRINT
ENDIF
CLPATH=''
IF (PRESENT (CDPATH)) THEN
  CLPATH = CDPATH
ENDIF
KSIZE = 0
LRECV1D = ALLOCATED (SELF%RECV1D)
IF (LRECV1D) THEN
  ISIZE = KIND (SELF%RECV1D) * SIZE (SELF%RECV1D)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%RECV1D'
  ENDIF
  KSIZE = KSIZE + ISIZE
ENDIF
LRSPECV2D = ALLOCATED (SELF%RSPECV2D)
IF (LRSPECV2D) THEN
  ISIZE = KIND (SELF%RSPECV2D) * SIZE (SELF%RSPECV2D)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%RSPECV2D'
  ENDIF
  KSIZE = KSIZE + ISIZE
ENDIF
LRGPECV2D = ALLOCATED (SELF%RGPECV2D)
IF (LRGPECV2D) THEN
  ISIZE = KIND (SELF%RGPECV2D) * SIZE (SELF%RGPECV2D)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%RGPECV2D'
  ENDIF
  KSIZE = KSIZE + ISIZE
ENDIF
LRSPECV3D = ALLOCATED (SELF%RSPECV3D)
IF (LRSPECV3D) THEN
  ISIZE = KIND (SELF%RSPECV3D) * SIZE (SELF%RSPECV3D)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%RSPECV3D'
  ENDIF
  KSIZE = KSIZE + ISIZE
ENDIF
LRGPECV3D = ALLOCATED (SELF%RGPECV3D)
IF (LRGPECV3D) THEN
  ISIZE = KIND (SELF%RGPECV3D) * SIZE (SELF%RGPECV3D)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%RGPECV3D'
  ENDIF
  KSIZE = KSIZE + ISIZE
ENDIF
LRLSM = ALLOCATED (SELF%RLSM)
IF (LRLSM) THEN
  ISIZE = KIND (SELF%RLSM) * SIZE (SELF%RLSM)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%RLSM'
  ENDIF
  KSIZE = KSIZE + ISIZE
ENDIF
END FUNCTION

END SUBMODULE
