SUBMODULE (MODEL_PHYSICS_RADIATION_MOD) MODEL_PHYSICS_RADIATION_MOD_MODEL_PHYSICS_RADIATION_TYPE_LOAD_SMOD

IMPLICIT NONE

CONTAINS

MODULE SUBROUTINE ACDC_LOAD_MODEL_PHYSICS_RADIATION_TYPE (SELF, KLUN)

IMPLICIT NONE
CLASS (MODEL_PHYSICS_RADIATION_TYPE), INTENT (OUT), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
INTEGER :: J1
INTEGER :: IL1(1), IU1(1)
LOGICAL :: LYRAD_GSGEOM
CALL SELF%YRERAD%ACDC_LOAD (KLUN)
CALL SELF%YRESWRT%ACDC_LOAD (KLUN)
CALL SELF%YREOVLP%ACDC_LOAD (KLUN)
CALL SELF%YRENEUR%ACDC_LOAD (KLUN)
CALL SELF%YRELWRAD%ACDC_LOAD (KLUN)
CALL SELF%YREAERD%ACDC_LOAD (KLUN)
CALL SELF%YREAERATM%ACDC_LOAD (KLUN)
CALL SELF%YREUVRAD%ACDC_LOAD (KLUN)
CALL SELF%YRERDI%ACDC_LOAD (KLUN)
CALL SELF%YREMCICA%ACDC_LOAD (KLUN)
CALL SELF%YRRCOEF%ACDC_LOAD (KLUN)
CALL SELF%YRTRC%ACDC_LOAD (KLUN)
CALL SELF%RADGRID%ACDC_LOAD (KLUN)
READ (KLUN) LYRAD_GSGEOM
IF (LYRAD_GSGEOM) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (SELF%YRAD_GSGEOM (IL1(1):IU1(1)))
  DO J1 = LBOUND (SELF%YRAD_GSGEOM, 1), UBOUND (SELF%YRAD_GSGEOM, 1)
    CALL SELF%YRAD_GSGEOM (J1)%ACDC_LOAD (KLUN)
  ENDDO
ELSE
  NULLIFY (SELF%YRAD_GSGEOM)
ENDIF
CALL SELF%YRERIP%ACDC_LOAD (KLUN)
CALL SELF%YRADIATION%ACDC_LOAD (KLUN)
CALL SELF%YRRI%ACDC_LOAD (KLUN)
CALL SELF%YRRO%ACDC_LOAD (KLUN)
END SUBROUTINE

END SUBMODULE
