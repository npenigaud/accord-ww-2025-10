CONTAINS

MODULE SUBROUTINE ACDC_CRC64_FIELD_5RM_ARRAY (SELF, KLUN, CDPATH)
USE FIELD_UTIL_MODULE
USE FXTRAN_ACDC_CRC64_INTRINSIC_MOD, ONLY : FCRC64 => FXTRAN_ACDC_CRC64
IMPLICIT NONE
CLASS (FIELD_5RM_ARRAY), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
CHARACTER(LEN=*), INTENT (IN) :: CDPATH
CHARACTER(LEN=128) :: CLIND
LOGICAL :: LF_P, LP_FIELD
LF_P = ASSOCIATED (SELF%F_P)
IF (LF_P) THEN
  CLIND = ''
  WRITE (KLUN, '(Z16.16," ",A)') SELF%F_P%CRC64 (), CDPATH//'%F_P'//TRIM(CLIND)
ENDIF

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (SELF%P_FIELD), CDPATH//'%P_FIELD'

END SUBROUTINE

END SUBMODULE
