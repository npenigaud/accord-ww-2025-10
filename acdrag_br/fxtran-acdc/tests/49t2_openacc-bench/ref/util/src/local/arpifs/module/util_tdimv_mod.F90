FUNCTION ACDC_SIZE_TDIMV (SELF, CDPATH, LDPRINT) RESULT (KSIZE)

IMPLICIT NONE
TYPE (TDIMV),     INTENT (IN), TARGET :: SELF
CHARACTER(LEN=*), INTENT (IN), OPTIONAL :: CDPATH
LOGICAL,          INTENT (IN), OPTIONAL :: LDPRINT
INTEGER*8 :: KSIZE
INTEGER*8 :: ISIZE, JSIZE
LOGICAL :: LLPRINT
CHARACTER(LEN=128) :: CLPATH
LLPRINT = .FALSE.
IF (PRESENT (LDPRINT)) THEN
  LLPRINT = LDPRINT
ENDIF
CLPATH=''
IF (PRESENT (CDPATH)) THEN
  CLPATH = CDPATH
ENDIF
KSIZE = 0
ISIZE = KIND (SELF%NFLEVG)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%NFLEVG'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%NFLEVL)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%NFLEVL'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%NFLEVLMX)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%NFLEVLMX'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%NFLSUR)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%NFLSUR'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%NFLSUL)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%NFLSUL'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%NFLSA)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%NFLSA'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%NFLEN)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%NFLEN'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%NIOLEVG)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%NIOLEVG'
ENDIF
KSIZE = KSIZE + ISIZE
END FUNCTION

