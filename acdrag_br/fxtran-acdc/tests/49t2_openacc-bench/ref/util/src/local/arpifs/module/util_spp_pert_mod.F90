SUBROUTINE ACDC_LOAD_SPP_PERT (SELF, KLUN)
USE PARKIND1, ONLY : JPRD
IMPLICIT NONE
TYPE (SPP_PERT), INTENT (OUT), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
REAL(KIND=JPRD) :: ZTMP0
REAL(KIND=JPRD), ALLOCATABLE :: ZTMP1 (:)
READ (KLUN) SELF%LABEL
READ (KLUN) SELF%ON
READ (KLUN) SELF%IDISTR
ALLOCATE (ZTMP1(LBOUND(SELF%XMAG,1):UBOUND(SELF%XMAG,1)))
READ (KLUN) ZTMP1
SELF%XMAG = ZTMP1
DEALLOCATE (ZTMP1)
ALLOCATE (ZTMP1(LBOUND(SELF%MU,1):UBOUND(SELF%MU,1)))
READ (KLUN) ZTMP1
SELF%MU = ZTMP1
DEALLOCATE (ZTMP1)
READ (KLUN) ZTMP0
SELF%XCLIPMIN = ZTMP0
READ (KLUN) ZTMP0
SELF%XCLIPMAX = ZTMP0
READ (KLUN) ZTMP0
SELF%XUNIFORM_OFFSET = ZTMP0
READ (KLUN) SELF%NMAG
READ (KLUN) SELF%LN1
READ (KLUN) SELF%NSEED_OFF
READ (KLUN) SELF%NOFF
READ (KLUN) SELF%NRF
READ (KLUN) SELF%NRF_RADGRID
READ (KLUN) SELF%RADGRID
READ (KLUN) SELF%RF_PERT_LABEL
READ (KLUN) SELF%IC
READ (KLUN) SELF%MP
READ (KLUN) SELF%MP_RADGRID
READ (KLUN) ZTMP0
SELF%TAU = ZTMP0
READ (KLUN) ZTMP0
SELF%SDEV = ZTMP0
READ (KLUN) ZTMP0
SELF%XLCOR = ZTMP0
END SUBROUTINE

