MODULE UTIL_MF_PHYS_STATE_GSP_SG_TYPE_MOD

USE MF_PHYS_BASE_STATE_TYPE_MOD, ONLY : MF_PHYS_STATE_GSP_SG_TYPE



INTERFACE ACDC_COPY
MODULE PROCEDURE ACDC_COPY_MF_PHYS_STATE_GSP_SG_TYPE
END INTERFACE




INTERFACE ACDC_WIPE
MODULE PROCEDURE ACDC_WIPE_MF_PHYS_STATE_GSP_SG_TYPE
END INTERFACE


CONTAINS





SUBROUTINE ACDC_COPY_MF_PHYS_STATE_GSP_SG_TYPE (SELF, LDCREATED, LDFIELDAPI)
USE FIELD_UTIL_MODULE
IMPLICIT NONE
TYPE (MF_PHYS_STATE_GSP_SG_TYPE), INTENT (IN), TARGET :: SELF
LOGICAL, OPTIONAL, INTENT (IN) :: LDCREATED, LDFIELDAPI
LOGICAL :: LLCREATED
LOGICAL :: LLFIELDAPI
LOGICAL :: LF_A, LF_F, LF_R
LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF
LLCREATED = .FALSE.
IF (PRESENT (LDCREATED)) THEN
  LLCREATED = LDCREATED
ENDIF
IF (.NOT. LLCREATED) THEN
  !$ACC ENTER DATA CREATE (SELF)
  !$ACC UPDATE DEVICE (SELF)
ENDIF
LF_F = ASSOCIATED (SELF%F_F)
IF (LF_F .AND. LLFIELDAPI) THEN
  CALL COPY_FIELD_3RB (SELF%F_F)
  !$ACC ENTER DATA ATTACH (SELF%F_F)
ENDIF

LF_A = ASSOCIATED (SELF%F_A)
IF (LF_A .AND. LLFIELDAPI) THEN
  CALL COPY_FIELD_3RB (SELF%F_A)
  !$ACC ENTER DATA ATTACH (SELF%F_A)
ENDIF

LF_R = ASSOCIATED (SELF%F_R)
IF (LF_R .AND. LLFIELDAPI) THEN
  CALL COPY_FIELD_3RB (SELF%F_R)
  !$ACC ENTER DATA ATTACH (SELF%F_R)
ENDIF

END SUBROUTINE







SUBROUTINE ACDC_WIPE_MF_PHYS_STATE_GSP_SG_TYPE (SELF, LDDELETED, LDFIELDAPI)
USE FIELD_UTIL_MODULE
IMPLICIT NONE
TYPE (MF_PHYS_STATE_GSP_SG_TYPE), INTENT (IN), TARGET :: SELF
LOGICAL, OPTIONAL, INTENT (IN) :: LDDELETED, LDFIELDAPI
LOGICAL :: LLDELETED
LOGICAL :: LLFIELDAPI
LOGICAL :: LF_A, LF_F, LF_R
LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF
LF_F = ASSOCIATED (SELF%F_F)
IF (LF_F .AND. LLFIELDAPI) THEN
  CALL WIPE_FIELD_3RB (SELF%F_F)
ENDIF

LF_A = ASSOCIATED (SELF%F_A)
IF (LF_A .AND. LLFIELDAPI) THEN
  CALL WIPE_FIELD_3RB (SELF%F_A)
ENDIF

LF_R = ASSOCIATED (SELF%F_R)
IF (LF_R .AND. LLFIELDAPI) THEN
  CALL WIPE_FIELD_3RB (SELF%F_R)
ENDIF

LLDELETED = .FALSE.
IF (PRESENT (LDDELETED)) THEN
  LLDELETED = LDDELETED
ENDIF
IF (.NOT. LLDELETED) THEN
  !$ACC EXIT DATA DELETE (SELF)
ENDIF
END SUBROUTINE



END MODULE
