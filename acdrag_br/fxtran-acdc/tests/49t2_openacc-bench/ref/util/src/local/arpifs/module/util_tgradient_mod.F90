MODULE UTIL_TGRADIENT_MOD

USE YOMDGRADIENT, ONLY : TGRADIENT

INTERFACE ACDC_SAVE
MODULE PROCEDURE ACDC_SAVE_TGRADIENT
END INTERFACE

INTERFACE ACDC_LOAD
MODULE PROCEDURE ACDC_LOAD_TGRADIENT
END INTERFACE

INTERFACE ACDC_COPY
MODULE PROCEDURE ACDC_COPY_TGRADIENT
END INTERFACE

INTERFACE ACDC_HOST
MODULE PROCEDURE ACDC_HOST_TGRADIENT
END INTERFACE


INTERFACE ACDC_CRC64
MODULE PROCEDURE ACDC_CRC64_TGRADIENT
END INTERFACE

INTERFACE ACDC_WIPE
MODULE PROCEDURE ACDC_WIPE_TGRADIENT
END INTERFACE

INTERFACE ACDC_SIZE
MODULE PROCEDURE ACDC_SIZE_TGRADIENT
END INTERFACE

CONTAINS

SUBROUTINE ACDC_SAVE_TGRADIENT (SELF, KLUN)
USE UTIL_TGRADIENT_PTR_MOD
IMPLICIT NONE
TYPE (TGRADIENT), INTENT (IN), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN

CALL ACDC_SAVE_TGRADIENT_PTR (SELF%YPTR, KLUN)
END SUBROUTINE

SUBROUTINE ACDC_LOAD_TGRADIENT (SELF, KLUN)
USE UTIL_TGRADIENT_PTR_MOD
IMPLICIT NONE
TYPE (TGRADIENT), INTENT (OUT), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN

CALL ACDC_LOAD_TGRADIENT_PTR (SELF%YPTR, KLUN)
END SUBROUTINE

SUBROUTINE ACDC_COPY_TGRADIENT (SELF, LDCREATED, LDFIELDAPI)
USE UTIL_TGRADIENT_PTR_MOD
IMPLICIT NONE
TYPE (TGRADIENT), INTENT (IN), TARGET :: SELF
LOGICAL, OPTIONAL, INTENT (IN) :: LDCREATED, LDFIELDAPI
LOGICAL :: LLCREATED
LOGICAL :: LLFIELDAPI
LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF
LLCREATED = .FALSE.
IF (PRESENT (LDCREATED)) THEN
  LLCREATED = LDCREATED
ENDIF
IF (.NOT. LLCREATED) THEN
  !$ACC ENTER DATA CREATE (SELF)
  !$ACC UPDATE DEVICE (SELF)
ENDIF
CALL ACDC_COPY_TGRADIENT_PTR (SELF%YPTR, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

END SUBROUTINE

SUBROUTINE ACDC_HOST_TGRADIENT (SELF)
USE UTIL_TGRADIENT_PTR_MOD
IMPLICIT NONE
TYPE (TGRADIENT), TARGET :: SELF

CALL ACDC_HOST_TGRADIENT_PTR (SELF%YPTR)

END SUBROUTINE



SUBROUTINE ACDC_CRC64_TGRADIENT (SELF, KLUN, CDPATH)
USE UTIL_TGRADIENT_PTR_MOD
USE FXTRAN_ACDC_CRC64_INTRINSIC_MOD, ONLY : FCRC64 => FXTRAN_ACDC_CRC64
IMPLICIT NONE
TYPE (TGRADIENT), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
CHARACTER(LEN=*), INTENT (IN) :: CDPATH
CHARACTER(LEN=128) :: CLIND
CLIND = ''
CALL ACDC_CRC64_TGRADIENT_PTR (SELF%YPTR, KLUN, CDPATH//'%YPTR'//TRIM(CLIND))

END SUBROUTINE

SUBROUTINE ACDC_WIPE_TGRADIENT (SELF, LDDELETED, LDFIELDAPI)
USE UTIL_TGRADIENT_PTR_MOD
IMPLICIT NONE
TYPE (TGRADIENT), INTENT (IN), TARGET :: SELF
LOGICAL, OPTIONAL, INTENT (IN) :: LDDELETED, LDFIELDAPI
LOGICAL :: LLDELETED
LOGICAL :: LLFIELDAPI
LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF
CALL ACDC_WIPE_TGRADIENT_PTR (SELF%YPTR, LDDELETED=.TRUE., LDFIELDAPI=LDFIELDAPI)

LLDELETED = .FALSE.
IF (PRESENT (LDDELETED)) THEN
  LLDELETED = LDDELETED
ENDIF
IF (.NOT. LLDELETED) THEN
  !$ACC EXIT DATA DELETE (SELF)
ENDIF
END SUBROUTINE

FUNCTION ACDC_SIZE_TGRADIENT (SELF, CDPATH, LDPRINT) RESULT (KSIZE)
USE UTIL_TGRADIENT_PTR_MOD
IMPLICIT NONE
TYPE (TGRADIENT),     INTENT (IN), TARGET :: SELF
CHARACTER(LEN=*), INTENT (IN), OPTIONAL :: CDPATH
LOGICAL,          INTENT (IN), OPTIONAL :: LDPRINT
INTEGER*8 :: KSIZE
INTEGER*8 :: ISIZE, JSIZE
LOGICAL :: LLPRINT
CHARACTER(LEN=128) :: CLPATH
LLPRINT = .FALSE.
IF (PRESENT (LDPRINT)) THEN
  LLPRINT = LDPRINT
ENDIF
CLPATH=''
IF (PRESENT (CDPATH)) THEN
  CLPATH = CDPATH
ENDIF
KSIZE = 0
JSIZE = 0
ISIZE = ACDC_SIZE_TGRADIENT_PTR (SELF%YPTR, CLPATH//'%YPTR', .FALSE.)
JSIZE = JSIZE + ISIZE
KSIZE = KSIZE + ISIZE
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') JSIZE
  WRITE (*, *) TRIM (CLPATH)//'%YPTR'
ENDIF
END FUNCTION

END MODULE
