MODULE UTIL_MF_PHYS_STATE_GSP_SB_TYPE_MOD

USE MF_PHYS_BASE_STATE_TYPE_MOD, ONLY : MF_PHYS_STATE_GSP_SB_TYPE



INTERFACE ACDC_COPY
MODULE PROCEDURE ACDC_COPY_MF_PHYS_STATE_GSP_SB_TYPE
END INTERFACE




INTERFACE ACDC_WIPE
MODULE PROCEDURE ACDC_WIPE_MF_PHYS_STATE_GSP_SB_TYPE
END INTERFACE


CONTAINS





SUBROUTINE ACDC_COPY_MF_PHYS_STATE_GSP_SB_TYPE (SELF, LDCREATED, LDFIELDAPI)
USE FIELD_UTIL_MODULE
IMPLICIT NONE
TYPE (MF_PHYS_STATE_GSP_SB_TYPE), INTENT (IN), TARGET :: SELF
LOGICAL, OPTIONAL, INTENT (IN) :: LDCREATED, LDFIELDAPI
LOGICAL :: LLCREATED
LOGICAL :: LLFIELDAPI
LOGICAL :: LF_Q, LF_T, LF_TL
LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF
LLCREATED = .FALSE.
IF (PRESENT (LDCREATED)) THEN
  LLCREATED = LDCREATED
ENDIF
IF (.NOT. LLCREATED) THEN
  !$ACC ENTER DATA CREATE (SELF)
  !$ACC UPDATE DEVICE (SELF)
ENDIF
LF_Q = ASSOCIATED (SELF%F_Q)
IF (LF_Q .AND. LLFIELDAPI) THEN
  CALL COPY_FIELD_3RB (SELF%F_Q)
  !$ACC ENTER DATA ATTACH (SELF%F_Q)
ENDIF

LF_TL = ASSOCIATED (SELF%F_TL)
IF (LF_TL .AND. LLFIELDAPI) THEN
  CALL COPY_FIELD_3RB (SELF%F_TL)
  !$ACC ENTER DATA ATTACH (SELF%F_TL)
ENDIF

LF_T = ASSOCIATED (SELF%F_T)
IF (LF_T .AND. LLFIELDAPI) THEN
  CALL COPY_FIELD_3RB (SELF%F_T)
  !$ACC ENTER DATA ATTACH (SELF%F_T)
ENDIF

END SUBROUTINE







SUBROUTINE ACDC_WIPE_MF_PHYS_STATE_GSP_SB_TYPE (SELF, LDDELETED, LDFIELDAPI)
USE FIELD_UTIL_MODULE
IMPLICIT NONE
TYPE (MF_PHYS_STATE_GSP_SB_TYPE), INTENT (IN), TARGET :: SELF
LOGICAL, OPTIONAL, INTENT (IN) :: LDDELETED, LDFIELDAPI
LOGICAL :: LLDELETED
LOGICAL :: LLFIELDAPI
LOGICAL :: LF_Q, LF_T, LF_TL
LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF
LF_Q = ASSOCIATED (SELF%F_Q)
IF (LF_Q .AND. LLFIELDAPI) THEN
  CALL WIPE_FIELD_3RB (SELF%F_Q)
ENDIF

LF_TL = ASSOCIATED (SELF%F_TL)
IF (LF_TL .AND. LLFIELDAPI) THEN
  CALL WIPE_FIELD_3RB (SELF%F_TL)
ENDIF

LF_T = ASSOCIATED (SELF%F_T)
IF (LF_T .AND. LLFIELDAPI) THEN
  CALL WIPE_FIELD_3RB (SELF%F_T)
ENDIF

LLDELETED = .FALSE.
IF (PRESENT (LDDELETED)) THEN
  LLDELETED = LDDELETED
ENDIF
IF (.NOT. LLDELETED) THEN
  !$ACC EXIT DATA DELETE (SELF)
ENDIF
END SUBROUTINE



END MODULE
