SUBROUTINE ACDC_LOAD_TEGWWMS (SELF, KLUN)
USE PARKIND1, ONLY : JPRD
IMPLICIT NONE
TYPE (TEGWWMS), INTENT (OUT), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
REAL(KIND=JPRD) :: ZTMP0
REAL(KIND=JPRD), ALLOCATABLE :: ZTMP1 (:)
READ (KLUN) SELF%LOZPR
READ (KLUN) SELF%LGACALC
READ (KLUN) SELF%LGSATL
READ (KLUN) SELF%LGINDL
READ (KLUN) SELF%NLAUNCHL
READ (KLUN) SELF%NLAUNCHLEV
READ (KLUN) SELF%NLAUNCH
READ (KLUN) SELF%NSLOPE
READ (KLUN) SELF%NGAUSS
ALLOCATE (ZTMP1(LBOUND(SELF%GFLUXLAUNL,1):UBOUND(SELF%GFLUXLAUNL,1)))
READ (KLUN) ZTMP1
SELF%GFLUXLAUNL = ZTMP1
DEALLOCATE (ZTMP1)
READ (KLUN) ZTMP0
SELF%GFLUXLAUN = ZTMP0
ALLOCATE (ZTMP1(LBOUND(SELF%GMSTAR_L,1):UBOUND(SELF%GMSTAR_L,1)))
READ (KLUN) ZTMP1
SELF%GMSTAR_L = ZTMP1
DEALLOCATE (ZTMP1)
READ (KLUN) ZTMP0
SELF%GCSTAR = ZTMP0
READ (KLUN) ZTMP0
SELF%GPTWO = ZTMP0
READ (KLUN) ZTMP0
SELF%GTPHYGWWMS = ZTMP0
READ (KLUN) ZTMP0
SELF%GGAUSSA = ZTMP0
ALLOCATE (ZTMP1(LBOUND(SELF%GGAUSSB,1):UBOUND(SELF%GGAUSSB,1)))
READ (KLUN) ZTMP1
SELF%GGAUSSB = ZTMP1
DEALLOCATE (ZTMP1)
READ (KLUN) ZTMP0
SELF%GCOEFF = ZTMP0
END SUBROUTINE

