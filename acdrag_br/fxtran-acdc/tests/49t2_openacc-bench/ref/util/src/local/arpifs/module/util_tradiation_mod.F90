SUBROUTINE ACDC_SAVE_TRADIATION (SELF, KLUN)

IMPLICIT NONE
TYPE (TRADIATION), INTENT (IN), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN

WRITE (KLUN) SELF%NWEIGHT_UV
WRITE (KLUN) SELF%IBAND_UV
WRITE (KLUN) SELF%WEIGHT_UV
WRITE (KLUN) SELF%NWEIGHT_PAR
WRITE (KLUN) SELF%IBAND_PAR
WRITE (KLUN) SELF%WEIGHT_PAR
WRITE (KLUN) SELF%TROP_BG_AER_MASS_EXT
WRITE (KLUN) SELF%STRAT_BG_AER_MASS_EXT
END SUBROUTINE

SUBROUTINE ACDC_LOAD_TRADIATION (SELF, KLUN)
USE PARKIND1, ONLY : JPRD
IMPLICIT NONE
TYPE (TRADIATION), INTENT (OUT), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
REAL(KIND=JPRD) :: ZTMP0
REAL(KIND=JPRD), ALLOCATABLE :: ZTMP1 (:)
READ (KLUN) SELF%NWEIGHT_UV
READ (KLUN) SELF%IBAND_UV
ALLOCATE (ZTMP1(LBOUND(SELF%WEIGHT_UV,1):UBOUND(SELF%WEIGHT_UV,1)))
READ (KLUN) ZTMP1
SELF%WEIGHT_UV = ZTMP1
DEALLOCATE (ZTMP1)
READ (KLUN) SELF%NWEIGHT_PAR
READ (KLUN) SELF%IBAND_PAR
ALLOCATE (ZTMP1(LBOUND(SELF%WEIGHT_PAR,1):UBOUND(SELF%WEIGHT_PAR,1)))
READ (KLUN) ZTMP1
SELF%WEIGHT_PAR = ZTMP1
DEALLOCATE (ZTMP1)
READ (KLUN) ZTMP0
SELF%TROP_BG_AER_MASS_EXT = ZTMP0
READ (KLUN) ZTMP0
SELF%STRAT_BG_AER_MASS_EXT = ZTMP0
END SUBROUTINE

