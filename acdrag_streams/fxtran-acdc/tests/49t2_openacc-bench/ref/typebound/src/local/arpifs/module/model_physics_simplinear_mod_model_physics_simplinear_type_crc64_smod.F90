SUBMODULE (MODEL_PHYSICS_SIMPLINEAR_MOD) MODEL_PHYSICS_SIMPLINEAR_MOD_MODEL_PHYSICS_SIMPLINEAR_TYPE_CRC64_SMOD

IMPLICIT NONE

CONTAINS

MODULE SUBROUTINE ACDC_CRC64_MODEL_PHYSICS_SIMPLINEAR_TYPE (SELF, KLUN, CDPATH)
USE FXTRAN_ACDC_CRC64_INTRINSIC_MOD, ONLY : FCRC64 => FXTRAN_ACDC_CRC64
IMPLICIT NONE
CLASS (MODEL_PHYSICS_SIMPLINEAR_TYPE), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
CHARACTER(LEN=*), INTENT (IN) :: CDPATH
CHARACTER(LEN=128) :: CLIND
INTEGER :: J1
LOGICAL :: LGPHIST
CLIND = ''
CALL SELF%YREPHLI%ACDC_CRC64 (KLUN, CDPATH//'%YREPHLI'//TRIM(CLIND))

CLIND = ''
CALL SELF%YRCUMFS%ACDC_CRC64 (KLUN, CDPATH//'%YRCUMFS'//TRIM(CLIND))

CLIND = ''
CALL SELF%YREGWDWMS%ACDC_CRC64 (KLUN, CDPATH//'%YREGWDWMS'//TRIM(CLIND))

CLIND = ''
CALL SELF%YRECUMF2%ACDC_CRC64 (KLUN, CDPATH//'%YRECUMF2'//TRIM(CLIND))

CLIND = ''
CALL SELF%YRPHLC%ACDC_CRC64 (KLUN, CDPATH//'%YRPHLC'//TRIM(CLIND))

CLIND = ''
CALL SELF%YRPHNC%ACDC_CRC64 (KLUN, CDPATH//'%YRPHNC'//TRIM(CLIND))

CLIND = ''
CALL SELF%YRNCL%ACDC_CRC64 (KLUN, CDPATH//'%YRNCL'//TRIM(CLIND))

CLIND = ''
CALL SELF%YRSRFTLAD%ACDC_CRC64 (KLUN, CDPATH//'%YRSRFTLAD'//TRIM(CLIND))

LGPHIST = ASSOCIATED (SELF%GPHIST)
IF (LGPHIST) THEN
  DO J1 = LBOUND (SELF%GPHIST, 1), UBOUND (SELF%GPHIST, 1)
    WRITE (CLIND, '("(",I0,")")') J1
    CALL SELF%GPHIST (J1)%ACDC_CRC64 (KLUN, CDPATH//'%GPHIST'//TRIM(CLIND))
  ENDDO
ENDIF

END SUBROUTINE

END SUBMODULE
