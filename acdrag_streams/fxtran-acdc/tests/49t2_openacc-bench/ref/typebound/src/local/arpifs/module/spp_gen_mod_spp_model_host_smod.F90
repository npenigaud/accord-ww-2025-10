SUBMODULE (SPP_GEN_MOD) SPP_GEN_MOD_SPP_MODEL_HOST_SMOD

IMPLICIT NONE

CONTAINS

MODULE SUBROUTINE ACDC_HOST_SPP_MODEL (SELF)

IMPLICIT NONE
CLASS (SPP_MODEL), TARGET :: SELF
INTEGER :: J1

DO J1 = LBOUND (SELF%PNDEF, 1), UBOUND (SELF%PNDEF, 1)
  CALL SELF%PNDEF (J1)%ACDC_HOST ()
ENDDO

DO J1 = LBOUND (SELF%PN, 1), UBOUND (SELF%PN, 1)
  CALL SELF%PN (J1)%ACDC_HOST ()
ENDDO

END SUBROUTINE

END SUBMODULE
