IF (LSTPREH) THEN
  ISIZE = KIND (SELF%STPREH) * SIZE (SELF%STPREH)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%STPREH'
  ENDIF
  KSIZE = KSIZE + ISIZE
ENDIF
