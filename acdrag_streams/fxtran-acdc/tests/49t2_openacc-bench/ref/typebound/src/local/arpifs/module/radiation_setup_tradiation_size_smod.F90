SUBMODULE (RADIATION_SETUP) RADIATION_SETUP_TRADIATION_SIZE_SMOD

IMPLICIT NONE

CONTAINS

MODULE FUNCTION ACDC_SIZE_TRADIATION (SELF, CDPATH, LDPRINT) RESULT (KSIZE)

IMPLICIT NONE
CLASS (TRADIATION),     INTENT (IN), TARGET :: SELF
CHARACTER(LEN=*), INTENT (IN), OPTIONAL :: CDPATH
LOGICAL,          INTENT (IN), OPTIONAL :: LDPRINT
INTEGER*8 :: KSIZE
INTEGER*8 :: ISIZE, JSIZE
LOGICAL :: LLPRINT
CHARACTER(LEN=128) :: CLPATH
LLPRINT = .FALSE.
IF (PRESENT (LDPRINT)) THEN
  LLPRINT = LDPRINT
ENDIF
CLPATH=''
IF (PRESENT (CDPATH)) THEN
  CLPATH = CDPATH
ENDIF
KSIZE = 0
ISIZE = KIND (SELF%NWEIGHT_UV)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%NWEIGHT_UV'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%IBAND_UV) * SIZE (SELF%IBAND_UV)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%IBAND_UV'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%WEIGHT_UV) * SIZE (SELF%WEIGHT_UV)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%WEIGHT_UV'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%NWEIGHT_PAR)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%NWEIGHT_PAR'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%IBAND_PAR) * SIZE (SELF%IBAND_PAR)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%IBAND_PAR'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%WEIGHT_PAR) * SIZE (SELF%WEIGHT_PAR)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%WEIGHT_PAR'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%TROP_BG_AER_MASS_EXT)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%TROP_BG_AER_MASS_EXT'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%STRAT_BG_AER_MASS_EXT)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%STRAT_BG_AER_MASS_EXT'
ENDIF
KSIZE = KSIZE + ISIZE
END FUNCTION

END SUBMODULE
