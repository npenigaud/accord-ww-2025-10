MODULE SUBROUTINE ACDC_LOAD_TVERTICAL_GEOM (SELF, KLUN)

IMPLICIT NONE
CLASS (TVERTICAL_GEOM), INTENT (OUT), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN

READ (KLUN) SELF%LNONHYD_GEOM
CALL SELF%YRVAB%ACDC_LOAD (KLUN)
CALL SELF%YRVETA%ACDC_LOAD (KLUN)
CALL SELF%YRVFE%ACDC_LOAD (KLUN)
CALL SELF%YRCVER%ACDC_LOAD (KLUN)
END SUBROUTINE

END SUBMODULE
