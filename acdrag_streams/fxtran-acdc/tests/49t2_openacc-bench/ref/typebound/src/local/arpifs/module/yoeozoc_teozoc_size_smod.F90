SUBMODULE (YOEOZOC) YOEOZOC_TEOZOC_SIZE_SMOD

IMPLICIT NONE

CONTAINS

MODULE FUNCTION ACDC_SIZE_TEOZOC (SELF, CDPATH, LDPRINT) RESULT (KSIZE)

IMPLICIT NONE
CLASS (TEOZOC),     INTENT (IN), TARGET :: SELF
CHARACTER(LEN=*), INTENT (IN), OPTIONAL :: CDPATH
LOGICAL,          INTENT (IN), OPTIONAL :: LDPRINT
INTEGER*8 :: KSIZE
INTEGER*8 :: ISIZE, JSIZE
LOGICAL :: LLPRINT
CHARACTER(LEN=128) :: CLPATH
LOGICAL :: LZOZCL, LZOZCLAQUA
LLPRINT = .FALSE.
IF (PRESENT (LDPRINT)) THEN
  LLPRINT = LDPRINT
ENDIF
CLPATH=''
IF (PRESENT (CDPATH)) THEN
  CLPATH = CDPATH
ENDIF
KSIZE = 0
LZOZCL = ALLOCATED (SELF%ZOZCL)
IF (LZOZCL) THEN
  ISIZE = KIND (SELF%ZOZCL) * SIZE (SELF%ZOZCL)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%ZOZCL'
  ENDIF
  KSIZE = KSIZE + ISIZE
ENDIF
LZOZCLAQUA = ALLOCATED (SELF%ZOZCLAQUA)
IF (LZOZCLAQUA) THEN
  ISIZE = KIND (SELF%ZOZCLAQUA) * SIZE (SELF%ZOZCLAQUA)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%ZOZCLAQUA'
  ENDIF
  KSIZE = KSIZE + ISIZE
ENDIF
ISIZE = KIND (SELF%COZQC) * SIZE (SELF%COZQC)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%COZQC'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%COZQS) * SIZE (SELF%COZQS)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%COZQS'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%COZHC) * SIZE (SELF%COZHC)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%COZHC'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%COZHS) * SIZE (SELF%COZHS)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%COZHS'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%RSINC) * SIZE (SELF%RSINC)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%RSINC'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%ROZT) * SIZE (SELF%ROZT)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%ROZT'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%RPROC) * SIZE (SELF%RPROC)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%RPROC'
ENDIF
KSIZE = KSIZE + ISIZE
END FUNCTION

END SUBMODULE
