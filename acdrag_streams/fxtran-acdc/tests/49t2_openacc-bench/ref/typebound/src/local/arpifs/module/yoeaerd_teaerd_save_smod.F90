SUBMODULE (YOEAERD) YOEAERD_TEAERD_SAVE_SMOD

IMPLICIT NONE

CONTAINS

MODULE SUBROUTINE ACDC_SAVE_TEAERD (SELF, KLUN)

IMPLICIT NONE
CLASS (TEAERD), INTENT (IN), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
LOGICAL :: LCVDAED, LCVDAEL, LCVDAES, LCVDAEU
LCVDAES = ALLOCATED (SELF%CVDAES)
WRITE (KLUN) LCVDAES
IF (LCVDAES) THEN
  WRITE (KLUN) LBOUND (SELF%CVDAES)
  WRITE (KLUN) UBOUND (SELF%CVDAES)
  WRITE (KLUN) SELF%CVDAES
ENDIF
LCVDAEL = ALLOCATED (SELF%CVDAEL)
WRITE (KLUN) LCVDAEL
IF (LCVDAEL) THEN
  WRITE (KLUN) LBOUND (SELF%CVDAEL)
  WRITE (KLUN) UBOUND (SELF%CVDAEL)
  WRITE (KLUN) SELF%CVDAEL
ENDIF
LCVDAEU = ALLOCATED (SELF%CVDAEU)
WRITE (KLUN) LCVDAEU
IF (LCVDAEU) THEN
  WRITE (KLUN) LBOUND (SELF%CVDAEU)
  WRITE (KLUN) UBOUND (SELF%CVDAEU)
  WRITE (KLUN) SELF%CVDAEU
ENDIF
LCVDAED = ALLOCATED (SELF%CVDAED)
WRITE (KLUN) LCVDAED
IF (LCVDAED) THEN
  WRITE (KLUN) LBOUND (SELF%CVDAED)
  WRITE (KLUN) UBOUND (SELF%CVDAED)
  WRITE (KLUN) SELF%CVDAED
ENDIF
WRITE (KLUN) SELF%RAESC
WRITE (KLUN) SELF%RAESS
WRITE (KLUN) SELF%RAELC
WRITE (KLUN) SELF%RAELS
WRITE (KLUN) SELF%RAEUC
WRITE (KLUN) SELF%RAEUS
WRITE (KLUN) SELF%RAEDC
WRITE (KLUN) SELF%RAEDS
WRITE (KLUN) SELF%RCAEOPS
WRITE (KLUN) SELF%RCAEOPL
WRITE (KLUN) SELF%RCAEOPU
WRITE (KLUN) SELF%RCAEOPD
WRITE (KLUN) SELF%RCTRBGA
WRITE (KLUN) SELF%RCVOBGA
WRITE (KLUN) SELF%RCSTBGA
WRITE (KLUN) SELF%RCTRPT
WRITE (KLUN) SELF%RCAEADK
WRITE (KLUN) SELF%RCAEADM
WRITE (KLUN) SELF%RCAEROS
END SUBROUTINE

END SUBMODULE
