SUBMODULE (YEMDIM) YEMDIM_TEDIM_LOAD_SMOD

IMPLICIT NONE

CONTAINS

MODULE SUBROUTINE ACDC_LOAD_TEDIM (SELF, KLUN)

IMPLICIT NONE
CLASS (TEDIM), INTENT (OUT), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
INTEGER :: IL1(1), IU1(1)
LOGICAL :: LNISMAX, LNISNAX
READ (KLUN) SELF%NSECPLG
READ (KLUN) SELF%NBZONG
READ (KLUN) SELF%NBZONL
READ (KLUN) SELF%NBZONU
READ (KLUN) SELF%NNOEXTZG
READ (KLUN) SELF%NNOEXTZL
READ (KLUN) LNISMAX
IF (LNISMAX) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (SELF%NISMAX (IL1(1):IU1(1)))
  READ (KLUN) SELF%NISMAX
ELSE
  NULLIFY (SELF%NISMAX)
ENDIF
READ (KLUN) LNISNAX
IF (LNISNAX) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (SELF%NISNAX (IL1(1):IU1(1)))
  READ (KLUN) SELF%NISNAX
ELSE
  NULLIFY (SELF%NISNAX)
ENDIF
READ (KLUN) SELF%LBIPINCI
READ (KLUN) SELF%NBIPINCIX
READ (KLUN) SELF%NBIPINCIY
READ (KLUN) SELF%NEDOM
END SUBROUTINE

END SUBMODULE
