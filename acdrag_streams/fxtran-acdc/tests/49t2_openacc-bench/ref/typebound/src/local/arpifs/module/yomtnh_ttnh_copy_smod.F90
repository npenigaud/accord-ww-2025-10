SUBMODULE (YOMTNH) YOMTNH_TTNH_COPY_SMOD

IMPLICIT NONE

CONTAINS

MODULE SUBROUTINE ACDC_COPY_TTNH (SELF, LDCREATED, LDFIELDAPI)

IMPLICIT NONE
CLASS (TTNH), INTENT (IN), TARGET :: SELF
LOGICAL, OPTIONAL, INTENT (IN) :: LDCREATED, LDFIELDAPI
LOGICAL :: LLCREATED
LOGICAL :: LLFIELDAPI
LOGICAL :: LTRAJNH
LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF
LLCREATED = .FALSE.
IF (PRESENT (LDCREATED)) THEN
  LLCREATED = LDCREATED
ENDIF
IF (.NOT. LLCREATED) THEN
  !$ACC ENTER DATA CREATE (SELF)
  !$ACC UPDATE DEVICE (SELF)
ENDIF

LTRAJNH = ALLOCATED (SELF%TRAJNH)
IF (LTRAJNH) THEN
  !$ACC ENTER DATA CREATE (SELF%TRAJNH)
  !$ACC UPDATE DEVICE (SELF%TRAJNH)
  !$ACC ENTER DATA ATTACH (SELF%TRAJNH)
ENDIF

END SUBROUTINE

END SUBMODULE
