MODULE YOMLAP

!$ACDC methods 


USE PARKIND1  ,ONLY : JPIM     ,JPRB

IMPLICIT NONE

SAVE

!     ------------------------------------------------------------------
!*    Constants related to the Laplace space


!  NASN0(0:NSMAX)  : address in a spectral array of (n, m=0)
!  NASM0(0:NSMAX)  : address in a spectral array of (m, n=m)
!  NASM0G(0:NSMAX) : address in a global version of spectral array of (m, n=m)
!  NVALUE(NTPEC2)  : n value for each NSPEC2 spectral coeffient
!  MYMS(0:NUMP)    : actual wave numbers handled by this processor
!  NSPZERO(0:NSMAX): Index for the imaginary m=0 values
!  NSE0L(NUMP)     : Index array, used for example in Helmholtz operator and
!                    horizontal diffusion operator, to find the first element
!                    of the non-zero diagonals of these operators for each
!                    zonal wave number m. DM-local
!  RLAPDI(-1:NSMAX+2) :  eigen-values of the Laplace operator
!  RLAPIN(-1:NSMAX+2) :  eigen-values of its inverse

TYPE TLAP
  INTEGER(KIND=JPIM), ALLOCATABLE :: NASN0(:)
  INTEGER(KIND=JPIM), ALLOCATABLE :: NASM0(:)
  INTEGER(KIND=JPIM), ALLOCATABLE :: NASM0G(:)
  INTEGER(KIND=JPIM), ALLOCATABLE :: NVALUE(:)
  INTEGER(KIND=JPIM), ALLOCATABLE :: MYMS(:)
  INTEGER(KIND=JPIM), ALLOCATABLE :: NSPZERO(:)
  INTEGER(KIND=JPIM), ALLOCATABLE :: NSE0L(:)

  REAL(KIND=JPRB),    ALLOCATABLE :: RLAPDI(:)
  REAL(KIND=JPRB),    ALLOCATABLE :: RLAPIN(:)
CONTAINS
PROCEDURE :: ACDC_COPY => ACDC_COPY_TLAP
PROCEDURE :: ACDC_CRC64 => ACDC_CRC64_TLAP
PROCEDURE :: ACDC_HOST => ACDC_HOST_TLAP
PROCEDURE :: ACDC_LOAD => ACDC_LOAD_TLAP
PROCEDURE :: ACDC_SAVE => ACDC_SAVE_TLAP
PROCEDURE :: ACDC_SIZE => ACDC_SIZE_TLAP
PROCEDURE :: ACDC_WIPE => ACDC_WIPE_TLAP
END TYPE TLAP


INTERFACE

MODULE SUBROUTINE ACDC_COPY_TLAP (SELF, LDCREATED, LDFIELDAPI)

IMPLICIT NONE
CLASS (TLAP), INTENT (IN), TARGET :: SELF
LOGICAL, OPTIONAL, INTENT (IN) :: LDCREATED, LDFIELDAPI
END SUBROUTINE

MODULE SUBROUTINE ACDC_CRC64_TLAP (SELF, KLUN, CDPATH)
USE FXTRAN_ACDC_CRC64_INTRINSIC_MOD, ONLY : FCRC64 => FXTRAN_ACDC_CRC64
IMPLICIT NONE
CLASS (TLAP), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
CHARACTER(LEN=*), INTENT (IN) :: CDPATH
END SUBROUTINE

MODULE SUBROUTINE ACDC_HOST_TLAP (SELF)

IMPLICIT NONE
CLASS (TLAP), TARGET :: SELF
END SUBROUTINE

MODULE SUBROUTINE ACDC_LOAD_TLAP (SELF, KLUN)
USE PARKIND1, ONLY : JPRD
IMPLICIT NONE
CLASS (TLAP), INTENT (OUT), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
END SUBROUTINE

MODULE SUBROUTINE ACDC_SAVE_TLAP (SELF, KLUN)

IMPLICIT NONE
CLASS (TLAP), INTENT (IN), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
END SUBROUTINE

MODULE FUNCTION ACDC_SIZE_TLAP (SELF, CDPATH, LDPRINT) RESULT (KSIZE)

IMPLICIT NONE
CLASS (TLAP),     INTENT (IN), TARGET :: SELF
CHARACTER(LEN=*), INTENT (IN), OPTIONAL :: CDPATH
LOGICAL,          INTENT (IN), OPTIONAL :: LDPRINT
INTEGER*8 :: KSIZE
END FUNCTION

MODULE SUBROUTINE ACDC_WIPE_TLAP (SELF, LDDELETED, LDFIELDAPI)

IMPLICIT NONE
CLASS (TLAP), INTENT (IN), TARGET :: SELF
LOGICAL, OPTIONAL, INTENT (IN) :: LDDELETED, LDFIELDAPI
END SUBROUTINE


END INTERFACE

END MODULE YOMLAP
