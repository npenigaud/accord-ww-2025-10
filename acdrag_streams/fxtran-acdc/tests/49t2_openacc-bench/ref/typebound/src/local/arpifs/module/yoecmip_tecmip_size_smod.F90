SUBMODULE (YOECMIP) YOECMIP_TECMIP_SIZE_SMOD

IMPLICIT NONE

CONTAINS

MODULE FUNCTION ACDC_SIZE_TECMIP (SELF, CDPATH, LDPRINT) RESULT (KSIZE)

IMPLICIT NONE
CLASS (TECMIP),     INTENT (IN), TARGET :: SELF
CHARACTER(LEN=*), INTENT (IN), OPTIONAL :: CDPATH
LOGICAL,          INTENT (IN), OPTIONAL :: LDPRINT
INTEGER*8 :: KSIZE
INTEGER*8 :: ISIZE, JSIZE
LOGICAL :: LLPRINT
CHARACTER(LEN=128) :: CLPATH
LOGICAL :: LRLATCLI, LRLONCLI, LROZT1, LRPROC1, LRSINC1, LZOZCL
LLPRINT = .FALSE.
IF (PRESENT (LDPRINT)) THEN
  LLPRINT = LDPRINT
ENDIF
CLPATH=''
IF (PRESENT (CDPATH)) THEN
  CLPATH = CDPATH
ENDIF
KSIZE = 0
ISIZE = KIND (SELF%NRCP)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%NRCP'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%NO3CMIP)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%NO3CMIP'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%NGHGCMIP)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%NGHGCMIP'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%NCMIPFIXYR)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%NCMIPFIXYR'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%NCURRYR)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%NCURRYR'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%GHGDATADIR) * LEN (SELF%GHGDATADIR)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%GHGDATADIR'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%CSOLARDATA) * LEN (SELF%CSOLARDATA)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%CSOLARDATA'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%CO3DATADIR) * LEN (SELF%CO3DATADIR)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%CO3DATADIR'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%CO3DATAFIL) * LEN (SELF%CO3DATAFIL)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%CO3DATAFIL'
ENDIF
KSIZE = KSIZE + ISIZE
LZOZCL = ALLOCATED (SELF%ZOZCL)
IF (LZOZCL) THEN
  ISIZE = KIND (SELF%ZOZCL) * SIZE (SELF%ZOZCL)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%ZOZCL'
  ENDIF
  KSIZE = KSIZE + ISIZE
ENDIF
LRSINC1 = ALLOCATED (SELF%RSINC1)
IF (LRSINC1) THEN
  ISIZE = KIND (SELF%RSINC1) * SIZE (SELF%RSINC1)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%RSINC1'
  ENDIF
  KSIZE = KSIZE + ISIZE
ENDIF
LRPROC1 = ALLOCATED (SELF%RPROC1)
IF (LRPROC1) THEN
  ISIZE = KIND (SELF%RPROC1) * SIZE (SELF%RPROC1)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%RPROC1'
  ENDIF
  KSIZE = KSIZE + ISIZE
ENDIF
LRLATCLI = ALLOCATED (SELF%RLATCLI)
IF (LRLATCLI) THEN
  ISIZE = KIND (SELF%RLATCLI) * SIZE (SELF%RLATCLI)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%RLATCLI'
  ENDIF
  KSIZE = KSIZE + ISIZE
ENDIF
LRLONCLI = ALLOCATED (SELF%RLONCLI)
IF (LRLONCLI) THEN
  ISIZE = KIND (SELF%RLONCLI) * SIZE (SELF%RLONCLI)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%RLONCLI'
  ENDIF
  KSIZE = KSIZE + ISIZE
ENDIF
LROZT1 = ALLOCATED (SELF%ROZT1)
IF (LROZT1) THEN
  ISIZE = KIND (SELF%ROZT1) * SIZE (SELF%ROZT1)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%ROZT1'
  ENDIF
  KSIZE = KSIZE + ISIZE
ENDIF
END FUNCTION

END SUBMODULE
