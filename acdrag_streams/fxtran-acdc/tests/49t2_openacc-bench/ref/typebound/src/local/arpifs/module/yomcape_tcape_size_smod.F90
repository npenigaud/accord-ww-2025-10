SUBMODULE (YOMCAPE) YOMCAPE_TCAPE_SIZE_SMOD

IMPLICIT NONE

CONTAINS

MODULE FUNCTION ACDC_SIZE_TCAPE (SELF, CDPATH, LDPRINT) RESULT (KSIZE)

IMPLICIT NONE
CLASS (TCAPE),     INTENT (IN), TARGET :: SELF
CHARACTER(LEN=*), INTENT (IN), OPTIONAL :: CDPATH
LOGICAL,          INTENT (IN), OPTIONAL :: LDPRINT
INTEGER*8 :: KSIZE
INTEGER*8 :: ISIZE, JSIZE
LOGICAL :: LLPRINT
CHARACTER(LEN=128) :: CLPATH
LLPRINT = .FALSE.
IF (PRESENT (LDPRINT)) THEN
  LLPRINT = LDPRINT
ENDIF
CLPATH=''
IF (PRESENT (CDPATH)) THEN
  CLPATH = CDPATH
ENDIF
KSIZE = 0
ISIZE = KIND (SELF%NCAPEITER)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%NCAPEITER'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%NETAPES)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%NETAPES'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%NCAPEPSD)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%NCAPEPSD'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%GCAPERET)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%GCAPERET'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%GCAPEPSD)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%GCAPEPSD'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%GMISCINV)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%GMISCINV'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%GCAPEMIN)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%GCAPEMIN'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%GCINMAX)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%GCINMAX'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%LADAE)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%LADAE'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%LMCAPEA)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%LMCAPEA'
ENDIF
KSIZE = KSIZE + ISIZE
END FUNCTION

END SUBMODULE
