MODULE SUBROUTINE ACDC_CRC64_TRADIATION (SELF, KLUN, CDPATH)
USE FXTRAN_ACDC_CRC64_INTRINSIC_MOD, ONLY : FCRC64 => FXTRAN_ACDC_CRC64
IMPLICIT NONE
CLASS (TRADIATION), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
CHARACTER(LEN=*), INTENT (IN) :: CDPATH
CHARACTER(LEN=128) :: CLIND
WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (SELF%NWEIGHT_UV), CDPATH//'%NWEIGHT_UV'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (SELF%IBAND_UV), CDPATH//'%IBAND_UV'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (SELF%WEIGHT_UV), CDPATH//'%WEIGHT_UV'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (SELF%NWEIGHT_PAR), CDPATH//'%NWEIGHT_PAR'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (SELF%IBAND_PAR), CDPATH//'%IBAND_PAR'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (SELF%WEIGHT_PAR), CDPATH//'%WEIGHT_PAR'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (SELF%TROP_BG_AER_MASS_EXT), CDPATH//'%TROP_BG_AER_MASS_EXT'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (SELF%STRAT_BG_AER_MASS_EXT), CDPATH//'%STRAT_BG_AER_MASS_EXT'

END SUBROUTINE

