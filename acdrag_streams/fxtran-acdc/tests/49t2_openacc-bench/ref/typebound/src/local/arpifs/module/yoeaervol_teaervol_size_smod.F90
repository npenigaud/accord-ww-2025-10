SUBMODULE (YOEAERVOL) YOEAERVOL_TEAERVOL_SIZE_SMOD

IMPLICIT NONE

CONTAINS

MODULE FUNCTION ACDC_SIZE_TEAERVOL (SELF, CDPATH, LDPRINT) RESULT (KSIZE)

IMPLICIT NONE
CLASS (TEAERVOL),     INTENT (IN), TARGET :: SELF
CHARACTER(LEN=*), INTENT (IN), OPTIONAL :: CDPATH
LOGICAL,          INTENT (IN), OPTIONAL :: LDPRINT
INTEGER*8 :: KSIZE
INTEGER*8 :: ISIZE, JSIZE
LOGICAL :: LLPRINT
CHARACTER(LEN=128) :: CLPATH
LLPRINT = .FALSE.
IF (PRESENT (LDPRINT)) THEN
  LLPRINT = LDPRINT
ENDIF
CLPATH=''
IF (PRESENT (CDPATH)) THEN
  CLPATH = CDPATH
ENDIF
KSIZE = 0
ISIZE = KIND (SELF%NAERVOLC)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%NAERVOLC'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%NAERVOLE)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%NAERVOLE'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%NINTERPT)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%NINTERPT'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%NTVOLC)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%NTVOLC'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%NVOLERUP)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%NVOLERUP'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%NVOLHOMO)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%NVOLHOMO'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%NVOLOPTP)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%NVOLOPTP'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%NVOLERUZ) * SIZE (SELF%NVOLERUZ)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%NVOLERUZ'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%NVOLDATS) * SIZE (SELF%NVOLDATS)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%NVOLDATS'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%NVOLDATE) * SIZE (SELF%NVOLDATE)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%NVOLDATE'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%RAERVOLC) * SIZE (SELF%RAERVOLC)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%RAERVOLC'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%RAERVOLE) * SIZE (SELF%RAERVOLE)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%RAERVOLE'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%RVOLERUZ) * SIZE (SELF%RVOLERUZ)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%RVOLERUZ'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%RVSEDVOL)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%RVSEDVOL'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%RVDEPVOL)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%RVDEPVOL'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%RWDEPVOL)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%RWDEPVOL'
ENDIF
KSIZE = KSIZE + ISIZE
END FUNCTION

END SUBMODULE
