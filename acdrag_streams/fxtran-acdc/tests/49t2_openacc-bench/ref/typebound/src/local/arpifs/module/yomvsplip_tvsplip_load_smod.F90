SUBMODULE (YOMVSPLIP) YOMVSPLIP_TVSPLIP_LOAD_SMOD

IMPLICIT NONE

CONTAINS

MODULE SUBROUTINE ACDC_LOAD_TVSPLIP (SELF, KLUN)
USE PARKIND1, ONLY : JPRD
IMPLICIT NONE
CLASS (TVSPLIP), INTENT (OUT), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
INTEGER :: IL1(1), IU1(1), IL2(2), IU2(2), IL3(3), IU3(3)
LOGICAL :: LRFVV, LRVSPC, LRVSPTRI
REAL(KIND=JPRD), ALLOCATABLE :: ZTMP1 (:)
REAL(KIND=JPRD), ALLOCATABLE :: ZTMP2 (:,:)
REAL(KIND=JPRD), ALLOCATABLE :: ZTMP3 (:,:,:)
READ (KLUN) LRVSPTRI
IF (LRVSPTRI) THEN
  READ (KLUN) IL2
  READ (KLUN) IU2
  ALLOCATE (SELF%RVSPTRI (IL2(1):IU2(1), IL2(2):IU2(2)))
  ALLOCATE (ZTMP2(LBOUND(SELF%RVSPTRI,1):UBOUND(SELF%RVSPTRI,1), LBOUND(SELF%RVSPTRI,2):UBOUND(SELF%RVSPTRI,2)))
  READ (KLUN) ZTMP2
  SELF%RVSPTRI = ZTMP2
  DEALLOCATE (ZTMP2)
ENDIF
READ (KLUN) LRVSPC
IF (LRVSPC) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (SELF%RVSPC (IL1(1):IU1(1)))
  ALLOCATE (ZTMP1(LBOUND(SELF%RVSPC,1):UBOUND(SELF%RVSPC,1)))
  READ (KLUN) ZTMP1
  SELF%RVSPC = ZTMP1
  DEALLOCATE (ZTMP1)
ENDIF
READ (KLUN) LRFVV
IF (LRFVV) THEN
  READ (KLUN) IL3
  READ (KLUN) IU3
  ALLOCATE (SELF%RFVV (IL3(1):IU3(1), IL3(2):IU3(2), IL3(3):IU3(3)))
  ALLOCATE (ZTMP3(LBOUND(SELF%RFVV,1):UBOUND(SELF%RFVV,1), LBOUND(SELF%RFVV,2):UBOUND(SELF%RFVV,2), LBOUND(SELF%RFVV,3):UBOUND(SELF%RFVV,3)))
  READ (KLUN) ZTMP3
  SELF%RFVV = ZTMP3
  DEALLOCATE (ZTMP3)
ENDIF
END SUBROUTINE

END SUBMODULE
