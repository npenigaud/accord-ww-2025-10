INTEGER, INTENT (IN) :: KLUN
LOGICAL :: LCIPATINIT_SDT, LCOPATGP_SDT, LCOPATSP_SDT, LCOPATTRUN_SDT, LLACTIVEO_SDT, LLACTIVEQ_SDT
WRITE (KLUN) SELF%LSPSDT
