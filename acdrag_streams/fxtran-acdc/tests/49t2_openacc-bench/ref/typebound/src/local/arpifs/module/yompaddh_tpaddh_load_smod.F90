SUBMODULE (YOMPADDH) YOMPADDH_TPADDH_LOAD_SMOD

IMPLICIT NONE

CONTAINS

MODULE SUBROUTINE ACDC_LOAD_TPADDH (SELF, KLUN)

IMPLICIT NONE
CLASS (TPADDH), INTENT (OUT), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
INTEGER :: IL2(2), IU2(2)
LOGICAL :: LNGLALIST, LNGPUMASK
READ (KLUN) LNGLALIST
IF (LNGLALIST) THEN
  READ (KLUN) IL2
  READ (KLUN) IU2
  ALLOCATE (SELF%NGLALIST (IL2(1):IU2(1), IL2(2):IU2(2)))
  READ (KLUN) SELF%NGLALIST
ENDIF
READ (KLUN) LNGPUMASK
IF (LNGPUMASK) THEN
  READ (KLUN) IL2
  READ (KLUN) IU2
  ALLOCATE (SELF%NGPUMASK (IL2(1):IU2(1), IL2(2):IU2(2)))
  READ (KLUN) SELF%NGPUMASK
ENDIF
END SUBROUTINE

END SUBMODULE
