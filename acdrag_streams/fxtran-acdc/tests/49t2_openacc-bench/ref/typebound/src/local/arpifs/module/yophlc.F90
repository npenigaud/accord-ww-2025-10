USE PARKIND1, ONLY : JPRD
IMPLICIT NONE
CLASS (TPHLC), INTENT (OUT), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
END SUBROUTINE

MODULE SUBROUTINE ACDC_SAVE_TPHLC (SELF, KLUN)

IMPLICIT NONE
CLASS (TPHLC), INTENT (IN), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
END SUBROUTINE

