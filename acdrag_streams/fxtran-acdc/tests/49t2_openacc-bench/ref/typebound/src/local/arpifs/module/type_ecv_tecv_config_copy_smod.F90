SUBMODULE (TYPE_ECV) TYPE_ECV_TECV_CONFIG_COPY_SMOD

IMPLICIT NONE

CONTAINS

MODULE SUBROUTINE ACDC_COPY_TECV_CONFIG (SELF, LDCREATED, LDFIELDAPI)

IMPLICIT NONE
CLASS (TECV_CONFIG), INTENT (IN), TARGET :: SELF
LOGICAL, OPTIONAL, INTENT (IN) :: LDCREATED, LDFIELDAPI
LOGICAL :: LLCREATED
LOGICAL :: LLFIELDAPI
INTEGER :: J1
LOGICAL :: LYRCONFIG
LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF
LLCREATED = .FALSE.
IF (PRESENT (LDCREATED)) THEN
  LLCREATED = LDCREATED
ENDIF
IF (.NOT. LLCREATED) THEN
  !$ACC ENTER DATA CREATE (SELF)
  !$ACC UPDATE DEVICE (SELF)
ENDIF

LYRCONFIG = ALLOCATED (SELF%YRCONFIG)
IF (LYRCONFIG) THEN
  !$ACC ENTER DATA CREATE (SELF%YRCONFIG)
  !$ACC UPDATE DEVICE (SELF%YRCONFIG)
  DO J1 = LBOUND (SELF%YRCONFIG, 1), UBOUND (SELF%YRCONFIG, 1)
    CALL SELF%YRCONFIG (J1)%ACDC_COPY (LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)
  ENDDO
  !$ACC ENTER DATA ATTACH (SELF%YRCONFIG)
ENDIF

END SUBROUTINE

END SUBMODULE
