SUBMODULE (YOMPARAR) YOMPARAR_TPARAR_COPY_SMOD

IMPLICIT NONE

CONTAINS

MODULE SUBROUTINE ACDC_COPY_TPARAR (SELF, LDCREATED, LDFIELDAPI)

IMPLICIT NONE
CLASS (TPARAR), INTENT (IN), TARGET :: SELF
LOGICAL, OPTIONAL, INTENT (IN) :: LDCREATED, LDFIELDAPI
LOGICAL :: LLCREATED
LOGICAL :: LLFIELDAPI
LOGICAL :: LXSW_BANDS
LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF
LLCREATED = .FALSE.
IF (PRESENT (LDCREATED)) THEN
  LLCREATED = LDCREATED
ENDIF
IF (.NOT. LLCREATED) THEN
  !$ACC ENTER DATA CREATE (SELF)
  !$ACC UPDATE DEVICE (SELF)
ENDIF

CALL SELF%PHYEX%ACDC_COPY (LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

LXSW_BANDS = ALLOCATED (SELF%XSW_BANDS)
IF (LXSW_BANDS) THEN
  !$ACC ENTER DATA CREATE (SELF%XSW_BANDS)
  !$ACC UPDATE DEVICE (SELF%XSW_BANDS)
  !$ACC ENTER DATA ATTACH (SELF%XSW_BANDS)
ENDIF

END SUBROUTINE

END SUBMODULE
