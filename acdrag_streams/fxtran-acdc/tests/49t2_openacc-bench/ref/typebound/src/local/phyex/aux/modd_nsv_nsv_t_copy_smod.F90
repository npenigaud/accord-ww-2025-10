SUBMODULE (MODD_NSV) MODD_NSV_NSV_T_COPY_SMOD

IMPLICIT NONE

CONTAINS

MODULE SUBROUTINE ACDC_COPY_NSV_T (SELF, LDCREATED, LDFIELDAPI)

IMPLICIT NONE
CLASS (NSV_T), INTENT (IN), TARGET :: SELF
LOGICAL, OPTIONAL, INTENT (IN) :: LDCREATED, LDFIELDAPI
LOGICAL :: LLCREATED
LOGICAL :: LLFIELDAPI
INTEGER :: J1, J2
LOGICAL :: LCSV, LCSV_A, LCSV_CHEM_LIST, LCSV_CHEM_LIST_A, LTSVLIST, LTSVLIST_A
LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF
LLCREATED = .FALSE.
IF (PRESENT (LDCREATED)) THEN
  LLCREATED = LDCREATED
ENDIF
IF (.NOT. LLCREATED) THEN
  !$ACC ENTER DATA CREATE (SELF)
  !$ACC UPDATE DEVICE (SELF)
ENDIF

LCSV_CHEM_LIST_A = ALLOCATED (SELF%CSV_CHEM_LIST_A)
IF (LCSV_CHEM_LIST_A) THEN
  !$ACC ENTER DATA CREATE (SELF%CSV_CHEM_LIST_A)
  !$ACC UPDATE DEVICE (SELF%CSV_CHEM_LIST_A)
  !$ACC ENTER DATA ATTACH (SELF%CSV_CHEM_LIST_A)
ENDIF

LCSV_A = ALLOCATED (SELF%CSV_A)
IF (LCSV_A) THEN
  !$ACC ENTER DATA CREATE (SELF%CSV_A)
  !$ACC UPDATE DEVICE (SELF%CSV_A)
  !$ACC ENTER DATA ATTACH (SELF%CSV_A)
ENDIF

LTSVLIST_A = ALLOCATED (SELF%TSVLIST_A)
IF (LTSVLIST_A) THEN
  !$ACC ENTER DATA CREATE (SELF%TSVLIST_A)
  !$ACC UPDATE DEVICE (SELF%TSVLIST_A)
  DO J2 = LBOUND (SELF%TSVLIST_A, 2), UBOUND (SELF%TSVLIST_A, 2)
    DO J1 = LBOUND (SELF%TSVLIST_A, 1), UBOUND (SELF%TSVLIST_A, 1)
      CALL SELF%TSVLIST_A (J1, J2)%ACDC_COPY (LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)
    ENDDO
  ENDDO
  !$ACC ENTER DATA ATTACH (SELF%TSVLIST_A)
ENDIF

LCSV_CHEM_LIST = ASSOCIATED (SELF%CSV_CHEM_LIST)
IF (LCSV_CHEM_LIST) THEN
  !$ACC ENTER DATA CREATE (SELF%CSV_CHEM_LIST)
  !$ACC UPDATE DEVICE (SELF%CSV_CHEM_LIST)
  !$ACC ENTER DATA ATTACH (SELF%CSV_CHEM_LIST)
ENDIF

LCSV = ASSOCIATED (SELF%CSV)
IF (LCSV) THEN
  !$ACC ENTER DATA CREATE (SELF%CSV)
  !$ACC UPDATE DEVICE (SELF%CSV)
  !$ACC ENTER DATA ATTACH (SELF%CSV)
ENDIF

LTSVLIST = ASSOCIATED (SELF%TSVLIST)
IF (LTSVLIST) THEN
  !$ACC ENTER DATA CREATE (SELF%TSVLIST)
  !$ACC UPDATE DEVICE (SELF%TSVLIST)
  DO J1 = LBOUND (SELF%TSVLIST, 1), UBOUND (SELF%TSVLIST, 1)
    CALL SELF%TSVLIST (J1)%ACDC_COPY (LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)
  ENDDO
  !$ACC ENTER DATA ATTACH (SELF%TSVLIST)
ENDIF

END SUBROUTINE

END SUBMODULE
