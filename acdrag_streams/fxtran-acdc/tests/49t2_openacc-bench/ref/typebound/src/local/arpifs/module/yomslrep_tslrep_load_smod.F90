SUBMODULE (YOMSLREP) YOMSLREP_TSLREP_LOAD_SMOD

IMPLICIT NONE

CONTAINS

MODULE SUBROUTINE ACDC_LOAD_TSLREP (SELF, KLUN)
USE PARKIND1, ONLY : JPRD
IMPLICIT NONE
CLASS (TSLREP), INTENT (OUT), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
INTEGER :: IL1(1), IU1(1), IL2(2), IU2(2)
LOGICAL :: LLADCORE, LNADCORE, LNADMAP, LRSASIGN
REAL(KIND=JPRD), ALLOCATABLE :: ZTMP2 (:,:)
READ (KLUN) SELF%NGPTOTAD
READ (KLUN) LNADMAP
IF (LNADMAP) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (SELF%NADMAP (IL1(1):IU1(1)))
  READ (KLUN) SELF%NADMAP
ENDIF
READ (KLUN) LNADCORE
IF (LNADCORE) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (SELF%NADCORE (IL1(1):IU1(1)))
  READ (KLUN) SELF%NADCORE
ENDIF
READ (KLUN) LLADCORE
IF (LLADCORE) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (SELF%LADCORE (IL1(1):IU1(1)))
  READ (KLUN) SELF%LADCORE
ENDIF
READ (KLUN) LRSASIGN
IF (LRSASIGN) THEN
  READ (KLUN) IL2
  READ (KLUN) IU2
  ALLOCATE (SELF%RSASIGN (IL2(1):IU2(1), IL2(2):IU2(2)))
  ALLOCATE (ZTMP2(LBOUND(SELF%RSASIGN,1):UBOUND(SELF%RSASIGN,1), LBOUND(SELF%RSASIGN,2):UBOUND(SELF%RSASIGN,2)))
  READ (KLUN) ZTMP2
  SELF%RSASIGN = ZTMP2
  DEALLOCATE (ZTMP2)
ENDIF
END SUBROUTINE

END SUBMODULE
