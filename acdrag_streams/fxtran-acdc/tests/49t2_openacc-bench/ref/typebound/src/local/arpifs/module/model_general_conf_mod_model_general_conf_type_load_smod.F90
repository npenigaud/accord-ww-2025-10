SUBMODULE (MODEL_GENERAL_CONF_MOD) MODEL_GENERAL_CONF_MOD_MODEL_GENERAL_CONF_TYPE_LOAD_SMOD

IMPLICIT NONE

CONTAINS

MODULE SUBROUTINE ACDC_LOAD_MODEL_GENERAL_CONF_TYPE (SELF, KLUN)

IMPLICIT NONE
CLASS (MODEL_GENERAL_CONF_TYPE), INTENT (OUT), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
LOGICAL :: LGEOM
READ (KLUN) LGEOM
IF (LGEOM) THEN
  ALLOCATE (SELF%GEOM)
  CALL SELF%GEOM%ACDC_LOAD (KLUN)
ELSE
  NULLIFY (SELF%GEOM)
ENDIF
CALL SELF%YRDIMF%ACDC_LOAD (KLUN)
CALL SELF%YGFL%ACDC_LOAD (KLUN)
CALL SELF%YRRIP%ACDC_LOAD (KLUN)
CALL SELF%YRDIMECV%ACDC_LOAD (KLUN)
CALL SELF%YRMODERR%ACDC_LOAD (KLUN)
CALL SELF%YRSPPT_CONFIG%ACDC_LOAD (KLUN)
CALL SELF%YRSPP_CONFIG%ACDC_LOAD (KLUN)
END SUBROUTINE

END SUBMODULE
