MODULE SUBROUTINE ACDC_COPY_TEOZOC (SELF, LDCREATED, LDFIELDAPI)

IMPLICIT NONE
CLASS (TEOZOC), INTENT (IN), TARGET :: SELF
LOGICAL, OPTIONAL, INTENT (IN) :: LDCREATED, LDFIELDAPI
LOGICAL :: LLCREATED
LOGICAL :: LLFIELDAPI
LOGICAL :: LZOZCL, LZOZCLAQUA
LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF
LLCREATED = .FALSE.
IF (PRESENT (LDCREATED)) THEN
  LLCREATED = LDCREATED
ENDIF
IF (.NOT. LLCREATED) THEN
  !$ACC ENTER DATA CREATE (SELF)
  !$ACC UPDATE DEVICE (SELF)
ENDIF
LZOZCL = ALLOCATED (SELF%ZOZCL)
IF (LZOZCL) THEN
  !$ACC ENTER DATA CREATE (SELF%ZOZCL)
  !$ACC UPDATE DEVICE (SELF%ZOZCL)
  !$ACC ENTER DATA ATTACH (SELF%ZOZCL)
ENDIF

LZOZCLAQUA = ALLOCATED (SELF%ZOZCLAQUA)
IF (LZOZCLAQUA) THEN
  !$ACC ENTER DATA CREATE (SELF%ZOZCLAQUA)
  !$ACC UPDATE DEVICE (SELF%ZOZCLAQUA)
  !$ACC ENTER DATA ATTACH (SELF%ZOZCLAQUA)
ENDIF

END SUBROUTINE

END SUBMODULE
