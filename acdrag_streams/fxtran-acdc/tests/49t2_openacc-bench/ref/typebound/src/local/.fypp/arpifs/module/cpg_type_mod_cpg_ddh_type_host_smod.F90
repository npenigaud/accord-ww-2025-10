SUBMODULE (CPG_TYPE_MOD) CPG_TYPE_MOD_CPG_DDH_TYPE_HOST_SMOD

IMPLICIT NONE

CONTAINS

MODULE SUBROUTINE ACDC_HOST_CPG_DDH_TYPE (SELF)
USE FIELD_UTIL_MODULE
IMPLICIT NONE
CLASS (CPG_DDH_TYPE), TARGET :: SELF
LOGICAL :: LF_AUX2D, LF_AUX3D, LF_AUXSM, LF_DDHI, LF_DHCV
LF_AUX3D = ASSOCIATED (SELF%F_AUX3D)
IF (LF_AUX3D) THEN
  CALL HOST_FIELD_4RB (SELF%F_AUX3D)
ENDIF

LF_AUX2D = ASSOCIATED (SELF%F_AUX2D)
IF (LF_AUX2D) THEN
  CALL HOST_FIELD_3RB (SELF%F_AUX2D)
ENDIF

LF_DHCV = ASSOCIATED (SELF%F_DHCV)
IF (LF_DHCV) THEN
  CALL HOST_FIELD_4RB (SELF%F_DHCV)
ENDIF

LF_AUXSM = ASSOCIATED (SELF%F_AUXSM)
IF (LF_AUXSM) THEN
  CALL HOST_FIELD_5RB (SELF%F_AUXSM)
ENDIF

LF_DDHI = ASSOCIATED (SELF%F_DDHI)
IF (LF_DDHI) THEN
  CALL HOST_FIELD_2IM (SELF%F_DDHI)
ENDIF

END SUBROUTINE

END SUBMODULE
