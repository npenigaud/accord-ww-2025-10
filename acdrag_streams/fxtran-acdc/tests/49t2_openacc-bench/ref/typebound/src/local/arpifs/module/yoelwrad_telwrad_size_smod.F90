SUBMODULE (YOELWRAD) YOELWRAD_TELWRAD_SIZE_SMOD

IMPLICIT NONE

CONTAINS

MODULE FUNCTION ACDC_SIZE_TELWRAD (SELF, CDPATH, LDPRINT) RESULT (KSIZE)

IMPLICIT NONE
CLASS (TELWRAD),     INTENT (IN), TARGET :: SELF
CHARACTER(LEN=*), INTENT (IN), OPTIONAL :: CDPATH
LOGICAL,          INTENT (IN), OPTIONAL :: LDPRINT
INTEGER*8 :: KSIZE
INTEGER*8 :: ISIZE, JSIZE
LOGICAL :: LLPRINT
CHARACTER(LEN=128) :: CLPATH
LLPRINT = .FALSE.
IF (PRESENT (LDPRINT)) THEN
  LLPRINT = LDPRINT
ENDIF
CLPATH=''
IF (PRESENT (CDPATH)) THEN
  CLPATH = CDPATH
ENDIF
KSIZE = 0
ISIZE = KIND (SELF%NLEVLWC)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%NLEVLWC'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%NLWFR)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%NLWFR'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%NLOOPLW)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%NLOOPLW'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%NLOOPLWO)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%NLOOPLWO'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%NPROMALW)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%NPROMALW'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%NLASTLW)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%NLASTLW'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%LOPTLWPR)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%LOPTLWPR'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%LWLCLHR)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%LWLCLHR'
ENDIF
KSIZE = KSIZE + ISIZE
END FUNCTION

END SUBMODULE
