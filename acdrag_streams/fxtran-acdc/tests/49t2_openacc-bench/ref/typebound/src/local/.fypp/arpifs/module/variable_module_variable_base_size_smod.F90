SUBMODULE (VARIABLE_MODULE) VARIABLE_MODULE_VARIABLE_BASE_SIZE_SMOD

IMPLICIT NONE

CONTAINS

MODULE FUNCTION ACDC_SIZE_VARIABLE_BASE (SELF, CDPATH, LDPRINT) RESULT (KSIZE)

IMPLICIT NONE
CLASS (VARIABLE_BASE),     INTENT (IN), TARGET :: SELF
CHARACTER(LEN=*), INTENT (IN), OPTIONAL :: CDPATH
LOGICAL,          INTENT (IN), OPTIONAL :: LDPRINT
INTEGER*8 :: KSIZE
INTEGER*8 :: ISIZE, JSIZE
LOGICAL :: LLPRINT
CHARACTER(LEN=128) :: CLPATH
LLPRINT = .FALSE.
IF (PRESENT (LDPRINT)) THEN
  LLPRINT = LDPRINT
ENDIF
CLPATH=''
IF (PRESENT (CDPATH)) THEN
  CLPATH = CDPATH
ENDIF
KSIZE = 0
ISIZE = KIND (SELF%NAME) * LEN (SELF%NAME)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%NAME'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%CNAME) * LEN (SELF%CNAME)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%CNAME'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%IGRBCODE)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%IGRBCODE'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%LACTIVE)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%LACTIVE'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%LT1)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%LT1'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%LT9)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%LT9'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%LPH9)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%LPH9'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%LDL)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%LDL'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%LDM)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%LDM'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%LDL9)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%LDL9'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%LDM9)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%LDM9'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%LPC_PH)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%LPC_PH'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%LADV)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%LADV'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%LGP)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%LGP'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%LWATER)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%LWATER'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%LTHERMACT)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%LTHERMACT'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%LCDERS)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%LCDERS'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%RCP)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%RCP'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%R)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%R'
ENDIF
KSIZE = KSIZE + ISIZE
JSIZE = 0
ISIZE = SELF%YCOMP%ACDC_SIZE (CLPATH//'%YCOMP', .FALSE.)
JSIZE = JSIZE + ISIZE
KSIZE = KSIZE + ISIZE
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') JSIZE
  WRITE (*, *) TRIM (CLPATH)//'%YCOMP'
ENDIF
END FUNCTION

