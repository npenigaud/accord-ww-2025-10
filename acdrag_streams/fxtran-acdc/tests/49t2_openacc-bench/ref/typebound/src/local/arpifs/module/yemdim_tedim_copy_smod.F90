SUBMODULE (YEMDIM) YEMDIM_TEDIM_COPY_SMOD

IMPLICIT NONE

CONTAINS

MODULE SUBROUTINE ACDC_COPY_TEDIM (SELF, LDCREATED, LDFIELDAPI)

IMPLICIT NONE
CLASS (TEDIM), INTENT (IN), TARGET :: SELF
LOGICAL, OPTIONAL, INTENT (IN) :: LDCREATED, LDFIELDAPI
LOGICAL :: LLCREATED
LOGICAL :: LLFIELDAPI
LOGICAL :: LNISMAX, LNISNAX
LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF
LLCREATED = .FALSE.
IF (PRESENT (LDCREATED)) THEN
  LLCREATED = LDCREATED
ENDIF
IF (.NOT. LLCREATED) THEN
  !$ACC ENTER DATA CREATE (SELF)
  !$ACC UPDATE DEVICE (SELF)
ENDIF

LNISMAX = ASSOCIATED (SELF%NISMAX)
IF (LNISMAX) THEN
  !$ACC ENTER DATA CREATE (SELF%NISMAX)
  !$ACC UPDATE DEVICE (SELF%NISMAX)
  !$ACC ENTER DATA ATTACH (SELF%NISMAX)
ENDIF

LNISNAX = ASSOCIATED (SELF%NISNAX)
IF (LNISNAX) THEN
  !$ACC ENTER DATA CREATE (SELF%NISNAX)
  !$ACC UPDATE DEVICE (SELF%NISNAX)
  !$ACC ENTER DATA ATTACH (SELF%NISNAX)
ENDIF

END SUBROUTINE

END SUBMODULE
