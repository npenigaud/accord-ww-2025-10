SUBMODULE (TYPE_MODEL) TYPE_MODEL_MODEL_LOAD_SMOD

IMPLICIT NONE

CONTAINS

MODULE SUBROUTINE ACDC_LOAD_MODEL (SELF, KLUN)

IMPLICIT NONE
CLASS (MODEL), INTENT (OUT), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
LOGICAL :: LYRCST
READ (KLUN) SELF%LINEAR_MODEL
CALL SELF%YRML_GCONF%ACDC_LOAD (KLUN)
CALL SELF%YRML_AOC%ACDC_LOAD (KLUN)
CALL SELF%YRML_DYN%ACDC_LOAD (KLUN)
CALL SELF%YRML_PHY_G%ACDC_LOAD (KLUN)
CALL SELF%YRML_PHY_EC%ACDC_LOAD (KLUN)
CALL SELF%YRML_PHY_SLIN%ACDC_LOAD (KLUN)
CALL SELF%YRML_PHY_AER%ACDC_LOAD (KLUN)
CALL SELF%YRML_PHY_RAD%ACDC_LOAD (KLUN)
CALL SELF%YRML_PHY_STOCH%ACDC_LOAD (KLUN)
CALL SELF%YRML_PHY_MF%ACDC_LOAD (KLUN)
CALL SELF%YRML_CHEM%ACDC_LOAD (KLUN)
CALL SELF%YRML_DIAG%ACDC_LOAD (KLUN)
CALL SELF%YREWCOU%ACDC_LOAD (KLUN)
READ (KLUN) LYRCST
IF (LYRCST) THEN
  ALLOCATE (SELF%YRCST)
  CALL SELF%YRCST%ACDC_LOAD (KLUN)
ELSE
  NULLIFY (SELF%YRCST)
ENDIF
CALL SELF%YRML_LBC%ACDC_LOAD (KLUN)
READ (KLUN) SELF%MOBJECT_ID
READ (KLUN) SELF%COBJECT_ID
END SUBROUTINE

END SUBMODULE
