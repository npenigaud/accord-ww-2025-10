SUBMODULE (YEMGSL) YEMGSL_TEGSL_SIZE_SMOD

IMPLICIT NONE

CONTAINS

MODULE FUNCTION ACDC_SIZE_TEGSL (SELF, CDPATH, LDPRINT) RESULT (KSIZE)

IMPLICIT NONE
CLASS (TEGSL),     INTENT (IN), TARGET :: SELF
CHARACTER(LEN=*), INTENT (IN), OPTIONAL :: CDPATH
LOGICAL,          INTENT (IN), OPTIONAL :: LDPRINT
INTEGER*8 :: KSIZE
INTEGER*8 :: ISIZE, JSIZE
LOGICAL :: LLPRINT
CHARACTER(LEN=128) :: CLPATH
LLPRINT = .FALSE.
IF (PRESENT (LDPRINT)) THEN
  LLPRINT = LDPRINT
ENDIF
CLPATH=''
IF (PRESENT (CDPATH)) THEN
  CLPATH = CDPATH
ENDIF
KSIZE = 0
ISIZE = KIND (SELF%RIPORE)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%RIPORE'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%RJPORE)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%RJPORE'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%REGPK)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%REGPK'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%RNOBILE)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%RNOBILE'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%RHSUD)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%RHSUD'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%RMERCAT)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%RMERCAT'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%RLON0R)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%RLON0R'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%RELONC)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%RELONC'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%RELATC)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%RELATC'
ENDIF
KSIZE = KSIZE + ISIZE
END FUNCTION

END SUBMODULE
