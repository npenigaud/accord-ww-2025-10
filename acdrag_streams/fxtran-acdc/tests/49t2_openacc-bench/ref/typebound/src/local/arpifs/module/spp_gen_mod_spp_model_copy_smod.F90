SUBMODULE (SPP_GEN_MOD) SPP_GEN_MOD_SPP_MODEL_COPY_SMOD

IMPLICIT NONE

CONTAINS

MODULE SUBROUTINE ACDC_COPY_SPP_MODEL (SELF, LDCREATED, LDFIELDAPI)

IMPLICIT NONE
CLASS (SPP_MODEL), INTENT (IN), TARGET :: SELF
LOGICAL, OPTIONAL, INTENT (IN) :: LDCREATED, LDFIELDAPI
LOGICAL :: LLCREATED
LOGICAL :: LLFIELDAPI
INTEGER :: J1
LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF
LLCREATED = .FALSE.
IF (PRESENT (LDCREATED)) THEN
  LLCREATED = LDCREATED
ENDIF
IF (.NOT. LLCREATED) THEN
  !$ACC ENTER DATA CREATE (SELF)
  !$ACC UPDATE DEVICE (SELF)
ENDIF

DO J1 = LBOUND (SELF%PNDEF, 1), UBOUND (SELF%PNDEF, 1)
  CALL SELF%PNDEF (J1)%ACDC_COPY (LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)
ENDDO

DO J1 = LBOUND (SELF%PN, 1), UBOUND (SELF%PN, 1)
  CALL SELF%PN (J1)%ACDC_COPY (LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)
ENDDO

END SUBROUTINE

END SUBMODULE
