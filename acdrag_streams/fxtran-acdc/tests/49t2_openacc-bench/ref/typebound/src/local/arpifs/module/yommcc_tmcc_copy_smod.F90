CONTAINS

MODULE SUBROUTINE ACDC_COPY_TMCC (SELF, LDCREATED, LDFIELDAPI)

IMPLICIT NONE
CLASS (TMCC), INTENT (IN), TARGET :: SELF
LOGICAL, OPTIONAL, INTENT (IN) :: LDCREATED, LDFIELDAPI
LOGICAL :: LLCREATED
LOGICAL :: LLFIELDAPI
INTEGER :: J1
LOGICAL :: LCLIMR, LCLIMRAER, LCLIMRCO2, LCLIMRCOMPO, LCPLNG_FLD, LOCEANBC, LPARTMASK, LPARTSST0, LRNEMOLVLS, LRNEMOMASK
LOGICAL :: LZLAKE
LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF
LLCREATED = .FALSE.
IF (PRESENT (LDCREATED)) THEN
  LLCREATED = LDCREATED
ENDIF
IF (.NOT. LLCREATED) THEN
  !$ACC ENTER DATA CREATE (SELF)
  !$ACC UPDATE DEVICE (SELF)
ENDIF

LCLIMRCO2 = ALLOCATED (SELF%CLIMRCO2)
IF (LCLIMRCO2) THEN
  !$ACC ENTER DATA CREATE (SELF%CLIMRCO2)
  !$ACC UPDATE DEVICE (SELF%CLIMRCO2)
  !$ACC ENTER DATA ATTACH (SELF%CLIMRCO2)
ENDIF

LCLIMRAER = ALLOCATED (SELF%CLIMRAER)
IF (LCLIMRAER) THEN
  !$ACC ENTER DATA CREATE (SELF%CLIMRAER)
  !$ACC UPDATE DEVICE (SELF%CLIMRAER)
  !$ACC ENTER DATA ATTACH (SELF%CLIMRAER)
ENDIF

LCLIMRCOMPO = ALLOCATED (SELF%CLIMRCOMPO)
IF (LCLIMRCOMPO) THEN
  !$ACC ENTER DATA CREATE (SELF%CLIMRCOMPO)
  !$ACC UPDATE DEVICE (SELF%CLIMRCOMPO)
  !$ACC ENTER DATA ATTACH (SELF%CLIMRCOMPO)
ENDIF

LZLAKE = ALLOCATED (SELF%ZLAKE)
IF (LZLAKE) THEN
  !$ACC ENTER DATA CREATE (SELF%ZLAKE)
  !$ACC UPDATE DEVICE (SELF%ZLAKE)
  !$ACC ENTER DATA ATTACH (SELF%ZLAKE)
ENDIF

LOCEANBC = ALLOCATED (SELF%OCEANBC)
IF (LOCEANBC) THEN
  !$ACC ENTER DATA CREATE (SELF%OCEANBC)
  !$ACC UPDATE DEVICE (SELF%OCEANBC)
  !$ACC ENTER DATA ATTACH (SELF%OCEANBC)
ENDIF

LCLIMR = ALLOCATED (SELF%CLIMR)
IF (LCLIMR) THEN
  !$ACC ENTER DATA CREATE (SELF%CLIMR)
  !$ACC UPDATE DEVICE (SELF%CLIMR)
  !$ACC ENTER DATA ATTACH (SELF%CLIMR)
ENDIF

LRNEMOLVLS = ALLOCATED (SELF%RNEMOLVLS)
IF (LRNEMOLVLS) THEN
  !$ACC ENTER DATA CREATE (SELF%RNEMOLVLS)
  !$ACC UPDATE DEVICE (SELF%RNEMOLVLS)
  !$ACC ENTER DATA ATTACH (SELF%RNEMOLVLS)
ENDIF

LRNEMOMASK = ALLOCATED (SELF%RNEMOMASK)
IF (LRNEMOMASK) THEN
  !$ACC ENTER DATA CREATE (SELF%RNEMOMASK)
  !$ACC UPDATE DEVICE (SELF%RNEMOMASK)
  !$ACC ENTER DATA ATTACH (SELF%RNEMOMASK)
ENDIF

LPARTSST0 = ALLOCATED (SELF%PARTSST0)
IF (LPARTSST0) THEN
  !$ACC ENTER DATA CREATE (SELF%PARTSST0)
  !$ACC UPDATE DEVICE (SELF%PARTSST0)
  !$ACC ENTER DATA ATTACH (SELF%PARTSST0)
ENDIF

LPARTMASK = ALLOCATED (SELF%PARTMASK)
IF (LPARTMASK) THEN
  !$ACC ENTER DATA CREATE (SELF%PARTMASK)
  !$ACC UPDATE DEVICE (SELF%PARTMASK)
  !$ACC ENTER DATA ATTACH (SELF%PARTMASK)
ENDIF

LCPLNG_FLD = ALLOCATED (SELF%CPLNG_FLD)
IF (LCPLNG_FLD) THEN
  !$ACC ENTER DATA CREATE (SELF%CPLNG_FLD)
  !$ACC UPDATE DEVICE (SELF%CPLNG_FLD)
  DO J1 = LBOUND (SELF%CPLNG_FLD, 1), UBOUND (SELF%CPLNG_FLD, 1)
    CALL SELF%CPLNG_FLD (J1)%ACDC_COPY (LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)
  ENDDO
  !$ACC ENTER DATA ATTACH (SELF%CPLNG_FLD)
