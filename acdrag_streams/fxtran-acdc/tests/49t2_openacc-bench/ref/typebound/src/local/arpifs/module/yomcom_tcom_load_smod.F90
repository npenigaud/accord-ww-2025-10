SUBMODULE (YOMCOM) YOMCOM_TCOM_LOAD_SMOD

IMPLICIT NONE

CONTAINS

MODULE SUBROUTINE ACDC_LOAD_TCOM (SELF, KLUN)
USE PARKIND1, ONLY : JPRD
IMPLICIT NONE
CLASS (TCOM), INTENT (OUT), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
INTEGER :: IL1(1), IU1(1)
LOGICAL :: LGTTLIN, LOMLDTH, LSSTMSK, LSSTPRE, LTRAFLX
REAL(KIND=JPRD), ALLOCATABLE :: ZTMP1 (:)
READ (KLUN) SELF%NVCOM
READ (KLUN) LOMLDTH
IF (LOMLDTH) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (SELF%OMLDTH (IL1(1):IU1(1)))
  ALLOCATE (ZTMP1(LBOUND(SELF%OMLDTH,1):UBOUND(SELF%OMLDTH,1)))
  READ (KLUN) ZTMP1
  SELF%OMLDTH = ZTMP1
  DEALLOCATE (ZTMP1)
ENDIF
READ (KLUN) LGTTLIN
IF (LGTTLIN) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (SELF%GTTLIN (IL1(1):IU1(1)))
  ALLOCATE (ZTMP1(LBOUND(SELF%GTTLIN,1):UBOUND(SELF%GTTLIN,1)))
  READ (KLUN) ZTMP1
  SELF%GTTLIN = ZTMP1
  DEALLOCATE (ZTMP1)
ENDIF
READ (KLUN) LSSTPRE
IF (LSSTPRE) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (SELF%SSTPRE (IL1(1):IU1(1)))
  ALLOCATE (ZTMP1(LBOUND(SELF%SSTPRE,1):UBOUND(SELF%SSTPRE,1)))
  READ (KLUN) ZTMP1
  SELF%SSTPRE = ZTMP1
  DEALLOCATE (ZTMP1)
ENDIF
READ (KLUN) LSSTMSK
IF (LSSTMSK) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (SELF%SSTMSK (IL1(1):IU1(1)))
  ALLOCATE (ZTMP1(LBOUND(SELF%SSTMSK,1):UBOUND(SELF%SSTMSK,1)))
  READ (KLUN) ZTMP1
  SELF%SSTMSK = ZTMP1
  DEALLOCATE (ZTMP1)
ENDIF
READ (KLUN) SELF%LOMLDTH
READ (KLUN) LTRAFLX
IF (LTRAFLX) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (SELF%TRAFLX (IL1(1):IU1(1)))
  ALLOCATE (ZTMP1(LBOUND(SELF%TRAFLX,1):UBOUND(SELF%TRAFLX,1)))
  READ (KLUN) ZTMP1
  SELF%TRAFLX = ZTMP1
  DEALLOCATE (ZTMP1)
ENDIF
END SUBROUTINE

END SUBMODULE
