SUBMODULE (YOMGEM) YOMGEM_TGEM_LOAD_SMOD

IMPLICIT NONE

CONTAINS

MODULE SUBROUTINE ACDC_LOAD_TGEM (SELF, KLUN)
USE PARKIND1, ONLY : JPRD
IMPLICIT NONE
CLASS (TGEM), INTENT (OUT), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
INTEGER :: IL1(1), IU1(1), IL2(2), IU2(2)
LOGICAL :: LNDGLU, LNGPTOTL, LNLOEN, LNLOENG, LNMEN, LNMENG, LNSTAGP, LNTSTAGP
REAL(KIND=JPRD) :: ZTMP0
READ (KLUN) SELF%NGPTOT
READ (KLUN) SELF%NGPTOT_CAP
READ (KLUN) SELF%NGPTOTMX
READ (KLUN) SELF%NGPTOTG
READ (KLUN) LNGPTOTL
IF (LNGPTOTL) THEN
  READ (KLUN) IL2
  READ (KLUN) IU2
  ALLOCATE (SELF%NGPTOTL (IL2(1):IU2(1), IL2(2):IU2(2)))
  READ (KLUN) SELF%NGPTOTL
ENDIF
READ (KLUN) ZTMP0
SELF%RDELXN = ZTMP0
READ (KLUN) ZTMP0
SELF%SLHDP = ZTMP0
READ (KLUN) ZTMP0
SELF%RMUCEN = ZTMP0
READ (KLUN) ZTMP0
SELF%RLOCEN = ZTMP0
READ (KLUN) ZTMP0
SELF%RSTRET = ZTMP0
READ (KLUN) SELF%NSTTYP
READ (KLUN) SELF%NHTYP
READ (KLUN) ZTMP0
SELF%RNLGINC = ZTMP0
READ (KLUN) ZTMP0
SELF%R4JP = ZTMP0
READ (KLUN) ZTMP0
SELF%RC2P1 = ZTMP0
READ (KLUN) ZTMP0
SELF%RC2M1 = ZTMP0
READ (KLUN) ZTMP0
SELF%RCOR0 = ZTMP0
READ (KLUN) ZTMP0
SELF%RCOR1 = ZTMP0
READ (KLUN) LNLOEN
IF (LNLOEN) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (SELF%NLOEN (IL1(1):IU1(1)))
  READ (KLUN) SELF%NLOEN
ENDIF
READ (KLUN) LNLOENG
IF (LNLOENG) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (SELF%NLOENG (IL1(1):IU1(1)))
  READ (KLUN) SELF%NLOENG
ENDIF
READ (KLUN) LNMEN
IF (LNMEN) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (SELF%NMEN (IL1(1):IU1(1)))
  READ (KLUN) SELF%NMEN
ENDIF
READ (KLUN) LNMENG
IF (LNMENG) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (SELF%NMENG (IL1(1):IU1(1)))
  READ (KLUN) SELF%NMENG
ENDIF
READ (KLUN) LNDGLU
IF (LNDGLU) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (SELF%NDGLU (IL1(1):IU1(1)))
  READ (KLUN) SELF%NDGLU
ENDIF
READ (KLUN) LNSTAGP
IF (LNSTAGP) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (SELF%NSTAGP (IL1(1):IU1(1)))
  READ (KLUN) SELF%NSTAGP
ENDIF
READ (KLUN) LNTSTAGP
IF (LNTSTAGP) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (SELF%NTSTAGP (IL1(1):IU1(1)))
  READ (KLUN) SELF%NTSTAGP
ENDIF
END SUBROUTINE

END SUBMODULE
