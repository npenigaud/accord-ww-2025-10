SUBMODULE (YOMOZO) YOMOZO_TOZO_SIZE_SMOD

IMPLICIT NONE

CONTAINS

MODULE FUNCTION ACDC_SIZE_TOZO (SELF, CDPATH, LDPRINT) RESULT (KSIZE)

IMPLICIT NONE
CLASS (TOZO),     INTENT (IN), TARGET :: SELF
CHARACTER(LEN=*), INTENT (IN), OPTIONAL :: CDPATH
LOGICAL,          INTENT (IN), OPTIONAL :: LDPRINT
INTEGER*8 :: KSIZE
INTEGER*8 :: ISIZE, JSIZE
LOGICAL :: LLPRINT
CHARACTER(LEN=128) :: CLPATH
LOGICAL :: LTOZ2DG, LTOZ2DL, LTOZ3DBL
LLPRINT = .FALSE.
IF (PRESENT (LDPRINT)) THEN
  LLPRINT = LDPRINT
ENDIF
CLPATH=''
IF (PRESENT (CDPATH)) THEN
  CLPATH = CDPATH
ENDIF
KSIZE = 0
LTOZ2DG = ALLOCATED (SELF%TOZ2DG)
IF (LTOZ2DG) THEN
  ISIZE = KIND (SELF%TOZ2DG) * SIZE (SELF%TOZ2DG)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%TOZ2DG'
  ENDIF
  KSIZE = KSIZE + ISIZE
ENDIF
LTOZ2DL = ALLOCATED (SELF%TOZ2DL)
IF (LTOZ2DL) THEN
  ISIZE = KIND (SELF%TOZ2DL) * SIZE (SELF%TOZ2DL)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%TOZ2DL'
  ENDIF
  KSIZE = KSIZE + ISIZE
ENDIF
LTOZ3DBL = ALLOCATED (SELF%TOZ3DBL)
IF (LTOZ3DBL) THEN
  ISIZE = KIND (SELF%TOZ3DBL) * SIZE (SELF%TOZ3DBL)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%TOZ3DBL'
  ENDIF
  KSIZE = KSIZE + ISIZE
ENDIF
ISIZE = KIND (SELF%TEECL2)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%TEECL2'
ENDIF
KSIZE = KSIZE + ISIZE
END FUNCTION

END SUBMODULE
