SUBMODULE (YEMMP) YEMMP_TEMMP_SIZE_SMOD

IMPLICIT NONE

CONTAINS

MODULE FUNCTION ACDC_SIZE_TEMMP (SELF, CDPATH, LDPRINT) RESULT (KSIZE)

IMPLICIT NONE
CLASS (TEMMP),     INTENT (IN), TARGET :: SELF
CHARACTER(LEN=*), INTENT (IN), OPTIONAL :: CDPATH
LOGICAL,          INTENT (IN), OPTIONAL :: LDPRINT
INTEGER*8 :: KSIZE
INTEGER*8 :: ISIZE, JSIZE
LOGICAL :: LLPRINT
CHARACTER(LEN=128) :: CLPATH
LOGICAL :: LMYENS, LNEALLNS, LNEPROCN, LNEPTRNS, LNUEMPP
LLPRINT = .FALSE.
IF (PRESENT (LDPRINT)) THEN
  LLPRINT = LDPRINT
ENDIF
CLPATH=''
IF (PRESENT (CDPATH)) THEN
  CLPATH = CDPATH
ENDIF
KSIZE = 0
LNEPROCN = ASSOCIATED (SELF%NEPROCN)
IF (LNEPROCN) THEN
  ISIZE = KIND (SELF%NEPROCN) * SIZE (SELF%NEPROCN)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%NEPROCN'
  ENDIF
  KSIZE = KSIZE + ISIZE
ENDIF
ISIZE = KIND (SELF%NUEMP)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%NUEMP'
ENDIF
KSIZE = KSIZE + ISIZE
LMYENS = ASSOCIATED (SELF%MYENS)
IF (LMYENS) THEN
  ISIZE = KIND (SELF%MYENS) * SIZE (SELF%MYENS)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%MYENS'
  ENDIF
  KSIZE = KSIZE + ISIZE
ENDIF
LNUEMPP = ASSOCIATED (SELF%NUEMPP)
IF (LNUEMPP) THEN
  ISIZE = KIND (SELF%NUEMPP) * SIZE (SELF%NUEMPP)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%NUEMPP'
  ENDIF
  KSIZE = KSIZE + ISIZE
ENDIF
LNEALLNS = ASSOCIATED (SELF%NEALLNS)
IF (LNEALLNS) THEN
  ISIZE = KIND (SELF%NEALLNS) * SIZE (SELF%NEALLNS)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%NEALLNS'
  ENDIF
  KSIZE = KSIZE + ISIZE
ENDIF
LNEPTRNS = ASSOCIATED (SELF%NEPTRNS)
IF (LNEPTRNS) THEN
  ISIZE = KIND (SELF%NEPTRNS) * SIZE (SELF%NEPTRNS)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%NEPTRNS'
  ENDIF
  KSIZE = KSIZE + ISIZE
ENDIF
END FUNCTION

END SUBMODULE
