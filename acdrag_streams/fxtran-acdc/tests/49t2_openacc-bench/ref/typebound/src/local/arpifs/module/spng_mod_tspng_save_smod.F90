SUBMODULE (SPNG_MOD) SPNG_MOD_TSPNG_SAVE_SMOD

IMPLICIT NONE

CONTAINS

MODULE SUBROUTINE ACDC_SAVE_TSPNG (SELF, KLUN)

IMPLICIT NONE
CLASS (TSPNG), INTENT (IN), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
LOGICAL :: LRSPONGF
WRITE (KLUN) SELF%LNSPONGE
LRSPONGF = ALLOCATED (SELF%RSPONGF)
WRITE (KLUN) LRSPONGF
IF (LRSPONGF) THEN
  WRITE (KLUN) LBOUND (SELF%RSPONGF)
  WRITE (KLUN) UBOUND (SELF%RSPONGF)
  WRITE (KLUN) SELF%RSPONGF
ENDIF
END SUBROUTINE

END SUBMODULE
