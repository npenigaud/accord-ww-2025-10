SUBMODULE (TYPE_ECV) TYPE_ECV_TECV_CONFIG_LOAD_SMOD

IMPLICIT NONE

CONTAINS

MODULE SUBROUTINE ACDC_LOAD_TECV_CONFIG (SELF, KLUN)

IMPLICIT NONE
CLASS (TECV_CONFIG), INTENT (OUT), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
INTEGER :: J1
INTEGER :: IL1(1), IU1(1)
LOGICAL :: LYRCONFIG
READ (KLUN) SELF%NECV
READ (KLUN) LYRCONFIG
IF (LYRCONFIG) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (SELF%YRCONFIG (IL1(1):IU1(1)))
  DO J1 = LBOUND (SELF%YRCONFIG, 1), UBOUND (SELF%YRCONFIG, 1)
    CALL SELF%YRCONFIG (J1)%ACDC_LOAD (KLUN)
  ENDDO
ENDIF
END SUBROUTINE

END SUBMODULE
