MODULE SUBROUTINE ACDC_LOAD_TOROG (SELF, KLUN)
USE PARKIND1, ONLY : JPRD
IMPLICIT NONE
CLASS (TOROG), INTENT (OUT), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
INTEGER :: IL1(1), IU1(1)
LOGICAL :: LOROG, LOROGL, LOROGLL, LOROGLM, LOROGM, LOROGMM
REAL(KIND=JPRD), ALLOCATABLE :: ZTMP1 (:)
READ (KLUN) LOROG
IF (LOROG) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (SELF%OROG (IL1(1):IU1(1)))
  ALLOCATE (ZTMP1(LBOUND(SELF%OROG,1):UBOUND(SELF%OROG,1)))
  READ (KLUN) ZTMP1
  SELF%OROG = ZTMP1
  DEALLOCATE (ZTMP1)
ENDIF
