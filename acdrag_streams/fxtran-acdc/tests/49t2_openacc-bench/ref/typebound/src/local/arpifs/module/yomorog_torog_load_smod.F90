  READ (KLUN) ZTMP1
  SELF%OROGL = ZTMP1
  DEALLOCATE (ZTMP1)
ENDIF
READ (KLUN) LOROGM
IF (LOROGM) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
