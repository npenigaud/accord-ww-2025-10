IF (LOROGLL) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (SELF%OROGLL (IL1(1):IU1(1)))
  ALLOCATE (ZTMP1(LBOUND(SELF%OROGLL,1):UBOUND(SELF%OROGLL,1)))
  READ (KLUN) ZTMP1
  SELF%OROGLL = ZTMP1
  DEALLOCATE (ZTMP1)
