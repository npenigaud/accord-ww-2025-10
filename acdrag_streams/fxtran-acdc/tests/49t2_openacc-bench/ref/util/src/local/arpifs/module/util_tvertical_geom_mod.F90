SUBROUTINE ACDC_CRC64_TVERTICAL_GEOM (SELF, KLUN, CDPATH)
USE UTIL_TCVER_MOD
USE UTIL_TVAB_MOD
USE UTIL_TVETA_MOD
USE UTIL_TVFE_MOD
USE FXTRAN_ACDC_CRC64_INTRINSIC_MOD, ONLY : FCRC64 => FXTRAN_ACDC_CRC64
IMPLICIT NONE
TYPE (TVERTICAL_GEOM), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
CHARACTER(LEN=*), INTENT (IN) :: CDPATH
CHARACTER(LEN=128) :: CLIND
WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (SELF%LNONHYD_GEOM), CDPATH//'%LNONHYD_GEOM'

CLIND = ''
CALL ACDC_CRC64_TVAB (SELF%YRVAB, KLUN, CDPATH//'%YRVAB'//TRIM(CLIND))

CLIND = ''
CALL ACDC_CRC64_TVETA (SELF%YRVETA, KLUN, CDPATH//'%YRVETA'//TRIM(CLIND))

CLIND = ''
CALL ACDC_CRC64_TVFE (SELF%YRVFE, KLUN, CDPATH//'%YRVFE'//TRIM(CLIND))

CLIND = ''
CALL ACDC_CRC64_TCVER (SELF%YRCVER, KLUN, CDPATH//'%YRCVER'//TRIM(CLIND))

END SUBROUTINE

