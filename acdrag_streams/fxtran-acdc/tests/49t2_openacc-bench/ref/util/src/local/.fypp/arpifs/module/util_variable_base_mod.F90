ENDIF
END SUBROUTINE

FUNCTION ACDC_SIZE_VARIABLE_BASE (SELF, CDPATH, LDPRINT) RESULT (KSIZE)
USE UTIL_TYPE_GFL_COMP_MOD
IMPLICIT NONE
CLASS (VARIABLE_BASE),     INTENT (IN), TARGET :: SELF
CHARACTER(LEN=*), INTENT (IN), OPTIONAL :: CDPATH
LOGICAL,          INTENT (IN), OPTIONAL :: LDPRINT
