SUBROUTINE ACDC_CRC64_VARIABLE_BASE (SELF, KLUN, CDPATH)
USE UTIL_TYPE_GFL_COMP_MOD
USE FXTRAN_ACDC_CRC64_INTRINSIC_MOD, ONLY : FCRC64 => FXTRAN_ACDC_CRC64
IMPLICIT NONE
CLASS (VARIABLE_BASE), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
CHARACTER(LEN=*), INTENT (IN) :: CDPATH
CHARACTER(LEN=128) :: CLIND
WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (SELF%NAME), CDPATH//'%NAME'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (SELF%CNAME), CDPATH//'%CNAME'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (SELF%IGRBCODE), CDPATH//'%IGRBCODE'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (SELF%LACTIVE), CDPATH//'%LACTIVE'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (SELF%LT1), CDPATH//'%LT1'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (SELF%LT9), CDPATH//'%LT9'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (SELF%LPH9), CDPATH//'%LPH9'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (SELF%LDL), CDPATH//'%LDL'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (SELF%LDM), CDPATH//'%LDM'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (SELF%LDL9), CDPATH//'%LDL9'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (SELF%LDM9), CDPATH//'%LDM9'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (SELF%LPC_PH), CDPATH//'%LPC_PH'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (SELF%LADV), CDPATH//'%LADV'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (SELF%LGP), CDPATH//'%LGP'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (SELF%LWATER), CDPATH//'%LWATER'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (SELF%LTHERMACT), CDPATH//'%LTHERMACT'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (SELF%LCDERS), CDPATH//'%LCDERS'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (SELF%RCP), CDPATH//'%RCP'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (SELF%R), CDPATH//'%R'

CLIND = ''
CALL ACDC_CRC64_TYPE_GFL_COMP (SELF%YCOMP, KLUN, CDPATH//'%YCOMP'//TRIM(CLIND))

END SUBROUTINE

SUBROUTINE ACDC_WIPE_VARIABLE_BASE (SELF, LDDELETED, LDFIELDAPI)
USE UTIL_TYPE_GFL_COMP_MOD
IMPLICIT NONE
CLASS (VARIABLE_BASE), INTENT (IN), TARGET :: SELF
LOGICAL, OPTIONAL, INTENT (IN) :: LDDELETED, LDFIELDAPI
