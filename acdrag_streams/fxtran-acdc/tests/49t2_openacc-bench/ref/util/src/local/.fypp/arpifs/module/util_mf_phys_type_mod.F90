MODULE UTIL_MF_PHYS_TYPE_MOD

USE MF_PHYS_TYPE_MOD, ONLY : MF_PHYS_TYPE



INTERFACE ACDC_COPY
MODULE PROCEDURE ACDC_COPY_MF_PHYS_TYPE
END INTERFACE

INTERFACE ACDC_HOST
MODULE PROCEDURE ACDC_HOST_MF_PHYS_TYPE
END INTERFACE



INTERFACE ACDC_WIPE
MODULE PROCEDURE ACDC_WIPE_MF_PHYS_TYPE
END INTERFACE


CONTAINS





SUBROUTINE ACDC_COPY_MF_PHYS_TYPE (SELF, LDCREATED, LDFIELDAPI)
USE UTIL_MF_PHYS_FOR_TYPE_MOD
USE UTIL_MF_PHYS_OPT_TYPE_MOD
USE UTIL_MF_PHYS_OUT_TYPE_MOD
IMPLICIT NONE
TYPE (MF_PHYS_TYPE), INTENT (IN), TARGET :: SELF
LOGICAL, OPTIONAL, INTENT (IN) :: LDCREATED, LDFIELDAPI
LOGICAL :: LLCREATED
LOGICAL :: LLFIELDAPI
LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF
LLCREATED = .FALSE.
IF (PRESENT (LDCREATED)) THEN
  LLCREATED = LDCREATED
ENDIF
IF (.NOT. LLCREATED) THEN
  !$ACC ENTER DATA CREATE (SELF)
  !$ACC UPDATE DEVICE (SELF)
ENDIF
CALL ACDC_COPY_MF_PHYS_OUT_TYPE (SELF%OUT, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL ACDC_COPY_MF_PHYS_OPT_TYPE (SELF%OPT, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL ACDC_COPY_MF_PHYS_FOR_TYPE (SELF%FOR, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

END SUBROUTINE

SUBROUTINE ACDC_HOST_MF_PHYS_TYPE (SELF)
USE UTIL_MF_PHYS_FOR_TYPE_MOD
USE UTIL_MF_PHYS_OPT_TYPE_MOD
USE UTIL_MF_PHYS_OUT_TYPE_MOD
IMPLICIT NONE
TYPE (MF_PHYS_TYPE), TARGET :: SELF

CALL ACDC_HOST_MF_PHYS_OUT_TYPE (SELF%OUT)

CALL ACDC_HOST_MF_PHYS_OPT_TYPE (SELF%OPT)

CALL ACDC_HOST_MF_PHYS_FOR_TYPE (SELF%FOR)

END SUBROUTINE





SUBROUTINE ACDC_WIPE_MF_PHYS_TYPE (SELF, LDDELETED, LDFIELDAPI)
USE UTIL_MF_PHYS_FOR_TYPE_MOD
USE UTIL_MF_PHYS_OPT_TYPE_MOD
USE UTIL_MF_PHYS_OUT_TYPE_MOD
IMPLICIT NONE
TYPE (MF_PHYS_TYPE), INTENT (IN), TARGET :: SELF
LOGICAL, OPTIONAL, INTENT (IN) :: LDDELETED, LDFIELDAPI
LOGICAL :: LLDELETED
LOGICAL :: LLFIELDAPI
LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF
CALL ACDC_WIPE_MF_PHYS_OUT_TYPE (SELF%OUT, LDDELETED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL ACDC_WIPE_MF_PHYS_OPT_TYPE (SELF%OPT, LDDELETED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL ACDC_WIPE_MF_PHYS_FOR_TYPE (SELF%FOR, LDDELETED=.TRUE., LDFIELDAPI=LDFIELDAPI)

LLDELETED = .FALSE.
IF (PRESENT (LDDELETED)) THEN
  LLDELETED = LDDELETED
ENDIF
IF (.NOT. LLDELETED) THEN
  !$ACC EXIT DATA DELETE (SELF)
ENDIF
END SUBROUTINE



END MODULE
