SUBROUTINE APL_ARPEGE_INIT_OUTLINE_006 (YDCPG_BNDS, YDCPG_OPTS, YDMODEL, PPFL_FPLCH, PPFL_FPLSH)
USE CPG_OPTS_TYPE_MOD,ONLY:CPG_BNDS_TYPE, CPG_OPTS_TYPE
USE PARKIND1,ONLY:JPIM, JPRB
USE TYPE_MODEL,ONLY:MODEL
USE YOMHOOK,ONLY:LHOOK, JPHOOK, DR_HOOK

IMPLICIT NONE

TYPE (CPG_BNDS_TYPE), INTENT (IN)::YDCPG_BNDS
TYPE (CPG_OPTS_TYPE), INTENT (IN)::YDCPG_OPTS
TYPE (MODEL), INTENT (IN)::YDMODEL
REAL (KIND=JPRB), INTENT (OUT)::PPFL_FPLCH (YDCPG_OPTS%KLON, 0:YDCPG_OPTS%KFLEVG)
REAL (KIND=JPRB), INTENT (OUT)::PPFL_FPLSH (YDCPG_OPTS%KLON, 0:YDCPG_OPTS%KFLEVG)
INTEGER (KIND=JPIM)::JLEV
INTEGER (KIND=JPIM)::JLON
REAL (KIND=JPHOOK)::ZHOOK_HANDLE
IF (LHOOK) CALL DR_HOOK ('APL_ARPEGE_INIT_OUTLINE_006', 0, ZHOOK_HANDLE)

IF ((YDMODEL%YRML_PHY_MF%YRPHY%LNEBN.OR.YDMODEL%YRML_PHY_MF&
  &%YRPHY%LRRGUST).AND.YDCPG_OPTS%NSTEP==0) THEN

  DO JLEV=YDCPG_OPTS%KTDIA-1, YDCPG_OPTS%KFLEVG

    DO JLON=YDCPG_BNDS%KIDIA, YDCPG_BNDS%KFDIA
      PPFL_FPLCH (JLON, JLEV)=0.0_JPRB
    ENDDO

  ENDDO

ENDIF


IF (YDMODEL%YRML_PHY_MF%YRPHY%LRRGUST.AND.YDCPG_OPTS%NSTEP==0) THEN

  DO JLEV=YDCPG_OPTS%KTDIA-1, YDCPG_OPTS%KFLEVG

    DO JLON=YDCPG_BNDS%KIDIA, YDCPG_BNDS%KFDIA
      PPFL_FPLSH (JLON, JLEV)=0.0_JPRB
    ENDDO

  ENDDO

ENDIF

IF (LHOOK) CALL DR_HOOK ('APL_ARPEGE_INIT_OUTLINE_006', 1, ZHOOK_HANDLE)
ENDSUBROUTINE
