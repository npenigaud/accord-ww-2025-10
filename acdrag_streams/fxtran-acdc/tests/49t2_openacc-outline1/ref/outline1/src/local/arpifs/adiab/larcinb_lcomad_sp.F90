SUBROUTINE LARCINB_LCOMAD_SP (KQM, KWNT, KASLB1, YDCPG_BNDS, YDCPG_SL1&
&, YDGEOMETRY, YDML_DYN, KLH0, PDP, PLSCAW, PRSCAW)
USE CPG_OPTS_TYPE_MOD,ONLY:CPG_OPTS_TYPE, CPG_BNDS_TYPE
USE CPG_SL1_TYPE_MOD,ONLY:CPG_SL1F_TYPE
USE GEOMETRY_MOD,ONLY:GEOMETRY
USE PARKIND1,ONLY:JPIM, JPRB
USE MODEL_DYNAMICS_MOD,ONLY:MODEL_DYNAMICS_TYPE
USE YOMHOOK,ONLY:LHOOK, JPHOOK, DR_HOOK

IMPLICIT NONE

INTEGER (KIND=JPIM), INTENT (INOUT)::KQM
INTEGER (KIND=JPIM), INTENT (INOUT)::KWNT
INTEGER (KIND=JPIM), INTENT (IN)::KASLB1
TYPE (CPG_BNDS_TYPE), INTENT (IN)::YDCPG_BNDS
TYPE (CPG_SL1F_TYPE), INTENT (IN)::YDCPG_SL1
TYPE (GEOMETRY), INTENT (IN)::YDGEOMETRY
TYPE (MODEL_DYNAMICS_TYPE), INTENT (IN)::YDML_DYN
INTEGER (KIND=JPIM), INTENT (IN)::KLH0 (YDGEOMETRY%YRDIM%NPROMA, YDGEOMETRY%YRDIMV%NFLEVG, 0:3)
REAL (KIND=JPRB), INTENT (OUT)::PDP (YDGEOMETRY%YRDIM%NPROMA, YDGEOMETRY%YRDIMV%NFLEVG)
REAL (KIND=JPRB), INTENT (IN)::PLSCAW (YDGEOMETRY%YRDIM%NPROMA&
&, YDGEOMETRY%YRDIMV%NFLEVG, YDML_DYN%YYTLSCAW%NDIM)
REAL (KIND=JPRB), INTENT (IN)::PRSCAW (YDGEOMETRY%YRDIM%NPROMA&
&, YDGEOMETRY%YRDIMV%NFLEVG, YDML_DYN%YYTRSCAW%NDIM)
#include "laiddi.intfb.h"
REAL (KIND=JPHOOK)::ZHOOK_HANDLE
IF (LHOOK) CALL DR_HOOK ('LARCINB_LCOMAD_SP', 0, ZHOOK_HANDLE)

IF (YDML_DYN%YRDYNA%LCOMAD_SP) THEN
  CALL LAIDDI (KASLB1, YDGEOMETRY%YRDIM%NPROMA, YDCPG_BNDS%KIDIA, YDCPG_BNDS%KFDIA, YDGEOMETRY&
  &%YRDIMV%NFLEVG, YDGEOMETRY%YRDIMV%NFLSA, YDGEOMETRY%YRDIMV%NFLEN, KQM, PRSCAW (:,:, YDML_DYN&
  &%YYTRSCAW%M_WCLAMAD (KWNT)), PLSCAW (:,:, YDML_DYN%YYTLSCAW%M_WDLOMAD), PRSCAW (:,:,&
  & YDML_DYN%YYTRSCAW%M_WCLOMAD (KWNT)), KLH0, YDCPG_SL1%SP9%P, PDP)
ELSE
  CALL LAIDDI (KASLB1, YDGEOMETRY%YRDIM%NPROMA, YDCPG_BNDS%KIDIA, YDCPG_BNDS%KFDIA, YDGEOMETRY&
  &%YRDIMV%NFLEVG, YDGEOMETRY%YRDIMV%NFLSA, YDGEOMETRY%YRDIMV%NFLEN, KQM, PRSCAW (:,:&
  &, YDML_DYN%YYTRSCAW%M_WCLA (KWNT)), PLSCAW (:,:, YDML_DYN%YYTLSCAW%M_WDLO), PRSCAW&
  & (:,:, YDML_DYN%YYTRSCAW%M_WCLO (KWNT)), KLH0, YDCPG_SL1%SP9%P, PDP)
ENDIF

IF (LHOOK) CALL DR_HOOK ('LARCINB_LCOMAD_SP', 1, ZHOOK_HANDLE)
ENDSUBROUTINE
