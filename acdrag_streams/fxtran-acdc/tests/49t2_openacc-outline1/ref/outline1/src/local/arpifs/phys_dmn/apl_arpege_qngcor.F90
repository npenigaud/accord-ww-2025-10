SUBROUTINE APL_ARPEGE_QNGCOR (YDCPG_BNDS, YDCPG_OPTS, YDMF_PHYS, YDMF_PHYS_BASE_STATE, YDMODEL&
&, PFPCOR, PFPLSL, PFPLSN, PPFL_FPLCH, PPFL_FPLSH, PQI, PQL, PQV, PSEDIQI, PSEDIQL)
USE CPG_OPTS_TYPE_MOD,ONLY:CPG_BNDS_TYPE, CPG_OPTS_TYPE
USE PARKIND1,ONLY:JPIM, JPRB
USE MF_PHYS_BASE_STATE_TYPE_MOD,ONLY:MF_PHYS_BASE_STATE_TYPE
USE MF_PHYS_TYPE_MOD,ONLY:MF_PHYS_TYPE
USE TYPE_MODEL,ONLY:MODEL
USE YOMHOOK,ONLY:LHOOK, JPHOOK, DR_HOOK

IMPLICIT NONE

TYPE (CPG_BNDS_TYPE), INTENT (IN)::YDCPG_BNDS
TYPE (CPG_OPTS_TYPE), INTENT (IN)::YDCPG_OPTS
TYPE (MF_PHYS_TYPE), INTENT (INOUT)::YDMF_PHYS
TYPE (MF_PHYS_BASE_STATE_TYPE), INTENT (IN)::YDMF_PHYS_BASE_STATE
TYPE (MODEL), INTENT (IN)::YDMODEL
REAL (KIND=JPRB), INTENT (INOUT)::PFPCOR (YDCPG_OPTS%KLON, 0:YDCPG_OPTS%KFLEVG)
REAL (KIND=JPRB), INTENT (INOUT)::PFPLSL (YDCPG_OPTS%KLON, 0:YDCPG_OPTS%KFLEVG)
REAL (KIND=JPRB), INTENT (INOUT)::PFPLSN (YDCPG_OPTS%KLON, 0:YDCPG_OPTS%KFLEVG)
REAL (KIND=JPRB), INTENT (INOUT)::PPFL_FPLCH (YDCPG_OPTS%KLON, 0:YDCPG_OPTS%KFLEVG)
REAL (KIND=JPRB), INTENT (INOUT)::PPFL_FPLSH (YDCPG_OPTS%KLON, 0:YDCPG_OPTS%KFLEVG)
REAL (KIND=JPRB), INTENT (INOUT)::PQI (YDCPG_OPTS%KLON, YDCPG_OPTS%KFLEVG)
REAL (KIND=JPRB), INTENT (INOUT)::PQL (YDCPG_OPTS%KLON, YDCPG_OPTS%KFLEVG)
REAL (KIND=JPRB), INTENT (INOUT)::PQV (YDCPG_OPTS%KLON, YDCPG_OPTS%KFLEVG)
REAL (KIND=JPRB), INTENT (INOUT)::PSEDIQI (YDCPG_OPTS%KLON, 0:YDCPG_OPTS%KFLEVG)
REAL (KIND=JPRB), INTENT (INOUT)::PSEDIQL (YDCPG_OPTS%KLON, 0:YDCPG_OPTS%KFLEVG)
#include "qngcor.intfb.h"
INTEGER (KIND=JPIM)::JLEV
INTEGER (KIND=JPIM)::JLON
REAL (KIND=JPRB)::ZFLX_LOTT_GWU (YDCPG_OPTS%KLON, 0:YDCPG_OPTS%KFLEVG)
REAL (KIND=JPRB)::ZFLX_LOTT_GWV (YDCPG_OPTS%KLON, 0:YDCPG_OPTS%KFLEVG)
REAL (KIND=JPHOOK)::ZHOOK_HANDLE
IF (LHOOK) CALL DR_HOOK ('APL_ARPEGE_QNGCOR', 0, ZHOOK_HANDLE)

DO JLEV=0, YDCPG_OPTS%KFLEVG

  DO JLON=YDCPG_BNDS%KIDIA, YDCPG_BNDS%KFDIA
    ZFLX_LOTT_GWU (JLON, JLEV)=0.0_JPRB
    ZFLX_LOTT_GWV (JLON, JLEV)=0.0_JPRB
  ENDDO

ENDDO


IF (YDMODEL%YRML_PHY_MF%YRPHY%LNEBN.OR.YDMODEL%YRML_PHY_MF%YRPHY%LRRGUST) THEN

  DO JLEV=YDCPG_OPTS%KTDIA-1, YDCPG_OPTS%KFLEVG

    DO JLON=YDCPG_BNDS%KIDIA, YDCPG_BNDS%KFDIA
      PPFL_FPLCH (JLON, JLEV)=PFPCOR (JLON, JLEV)
    ENDDO

  ENDDO

ENDIF


IF (YDMODEL%YRML_PHY_MF%YRPHY%LRRGUST) THEN

  DO JLEV=YDCPG_OPTS%KTDIA-1, YDCPG_OPTS%KFLEVG

    DO JLON=YDCPG_BNDS%KIDIA, YDCPG_BNDS%KFDIA
      PPFL_FPLSH (JLON, JLEV)=YDMF_PHYS%OUT%FPLSL (JLON, JLEV)+YDMF_PHYS&
      &%OUT%FPLSN (JLON, JLEV)+YDMF_PHYS%OUT%FPLSG (JLON, JLEV)
    ENDDO

  ENDDO

ENDIF


DO JLEV=YDCPG_OPTS%KTDIA, YDCPG_OPTS%KFLEVG

  DO JLON=YDCPG_BNDS%KIDIA, YDCPG_BNDS%KFDIA
    YDMF_PHYS%OUT%DIFTQL (JLON, JLEV)=YDMF_PHYS%OUT%DIFTQL (JLON, JLEV)+PSEDIQL (JLON, JLEV)
    YDMF_PHYS%OUT%DIFTQN (JLON, JLEV)=YDMF_PHYS%OUT%DIFTQN (JLON, JLEV)+PSEDIQI (JLON, JLEV)
    PFPLSL (JLON, JLEV)=YDMF_PHYS%OUT%DIFTQL (JLON, JLEV)+YDMF_PHYS%OUT%FPLSL (JLON, JLEV)
    PFPLSN (JLON, JLEV)=YDMF_PHYS%OUT%DIFTQN (JLON, JLEV)+YDMF_PHYS%OUT%FPLSN (JLON, JLEV)
  ENDDO

ENDDO

CALL QNGCOR (YDMODEL%YRCST, YDMODEL%YRML_PHY_MF%YRPHY2, YDCPG_BNDS%KIDIA, YDCPG_BNDS%KFDIA, YDCPG_OPTS&
&%KLON, YDMODEL%YRML_PHY_MF%YRTOPH%NTPLUI, YDCPG_OPTS%KFLEVG, PQV, PQL, PQI, YDMF_PHYS_BASE_STATE&
&%YCPG_PHY%XYB%RDELP, YDMF_PHYS%OUT%DIFCQ, YDMF_PHYS%OUT%DIFCQN, YDMF_PHYS%OUT%DIFCQL, YDMF_PHYS&
&%OUT%DIFTQ, YDMF_PHYS%OUT%DIFTQN, YDMF_PHYS%OUT%DIFTQL, YDMF_PHYS%OUT%FPEVPSL, YDMF_PHYS%OUT&
&%FPEVPSN, YDMF_PHYS%OUT%FPEVPCL, YDMF_PHYS%OUT%FPEVPCN, YDMF_PHYS%OUT%FPFPSL, YDMF_PHYS%OUT&
&%FPFPSN, YDMF_PHYS%OUT%FPFPCL, YDMF_PHYS%OUT%FPFPCN, YDMF_PHYS%OUT%FCCQL, YDMF_PHYS%OUT%FCCQN&
&, YDMF_PHYS%OUT%FCSQL, YDMF_PHYS%OUT%FCSQN, YDMF_PHYS%OUT%FCQNG)
IF (LHOOK) CALL DR_HOOK ('APL_ARPEGE_QNGCOR', 1, ZHOOK_HANDLE)
ENDSUBROUTINE
