SUBROUTINE GPRCP_EXPL_OUTLINE_005 (YDCPG_BNDS, YDCPG_OPTS, PCP, PR, PKAP)
USE CPG_OPTS_TYPE_MOD,ONLY:CPG_BNDS_TYPE, CPG_OPTS_TYPE
USE PARKIND1,ONLY:JPIM, JPRB
USE YOMHOOK,ONLY:LHOOK, JPHOOK, DR_HOOK

IMPLICIT NONE

TYPE (CPG_BNDS_TYPE), INTENT (IN)::YDCPG_BNDS
TYPE (CPG_OPTS_TYPE), INTENT (IN)::YDCPG_OPTS
REAL (KIND=JPRB), INTENT (INOUT), POINTER::PCP (:,:)
REAL (KIND=JPRB), INTENT (INOUT), POINTER::PR (:,:)
REAL (KIND=JPRB), OPTIONAL, INTENT (OUT)::PKAP (YDCPG_OPTS%KLON, YDCPG_OPTS%KFLEVG)
INTEGER (KIND=JPIM)::JLEV
INTEGER (KIND=JPIM)::JLON
REAL (KIND=JPHOOK)::ZHOOK_HANDLE
IF (LHOOK) CALL DR_HOOK ('GPRCP_EXPL_OUTLINE_005', 0, ZHOOK_HANDLE)

DO JLEV=1, YDCPG_OPTS%KFLEVG

  DO JLON=YDCPG_BNDS%KIDIA, YDCPG_BNDS%KFDIA
    PKAP (JLON, JLEV)=PR (JLON, JLEV)/PCP (JLON, JLEV)
  ENDDO

ENDDO

IF (LHOOK) CALL DR_HOOK ('GPRCP_EXPL_OUTLINE_005', 1, ZHOOK_HANDLE)
ENDSUBROUTINE
