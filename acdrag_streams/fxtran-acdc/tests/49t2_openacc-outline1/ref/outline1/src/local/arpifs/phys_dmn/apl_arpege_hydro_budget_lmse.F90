SUBROUTINE APL_ARPEGE_HYDRO_BUDGET_LMSE (YDCPG_BNDS, YDCPG_GPAR&
&, YDCPG_OPTS, YDMF_PHYS, PFPLSL, PFPLSN)
USE CPG_OPTS_TYPE_MOD,ONLY:CPG_BNDS_TYPE, CPG_OPTS_TYPE
USE CPG_TYPE_MOD,ONLY:CPG_GPAR_TYPE
USE PARKIND1,ONLY:JPIM, JPRB
USE MF_PHYS_TYPE_MOD,ONLY:MF_PHYS_TYPE
USE YOMHOOK,ONLY:LHOOK, JPHOOK, DR_HOOK

IMPLICIT NONE

TYPE (CPG_BNDS_TYPE), INTENT (IN)::YDCPG_BNDS
TYPE (CPG_GPAR_TYPE), INTENT (INOUT)::YDCPG_GPAR
TYPE (CPG_OPTS_TYPE), INTENT (IN)::YDCPG_OPTS
TYPE (MF_PHYS_TYPE), INTENT (INOUT)::YDMF_PHYS
REAL (KIND=JPRB), INTENT (IN)::PFPLSL (YDCPG_OPTS%KLON, 0:YDCPG_OPTS%KFLEVG)
REAL (KIND=JPRB), INTENT (IN)::PFPLSN (YDCPG_OPTS%KLON, 0:YDCPG_OPTS%KFLEVG)
INTEGER (KIND=JPIM)::JLON
REAL (KIND=JPHOOK)::ZHOOK_HANDLE
IF (LHOOK) CALL DR_HOOK ('APL_ARPEGE_HYDRO_BUDGET_LMSE', 0, ZHOOK_HANDLE)

DO JLON=YDCPG_BNDS%KIDIA, YDCPG_BNDS%KFDIA
  YDCPG_GPAR%RAIN (JLON)=PFPLSL (JLON, YDCPG_OPTS%KFLEVG&
  &)+YDMF_PHYS%OUT%FPLCL (JLON, YDCPG_OPTS%KFLEVG)
  YDCPG_GPAR%SNOW (JLON)=PFPLSN (JLON, YDCPG_OPTS%KFLEVG&
  &)+YDMF_PHYS%OUT%FPLCN (JLON, YDCPG_OPTS%KFLEVG)
ENDDO

IF (LHOOK) CALL DR_HOOK ('APL_ARPEGE_HYDRO_BUDGET_LMSE', 1, ZHOOK_HANDLE)
ENDSUBROUTINE
