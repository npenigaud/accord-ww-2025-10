SUBROUTINE APL_ARPEGE_RADIATION_DIRECT (YDCPG_BNDS, YDCPG_OPTS, YDMF_PHYS, PRDG_MU0)
USE CPG_OPTS_TYPE_MOD,ONLY:CPG_BNDS_TYPE, CPG_OPTS_TYPE
USE PARKIND1,ONLY:JPIM, JPRB
USE MF_PHYS_TYPE_MOD,ONLY:MF_PHYS_TYPE
USE YOMHOOK,ONLY:LHOOK, JPHOOK, DR_HOOK

IMPLICIT NONE

TYPE (CPG_BNDS_TYPE), INTENT (IN)::YDCPG_BNDS
TYPE (CPG_OPTS_TYPE), INTENT (IN)::YDCPG_OPTS
TYPE (MF_PHYS_TYPE), INTENT (INOUT)::YDMF_PHYS
REAL (KIND=JPRB), INTENT (IN)::PRDG_MU0 (YDCPG_OPTS%KLON)
INTEGER (KIND=JPIM)::JLON
REAL (KIND=JPHOOK)::ZHOOK_HANDLE
IF (LHOOK) CALL DR_HOOK ('APL_ARPEGE_RADIATION_DIRECT', 0, ZHOOK_HANDLE)

DO JLON=YDCPG_BNDS%KIDIA, YDCPG_BNDS%KFDIA
  YDMF_PHYS%OUT%FRSDNI (JLON)=YDMF_PHYS%OUT%FRSOPS (JLON)

  IF (PRDG_MU0 (JLON)>3.0E-02_JPRB) THEN
    YDMF_PHYS%OUT%FRSDNI (JLON)=YDMF_PHYS%OUT%FRSOPS (JLON)/PRDG_MU0 (JLON)
  ENDIF

  YDMF_PHYS%OUT%FRSDNI (JLON)=MAX (0.0_JPRB, YDMF_PHYS%OUT%FRSDNI (JLON))
ENDDO


DO JLON=YDCPG_BNDS%KIDIA, YDCPG_BNDS%KFDIA
  YDMF_PHYS%OUT%FRSGNI (JLON)=YDMF_PHYS%OUT%FRSDNI (JLON)+0.5_JPRB*((1.0_JPRB+PRDG_MU0 (JLON&
  &))*(YDMF_PHYS%OUT%FRSODS (JLON)-YDMF_PHYS%OUT%FRSOPS (JLON))+(1.0_JPRB-PRDG_MU0 (JLON)&
  &)*(YDMF_PHYS%OUT%FRSODS (JLON)-YDMF_PHYS%OUT%FRSO (JLON, YDCPG_OPTS%KFLEVG, 1)))
ENDDO

IF (LHOOK) CALL DR_HOOK ('APL_ARPEGE_RADIATION_DIRECT', 1, ZHOOK_HANDLE)
ENDSUBROUTINE
