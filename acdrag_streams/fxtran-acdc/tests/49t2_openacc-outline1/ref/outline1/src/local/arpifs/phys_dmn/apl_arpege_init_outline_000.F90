SUBROUTINE APL_ARPEGE_INIT_OUTLINE_000 (YDCPG_BNDS, YDCPG_OPTS, YDMODEL, YDVARS, PAIPCMT, PDECRD)
USE CPG_OPTS_TYPE_MOD,ONLY:CPG_BNDS_TYPE, CPG_OPTS_TYPE
USE FIELD_VARIABLES_MOD,ONLY:FIELD_VARIABLES
USE PARKIND1,ONLY:JPIM, JPRB
USE TYPE_MODEL,ONLY:MODEL
USE YOMHOOK,ONLY:LHOOK, JPHOOK, DR_HOOK

IMPLICIT NONE

TYPE (CPG_BNDS_TYPE), INTENT (IN)::YDCPG_BNDS
TYPE (CPG_OPTS_TYPE), INTENT (IN)::YDCPG_OPTS
TYPE (MODEL), INTENT (IN)::YDMODEL
TYPE (FIELD_VARIABLES), INTENT (INOUT)::YDVARS
REAL (KIND=JPRB), INTENT (OUT)::PAIPCMT (YDCPG_OPTS%KLON)
REAL (KIND=JPRB), INTENT (OUT)::PDECRD (YDCPG_OPTS%KLON)
INTEGER (KIND=JPIM)::JLON
REAL (KIND=JPHOOK)::ZHOOK_HANDLE
IF (LHOOK) CALL DR_HOOK ('APL_ARPEGE_INIT_OUTLINE_000', 0, ZHOOK_HANDLE)
PAIPCMT (:)=0._JPRB

IF (YDMODEL%YRML_PHY_MF%YRPHY0%RDECRD<=0._JPRB.OR.YDMODEL%YRML_PHY_MF%YRPHY%LRNUEXP) THEN

  DO JLON=YDCPG_BNDS%KIDIA, YDCPG_BNDS%KFDIA
    PDECRD (JLON)=YDMODEL%YRML_PHY_MF%YRPHY0%RDECRD1+YDMODEL%YRML_PHY_MF%YRPHY0%RDECRD2&
    &*EXP (-((ASIN (YDVARS%GEOMETRY%GEMU%T0 (JLON))-YDMODEL%YRML_PHY_MF%YRPHY0%RDECRD3&
    &*YDMODEL%YRML_GCONF%YRRIP%RDECLI)/YDMODEL%YRML_PHY_MF%YRPHY0%RDECRD4)**2)
  ENDDO

ENDIF

IF (LHOOK) CALL DR_HOOK ('APL_ARPEGE_INIT_OUTLINE_000', 1, ZHOOK_HANDLE)
ENDSUBROUTINE
