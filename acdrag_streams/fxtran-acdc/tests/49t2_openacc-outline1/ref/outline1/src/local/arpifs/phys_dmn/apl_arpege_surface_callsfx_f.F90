SUBROUTINE APL_ARPEGE_SURFACE_CALLSFX_F (YDCPG_OPTS, YDMF_PHYS, PFEV)
USE CPG_OPTS_TYPE_MOD,ONLY:CPG_BNDS_TYPE, CPG_OPTS_TYPE
USE PARKIND1,ONLY:JPIM, JPRB
USE MF_PHYS_TYPE_MOD,ONLY:MF_PHYS_TYPE
USE YOMHOOK,ONLY:LHOOK, JPHOOK, DR_HOOK

IMPLICIT NONE

TYPE (CPG_OPTS_TYPE), INTENT (IN)::YDCPG_OPTS
TYPE (MF_PHYS_TYPE), INTENT (INOUT)::YDMF_PHYS
REAL (KIND=JPRB), INTENT (INOUT)::PFEV (YDCPG_OPTS%KLON)
INTEGER (KIND=JPIM)::JSG
REAL (KIND=JPHOOK)::ZHOOK_HANDLE
IF (LHOOK) CALL DR_HOOK ('APL_ARPEGE_SURFACE_CALLSFX_F', 0, ZHOOK_HANDLE)

DO JSG=1, YDCPG_OPTS%KTSSG+1
  YDMF_PHYS%OUT%FCS (:, JSG)=0.0_JPRB
ENDDO

PFEV (:)=0.0_JPRB
IF (LHOOK) CALL DR_HOOK ('APL_ARPEGE_SURFACE_CALLSFX_F', 1, ZHOOK_HANDLE)
ENDSUBROUTINE
