SUBROUTINE APL_ARPEGE_SURFACE_LMSE_F2 (YDCPG_BNDS, YDCPG_GPAR&
&, YDCPG_OPTS, YDMF_PHYS_SURF, PTSN, PTWSNOW)
USE CPG_OPTS_TYPE_MOD,ONLY:CPG_BNDS_TYPE, CPG_OPTS_TYPE
USE CPG_TYPE_MOD,ONLY:CPG_MISC_TYPE, CPG_GPAR_TYPE
USE PARKIND1,ONLY:JPIM, JPRB
USE MF_PHYS_SURFACE_TYPE_MOD,ONLY:MF_PHYS_SURF_TYPE
USE YOMHOOK,ONLY:LHOOK, JPHOOK, DR_HOOK

IMPLICIT NONE

TYPE (CPG_BNDS_TYPE), INTENT (IN)::YDCPG_BNDS
TYPE (CPG_GPAR_TYPE), INTENT (INOUT)::YDCPG_GPAR
TYPE (CPG_OPTS_TYPE), INTENT (IN)::YDCPG_OPTS
TYPE (MF_PHYS_SURF_TYPE), INTENT (INOUT)::YDMF_PHYS_SURF
REAL (KIND=JPRB), INTENT (INOUT)::PTSN (YDCPG_OPTS%KLON)
REAL (KIND=JPRB), INTENT (INOUT)::PTWSNOW (YDCPG_OPTS%KLON)
INTEGER (KIND=JPIM)::JLON
REAL (KIND=JPHOOK)::ZHOOK_HANDLE
IF (LHOOK) CALL DR_HOOK ('APL_ARPEGE_SURFACE_LMSE_F2', 0, ZHOOK_HANDLE)

DO JLON=YDCPG_BNDS%KIDIA, YDCPG_BNDS%KFDIA
  YDCPG_GPAR%VTS (JLON)=PTSN (JLON)
  YDMF_PHYS_SURF%GSP_SG%PF_T1 (JLON, 1)=PTWSNOW (JLON)
ENDDO

IF (LHOOK) CALL DR_HOOK ('APL_ARPEGE_SURFACE_LMSE_F2', 1, ZHOOK_HANDLE)
ENDSUBROUTINE
