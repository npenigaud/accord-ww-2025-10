SUBROUTINE GPRCP_EXPL_OUTLINE_001 (LDCP, JFLD, YDCPG_BNDS, YDCPG_OPTS, YDCST, YDVARS, PCP, PR)
USE CPG_OPTS_TYPE_MOD,ONLY:CPG_BNDS_TYPE, CPG_OPTS_TYPE
USE FIELD_VARIABLES_MOD,ONLY:FIELD_VARIABLES
USE PARKIND1,ONLY:JPIM, JPRB
USE YOMCST,ONLY:TCST
USE YOMHOOK,ONLY:LHOOK, JPHOOK, DR_HOOK

IMPLICIT NONE

LOGICAL, INTENT (INOUT)::LDCP
INTEGER (KIND=JPIM), INTENT (INOUT)::JFLD
TYPE (CPG_BNDS_TYPE), INTENT (IN)::YDCPG_BNDS
TYPE (CPG_OPTS_TYPE), INTENT (IN)::YDCPG_OPTS
TYPE (TCST), INTENT (IN)::YDCST
TYPE (FIELD_VARIABLES), INTENT (INOUT)::YDVARS
REAL (KIND=JPRB), INTENT (INOUT), POINTER::PCP (:,:)
REAL (KIND=JPRB), INTENT (INOUT), POINTER::PR (:,:)
INTEGER (KIND=JPIM)::JLEV
INTEGER (KIND=JPIM)::JLON
REAL (KIND=JPHOOK)::ZHOOK_HANDLE
IF (LHOOK) CALL DR_HOOK ('GPRCP_EXPL_OUTLINE_001', 0, ZHOOK_HANDLE)

DO JLEV=1, YDCPG_OPTS%KFLEVG

  DO JLON=YDCPG_BNDS%KIDIA, YDCPG_BNDS%KFDIA
    PR (JLON, JLEV)=PR (JLON, JLEV)+(YDVARS%GFL_PTR (JFLD)%YCOMP&
    &%R-YDCST%RD)*YDVARS%GFL_PTR (JFLD)%T0 (JLON, JLEV)
  ENDDO

ENDDO


IF (LDCP) THEN

  DO JLEV=1, YDCPG_OPTS%KFLEVG

    DO JLON=YDCPG_BNDS%KIDIA, YDCPG_BNDS%KFDIA
      PCP (JLON, JLEV)=PCP (JLON, JLEV)+(YDVARS%GFL_PTR (JFLD)%YCOMP&
      &%RCP-YDCST%RCPD)*YDVARS%GFL_PTR (JFLD)%T0 (JLON, JLEV)
    ENDDO

  ENDDO

ENDIF

IF (LHOOK) CALL DR_HOOK ('GPRCP_EXPL_OUTLINE_001', 1, ZHOOK_HANDLE)
ENDSUBROUTINE
