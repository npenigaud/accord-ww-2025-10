SUBROUTINE APL_ARPEGE_DEEP_CONVECTION_CULIGHT (YDCPG_BNDS, YDCPG_OPTS, YDMF_PHYS, YDMF_PHYS_BASE_STATE&
&, YDMODEL, YDVARS, KCBOT, KCTOP, LDCUM, LDLAND, PLU, PMFU)
USE CPG_OPTS_TYPE_MOD,ONLY:CPG_BNDS_TYPE, CPG_OPTS_TYPE
USE FIELD_VARIABLES_MOD,ONLY:FIELD_VARIABLES
USE PARKIND1,ONLY:JPIM, JPRB
USE MF_PHYS_BASE_STATE_TYPE_MOD,ONLY:MF_PHYS_BASE_STATE_TYPE
USE MF_PHYS_TYPE_MOD,ONLY:MF_PHYS_TYPE
USE TYPE_MODEL,ONLY:MODEL
USE YOMHOOK,ONLY:LHOOK, JPHOOK, DR_HOOK

IMPLICIT NONE

TYPE (CPG_BNDS_TYPE), INTENT (IN)::YDCPG_BNDS
TYPE (CPG_OPTS_TYPE), INTENT (IN)::YDCPG_OPTS
TYPE (MF_PHYS_TYPE), INTENT (INOUT)::YDMF_PHYS
TYPE (MF_PHYS_BASE_STATE_TYPE), INTENT (IN)::YDMF_PHYS_BASE_STATE
TYPE (MODEL), INTENT (IN)::YDMODEL
TYPE (FIELD_VARIABLES), INTENT (INOUT)::YDVARS
INTEGER (KIND=JPIM), INTENT (INOUT)::KCBOT (YDCPG_OPTS%KLON)
INTEGER (KIND=JPIM), INTENT (INOUT)::KCTOP (YDCPG_OPTS%KLON)
LOGICAL, INTENT (INOUT)::LDCUM (YDCPG_OPTS%KLON)
LOGICAL, INTENT (INOUT)::LDLAND (YDCPG_OPTS%KLON)
REAL (KIND=JPRB), INTENT (INOUT)::PLU (YDCPG_OPTS%KLON, YDCPG_OPTS%KFLEVG)
REAL (KIND=JPRB), INTENT (INOUT)::PMFU (YDCPG_OPTS%KLON, YDCPG_OPTS%KFLEVG)
#include "culight.intfb.h"
INTEGER (KIND=JPIM)::JLON
LOGICAL::LLLINOX (YDCPG_OPTS%KLON)
REAL (KIND=JPRB)::ZCDEPTH (YDCPG_OPTS%KLON)
REAL (KIND=JPRB)::ZCHARGE (YDCPG_OPTS%KLON, YDCPG_OPTS%KFLEVG)
REAL (KIND=JPRB)::ZCTOPH (YDCPG_OPTS%KLON)
REAL (KIND=JPRB)::ZGAW (YDCPG_OPTS%KLON)
REAL (KIND=JPRB)::ZICE (YDCPG_OPTS%KLON)
REAL (KIND=JPRB)::ZLIGH_CTG (YDCPG_OPTS%KLON)
REAL (KIND=JPRB)::ZPRECMX (YDCPG_OPTS%KLON)
REAL (KIND=JPRB)::ZQPFROZ (YDCPG_OPTS%KLON, YDCPG_OPTS%KFLEVG)
REAL (KIND=JPRB)::ZWMFU (YDCPG_OPTS%KLON)
REAL (KIND=JPHOOK)::ZHOOK_HANDLE
IF (LHOOK) CALL DR_HOOK ('APL_ARPEGE_DEEP_CONVECTION_CULIGHT', 0, ZHOOK_HANDLE)
ZGAW (:)=YDVARS%GEOMETRY%GAW%P (:)
ZQPFROZ (:,:)=0
ZCHARGE (:,:)=0
CALL CULIGHT (YDCPG_OPTS%RPLDARE, YDCPG_OPTS%RPLRG, YDMODEL%YRML_PHY_EC%YRTHF, YDMODEL%YRCST&
&, YDMODEL%YRML_PHY_EC%YREPHY, YDMODEL%YRML_GCONF%YGFL, YDMODEL%YRML_PHY_EC%YRECUMF, YDCPG_BNDS&
&%KIDIA, YDCPG_BNDS%KFDIA, YDCPG_OPTS%KLON, YDCPG_OPTS%KFLEVG, ZGAW, ZGAW, YDMF_PHYS_BASE_STATE&
&%YCPG_PHY%PREHYDF, YDMF_PHYS_BASE_STATE%YCPG_PHY%PREHYD, YDMF_PHYS_BASE_STATE%YCPG_DYN%PHI,&
& YDMF_PHYS_BASE_STATE%YCPG_DYN%PHIF, LDLAND, YDMF_PHYS_BASE_STATE%T, PLU, PMFU, YDMF_PHYS%OUT&
&%CAPE, YDMF_PHYS%OUT%FPLCL, YDMF_PHYS%OUT%FPLCN, ZQPFROZ, LDCUM, KCBOT, KCTOP, LLLINOX, YDMF_PHYS&
&%OUT%FLASH, ZLIGH_CTG, ZCTOPH, ZPRECMX, ZICE, ZCDEPTH, ZWMFU, ZCHARGE)

DO JLON=YDCPG_BNDS%KIDIA, YDCPG_BNDS%KFDIA
  YDMF_PHYS%OUT%FLASH (JLON)=YDMF_PHYS%OUT%FLASH (JLON)/86400._JPRB
ENDDO

IF (LHOOK) CALL DR_HOOK ('APL_ARPEGE_DEEP_CONVECTION_CULIGHT', 1, ZHOOK_HANDLE)
ENDSUBROUTINE
