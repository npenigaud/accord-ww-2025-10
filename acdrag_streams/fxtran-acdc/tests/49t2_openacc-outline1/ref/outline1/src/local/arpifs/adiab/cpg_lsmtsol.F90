SUBROUTINE CPG_LSMTSOL (YDCPG_BNDS, YDCPG_MISC, YDCPG_OPTS, YDGEOMETRY, YDMF_PHYS_SURF, YDVARS)
USE CPG_OPTS_TYPE_MOD,ONLY:CPG_BNDS_TYPE, CPG_OPTS_TYPE
USE CPG_TYPE_MOD,ONLY:CPG_DDH_TYPE, CPG_DYN_TYPE, CPG_GPAR_TYPE&
&, CPG_MISC_TYPE, CPG_PHY_TYPE, CPG_TND_TYPE
USE FIELD_VARIABLES_MOD,ONLY:FIELD_VARIABLES
USE GEOMETRY_MOD,ONLY:GEOMETRY
USE MF_PHYS_SURFACE_TYPE_MOD,ONLY:MF_PHYS_SURF_TYPE
USE YOMHOOK,ONLY:LHOOK, JPHOOK, DR_HOOK

IMPLICIT NONE

TYPE (CPG_BNDS_TYPE), INTENT (IN)::YDCPG_BNDS
TYPE (CPG_MISC_TYPE), INTENT (INOUT)::YDCPG_MISC
TYPE (CPG_OPTS_TYPE), INTENT (IN)::YDCPG_OPTS
TYPE (GEOMETRY), INTENT (IN)::YDGEOMETRY
TYPE (MF_PHYS_SURF_TYPE), INTENT (INOUT)::YDMF_PHYS_SURF
TYPE (FIELD_VARIABLES), INTENT (INOUT)::YDVARS
REAL (KIND=JPHOOK)::ZHOOK_HANDLE
IF (LHOOK) CALL DR_HOOK ('CPG_LSMTSOL', 0, ZHOOK_HANDLE)

IF (YDCPG_OPTS%YRSURF_OPTS%YSD_VF%YLSM%LSET) THEN
  YDCPG_MISC%LSM (YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA)=YDMF_PHYS_SURF&
  &%GSD_VF%PLSM (YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA)
ELSE
  YDCPG_MISC%LSM (YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA)=0.0_JPRB
ENDIF


IF (YDCPG_OPTS%YRSURF_OPTS%YSP_RR%YT%LSET) THEN
  YDCPG_MISC%TSOL (YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA)=YDMF_PHYS_SURF&
  &%GSP_RR%PT_T0 (YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA)
ELSE
  YDCPG_MISC%TSOL (YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA)=YDVARS%T%T0 &
  &(YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA, YDGEOMETRY%YRDIMV%NFLEVG)
ENDIF

IF (LHOOK) CALL DR_HOOK ('CPG_LSMTSOL', 1, ZHOOK_HANDLE)
ENDSUBROUTINE
