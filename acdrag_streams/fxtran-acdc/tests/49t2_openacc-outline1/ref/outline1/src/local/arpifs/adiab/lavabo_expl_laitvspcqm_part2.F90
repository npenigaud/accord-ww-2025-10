SUBROUTINE LAVABO_EXPL_LAITVSPCQM_PART2 (YDGEOMETRY, YDCPG_BNDS, YDCPG_OPTS, YDCPG_SL1)
USE PARKIND1,ONLY:JPIM, JPRB
USE YOMHOOK,ONLY:LHOOK, JPHOOK, DR_HOOK
USE GEOMETRY_MOD,ONLY:GEOMETRY
USE CPG_SL1_TYPE_MOD,ONLY:CPG_SL1B_TYPE
USE CPG_OPTS_TYPE_MOD,ONLY:CPG_OPTS_TYPE, CPG_BNDS_TYPE

IMPLICIT NONE


TYPE (GEOMETRY), INTENT (IN)::YDGEOMETRY
TYPE (CPG_BNDS_TYPE), INTENT (IN)::YDCPG_BNDS
TYPE (CPG_OPTS_TYPE), INTENT (IN)::YDCPG_OPTS
TYPE (CPG_SL1B_TYPE), INTENT (INOUT)::YDCPG_SL1
INTEGER (KIND=JPIM)::JGFL
INTEGER (KIND=JPIM)::JLEV1
INTEGER (KIND=JPIM)::JLEV0
REAL (KIND=JPHOOK)::ZHOOK_HANDLE
#include "lavabo_expl_laitvspcqm_part2_outline_000.intfb.h"
IF (LHOOK) CALL DR_HOOK ('LAVABO_EXPL_LAITVSPCQM_PART2', 0, ZHOOK_HANDLE)
JLEV0=0
JLEV1=YDGEOMETRY%YRDIMV%NFLEVG+1

DO JGFL=1, SIZE (YDCPG_SL1%GFL)

  IF ((YDCPG_SL1%GFL (JGFL)%YCOMP%CSLINT/='LAITVSPCQM  ').AND.YDCPG_SL1%GFL (JGFL)%YCOMP%LADV) THEN
    CALL LAVABO_EXPL_LAITVSPCQM_PART2_OUTLINE_000 (JGFL, JLEV0, JLEV1, YDCPG_BNDS, YDCPG_SL1)
  ENDIF

ENDDO

IF (LHOOK) CALL DR_HOOK ('LAVABO_EXPL_LAITVSPCQM_PART2', 1, ZHOOK_HANDLE)
ENDSUBROUTINE LAVABO_EXPL_LAITVSPCQM_PART2
