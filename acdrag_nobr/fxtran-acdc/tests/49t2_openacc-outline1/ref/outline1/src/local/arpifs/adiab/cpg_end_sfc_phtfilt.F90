SUBROUTINE CPG_END_SFC_PHTFILT (YDCPG_BNDS, YDCPG_OPTS, YDMF_PHYS_SURF, YDMODEL)
USE CPG_OPTS_TYPE_MOD,ONLY:CPG_OPTS_TYPE, CPG_BNDS_TYPE
USE MF_PHYS_SURFACE_TYPE_MOD,ONLY:MF_PHYS_SURF_TYPE
USE TYPE_MODEL,ONLY:MODEL
USE YOMHOOK,ONLY:LHOOK, JPHOOK, DR_HOOK

IMPLICIT NONE

TYPE (CPG_BNDS_TYPE), INTENT (IN)::YDCPG_BNDS
TYPE (CPG_OPTS_TYPE), INTENT (IN)::YDCPG_OPTS
TYPE (MF_PHYS_SURF_TYPE), INTENT (INOUT)::YDMF_PHYS_SURF
TYPE (MODEL), INTENT (IN)::YDMODEL
REAL (KIND=JPHOOK)::ZHOOK_HANDLE
IF (LHOOK) CALL DR_HOOK ('CPG_END_SFC_PHTFILT', 0, ZHOOK_HANDLE)
CALL YDMF_PHYS_SURF%PHTFILT (YDMODEL%YRML_DYN%YRDYN, YDCPG_OPTS, YDCPG_BNDS)
IF (LHOOK) CALL DR_HOOK ('CPG_END_SFC_PHTFILT', 1, ZHOOK_HANDLE)
ENDSUBROUTINE
