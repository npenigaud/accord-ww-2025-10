SUBROUTINE CPG_GP_HYD_OUTLINE_002 (LDGPXX, LDUVH, YDCPG_BNDS, YDCPG_DYN0, YDCPG_OPTS, YDCPG_TND, YDGEOMETRY&
&, YDMODEL, YDVARS, P_GWHT_T0, P_GPHL_T0, P_GPHM_T0, P_RDT_T0, P_RT_T0, P_XYBDER_ALPHL_T0, P_XYBDER_ALPHM_T0&
&, P_XYBDER_ALPHPLL_T0, P_XYBDER_ALPHPLM_T0, P_XYBDER_LNPRL_T0, P_XYBDER_LNPRM_T0)
USE CPG_OPTS_TYPE_MOD,ONLY:CPG_BNDS_TYPE, CPG_OPTS_TYPE
USE CPG_TYPE_MOD,ONLY:CPG_DYN_TYPE, CPG_TND_TYPE
USE FIELD_VARIABLES_MOD,ONLY:FIELD_VARIABLES
USE GEOMETRY_MOD,ONLY:GEOMETRY
USE PARKIND1,ONLY:JPIM, JPRB
USE TYPE_MODEL,ONLY:MODEL
USE YOMHOOK,ONLY:LHOOK, JPHOOK, DR_HOOK

IMPLICIT NONE

LOGICAL, INTENT (INOUT)::LDGPXX
LOGICAL, INTENT (INOUT)::LDUVH
TYPE (CPG_BNDS_TYPE), INTENT (IN)::YDCPG_BNDS
TYPE (CPG_DYN_TYPE), INTENT (INOUT)::YDCPG_DYN0
TYPE (CPG_OPTS_TYPE), INTENT (IN)::YDCPG_OPTS
TYPE (CPG_TND_TYPE), INTENT (INOUT)::YDCPG_TND
TYPE (GEOMETRY), INTENT (IN)::YDGEOMETRY
TYPE (MODEL), INTENT (IN)::YDMODEL
TYPE (FIELD_VARIABLES), INTENT (INOUT)::YDVARS
REAL (KIND=JPRB), INTENT (INOUT)::P_GWHT_T0 (YDGEOMETRY%YRDIM%NPROMA, 0:YDGEOMETRY%YRDIMV%NFLEVG)
REAL (KIND=JPRB), INTENT (INOUT)::P_GPHL_T0 (YDGEOMETRY%YRDIM%NPROMA, 0:YDGEOMETRY%YRDIMV%NFLEVG)
REAL (KIND=JPRB), INTENT (INOUT)::P_GPHM_T0 (YDGEOMETRY%YRDIM%NPROMA, 0:YDGEOMETRY%YRDIMV%NFLEVG)
REAL (KIND=JPRB), INTENT (INOUT)::P_RDT_T0 (YDGEOMETRY%YRDIM%NPROMA, YDGEOMETRY%YRDIMV%NFLEVG)
REAL (KIND=JPRB), INTENT (INOUT)::P_RT_T0 (YDGEOMETRY%YRDIM%NPROMA, YDGEOMETRY%YRDIMV%NFLEVG)
REAL (KIND=JPRB), INTENT (INOUT)::P_XYBDER_ALPHL_T0 (YDGEOMETRY&
&%YRDIM%NPROMA, YDGEOMETRY%YRDIMV%NFLEVG)
REAL (KIND=JPRB), INTENT (INOUT)::P_XYBDER_ALPHM_T0 (YDGEOMETRY&
&%YRDIM%NPROMA, YDGEOMETRY%YRDIMV%NFLEVG)
REAL (KIND=JPRB), INTENT (INOUT)::P_XYBDER_ALPHPLL_T0 &
&(YDGEOMETRY%YRDIM%NPROMA, YDGEOMETRY%YRDIMV%NFLEVG)
REAL (KIND=JPRB), INTENT (INOUT)::P_XYBDER_ALPHPLM_T0 &
&(YDGEOMETRY%YRDIM%NPROMA, YDGEOMETRY%YRDIMV%NFLEVG)
REAL (KIND=JPRB), INTENT (INOUT)::P_XYBDER_LNPRL_T0 (YDGEOMETRY&
&%YRDIM%NPROMA, YDGEOMETRY%YRDIMV%NFLEVG)
REAL (KIND=JPRB), INTENT (INOUT)::P_XYBDER_LNPRM_T0 (YDGEOMETRY&
&%YRDIM%NPROMA, YDGEOMETRY%YRDIMV%NFLEVG)
#include "gpgeo_expl.intfb.h"
#include "gpgrgeo_expl.intfb.h"
#include "gpgrp_expl.intfb.h"
#include "gpgw.intfb.h"
#include "gphluv_expl.intfb.h"
#include "gphlwi.intfb.h"
#include "gpuvs.intfb.h"
#include "gpxx.intfb.h"
#include "gp_tndlagadiab_uv.intfb.h"
LOGICAL, PARAMETER::LLDER=.FALSE.
LOGICAL, PARAMETER::LLGDWI=.FALSE.
LOGICAL, PARAMETER::LLGWF0=.TRUE.
INTEGER (KIND=JPIM)::JLEV
INTEGER (KIND=JPIM)::JROF
REAL (KIND=JPRB)::Z_DVER_T0 (YDGEOMETRY%YRDIM%NPROMA, YDGEOMETRY%YRDIMV%NFLEVG)
REAL (KIND=JPRB)::Z_NHXT_T0 (YDGEOMETRY%YRDIM%NPROMA, YDGEOMETRY%YRDIMV%NFLEVG)
REAL (KIND=JPRB)::Z_PSGRTL_T0 (YDGEOMETRY%YRDIM%NPROMA, YDGEOMETRY%YRDIMV%NFLEVG)
REAL (KIND=JPRB)::Z_PSGRTM_T0 (YDGEOMETRY%YRDIM%NPROMA, YDGEOMETRY%YRDIMV%NFLEVG)
REAL (KIND=JPRB)::Z_SGRTL_T0 (YDGEOMETRY%YRDIM%NPROMA, YDGEOMETRY%YRDIMV%NFLEVG)
REAL (KIND=JPRB)::Z_SGRTM_T0 (YDGEOMETRY%YRDIM%NPROMA, YDGEOMETRY%YRDIMV%NFLEVG)
REAL (KIND=JPRB)::Z_US_T0 (YDGEOMETRY%YRDIM%NPROMA)
REAL (KIND=JPRB)::Z_VS_T0 (YDGEOMETRY%YRDIM%NPROMA)
REAL (KIND=JPHOOK)::ZHOOK_HANDLE
IF (LHOOK) CALL DR_HOOK ('CPG_GP_HYD_OUTLINE_002', 0, ZHOOK_HANDLE)

IF (.NOT.YDMODEL%YRML_DYN%YRDYNA%LPGREUSE) THEN
  YDCPG_DYN0%PHI (YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA, YDGEOMETRY%YRDIMV%NFLEVG&
  &)=YDVARS%GEOMETRY%OROG%T0 (YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA)
  CALL GPGEO_EXPL (YDGEOMETRY%YRDIM%NPROMA, YDCPG_BNDS%KIDIA, YDCPG_BNDS%KFDIA, YDGEOMETRY&
  &%YRDIMV%NFLEVG, YDCPG_DYN0%PHI, YDCPG_DYN0%PHIF, YDVARS%T%T0, YDCPG_DYN0%RCP%R&
  &, YDCPG_DYN0%XYB%LNPR, YDCPG_DYN0%XYB%ALPH, YDGEOMETRY%YRVERT_GEOM)
  CALL GPGRGEO_EXPL (YDGEOMETRY, YDGEOMETRY%YRDIM%NPROMA, YDCPG_BNDS%KIDIA, YDCPG_BNDS%KFDIA, YDGEOMETRY&
  &%YRDIMV%NFLEVG, P_RT_T0, YDCPG_DYN0%RTL, YDCPG_DYN0%RTM, YDCPG_DYN0%XYB%LNPR, YDCPG_DYN0%XYB&
  &%ALPH, POROGL=YDCPG_DYN0%OROGL, POROGM=YDCPG_DYN0%OROGM, PHIFL=YDCPG_DYN0%PHIFL, PHIFM=YDCPG_DYN0&
  &%PHIFM, PHIHL=P_GPHL_T0, PHIHM=P_GPHM_T0, PLNPRL_DER=P_XYBDER_LNPRL_T0, PLNPRM_DER=P_XYBDER_LNPRM_T0&
  &, PALPHL_DER=P_XYBDER_ALPHL_T0, PALPHM_DER=P_XYBDER_ALPHM_T0)
ENDIF


DO JLEV=1, YDGEOMETRY%YRDIMV%NFLEVG

  DO JROF=YDCPG_BNDS%KIDIA, YDCPG_BNDS%KFDIA
    YDCPG_DYN0%KENE (JROF, JLEV)=0.5_JPRB*(YDVARS%U%T0 (JROF, JLEV)**2+YDVARS%V%T0 (JROF, JLEV)**2)
  ENDDO

ENDDO


IF (LDUVH) THEN
  CALL GPHLWI (YDGEOMETRY%YRVERT_GEOM%YRCVER%LVFE_COMPATIBLE, YDGEOMETRY%YRDIMV%NFLEVG, YDGEOMETRY%YRDIM&
  &%NPROMA, YDCPG_BNDS%KIDIA, YDCPG_BNDS%KFDIA, YDCPG_DYN0%XYB%LNPR, YDCPG_DYN0%XYB%ALPH, YDGEOMETRY&
  &%YRVERT_GEOM%YRVETA%VRDETAH, YDGEOMETRY%YRVERT_GEOM%YRVETA%VDETA_RATIO, YDCPG_DYN0%UVH%WWI)
  CALL GPHLUV_EXPL (YDGEOMETRY%YRDIMV, YDGEOMETRY%YRDIM%NPROMA, YDCPG_BNDS%KIDIA, YDCPG_BNDS%KFDIA, YDVARS&
  &%U%T0, YDVARS%V%T0, PWWI=YDCPG_DYN0%UVH%WWI, PUH=YDCPG_DYN0%UVH%UH, PVH=YDCPG_DYN0%UVH%VH)
ENDIF


IF (LDGPXX.OR.YDMODEL%YRML_DYN%YRDYNA%LSLHD) THEN
  CALL GPUVS (YDGEOMETRY%YRDIMV%NFLEVG, YDGEOMETRY%YRDIM%NPROMA, YDCPG_BNDS%KIDIA&
  &, YDCPG_BNDS%KFDIA, LLDER, YDVARS%U%T0, YDVARS%V%T0, Z_US_T0, Z_VS_T0)
ENDIF


IF (.NOT.YDMODEL%YRML_DYN%YRDYNA%LPGFSF) THEN
  CALL GPGRP_EXPL (YDGEOMETRY, YDCPG_BNDS%KIDIA, YDCPG_BNDS%KFDIA, P_RT_T0, YDCPG_DYN0%RTL, YDCPG_DYN0&
  &%RTM, YDCPG_DYN0%PREL, YDCPG_DYN0%PREM, YDCPG_DYN0%XYB%RTGR, YDCPG_DYN0%XYB%ALPH, PHIHL=P_GPHL_T0&
  &, PHIHM=P_GPHM_T0, PHIFL=YDCPG_DYN0%PHIFL, PHIFM=YDCPG_DYN0%PHIFM, PSGRTL=Z_PSGRTL_T0, PSGRTM&
  &=Z_PSGRTM_T0, PALPHPLL_DER=P_XYBDER_ALPHPLL_T0, PALPHPLM_DER=P_XYBDER_ALPHPLM_T0)
ENDIF


IF (YDMODEL%YRML_DYN%YRDYNA%LGRADSP) THEN

  DO JLEV=1, YDGEOMETRY%YRDIMV%NFLEVG

    DO JROF=YDCPG_BNDS%KIDIA, YDCPG_BNDS%KFDIA
      Z_SGRTL_T0 (JROF, JLEV)=Z_PSGRTL_T0 (JROF, JLEV)-(YDVARS&
      &%SGRTL%T9 (JROF, JLEV)-YDVARS%SGRTL%T0 (JROF, JLEV))
      Z_SGRTM_T0 (JROF, JLEV)=Z_PSGRTM_T0 (JROF, JLEV)-(YDVARS&
      &%SGRTM%T9 (JROF, JLEV)-YDVARS%SGRTM%T0 (JROF, JLEV))
    ENDDO

  ENDDO

  YDVARS%SGRTL%T0 (YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA, 1:YDGEOMETRY%YRDIMV%NFLEVG)&
  &=Z_PSGRTL_T0 (YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA, 1:YDGEOMETRY%YRDIMV%NFLEVG)
  YDVARS%SGRTM%T0 (YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA, 1:YDGEOMETRY%YRDIMV%NFLEVG)&
  &=Z_PSGRTM_T0 (YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA, 1:YDGEOMETRY%YRDIMV%NFLEVG)
  Z_PSGRTL_T0 (YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA, 1:YDGEOMETRY%YRDIMV%NFLEVG)=Z_SGRTL_T0&
  & (YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA, 1:YDGEOMETRY%YRDIMV%NFLEVG)
  Z_PSGRTM_T0 (YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA, 1:YDGEOMETRY%YRDIMV%NFLEVG)=Z_SGRTM_T0&
  & (YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA, 1:YDGEOMETRY%YRDIMV%NFLEVG)
ENDIF


IF (LDGPXX) THEN
  CALL GPXX (YDGEOMETRY, YDGEOMETRY%YRDIMV%NFLEVG, YDGEOMETRY%YRDIM%NPROMA, YDCPG_BNDS%KIDIA, YDCPG_BNDS&
  &%KFDIA, P_GPHL_T0, P_GPHM_T0, YDCPG_DYN0%PHIFL, YDCPG_DYN0%PHIFM, YDCPG_DYN0%XYB%LNPR, P_RT_T0, YDVARS&
  &%U%T0, YDVARS%V%T0, YDCPG_DYN0%UVH%UH, YDCPG_DYN0%UVH%VH, YDCPG_DYN0%UVH%WWI, Z_NHXT_T0)

  IF (YDMODEL%YRML_DYN%YRDYNA%L_RDRY_VD) THEN

    DO JLEV=1, YDGEOMETRY%YRDIMV%NFLEVG

      DO JROF=YDCPG_BNDS%KIDIA, YDCPG_BNDS%KFDIA
        Z_DVER_T0 (JROF, JLEV)=-(YDCPG_DYN0%RCP%R (JROF, JLEV)/YDMODEL%YRCST%RD)*(YDVARS%DIV%T0 (JROF, JLEV)+Z_NHXT_T0&
        & (JROF, JLEV)+(1.0_JPRB-YDCPG_DYN0%RCP%KAP (JROF, JLEV))*YDCPG_DYN0%CTY%VVEL (JROF, JLEV))
      ENDDO

    ENDDO

  ELSE

    DO JLEV=1, YDGEOMETRY%YRDIMV%NFLEVG

      DO JROF=YDCPG_BNDS%KIDIA, YDCPG_BNDS%KFDIA
        Z_DVER_T0 (JROF, JLEV)=-(YDVARS%DIV%T0 (JROF, JLEV)+Z_NHXT_T0 (JROF, JLEV)+(1&
        &.0_JPRB-YDCPG_DYN0%RCP%KAP (JROF, JLEV))*YDCPG_DYN0%CTY%VVEL (JROF, JLEV))
      ENDDO

    ENDDO

  ENDIF

  CALL GPGW (YDGEOMETRY, YDMODEL%YRML_DYN%YRDYNA%LNHDYN, YDGEOMETRY%YRDIMV%NFLEVG&
  &, YDGEOMETRY%YRDIM%NPROMA, YDCPG_BNDS%KIDIA, YDCPG_BNDS%KFDIA, LLGWF0, LLGDWI&
  &, YDCPG_DYN0%OROGL, YDCPG_DYN0%OROGM, YDCPG_DYN0%XYB%LNPR, YDCPG_DYN0%XYB%ALPH&
  &, Z_US_T0, Z_VS_T0, P_RDT_T0, Z_DVER_T0, P_GWHT_T0, YDCPG_DYN0%GWFT)
ENDIF

CALL GP_TNDLAGADIAB_UV (YDCPG_OPTS%LRPLANE, YDGEOMETRY, YDMODEL%YRML_PHY_EC%YREPHY, YDMODEL&
&%YRML_DYN%YRDYN, YDCPG_BNDS%KIDIA, YDCPG_BNDS%KFDIA, YDVARS%GEOMETRY%RCORI%T0, YDVARS%GEOMETRY&
&%GNORDL%T0, YDVARS%GEOMETRY%GNORDM%T0, Z_PSGRTL_T0, Z_PSGRTM_T0, YDVARS%U%T0, YDVARS%V%T0&
&, YDCPG_TND%TNDU, YDCPG_TND%TNDV, YDCPG_TND%TNDU_NOC, YDCPG_TND%TNDV_NOC)

DO JLEV=1, YDGEOMETRY%YRDIMV%NFLEVG

  DO JROF=YDCPG_BNDS%KIDIA, YDCPG_BNDS%KFDIA
    YDCPG_TND%TNDT (JROF, JLEV)=YDCPG_DYN0%RCP%KAP (JROF, JLEV)*YDVARS&
    &%T%T0 (JROF, JLEV)*YDCPG_DYN0%CTY%VVEL (JROF, JLEV)
  ENDDO

ENDDO

IF (LHOOK) CALL DR_HOOK ('CPG_GP_HYD_OUTLINE_002', 1, ZHOOK_HANDLE)
ENDSUBROUTINE
