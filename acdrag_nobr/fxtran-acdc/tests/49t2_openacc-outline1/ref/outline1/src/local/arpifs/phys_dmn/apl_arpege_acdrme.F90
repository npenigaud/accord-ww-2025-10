SUBROUTINE APL_ARPEGE_ACDRME (YDCPG_BNDS, YDCPG_MISC, YDCPG_OPTS, YDGEOMETRY&
&, YDMF_PHYS, YDMF_PHYS_BASE_STATE, YDMODEL, YDVARS, PMSC_FRMQ, PQV)
USE CPG_OPTS_TYPE_MOD,ONLY:CPG_BNDS_TYPE, CPG_OPTS_TYPE
USE CPG_TYPE_MOD,ONLY:CPG_MISC_TYPE, CPG_DYN_TYPE, CPG_GPAR_TYPE, CPG_PHY_TYPE
USE FIELD_VARIABLES_MOD,ONLY:FIELD_VARIABLES
USE GEOMETRY_MOD,ONLY:GEOMETRY
USE PARKIND1,ONLY:JPIM, JPRB
USE MF_PHYS_BASE_STATE_TYPE_MOD,ONLY:MF_PHYS_BASE_STATE_TYPE
USE MF_PHYS_TYPE_MOD,ONLY:MF_PHYS_TYPE
USE TYPE_MODEL,ONLY:MODEL
USE YOMHOOK,ONLY:LHOOK, JPHOOK, DR_HOOK

IMPLICIT NONE

TYPE (CPG_BNDS_TYPE), INTENT (IN)::YDCPG_BNDS
TYPE (CPG_MISC_TYPE), INTENT (INOUT)::YDCPG_MISC
TYPE (CPG_OPTS_TYPE), INTENT (IN)::YDCPG_OPTS
TYPE (GEOMETRY), INTENT (IN)::YDGEOMETRY
TYPE (MF_PHYS_TYPE), INTENT (INOUT)::YDMF_PHYS
TYPE (MF_PHYS_BASE_STATE_TYPE), INTENT (IN)::YDMF_PHYS_BASE_STATE
TYPE (MODEL), INTENT (IN)::YDMODEL
TYPE (FIELD_VARIABLES), INTENT (INOUT)::YDVARS
REAL (KIND=JPRB), INTENT (INOUT)::PMSC_FRMQ (YDCPG_OPTS%KLON, 0:YDCPG_OPTS%KFLEVG)
REAL (KIND=JPRB), INTENT (INOUT)::PQV (YDCPG_OPTS%KLON, YDCPG_OPTS%KFLEVG)
#include "acdrme.intfb.h"
INTEGER (KIND=JPIM)::JLEV
INTEGER (KIND=JPIM)::JLON
REAL (KIND=JPHOOK)::ZHOOK_HANDLE
IF (LHOOK) CALL DR_HOOK ('APL_ARPEGE_ACDRME', 0, ZHOOK_HANDLE)
CALL ACDRME (YDMODEL%YRCST, YDGEOMETRY%YRSTA, YDMODEL%YRML_PHY_MF%YRPHY2, YDMODEL%YRML_PHY_MF&
&%YRTOPH, YDCPG_BNDS%KIDIA, YDCPG_BNDS%KFDIA, YDCPG_OPTS%KLON, YDMODEL%YRML_PHY_MF%YRTOPH&
&%NTDRME, YDCPG_OPTS%KFLEVG, YDMF_PHYS_BASE_STATE%YCPG_DYN%RCP%CP, YDMF_PHYS_BASE_STATE%YCPG_PHY&
&%XYB%DELP, YDMF_PHYS_BASE_STATE%T, PQV, YDMF_PHYS_BASE_STATE%U, YDMF_PHYS_BASE_STATE%V, YDMF_PHYS&
&%OUT%FRMH, PMSC_FRMQ, YDMF_PHYS%OUT%STRMU, YDMF_PHYS%OUT%STRMV)

IF (YDMODEL%YRML_GCONF%YGFL%YIRAD%LGP) THEN

  DO JLEV=1, YDCPG_OPTS%KFLEVG

    DO JLON=YDCPG_BNDS%KIDIA, YDCPG_BNDS%KFDIA
      YDVARS%IRAD%T1 (JLON, JLEV)=YDCPG_MISC%QICE (JLON, JLEV)
    ENDDO

  ENDDO

ENDIF


IF (YDMODEL%YRML_GCONF%YGFL%YLRAD%LGP) THEN

  DO JLEV=1, YDCPG_OPTS%KFLEVG

    DO JLON=YDCPG_BNDS%KIDIA, YDCPG_BNDS%KFDIA
      YDVARS%LRAD%T1 (JLON, JLEV)=YDCPG_MISC%QLI (JLON, JLEV)
    ENDDO

  ENDDO

ENDIF


IF (YDMODEL%YRML_GCONF%YGFL%YA%LGP) THEN

  DO JLEV=1, YDCPG_OPTS%KFLEVG

    DO JLON=YDCPG_BNDS%KIDIA, YDCPG_BNDS%KFDIA
      YDVARS%A%T1 (JLON, JLEV)=YDCPG_MISC%NEB (JLON, JLEV)
    ENDDO

  ENDDO

ENDIF

IF (LHOOK) CALL DR_HOOK ('APL_ARPEGE_ACDRME', 1, ZHOOK_HANDLE)
ENDSUBROUTINE
