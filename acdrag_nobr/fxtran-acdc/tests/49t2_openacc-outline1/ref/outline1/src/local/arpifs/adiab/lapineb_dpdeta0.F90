SUBROUTINE LAPINEB_DPDETA0 (JFLD, YDCPG_BNDS, YDGEOMETRY, YDVARS, PCMSLP, PDP9)
USE CPG_OPTS_TYPE_MOD,ONLY:CPG_OPTS_TYPE, CPG_BNDS_TYPE
USE FIELD_VARIABLES_MOD,ONLY:FIELD_VARIABLES
USE GEOMETRY_MOD,ONLY:GEOMETRY
USE PARKIND1,ONLY:JPIM, JPRB
USE YOMHOOK,ONLY:LHOOK, JPHOOK, DR_HOOK

IMPLICIT NONE

INTEGER (KIND=JPIM), INTENT (INOUT)::JFLD
TYPE (CPG_BNDS_TYPE), INTENT (IN)::YDCPG_BNDS
TYPE (GEOMETRY), INTENT (IN)::YDGEOMETRY
TYPE (FIELD_VARIABLES), INTENT (INOUT)::YDVARS
REAL (KIND=JPRB), INTENT (INOUT)::PCMSLP
REAL (KIND=JPRB), INTENT (INOUT)::PDP9 (YDGEOMETRY%YRDIM%NPROMA, YDGEOMETRY%YRDIMV%NFLEVG)
INTEGER (KIND=JPIM)::JLEV
INTEGER (KIND=JPIM)::JROF
REAL (KIND=JPHOOK)::ZHOOK_HANDLE
IF (LHOOK) CALL DR_HOOK ('LAPINEB_DPDETA0', 0, ZHOOK_HANDLE)

IF (YDGEOMETRY%YRVERT_GEOM%YRCVER%LVERTFE) THEN

  DO JLEV=1, YDGEOMETRY%YRDIMV%NFLEVG

    DO JROF=YDCPG_BNDS%KIDIA, YDCPG_BNDS%KFDIA
      YDVARS%GFL_PTR (JFLD)%T1 (JROF, JLEV)=YDGEOMETRY%YRVERT_GEOM%YRVAB%VDELA (JLEV)+EXP (PDP9 (JROF&
      &, JLEV)-PCMSLP*YDVARS%GEOMETRY%OROG%P (JROF))*YDGEOMETRY%YRVERT_GEOM%YRVAB%VDELB (JLEV)
    ENDDO

  ENDDO

ELSE

  DO JLEV=1, YDGEOMETRY%YRDIMV%NFLEVG

    DO JROF=YDCPG_BNDS%KIDIA, YDCPG_BNDS%KFDIA
      YDVARS%GFL_PTR (JFLD)%T1 (JROF, JLEV)=(YDGEOMETRY%YRVERT_GEOM%YRVAB%VAH (JLEV&
      &)-YDGEOMETRY%YRVERT_GEOM%YRVAB%VAH (JLEV-1))+EXP (PDP9 (JROF, JLEV)-PCMSLP*YDVARS&
      &%GEOMETRY%OROG%P (JROF))*YDGEOMETRY%YRVERT_GEOM%YRVAB%VDELB (JLEV)
    ENDDO

  ENDDO

ENDIF

IF (LHOOK) CALL DR_HOOK ('LAPINEB_DPDETA0', 1, ZHOOK_HANDLE)
ENDSUBROUTINE
