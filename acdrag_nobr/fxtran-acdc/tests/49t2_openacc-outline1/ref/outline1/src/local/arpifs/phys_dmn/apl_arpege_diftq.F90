SUBROUTINE APL_ARPEGE_DIFTQ (YDCPG_BNDS, YDCPG_OPTS, YDMF_PHYS, YDMODEL, PDIFCVPPQ&
&, PDIFCVPPS, PDIFCVPPU, PDIFCVPPV, PEDMFQ, PEDMFS, PEDMFU, PEDMFV)
USE CPG_OPTS_TYPE_MOD,ONLY:CPG_BNDS_TYPE, CPG_OPTS_TYPE
USE PARKIND1,ONLY:JPIM, JPRB
USE MF_PHYS_TYPE_MOD,ONLY:MF_PHYS_TYPE
USE TYPE_MODEL,ONLY:MODEL
USE YOMHOOK,ONLY:LHOOK, JPHOOK, DR_HOOK

IMPLICIT NONE

TYPE (CPG_BNDS_TYPE), INTENT (IN)::YDCPG_BNDS
TYPE (CPG_OPTS_TYPE), INTENT (IN)::YDCPG_OPTS
TYPE (MF_PHYS_TYPE), INTENT (INOUT)::YDMF_PHYS
TYPE (MODEL), INTENT (IN)::YDMODEL
REAL (KIND=JPRB), INTENT (INOUT)::PDIFCVPPQ (YDCPG_OPTS%KLON, 0:YDCPG_OPTS%KFLEVG)
REAL (KIND=JPRB), INTENT (INOUT)::PDIFCVPPS (YDCPG_OPTS%KLON, 0:YDCPG_OPTS%KFLEVG)
REAL (KIND=JPRB), INTENT (INOUT)::PDIFCVPPU (YDCPG_OPTS%KLON, 0:YDCPG_OPTS%KFLEVG)
REAL (KIND=JPRB), INTENT (INOUT)::PDIFCVPPV (YDCPG_OPTS%KLON, 0:YDCPG_OPTS%KFLEVG)
REAL (KIND=JPRB), INTENT (INOUT)::PEDMFQ (YDCPG_OPTS%KLON, 0:YDCPG_OPTS%KFLEVG)
REAL (KIND=JPRB), INTENT (INOUT)::PEDMFS (YDCPG_OPTS%KLON, 0:YDCPG_OPTS%KFLEVG)
REAL (KIND=JPRB), INTENT (INOUT)::PEDMFU (YDCPG_OPTS%KLON, 0:YDCPG_OPTS%KFLEVG)
REAL (KIND=JPRB), INTENT (INOUT)::PEDMFV (YDCPG_OPTS%KLON, 0:YDCPG_OPTS%KFLEVG)
INTEGER (KIND=JPIM)::JLEV
INTEGER (KIND=JPIM)::JLON
REAL (KIND=JPHOOK)::ZHOOK_HANDLE
IF (LHOOK) CALL DR_HOOK ('APL_ARPEGE_DIFTQ', 0, ZHOOK_HANDLE)

IF (YDMODEL%YRML_PHY_MF%YRPHY%LCVPPKF.OR.(YDMODEL%YRML_PHY_MF%YRPHY&
  &%LEDKF.AND..NOT.YDMODEL%YRML_PHY_MF%YRPHY%LEDMFI)) THEN

  IF (YDMODEL%YRML_PHY_MF%YRPHY%LCVPPKF) THEN

    DO JLEV=YDCPG_OPTS%KTDIA, YDCPG_OPTS%KFLEVG

      DO JLON=YDCPG_BNDS%KIDIA, YDCPG_BNDS%KFDIA
        YDMF_PHYS%OUT%DIFTQ (JLON, JLEV)=YDMF_PHYS%OUT%DIFTQ (JLON, JLEV)+PDIFCVPPQ (JLON, JLEV)
        YDMF_PHYS%OUT%DIFTS (JLON, JLEV)=YDMF_PHYS%OUT%DIFTS (JLON, JLEV)+PDIFCVPPS (JLON, JLEV)
        YDMF_PHYS%OUT%STRTU (JLON, JLEV)=YDMF_PHYS%OUT%STRTU (JLON, JLEV)+PDIFCVPPU (JLON, JLEV)
        YDMF_PHYS%OUT%STRTV (JLON, JLEV)=YDMF_PHYS%OUT%STRTV (JLON, JLEV)+PDIFCVPPV (JLON, JLEV)
      ENDDO

    ENDDO

  ELSEIF (YDMODEL%YRML_PHY_MF%YRPHY%LEDKF) THEN

    DO JLEV=YDCPG_OPTS%KTDIA, YDCPG_OPTS%KFLEVG

      DO JLON=YDCPG_BNDS%KIDIA, YDCPG_BNDS%KFDIA
        YDMF_PHYS%OUT%DIFTQ (JLON, JLEV)=YDMF_PHYS%OUT%DIFTQ (JLON, JLEV)+PEDMFQ (JLON, JLEV)
        YDMF_PHYS%OUT%DIFTS (JLON, JLEV)=YDMF_PHYS%OUT%DIFTS (JLON, JLEV)+PEDMFS (JLON, JLEV)
        YDMF_PHYS%OUT%STRTU (JLON, JLEV)=YDMF_PHYS%OUT%STRTU (JLON, JLEV)+PEDMFU (JLON, JLEV)
        YDMF_PHYS%OUT%STRTV (JLON, JLEV)=YDMF_PHYS%OUT%STRTV (JLON, JLEV)+PEDMFV (JLON, JLEV)
      ENDDO

    ENDDO

  ENDIF

ENDIF

IF (LHOOK) CALL DR_HOOK ('APL_ARPEGE_DIFTQ', 1, ZHOOK_HANDLE)
ENDSUBROUTINE
