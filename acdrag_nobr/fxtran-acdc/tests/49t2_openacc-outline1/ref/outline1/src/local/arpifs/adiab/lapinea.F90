#ifdef RS6K
@PROCESS NOCHECK
#endif
SUBROUTINE LAPINEA (YDMODEL, YDGEOMETRY, YDCPG_OPTS, YDCPG_BNDS, YDSL, YDCPG_SL1, YDCPG_SL2, YDA_PWRL9&
&, YDMASK_SL2, YDA_KVSEPC, YDA_KVSEPL, YDA_RSAVEDP, YDA_PCCO, YDA_PUF, YDA_PVF, YDA_KL0, YDA_KLH0,&
& YDA_PLSCAW, YDA_PRSCAW, YDA_KL0H, YDA_PLSCAWH, YDA_PRSCAWH, YDA_PSCO, YDVARS, YDA_KNOWENO)
USE TYPE_MODEL,ONLY:MODEL
USE GEOMETRY_MOD,ONLY:GEOMETRY
USE PARKIND1,ONLY:JPIM, JPRB
USE YOMHOOK,ONLY:LHOOK, DR_HOOK, JPHOOK
USE EINT_MOD,ONLY:SL_STRUCT
USE FIELD_VARIABLES_MOD,ONLY:FIELD_VARIABLES
USE CPG_SL1_TYPE_MOD,ONLY:CPG_SL1F_TYPE
USE CPG_SL2_TYPE_MOD,ONLY:CPG_SL2_TYPE
USE CPG_SL_MASK_TYPE_MOD,ONLY:CPG_SL_MASK_TYPE
USE FIELD_ARRAY_MODULE,ONLY:FIELD_3RB_ARRAY, FIELD_2IM_ARRAY&
&, FIELD_4RB_ARRAY, FIELD_4IM_ARRAY, FIELD_3IM_ARRAY
USE CPG_OPTS_TYPE_MOD,ONLY:CPG_OPTS_TYPE, CPG_BNDS_TYPE

IMPLICIT NONE


TYPE (MODEL), INTENT (IN)::YDMODEL
TYPE (GEOMETRY), INTENT (IN)::YDGEOMETRY
TYPE (CPG_OPTS_TYPE), INTENT (IN)::YDCPG_OPTS
TYPE (CPG_BNDS_TYPE), INTENT (IN)::YDCPG_BNDS
TYPE (SL_STRUCT), INTENT (IN)::YDSL
TYPE (CPG_SL1F_TYPE), INTENT (IN)::YDCPG_SL1
TYPE (CPG_SL2_TYPE), INTENT (IN)::YDCPG_SL2
TYPE (FIELD_3RB_ARRAY), INTENT (IN)::YDA_PWRL9
TYPE (CPG_SL_MASK_TYPE), INTENT (INOUT)::YDMASK_SL2
TYPE (FIELD_2IM_ARRAY), INTENT (INOUT)::YDA_KVSEPC
TYPE (FIELD_2IM_ARRAY), INTENT (INOUT)::YDA_KVSEPL
TYPE (FIELD_4RB_ARRAY), INTENT (INOUT)::YDA_RSAVEDP
TYPE (FIELD_4RB_ARRAY), INTENT (IN)::YDA_PCCO
TYPE (FIELD_3RB_ARRAY), INTENT (IN)::YDA_PUF
TYPE (FIELD_3RB_ARRAY), INTENT (IN)::YDA_PVF
TYPE (FIELD_4IM_ARRAY), INTENT (IN)::YDA_KL0
TYPE (FIELD_4IM_ARRAY), INTENT (IN)::YDA_KLH0
TYPE (FIELD_4RB_ARRAY), INTENT (IN)::YDA_PLSCAW
TYPE (FIELD_4RB_ARRAY), INTENT (IN)::YDA_PRSCAW
TYPE (FIELD_4IM_ARRAY), INTENT (IN)::YDA_KL0H
TYPE (FIELD_4RB_ARRAY), INTENT (IN)::YDA_PLSCAWH
TYPE (FIELD_4RB_ARRAY), INTENT (IN)::YDA_PRSCAWH
TYPE (FIELD_4RB_ARRAY), INTENT (IN)::YDA_PSCO
TYPE (FIELD_VARIABLES), INTENT (INOUT)::YDVARS
TYPE (FIELD_3IM_ARRAY), INTENT (IN)::YDA_KNOWENO
INTEGER (KIND=JPIM)::ILEV (YDGEOMETRY%YRDIM%NPROMA, YDGEOMETRY%YRDIMV%NFLEVG)
INTEGER (KIND=JPIM)::IGLGLO
INTEGER (KIND=JPIM)::IHVI
INTEGER (KIND=JPIM)::IROT
INTEGER (KIND=JPIM)::ITIP
INTEGER (KIND=JPIM)::JGL
INTEGER (KIND=JPIM)::JROF
INTEGER (KIND=JPIM)::JLEV
INTEGER (KIND=JPIM)::JGFL

INTEGER (KIND=JPIM)::I_DPDETA0
INTEGER (KIND=JPIM)::I_UTRAJ
INTEGER (KIND=JPIM)::I_VTRAJ
INTEGER (KIND=JPIM)::I_WTRAJ
INTEGER (KIND=JPIM)::I_LAMBDA0
INTEGER (KIND=JPIM)::I_THETA0
INTEGER (KIND=JPIM)::I_ETA0
LOGICAL::LLINTV









REAL (KIND=JPRB)::ZWF (YDGEOMETRY%YRDIM%NPROMA, YDGEOMETRY%YRDIMV%NFLEVG)
REAL (KIND=JPRB)::ZGMDTX (YDGEOMETRY%YRDIM%NPROMA)
REAL (KIND=JPRB)::ZGMDTY (YDGEOMETRY%YRDIM%NPROMA)
REAL (KIND=JPRB)::ZLEV (YDGEOMETRY%YRDIM%NPROMA, YDGEOMETRY%YRDIMV%NFLEVG)
#include "elarmes.intfb.h"
#include "lapinea_larcina.intfb.h"

#include "larcinha.intfb.h"
#include "larmes.intfb.h"
#include "larmes_rk.intfb.h"
#include "larmes_xyz.intfb.h"
#include "abor1.intfb.h"
REAL (KIND=JPHOOK)::ZHOOK_HANDLE
IF (LHOOK) CALL DR_HOOK ('LAPINEA', 0, ZHOOK_HANDLE)


I_DPDETA0=-1
I_UTRAJ=-1
I_VTRAJ=-1
I_WTRAJ=-1
I_LAMBDA0=-1
I_THETA0=-1
I_ETA0=-1

DO JGFL=1, SIZE (YDVARS%SLDIA)

  IF (TRIM (YDVARS%SLDIA (JGFL)%CNAME)=='DPDETA0') THEN
    I_DPDETA0=JGFL
  ELSEIF (TRIM (YDVARS%SLDIA (JGFL)%CNAME)=='UTRAJ') THEN
    I_UTRAJ=JGFL
  ELSEIF (TRIM (YDVARS%SLDIA (JGFL)%CNAME)=='VTRAJ') THEN
    I_VTRAJ=JGFL
  ELSEIF (TRIM (YDVARS%SLDIA (JGFL)%CNAME)=='WTRAJ') THEN
    I_WTRAJ=JGFL
  ELSEIF (TRIM (YDVARS%SLDIA (JGFL)%CNAME)=='LAMBDA0') THEN
    I_LAMBDA0=JGFL
  ELSEIF (TRIM (YDVARS%SLDIA (JGFL)%CNAME)=='THETA0') THEN
    I_THETA0=JGFL
  ELSEIF (TRIM (YDVARS%SLDIA (JGFL)%CNAME)=='ETA0') THEN
    I_ETA0=JGFL
  ENDIF

ENDDO

IHVI=0

DO JGFL=1, YDMODEL%YRML_GCONF%YGFL%NUMFLDS

  IF (YDMODEL%YRML_GCONF%YGFL%YCOMP(JGFL)%CSLINT=='LAIHVT      '.OR.YDMODEL%YRML_GCONF%YGFL%YCOMP(JGFL&
    &)%CSLINT=='LAIHVTQM    '.OR.YDMODEL%YRML_GCONF%YGFL%YCOMP(JGFL)%CSLINT=='LAIHVTQMH   ') THEN
    IHVI=1
  ENDIF

ENDDO


IF (YDCPG_OPTS%LRPLANE) THEN
  

  DO JROF=YDCPG_BNDS%KIDIA, YDCPG_BNDS%KFDIA
    ZGMDTX (JROF)=0.5_JPRB*YDVARS%GEOMETRY%GM%P (JROF)*YDMODEL&
    &%YRML_GCONF%YRRIP%RTDT/YDGEOMETRY%YREGEO%EDELX
    ZGMDTY (JROF)=0.5_JPRB*YDVARS%GEOMETRY%GM%P (JROF)*YDMODEL&
    &%YRML_GCONF%YRRIP%RTDT/YDGEOMETRY%YREGEO%EDELY
  ENDDO

  
ENDIF


IF (YDCPG_OPTS%LRPLANE) THEN
  

  IF (YDMODEL%YRML_DYN%YRDYN%LSLDP_RK) THEN
    CALL ABOR1 (" LAPINEA : RK4 scheme is not yet available for LAM. ")
  ELSEIF (YDMODEL%YRML_DYN%YRDYN%LSLDP_XYZ) THEN
    CALL ABOR1 (" LAPINEA : DP scheme in XYZ coordinates is not yet available for LAM. ")
  ELSE
    CALL ELARMES (YDGEOMETRY, YDVARS%GEOMETRY, YDMODEL%YRCST, YDMODEL%YRML_DYN, YDMODEL&
    &%YRML_GCONF%YRRIP, YDCPG_OPTS, YDCPG_BNDS, YDSL, ZGMDTX, ZGMDTY, YDCPG_SL1, YDCPG_SL2&
    &, YDMASK_SL2, YDA_KVSEPC, YDA_KVSEPL, YDA_PSCO%P, ZLEV, YDA_PCCO%P, YDA_PUF%P, YDA_PVF&
    &%P, YDA_KL0%P, YDA_KLH0%P, ILEV, YDA_PLSCAW%P, YDA_PRSCAW%P)
  ENDIF

  
ELSE

  IF (YDMODEL%YRML_DYN%YRDYN%LSLDP_RK) THEN
    
    CALL LARMES_RK (YDGEOMETRY, YDVARS%GEOMETRY, YDMODEL%YRCST, YDMODEL%YRML_GCONF&
    &%YRRIP, YDMODEL%YRML_DYN, YDCPG_OPTS, YDCPG_BNDS, YDSL, YDCPG_SL1, YDCPG_SL2,&
    & YDMASK_SL2, YDA_KVSEPC, YDA_KVSEPL, YDA_PSCO%P, ZLEV, YDA_PCCO%P, YDA_PUF%P,&
    & YDA_PVF%P, ZWF, YDA_KL0%P, YDA_KLH0%P, ILEV, YDA_PLSCAW%P, YDA_PRSCAW%P)
    
  ELSEIF (YDMODEL%YRML_DYN%YRDYN%LSLDP_XYZ) THEN
    
    CALL LARMES_XYZ (YDGEOMETRY, YDVARS%GEOMETRY, YDMODEL%YRCST, YDMODEL%YRML_GCONF&
    &%YRRIP, YDMODEL%YRML_DYN, YDCPG_OPTS, YDCPG_BNDS, YDSL, YDCPG_SL1, YDCPG_SL2,&
    & YDA_PWRL9%P, YDMASK_SL2, YDA_KVSEPC, YDA_KVSEPL, YDA_RSAVEDP%P, YDA_PSCO%P, ZLEV&
    &, YDA_PCCO%P, YDA_PUF%P, YDA_PVF%P, ZWF, YDA_KL0%P, YDA_KLH0%P, ILEV)
    
  ELSE
    CALL LARMES (YDGEOMETRY, YDVARS%GEOMETRY, YDMODEL%YRCST, YDMODEL%YRML_GCONF%YRRIP&
    &, YDMODEL%YRML_DYN, YDCPG_OPTS, YDCPG_BNDS, YDSL, YDCPG_SL1, YDCPG_SL2, YDMASK_SL2&
    &, YDA_KVSEPC, YDA_KVSEPL, YDA_PSCO%P, ZLEV, YDA_PCCO%P, YDA_PUF%P, YDA_PVF%P&
    &, ZWF, YDA_KL0%P, YDA_KLH0%P, ILEV, YDA_PLSCAW%P, YDA_PRSCAW%P)
  ENDIF


  IF (YDMODEL%YRML_DYN%YRDYNA%LSLDIA.AND.(YDMODEL%YRML_DYN%YRDYNA%LPC_CHEAP.OR.(YDMODEL&
    &%YRML_DYN%YRDYN%NCURRENT_ITER==YDMODEL%YRML_DYN%YRDYN%NSITER))) THEN
    

    IF (I_UTRAJ>0) THEN
      YDVARS%SLDIA (I_UTRAJ)%P (YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA, 1:YDGEOMETRY%YRDIMV%NFLEVG&
      &)=YDA_PUF%P (YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA, 1:YDGEOMETRY%YRDIMV%NFLEVG)
    ENDIF


    IF (I_VTRAJ>0) THEN
      YDVARS%SLDIA (I_VTRAJ)%P (YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA, 1:YDGEOMETRY%YRDIMV%NFLEVG&
      &)=YDA_PVF%P (YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA, 1:YDGEOMETRY%YRDIMV%NFLEVG)
    ENDIF


    IF (I_WTRAJ>0) THEN
      YDVARS%SLDIA (I_WTRAJ)%P (YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA, 1:YDGEOMETRY%YRDIMV&
      &%NFLEVG)=ZWF (YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA, 1:YDGEOMETRY%YRDIMV%NFLEVG)
    ENDIF


    IF (I_LAMBDA0>0) THEN

      DO JLEV=1, YDGEOMETRY%YRDIMV%NFLEVG

        DO JROF=YDCPG_BNDS%KIDIA, YDCPG_BNDS%KFDIA
          YDVARS%SLDIA (I_LAMBDA0)%P (JROF, JLEV)=SIN (YDA_PCCO&
          &%P (JROF, JLEV, YDMODEL%YRML_DYN%YYTCCO%M_RLON))
        ENDDO

      ENDDO

    ENDIF


    IF (I_THETA0>0) THEN

      DO JLEV=1, YDGEOMETRY%YRDIMV%NFLEVG

        DO JROF=YDCPG_BNDS%KIDIA, YDCPG_BNDS%KFDIA
          YDVARS%SLDIA (I_THETA0)%P (JROF, JLEV)=SIN (YDA_PCCO%P (JROF, JLEV, YDMODEL%YRML_DYN%YYTCCO%M_RLAT))
        ENDDO

      ENDDO

    ENDIF


    IF (I_ETA0>0) THEN
      YDVARS%SLDIA (I_ETA0)%P (YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA, 1:YDGEOMETRY%YRDIMV%NFLEVG&
      &)=ZLEV (YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA, 1:YDGEOMETRY%YRDIMV%NFLEVG)
    ENDIF

    
  ENDIF

ENDIF


IF (YDMODEL%YRML_DYN%YRDYN%LRHS_CURV) THEN
  IROT=0
ELSE
  IROT=1
ENDIF

ITIP=2
LLINTV=.FALSE.
CALL LAPINEA_LARCINA (LLINTV, IHVI, IROT, ITIP, YDA_KL0, YDA_KLH0, YDA_KNOWENO, YDA_KVSEPC&
&, YDA_KVSEPL, YDA_PCCO, YDA_PLSCAW, YDA_PRSCAW, YDA_PSCO, YDCPG_BNDS, YDCPG_OPTS,&
& YDCPG_SL2, YDGEOMETRY, YDMASK_SL2, YDMODEL, YDSL, YDVARS, ILEV, ZLEV)

IF (YDCPG_OPTS%LRPLANE) THEN
  

  IF (YDMODEL%YRML_DYN%YRDYN%LADVF) THEN
    YDA_PSCO%P (YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA, 1:YDGEOMETRY%YRDIMV%NFLEVG&
    &, YDMODEL%YRML_DYN%YYTSCO%M_SINLA)=YDA_PCCO%P (YDCPG_BNDS%KIDIA:YDCPG_BNDS&
    &%KFDIA, 1:YDGEOMETRY%YRDIMV%NFLEVG, YDMODEL%YRML_DYN%YYTCCO%M_RLON)
    YDA_PSCO%P (YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA, 1:YDGEOMETRY%YRDIMV%NFLEVG&
    &, YDMODEL%YRML_DYN%YYTSCO%M_COPHI)=YDA_PCCO%P (YDCPG_BNDS%KIDIA:YDCPG_BNDS&
    &%KFDIA, 1:YDGEOMETRY%YRDIMV%NFLEVG, YDMODEL%YRML_DYN%YYTCCO%M_RLAT)
  ENDIF

  
ENDIF


IF (YDMODEL%YRML_DYN%YRDYNA%LNHDYN.AND.YDMODEL%YRML_DYN%YRDYNA%LGWADV&
  &.AND.(.NOT.YDGEOMETRY%YRVERT_GEOM%YRCVER%LVFE_GW)) THEN
  
  IROT=0
  ITIP=2
  CALL LARCINHA (YDGEOMETRY, YDMODEL%YRCST, YDMODEL%YRML_DYN, YDCPG_BNDS%KIDIA, YDCPG_BNDS&
  &%KFDIA, YDSL, IHVI, ITIP, IROT, YDCPG_OPTS%LRPLANE, YDA_PSCO%P, YDA_PCCO%P, ZLEV, YDCPG_SL2&
  &%KAPPA, YDCPG_SL2%KAPPAT, YDCPG_SL2%KAPPAM, YDCPG_SL2%KAPPAH, YDCPG_SL2%STDDISU, YDCPG_SL2&
  &%STDDISV, YDCPG_SL2%STDDISW, YDMASK_SL2%P, YDA_KL0H%P, YDA_PLSCAWH%P, YDA_PRSCAWH%P, YDVARS&
  &%GEOMETRY%RCOLON%P, YDVARS%GEOMETRY%RSILON%P, YDVARS%GEOMETRY%GECLO%P, YDVARS%GEOMETRY&
  &%GEMU%P, YDVARS%GEOMETRY%GESLO%P, YDVARS%GEOMETRY%GSQM2%P)
  
ENDIF



IF (LHOOK) CALL DR_HOOK ('LAPINEA', 1, ZHOOK_HANDLE)
ENDSUBROUTINE LAPINEA
