SUBROUTINE APL_ARPEGE_RADIATION (YDMF_PHYS_BASE_STATE, YDCFU, YDGEOMETRY, YDCPG_BNDS, YDCPG_OPTS, YDCPG_MISC&
&, YDCPG_GPAR, YDMF_PHYS, YDMF_PHYS_SURF, YDVARS, YDMODEL, PCLCT_RAD, PALBDIR, PDECRD, PAER, PAERINDS&
&, PALBD, PALBP, PQCO2, PCEMTR, PCTRSO, PFLU_EMIS, PFLU_QSAT, PQO3, PQR, PQS, PQV, PRDG_MU0, PRDG_SOLO&
&, PRDG_MU0LU, PRDG_MU0M, PSFSWDIF, PSFSWDIR, PSUDU, PTENT, PTRSOD, PTRSODIF, PTRSODIR)
USE YOMHOOK,ONLY:LHOOK, DR_HOOK, JPHOOK
USE PARKIND1,ONLY:JPIM, JPRB
USE MF_PHYS_BASE_STATE_TYPE_MOD,ONLY:MF_PHYS_BASE_STATE_TYPE
USE GEOMETRY_MOD,ONLY:GEOMETRY
USE CPG_OPTS_TYPE_MOD,ONLY:CPG_BNDS_TYPE, CPG_OPTS_TYPE
USE CPG_TYPE_MOD,ONLY:CPG_MISC_TYPE, CPG_GPAR_TYPE
USE MF_PHYS_TYPE_MOD,ONLY:MF_PHYS_TYPE
USE MF_PHYS_SURFACE_TYPE_MOD,ONLY:MF_PHYS_SURF_TYPE
USE FIELD_VARIABLES_MOD,ONLY:FIELD_VARIABLES
USE TYPE_MODEL,ONLY:MODEL
USE YOMCFU,ONLY:TCFU

IMPLICIT NONE


TYPE (MF_PHYS_BASE_STATE_TYPE), INTENT (IN)::YDMF_PHYS_BASE_STATE
TYPE (TCFU), INTENT (IN)::YDCFU
TYPE (GEOMETRY), INTENT (IN)::YDGEOMETRY
TYPE (CPG_BNDS_TYPE), INTENT (IN)::YDCPG_BNDS
TYPE (CPG_OPTS_TYPE), INTENT (IN)::YDCPG_OPTS
TYPE (CPG_MISC_TYPE), INTENT (INOUT)::YDCPG_MISC
TYPE (CPG_GPAR_TYPE), INTENT (INOUT)::YDCPG_GPAR
TYPE (MF_PHYS_TYPE), INTENT (INOUT)::YDMF_PHYS
TYPE (MF_PHYS_SURF_TYPE), INTENT (INOUT)::YDMF_PHYS_SURF
TYPE (FIELD_VARIABLES), INTENT (INOUT)::YDVARS
TYPE (MODEL), INTENT (IN)::YDMODEL
REAL (KIND=JPRB), INTENT (IN)::PCLCT_RAD (YDCPG_OPTS%KLON)
REAL (KIND=JPRB), INTENT (IN)::PALBDIR (YDCPG_OPTS%KLON)
REAL (KIND=JPRB), INTENT (IN)::PDECRD (YDCPG_OPTS%KLON)
REAL (KIND=JPRB), INTENT (IN)::PAER (YDCPG_OPTS%KLON, YDCPG_OPTS%KFLEVG, 6)
REAL (KIND=JPRB), INTENT (IN)::PAERINDS (YDCPG_OPTS%KLON, YDCPG_OPTS%KFLEVG)
REAL (KIND=JPRB), INTENT (IN)::PALBD (YDCPG_OPTS%KLON, YDCPG_OPTS%KSW)
REAL (KIND=JPRB), INTENT (IN)::PALBP (YDCPG_OPTS%KLON, YDCPG_OPTS%KSW)
REAL (KIND=JPRB), INTENT (IN)::PQCO2 (YDCPG_OPTS%KLON, YDCPG_OPTS%KFLEVG)
REAL (KIND=JPRB), INTENT (OUT)::PCEMTR (YDCPG_OPTS%KLON, 0:1)
REAL (KIND=JPRB), INTENT (OUT)::PCTRSO (YDCPG_OPTS%KLON, 0:1)
REAL (KIND=JPRB), INTENT (IN)::PFLU_EMIS (YDCPG_OPTS%KLON)
REAL (KIND=JPRB), INTENT (IN)::PFLU_QSAT (YDCPG_OPTS%KLON, 1:YDCPG_OPTS%KFLEVG)
REAL (KIND=JPRB), INTENT (IN)::PQO3 (YDCPG_OPTS%KLON, 0:YDCPG_OPTS%KFLEVG)
REAL (KIND=JPRB), INTENT (IN)::PQR (YDCPG_OPTS%KLON, YDCPG_OPTS%KFLEVG)
REAL (KIND=JPRB), INTENT (IN)::PQS (YDCPG_OPTS%KLON, YDCPG_OPTS%KFLEVG)
REAL (KIND=JPRB), INTENT (IN)::PQV (YDCPG_OPTS%KLON, YDCPG_OPTS%KFLEVG)
REAL (KIND=JPRB), INTENT (IN)::PRDG_MU0 (YDCPG_OPTS%KLON)
REAL (KIND=JPRB), INTENT (IN)::PRDG_SOLO (YDCPG_OPTS%KLON)
REAL (KIND=JPRB), INTENT (IN)::PRDG_MU0LU (YDCPG_OPTS%KLON)
REAL (KIND=JPRB), INTENT (IN)::PRDG_MU0M (YDCPG_OPTS%KLON)
REAL (KIND=JPRB), INTENT (OUT)::PSFSWDIF (YDCPG_OPTS%KLON, YDCPG_OPTS%KSW)
REAL (KIND=JPRB), INTENT (OUT)::PSFSWDIR (YDCPG_OPTS%KLON, YDCPG_OPTS%KSW)
REAL (KIND=JPRB), INTENT (IN)::PSUDU (YDCPG_OPTS%KLON)
REAL (KIND=JPRB), INTENT (INOUT)::PTENT (YDCPG_OPTS%KLON, YDCPG_OPTS%KFLEVG)
REAL (KIND=JPRB), INTENT (OUT)::PTRSOD (YDCPG_OPTS%KLON)
REAL (KIND=JPRB), INTENT (OUT)::PTRSODIF (YDCPG_OPTS%KLON, YDCPG_OPTS%KSW)
REAL (KIND=JPRB), INTENT (OUT)::PTRSODIR (YDCPG_OPTS%KLON, YDCPG_OPTS%KSW)

#include "apl_arpege_radiation_radheat.intfb.h"
#include "apl_arpege_radiation_direct.intfb.h"
#include "apl_arpege_radiation_sunshine.intfb.h"
#include "apl_arpege_radiation_lmse_f.intfb.h"
#include "apl_arpege_radiation_acrso.intfb.h"
#include "apl_arpege_radiation_acdayd.intfb.h"
#include "apl_arpege_radiation_callrad_f.intfb.h"
#include "apl_arpege_radiation_acraneb2.intfb.h"


#include "recmwf.intfb.h"

#include "abor1.intfb.h"
REAL (KIND=JPRB)::ZCGA_DST (YDCPG_OPTS%KLON, 0, YDMODEL%YRML_PHY_RAD%YRERAD%NSW)
REAL (KIND=JPRB)::ZPIZA_DST (YDCPG_OPTS%KLON, 0, YDMODEL%YRML_PHY_RAD%YRERAD%NSW)
REAL (KIND=JPRB)::ZTAUREL_DST (YDCPG_OPTS%KLON, 0, YDMODEL%YRML_PHY_RAD%YRERAD%NSW)
REAL (KIND=JPRB)::ZFRSODS (YDCPG_OPTS%KLON)
REAL (KIND=JPRB)::ZFSDNN (YDCPG_OPTS%KLON)
REAL (KIND=JPRB)::ZFSDNV (YDCPG_OPTS%KLON)



INTEGER (KIND=JPIM), PARAMETER::IAERO=12
REAL (KIND=JPRB)::ZAERO (YDCPG_OPTS%KLON, YDCPG_OPTS%KFLEVG, IAERO)
REAL (KIND=JPRB)::ZDUM (YDCPG_OPTS%KLON, YDCPG_OPTS%KFLEVG)
REAL (KIND=JPRB)::ZAEROCPL (YDCPG_OPTS%KLON, YDCPG_OPTS%KFLEVG, YDMODEL%YRML_PHY_RAD&
&%YRERAD%NTLW, MERGE (6, 0, YDMODEL%YRML_PHY_RAD%YREAERATM%LAERRADCPL))
LOGICAL::LLCALLRAD


REAL (KIND=JPHOOK)::ZHOOK_HANDLE
IF (LHOOK) CALL DR_HOOK ('APL_ARPEGE_RADIATION', 0, ZHOOK_HANDLE)



IF (YDMODEL%YRML_PHY_MF%YRPHY%LRAY.AND.YDMODEL%YRML_PHY_MF%YRPHY%NRAY==2) THEN
  CALL APL_ARPEGE_RADIATION_ACRANEB2 (YDCFU, YDCPG_BNDS, YDCPG_MISC, YDCPG_OPTS, YDMF_PHYS&
  &, YDMF_PHYS_BASE_STATE, YDMF_PHYS_SURF, YDMODEL, YDVARS, PAER, PALBDIR, PCLCT_RAD&
  &, PDECRD, PFLU_EMIS, PQCO2, PQO3, PQV, PRDG_MU0, PRDG_MU0LU, ZFRSODS)
ELSEIF (YDMODEL%YRML_PHY_MF%YRPHY%LRAYFM) THEN
  LLCALLRAD=(MOD (YDCPG_OPTS%NSTEP, YDMODEL%YRML_PHY_RAD%YRERAD%NRADFR)==0)

  IF (YDMODEL%YRML_PHY_MF%YRPHY%NCALLRAD==2)  THEN
    LLCALLRAD=(LLCALLRAD.AND.(YDCPG_OPTS%NSTEP<=YDMODEL%YRML_GCONF%YRRIP%NSTOP-1))
  ENDIF


  IF (LLCALLRAD) THEN
    CALL RECMWF (YDGEOMETRY%YRDIMV, YDMODEL, YDCPG_OPTS, YDCPG_BNDS, YDMODEL%YRML_PHY_RAD%YRERAD%NOZOCL&
    &, YDMODEL%YRML_PHY_RAD%YRERAD%NAERMACC, IAERO, PALBD, PALBP, YDMF_PHYS_BASE_STATE%YCPG_PHY%PREHYD&
    &, YDMF_PHYS_BASE_STATE%YCPG_PHY%PREHYDF, YDCPG_MISC%NEB, PQO3, ZDUM, ZDUM, ZDUM, ZDUM, ZDUM, ZDUM&
    &, ZDUM, ZDUM, PAER, ZAERO, ZAEROCPL, YDMF_PHYS_BASE_STATE%YCPG_PHY%XYB%DELP, PFLU_EMIS, PRDG_MU0M&
    &, PQV, PFLU_QSAT, YDCPG_MISC%QICE, YDCPG_MISC%QLI, PQS, PQR, YDMF_PHYS_SURF%GSD_VF%PLSM, YDMF_PHYS_BASE_STATE&
    &%T, YDMF_PHYS_BASE_STATE%YGSP_RR%T, YDVARS%RADIATION%EMTD%T0, YDVARS%RADIATION%EMTU%T0, YDVARS%RADIATION&
    &%TRSW%T0, YDMF_PHYS%OUT%FRTHC, YDMF_PHYS%OUT%FRTH, YDMF_PHYS%OUT%FRSOC, YDMF_PHYS%OUT%FRSO, PSFSWDIR&
    &, PSFSWDIF, ZFSDNN, ZFSDNV, PCTRSO, PCEMTR, PTRSOD, PTRSODIR, PTRSODIF, ZPIZA_DST, ZCGA_DST, ZTAUREL_DST&
    &, PAERINDS, YDVARS%GEOMETRY%GELAM%T0, YDVARS%GEOMETRY%GEMU%T0, YDCPG_GPAR%SWDIR, YDCPG_GPAR%SWDIF&
    &, PRDG_MU0LU, YDMF_PHYS%OUT%ALB, YDVARS%RADIATION%RMOON%T0)
  ELSE
    CALL APL_ARPEGE_RADIATION_CALLRAD_F (YDCPG_BNDS, YDCPG_GPAR&
    &, YDCPG_OPTS, YDMODEL, PTRSODIF, PTRSODIR)
  ENDIF

  CALL APL_ARPEGE_RADIATION_RADHEAT (YDCPG_BNDS, YDCPG_OPTS, YDMF_PHYS, YDMF_PHYS_BASE_STATE&
  &, YDMODEL, YDVARS, PALBD, PALBP, PCEMTR, PCTRSO, PFLU_EMIS, PQV, PRDG_MU0, PRDG_SOLO&
  &, PSFSWDIF, PSFSWDIR, PSUDU, PTENT, PTRSOD, PTRSODIF, PTRSODIR, ZFRSODS)

  IF (YDMODEL%YRML_PHY_MF%YRPHY%LDAYD) THEN
    CALL APL_ARPEGE_RADIATION_ACDAYD (YDCPG_BNDS, YDCPG_OPTS, YDMF_PHYS&
    &, YDMF_PHYS_BASE_STATE, YDMODEL, YDVARS, PRDG_MU0)
  ENDIF


  IF (YDMODEL%YRML_PHY_MF%YRPHY0%GRSO<1._JPRB) THEN
    CALL APL_ARPEGE_RADIATION_ACRSO (YDCPG_BNDS, YDCPG_OPTS, YDMF_PHYS&
    &, YDMF_PHYS_BASE_STATE, YDMODEL, YDVARS, PRDG_MU0)
  ENDIF


  IF (.NOT.YDMODEL%YRML_PHY_MF%YRARPHY%LMSE) THEN
    CALL APL_ARPEGE_RADIATION_LMSE_F (YDCPG_BNDS, YDCPG_OPTS, YDMF_PHYS, YDMODEL, PALBD, PALBP)
  ENDIF


  IF (.NOT.YDCPG_OPTS%LCONFX) THEN
    CALL APL_ARPEGE_RADIATION_SUNSHINE (YDCPG_BNDS, YDMF_PHYS, YDMF_PHYS_SURF, YDMODEL)
  ENDIF

ELSE
  CALL ABOR1 ('APL_ARPEGE_RADIATION: WRONG SETTINGS')
ENDIF

CALL APL_ARPEGE_RADIATION_DIRECT (YDCPG_BNDS, YDCPG_OPTS, YDMF_PHYS, PRDG_MU0)

IF (LHOOK) CALL DR_HOOK ('APL_ARPEGE_RADIATION', 1, ZHOOK_HANDLE)
ENDSUBROUTINE APL_ARPEGE_RADIATION
