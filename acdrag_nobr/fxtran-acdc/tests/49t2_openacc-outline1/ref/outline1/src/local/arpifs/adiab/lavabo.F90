SUBROUTINE LAVABO (YDGEOMETRY, YDMODEL, YDCPG_BNDS, YDCPG_OPTS, YDCPG_SL1, LD2TLFF1)
USE PARKIND1,ONLY:JPRB, JPIM
USE GEOMETRY_MOD,ONLY:GEOMETRY
USE TYPE_MODEL,ONLY:MODEL
USE PARKIND1,ONLY:JPIM
USE YOMHOOK,ONLY:DR_HOOK, JPHOOK, LHOOK
USE CPG_SL1_TYPE_MOD,ONLY:CPG_SL1B_TYPE
USE CPG_OPTS_TYPE_MOD,ONLY:CPG_OPTS_TYPE, CPG_BNDS_TYPE

IMPLICIT NONE


TYPE (GEOMETRY), INTENT (IN)::YDGEOMETRY
TYPE (MODEL), INTENT (IN)::YDMODEL
TYPE (CPG_BNDS_TYPE), INTENT (IN)::YDCPG_BNDS
TYPE (CPG_OPTS_TYPE), INTENT (IN)::YDCPG_OPTS
TYPE (CPG_SL1B_TYPE), INTENT (INOUT)::YDCPG_SL1
LOGICAL, INTENT (IN)::LD2TLFF1
#include "lavabo_expl_laitvspcqm_part1.intfb.h"
#include "lavabo_outline_002.intfb.h"
#include "lavabo_outline_001.intfb.h"
#include "lavabo_outline_000.intfb.h"
#include "lavabo_expl_laitvspcqm_part2.intfb.h"
#include "lavabo_expl_lphy.intfb.h"
INTEGER (KIND=JPIM)::JLEV2
INTEGER (KIND=JPIM)::JLEV1
REAL (KIND=JPHOOK)::ZHOOK_HANDLE
IF (LHOOK) CALL DR_HOOK ('LAVABO', 0, ZHOOK_HANDLE)



JLEV1=0
JLEV2=YDGEOMETRY%YRDIMV%NFLEVG+1
CALL LAVABO_OUTLINE_000 (LD2TLFF1, JLEV1, JLEV2, YDCPG_BNDS, YDCPG_SL1, YDMODEL)
CALL LAVABO_EXPL_LAITVSPCQM_PART1 (YDGEOMETRY, YDCPG_BNDS, YDCPG_OPTS, YDCPG_SL1)
CALL LAVABO_OUTLINE_001 (JLEV1, JLEV2, YDCPG_BNDS, YDCPG_SL1, YDMODEL)

IF (YDMODEL%YRML_DYN%YRDYN%NSPLTHOI/=0.OR.YDMODEL%YRML_DYN%YRDYN%LSPLTHOIGFL) THEN
  CALL LAVABO_EXPL_LAITVSPCQM_PART2 (YDGEOMETRY, YDCPG_BNDS, YDCPG_OPTS, YDCPG_SL1)
ENDIF

CALL LAVABO_OUTLINE_002 (JLEV1, JLEV2, YDCPG_BNDS, YDCPG_SL1, YDMODEL)

IF (YDMODEL%YRML_PHY_EC%YREPHY%LSLPHY) THEN
  
  YDCPG_SL1%UP9%P (YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA, JLEV1)=0.0_JPRB
  YDCPG_SL1%UP9%P (YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA, JLEV2)=0.0_JPRB
  YDCPG_SL1%VP9%P (YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA, JLEV1)=0.0_JPRB
  YDCPG_SL1%VP9%P (YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA, JLEV2)=0.0_JPRB

  IF (YDMODEL%YRML_DYN%YRDYN%LRHS_CURV) THEN
    YDCPG_SL1%Z9%P (YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA, JLEV1)=0.0_JPRB
    YDCPG_SL1%Z9%P (YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA, JLEV2)=0.0_JPRB
  ENDIF

  YDCPG_SL1%TP9%P (YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA, JLEV1)=0.0_JPRB
  YDCPG_SL1%TP9%P (YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA, JLEV2)=0.0_JPRB
  CALL LAVABO_EXPL_LPHY (YDGEOMETRY, YDCPG_BNDS%KIDIA, YDCPG_BNDS%KFDIA, YDCPG_SL1)
  
ENDIF


IF (YDMODEL%YRML_DYN%YRDYNA%LSLINL) THEN
  
  YDCPG_SL1%U9_SI%P (YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA, JLEV1)=0.0_JPRB
  YDCPG_SL1%U9_SI%P (YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA, JLEV2)=0.0_JPRB
  YDCPG_SL1%V9_SI%P (YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA, JLEV1)=0.0_JPRB
  YDCPG_SL1%V9_SI%P (YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA, JLEV2)=0.0_JPRB
  YDCPG_SL1%T9_SI%P (YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA, JLEV1)=0.0_JPRB
  YDCPG_SL1%T9_SI%P (YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA, JLEV2)=0.0_JPRB
  YDCPG_SL1%VD9_SI%P (YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA, JLEV1)=0.0_JPRB
  YDCPG_SL1%VD9_SI%P (YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA, JLEV2)=0.0_JPRB

  IF (YDMODEL%YRML_DYN%YRDYNA%LNHDYN) THEN
    YDCPG_SL1%PD9_SI%P (YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA, JLEV1)=0.0_JPRB
    YDCPG_SL1%PD9_SI%P (YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA, JLEV2)=0.0_JPRB
  ENDIF

  
ENDIF




IF (LHOOK) CALL DR_HOOK ('LAVABO', 1, ZHOOK_HANDLE)
ENDSUBROUTINE LAVABO
