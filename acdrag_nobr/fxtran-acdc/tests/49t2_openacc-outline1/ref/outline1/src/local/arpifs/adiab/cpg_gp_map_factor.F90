SUBROUTINE CPG_GP_MAP_FACTOR (YDCPG_BNDS, YDCPG_DYN0, YDGEOMETRY&
&, YDMODEL, YDVARS, P_OROGLL_T0, P_OROGLM_T0, P_OROGMM_T0)
USE CPG_OPTS_TYPE_MOD,ONLY:CPG_BNDS_TYPE, CPG_OPTS_TYPE
USE CPG_TYPE_MOD,ONLY:CPG_DYN_TYPE, CPG_GPAR_TYPE, CPG_MISC_TYPE, CPG_TND_TYPE
USE FIELD_VARIABLES_MOD,ONLY:FIELD_VARIABLES
USE GEOMETRY_MOD,ONLY:GEOMETRY
USE PARKIND1,ONLY:JPIM, JPRB
USE TYPE_MODEL,ONLY:MODEL
USE YOMHOOK,ONLY:LHOOK, JPHOOK, DR_HOOK

IMPLICIT NONE

TYPE (CPG_BNDS_TYPE), INTENT (IN)::YDCPG_BNDS
TYPE (CPG_DYN_TYPE), INTENT (INOUT)::YDCPG_DYN0
TYPE (GEOMETRY), INTENT (IN)::YDGEOMETRY
TYPE (MODEL), INTENT (IN)::YDMODEL
TYPE (FIELD_VARIABLES), INTENT (INOUT)::YDVARS
REAL (KIND=JPRB), INTENT (INOUT)::P_OROGLL_T0 (YDGEOMETRY%YRDIM%NPROMA)
REAL (KIND=JPRB), INTENT (INOUT)::P_OROGLM_T0 (YDGEOMETRY%YRDIM%NPROMA)
REAL (KIND=JPRB), INTENT (INOUT)::P_OROGMM_T0 (YDGEOMETRY%YRDIM%NPROMA)
INTEGER (KIND=JPIM)::JROF
REAL (KIND=JPRB)::ZGM2
REAL (KIND=JPHOOK)::ZHOOK_HANDLE
IF (LHOOK) CALL DR_HOOK ('CPG_GP_MAP_FACTOR', 0, ZHOOK_HANDLE)
YDCPG_DYN0%OROGL (YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA)=YDVARS%GEOMETRY%OROGL%T0 (YDCPG_BNDS&
&%KIDIA:YDCPG_BNDS%KFDIA)*YDVARS%GEOMETRY%GM%T0 (YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA)
YDCPG_DYN0%OROGM (YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA)=YDVARS%GEOMETRY%OROGM%T0 (YDCPG_BNDS&
&%KIDIA:YDCPG_BNDS%KFDIA)*YDVARS%GEOMETRY%GM%T0 (YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA)

IF (YDMODEL%YRML_DYN%YRDYNA%LNHDYN) THEN

  DO JROF=YDCPG_BNDS%KIDIA, YDCPG_BNDS%KFDIA
    ZGM2=YDVARS%GEOMETRY%GM%T0 (JROF)**2
    P_OROGLL_T0 (JROF)=YDVARS%GEOMETRY%OROGLL%T0 (JROF)*ZGM2
    P_OROGMM_T0 (JROF)=YDVARS%GEOMETRY%OROGMM%T0 (JROF)*ZGM2
    P_OROGLM_T0 (JROF)=YDVARS%GEOMETRY%OROGLM%T0 (JROF)*ZGM2
  ENDDO

ENDIF

IF (LHOOK) CALL DR_HOOK ('CPG_GP_MAP_FACTOR', 1, ZHOOK_HANDLE)
ENDSUBROUTINE
