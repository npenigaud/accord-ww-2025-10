SUBROUTINE APL_ARPEGE_AEROSOLS_FOR_RADIATION (YDMF_PHYS_BASE_STATE, YDCPG_BNDS&
&, YDCPG_OPTS, YDMF_PHYS_SURF, YDMODEL, PAER, PAERINDS)
USE YOMHOOK,ONLY:LHOOK, DR_HOOK, JPHOOK
USE PARKIND1,ONLY:JPIM, JPRB
USE MF_PHYS_BASE_STATE_TYPE_MOD,ONLY:MF_PHYS_BASE_STATE_TYPE
USE CPG_OPTS_TYPE_MOD,ONLY:CPG_BNDS_TYPE, CPG_OPTS_TYPE
USE MF_PHYS_SURFACE_TYPE_MOD,ONLY:MF_PHYS_SURF_TYPE
USE TYPE_MODEL,ONLY:MODEL

IMPLICIT NONE


TYPE (MF_PHYS_BASE_STATE_TYPE), INTENT (IN)::YDMF_PHYS_BASE_STATE
TYPE (CPG_BNDS_TYPE), INTENT (IN)::YDCPG_BNDS
TYPE (CPG_OPTS_TYPE), INTENT (IN)::YDCPG_OPTS
TYPE (MF_PHYS_SURF_TYPE), INTENT (INOUT)::YDMF_PHYS_SURF
TYPE (MODEL), INTENT (IN)::YDMODEL
REAL (KIND=JPRB), INTENT (OUT)::PAER (YDCPG_OPTS%KLON, YDCPG_OPTS%KFLEVG, 6)
REAL (KIND=JPRB), INTENT (OUT)::PAERINDS (YDCPG_OPTS%KLON, YDCPG_OPTS%KFLEVG)








LOGICAL::LLAERO
REAL (KIND=JPHOOK)::ZHOOK_HANDLE
#include "apl_arpege_aerosols_for_radiation_radaer.intfb.h"
IF (LHOOK) CALL DR_HOOK ('APL_ARPEGE_AEROSOLS_FOR_RADIATION', 0, ZHOOK_HANDLE)

LLAERO=YDMODEL%YRML_PHY_MF%YRPHY%LAEROSEA.AND.YDMODEL%YRML_PHY_MF%YRPHY%LAEROLAN.AND&
&.YDMODEL%YRML_PHY_MF%YRPHY%LAEROSOO.AND.YDMODEL%YRML_PHY_MF%YRPHY%LAERODES

IF ((MOD (YDCPG_OPTS%NSTEP, YDMODEL%YRML_PHY_RAD%YRERAD%NRADFR)==0).OR.(YDMODEL&
  &%YRML_PHY_MF%YRPHY%LRAY.AND.(.NOT.YDMODEL%YRML_PHY_MF%YRPHY%LRSTAER))) THEN
  CALL APL_ARPEGE_AEROSOLS_FOR_RADIATION_RADAER (LLAERO, YDCPG_BNDS, YDCPG_OPTS&
  &, YDMF_PHYS_BASE_STATE, YDMF_PHYS_SURF, YDMODEL, PAER, PAERINDS)
ENDIF


IF (LHOOK) CALL DR_HOOK ('APL_ARPEGE_AEROSOLS_FOR_RADIATION', 1, ZHOOK_HANDLE)
ENDSUBROUTINE APL_ARPEGE_AEROSOLS_FOR_RADIATION
