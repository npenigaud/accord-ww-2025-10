SUBROUTINE CPG_DIA_FLU_OUTLINE_004 (YDCPG_BNDS, YDCPG_OPTS&
&, YDMF_PHYS_SURF, YDMODEL, PDPRECIPS, PDPRECIPS2)
USE CPG_OPTS_TYPE_MOD,ONLY:CPG_BNDS_TYPE, CPG_OPTS_TYPE
USE PARKIND1,ONLY:JPIM, JPRB
USE MF_PHYS_SURFACE_TYPE_MOD,ONLY:MF_PHYS_SURF_TYPE
USE TYPE_MODEL,ONLY:MODEL
USE YOMHOOK,ONLY:LHOOK, JPHOOK, DR_HOOK

IMPLICIT NONE

TYPE (CPG_BNDS_TYPE), INTENT (IN)::YDCPG_BNDS
TYPE (CPG_OPTS_TYPE), INTENT (IN)::YDCPG_OPTS
TYPE (MF_PHYS_SURF_TYPE), INTENT (IN)::YDMF_PHYS_SURF
TYPE (MODEL), INTENT (IN)::YDMODEL
REAL (KIND=JPRB), INTENT (INOUT)::PDPRECIPS (YDCPG_OPTS&
&%KLON, YDMODEL%YRML_PHY_MF%YRPHY%YRDPRECIPS%NDTPREC)
REAL (KIND=JPRB), INTENT (INOUT)::PDPRECIPS2 (YDCPG_OPTS&
&%KLON, YDMODEL%YRML_PHY_MF%YRPHY%YRDPRECIPS%NDTPREC2)
REAL (KIND=JPHOOK)::ZHOOK_HANDLE
IF (LHOOK) CALL DR_HOOK ('CPG_DIA_FLU_OUTLINE_004', 0, ZHOOK_HANDLE)

IF (YDMODEL%YRML_PHY_MF%YRPHY%LDPRECIPS) THEN
  PDPRECIPS (YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA, 1:YDMODEL%YRML_PHY_MF%YRPHY&
  &%YRDPRECIPS%NDTPREC)=YDMF_PHYS_SURF%GSD_XP%PPRECIP (YDCPG_BNDS%KIDIA:YDCPG_BNDS&
  &%KFDIA, 1:YDMODEL%YRML_PHY_MF%YRPHY%YRDPRECIPS%NDTPREC)
ENDIF


IF (YDMODEL%YRML_PHY_MF%YRPHY%LDPRECIPS2) THEN
  PDPRECIPS2 (YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA, 1:YDMODEL%YRML_PHY_MF%YRPHY&
  &%YRDPRECIPS%NDTPREC2)=YDMF_PHYS_SURF%GSD_XP2%PPRECIP2 (YDCPG_BNDS%KIDIA&
  &:YDCPG_BNDS%KFDIA, 1:YDMODEL%YRML_PHY_MF%YRPHY%YRDPRECIPS%NDTPREC2)
ENDIF

IF (LHOOK) CALL DR_HOOK ('CPG_DIA_FLU_OUTLINE_004', 1, ZHOOK_HANDLE)
ENDSUBROUTINE
