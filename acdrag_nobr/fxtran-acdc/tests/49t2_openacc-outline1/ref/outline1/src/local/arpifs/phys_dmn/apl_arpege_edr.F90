SUBROUTINE APL_ARPEGE_EDR (YDCPG_OPTS, YDMF_PHYS, YDMF_PHYS_SURF, YDMODEL)
USE CPG_OPTS_TYPE_MOD,ONLY:CPG_BNDS_TYPE, CPG_OPTS_TYPE
USE PARKIND1,ONLY:JPIM, JPRB
USE MF_PHYS_SURFACE_TYPE_MOD,ONLY:MF_PHYS_SURF_TYPE
USE MF_PHYS_TYPE_MOD,ONLY:MF_PHYS_TYPE
USE TYPE_MODEL,ONLY:MODEL
USE YOMHOOK,ONLY:LHOOK, JPHOOK, DR_HOOK

IMPLICIT NONE

TYPE (CPG_OPTS_TYPE), INTENT (IN)::YDCPG_OPTS
TYPE (MF_PHYS_TYPE), INTENT (INOUT)::YDMF_PHYS
TYPE (MF_PHYS_SURF_TYPE), INTENT (INOUT)::YDMF_PHYS_SURF
TYPE (MODEL), INTENT (IN)::YDMODEL
INTEGER (KIND=JPIM)::JLEV
REAL (KIND=JPHOOK)::ZHOOK_HANDLE
IF (LHOOK) CALL DR_HOOK ('APL_ARPEGE_EDR', 0, ZHOOK_HANDLE)

IF (YDMODEL%YRML_PHY_MF%YRPHY%LEDR) THEN

  DO JLEV=1, YDCPG_OPTS%YRSURF_DIMS%YSD_DID%NLEVS
    YDMF_PHYS_SURF%GSD_DI%PXEDR (:, JLEV)=YDMF_PHYS%OUT%EDR (:, JLEV)
  ENDDO

ENDIF

IF (LHOOK) CALL DR_HOOK ('APL_ARPEGE_EDR', 1, ZHOOK_HANDLE)
ENDSUBROUTINE
