SUBROUTINE SCAN2M_TS_OUTLINE_000 (JFLD, YDCPG_BNDS, YDGEOMETRY, YDVARS)
USE CPG_OPTS_TYPE_MOD,ONLY:CPG_OPTS_TYPE, CPG_BNDS_TYPE
USE FIELD_VARIABLES_MOD,ONLY:FIELD_VARIABLES
USE GEOMETRY_MOD,ONLY:GEOMETRY
USE PARKIND1,ONLY:JPIM
USE YOMHOOK,ONLY:LHOOK, JPHOOK, DR_HOOK

IMPLICIT NONE

INTEGER (KIND=JPIM), INTENT (INOUT)::JFLD
TYPE (CPG_BNDS_TYPE), INTENT (IN)::YDCPG_BNDS
TYPE (GEOMETRY), INTENT (IN)::YDGEOMETRY
TYPE (FIELD_VARIABLES), INTENT (INOUT)::YDVARS
INTEGER (KIND=JPIM)::JLEV
INTEGER (KIND=JPIM)::JROF
REAL (KIND=JPHOOK)::ZHOOK_HANDLE
IF (LHOOK) CALL DR_HOOK ('SCAN2M_TS_OUTLINE_000', 0, ZHOOK_HANDLE)

DO JLEV=1, YDGEOMETRY%YRDIMV%NFLEVG

  DO JROF=YDCPG_BNDS%KIDIA, YDCPG_BNDS%KFDIA
    YDVARS%GFL_PTR (JFLD)%T0 (JROF, JLEV)=YDVARS%GFL_PTR (JFLD)%T1 (JROF, JLEV)
  ENDDO

ENDDO

IF (LHOOK) CALL DR_HOOK ('SCAN2M_TS_OUTLINE_000', 1, ZHOOK_HANDLE)
ENDSUBROUTINE
