SUBROUTINE CPG_GP_SETZERO (YDCPG_BNDS, YDCPG_MISC)
USE CPG_OPTS_TYPE_MOD,ONLY:CPG_BNDS_TYPE, CPG_OPTS_TYPE
USE CPG_TYPE_MOD,ONLY:CPG_DYN_TYPE, CPG_GPAR_TYPE, CPG_MISC_TYPE, CPG_TND_TYPE
USE YOMHOOK,ONLY:LHOOK, JPHOOK, DR_HOOK

IMPLICIT NONE

TYPE (CPG_BNDS_TYPE), INTENT (IN)::YDCPG_BNDS
TYPE (CPG_MISC_TYPE), INTENT (INOUT)::YDCPG_MISC
REAL (KIND=JPHOOK)::ZHOOK_HANDLE
IF (LHOOK) CALL DR_HOOK ('CPG_GP_SETZERO', 0, ZHOOK_HANDLE)
YDCPG_MISC%QS (YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA)=0.0_JPRB
IF (LHOOK) CALL DR_HOOK ('CPG_GP_SETZERO', 1, ZHOOK_HANDLE)
ENDSUBROUTINE
