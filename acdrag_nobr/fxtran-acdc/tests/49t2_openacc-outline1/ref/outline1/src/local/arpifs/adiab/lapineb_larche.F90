SUBROUTINE LAPINEB_LARCHE (YDA_PCCO, YDA_PSCO, YDCPG_BNDS, YDCPG_GMV9, YDCPG_OPTS&
&, YDGEOMETRY, YDMODEL, YDVARS, PDEPI, PDSTRET, PP, PQ, PUT1, PUZ9, PVT1, PVZ9)
USE CPG_OPTS_TYPE_MOD,ONLY:CPG_OPTS_TYPE, CPG_BNDS_TYPE
USE CPG_GMV_TYPE_MOD,ONLY:CPG_GMV_TYPE
USE FIELD_ARRAY_MODULE,ONLY:FIELD_3IM_ARRAY, FIELD_4IM_ARRAY, FIELD_3RB_ARRAY, FIELD_4RB_ARRAY
USE FIELD_VARIABLES_MOD,ONLY:FIELD_VARIABLES
USE GEOMETRY_MOD,ONLY:GEOMETRY
USE PARKIND1,ONLY:JPIM, JPRB
USE TYPE_MODEL,ONLY:MODEL
USE YOMHOOK,ONLY:LHOOK, JPHOOK, DR_HOOK

IMPLICIT NONE

TYPE (FIELD_4RB_ARRAY), INTENT (IN)::YDA_PCCO
TYPE (FIELD_4RB_ARRAY), INTENT (IN)::YDA_PSCO
TYPE (CPG_BNDS_TYPE), INTENT (IN)::YDCPG_BNDS
TYPE (CPG_GMV_TYPE), INTENT (INOUT)::YDCPG_GMV9
TYPE (CPG_OPTS_TYPE), INTENT (IN)::YDCPG_OPTS
TYPE (GEOMETRY), INTENT (IN)::YDGEOMETRY
TYPE (MODEL), INTENT (IN)::YDMODEL
TYPE (FIELD_VARIABLES), INTENT (INOUT)::YDVARS
REAL (KIND=JPRB), INTENT (INOUT)::PDEPI
REAL (KIND=JPRB), INTENT (INOUT)::PDSTRET
REAL (KIND=JPRB), INTENT (INOUT)::PP (YDGEOMETRY%YRDIM%NPROMA, YDGEOMETRY%YRDIMV%NFLEVG)
REAL (KIND=JPRB), INTENT (INOUT)::PQ (YDGEOMETRY%YRDIM%NPROMA, YDGEOMETRY%YRDIMV%NFLEVG)
REAL (KIND=JPRB), INTENT (INOUT)::PUT1 (YDGEOMETRY%YRDIM%NPROMA, YDGEOMETRY%YRDIMV%NFLEVG)
REAL (KIND=JPRB), INTENT (INOUT)::PUZ9 (YDGEOMETRY%YRDIM%NPROMA, YDGEOMETRY%YRDIMV%NFLEVG)
REAL (KIND=JPRB), INTENT (INOUT)::PVT1 (YDGEOMETRY%YRDIM%NPROMA, YDGEOMETRY%YRDIMV%NFLEVG)
REAL (KIND=JPRB), INTENT (INOUT)::PVZ9 (YDGEOMETRY%YRDIM%NPROMA, YDGEOMETRY%YRDIMV%NFLEVG)
#include "lacone.intfb.h"
#include "larche.intfb.h"
#include "mcoords.intfb.h"
#include "rotuv.intfb.h"
INTEGER (KIND=JPIM)::JLEV
INTEGER (KIND=JPIM)::JROF
REAL (KIND=JPRB)::ZPP1
REAL (KIND=JPRB)::ZPP9
REAL (KIND=JPRB)::ZQQ1
REAL (KIND=JPRB)::ZQQ9
REAL (KIND=JPHOOK)::ZHOOK_HANDLE
IF (LHOOK) CALL DR_HOOK ('LAPINEB_LARCHE', 0, ZHOOK_HANDLE)

DO JLEV=1, YDGEOMETRY%YRDIMV%NFLEVG

  DO JROF=YDCPG_BNDS%KIDIA, YDCPG_BNDS%KFDIA

    IF (YDMODEL%YRML_DYN%YRDYN%LRHS_CURV) THEN
      PUT1 (JROF, JLEV)=YDMODEL%YRML_DYN%YRDYN%RW2TLFF*0.5_JPRB*(YDVARS%U%T1 (JROF, JLEV)+PUZ9&
      & (JROF, JLEV))+(1.0_JPRB-YDMODEL%YRML_DYN%YRDYN%RW2TLFF)*YDCPG_GMV9%U%P (JROF, JLEV)
      PVT1 (JROF, JLEV)=YDMODEL%YRML_DYN%YRDYN%RW2TLFF*0.5_JPRB*(YDVARS%V%T1 (JROF, JLEV)+PVZ9&
      & (JROF, JLEV))+(1.0_JPRB-YDMODEL%YRML_DYN%YRDYN%RW2TLFF)*YDCPG_GMV9%V%P (JROF, JLEV)
    ELSE
      ZPP9=YDA_PCCO%P (JROF, JLEV, YDMODEL%YRML_DYN%YYTCCO%M_RQX)
      ZQQ9=YDA_PCCO%P (JROF, JLEV, YDMODEL%YRML_DYN%YYTCCO%M_RQY)
      ZPP1=YDVARS%GEOMETRY%GNORDM%T0 (JROF)
      ZQQ1=-YDVARS%GEOMETRY%GNORDL%T0 (JROF)
      PUT1 (JROF, JLEV)=YDMODEL%YRML_DYN%YRDYN%RW2TLFF*0.5_JPRB*(ZPP1*YDVARS%U%T1 (JROF, JLEV)+ZQQ1&
      &*YDVARS%V%T1 (JROF, JLEV)+ZPP9*PUZ9 (JROF, JLEV)+ZQQ9*PVZ9 (JROF, JLEV))+(1.0_JPRB-YDMODEL%YRML_DYN&
      &%YRDYN%RW2TLFF)*(ZPP1*YDCPG_GMV9%U%P (JROF, JLEV)+ZQQ1*YDCPG_GMV9%V%P (JROF, JLEV))
      PVT1 (JROF, JLEV)=YDMODEL%YRML_DYN%YRDYN%RW2TLFF*0.5_JPRB*(-ZQQ1*YDVARS%U%T1 (JROF, JLEV)+ZPP1&
      &*YDVARS%V%T1 (JROF, JLEV)-ZQQ9*PUZ9 (JROF, JLEV)+ZPP9*PVZ9 (JROF, JLEV))+(1.0_JPRB-YDMODEL%YRML_DYN&
      &%YRDYN%RW2TLFF)*(-ZQQ1*YDCPG_GMV9%U%P (JROF, JLEV)+ZPP1*YDCPG_GMV9%V%P (JROF, JLEV))
    ENDIF

  ENDDO

ENDDO

CALL MCOORDS (YDMODEL%YRML_DYN%YYTSCO, YDMODEL%YRML_GCONF%YRRIP, YDGEOMETRY%YRDIMV%NFLEVG&
&, YDGEOMETRY%YRDIM%NPROMA, YDCPG_BNDS%KIDIA, YDCPG_BNDS%KFDIA, YDMODEL%YRCST%RA, YDVARS&
&%GEOMETRY%GEMU%P, YDVARS%GEOMETRY%GSQM2%P, YDVARS%GEOMETRY%GNORDM%P, YDVARS%GEOMETRY%GNORDL&
&%P, PUT1, PVT1, YDA_PSCO%P, YDMODEL%YRML_DYN%YRDYNA%LTWOTL, LD2TLM=.FALSE.)

IF (YDMODEL%YRML_DYN%YRDYN%LRHS_CURV) THEN

  DO JLEV=1, YDGEOMETRY%YRDIMV%NFLEVG

    DO JROF=YDCPG_BNDS%KIDIA, YDCPG_BNDS%KFDIA
      YDVARS%U%T1 (JROF, JLEV)=YDVARS%U%T1 (JROF, JLEV)-YDCPG_GMV9%U%P (JROF, JLEV)
      YDVARS%V%T1 (JROF, JLEV)=YDVARS%V%T1 (JROF, JLEV)-YDCPG_GMV9%V%P (JROF, JLEV)
    ENDDO

  ENDDO

ELSE
  CALL ROTUV (YDGEOMETRY%YRDIMV%NFLEVG, YDGEOMETRY%YRDIM%NPROMA, YDCPG_BNDS%KIDIA, YDCPG_BNDS%KFDIA,&
  & YDCPG_GMV9%U%P, YDCPG_GMV9%V%P, PP, PQ, YDVARS%U%T1, YDVARS%V%T1, LDIR=.FALSE., LDSUB=.TRUE.)
ENDIF

CALL LARCHE (YDMODEL%YRML_DYN%YYTCCO, YDMODEL%YRML_DYN%YYTSCO, YDGEOMETRY%YRDIM%NPROMA, YDCPG_BNDS%KIDIA&
&, YDCPG_BNDS%KFDIA, YDGEOMETRY%YRDIMV%NFLEVG, YDGEOMETRY%YRGEM%NSTTYP, PDSTRET, YDGEOMETRY%YRGEM%RC2M1&
&, YDGEOMETRY%YRGEM%RC2P1, YDMODEL%YRCST%RPI, PDEPI, YDGEOMETRY%YRGEM%RLOCEN, YDGEOMETRY%YRGEM%RMUCEN&
&, YDA_PSCO%P, 1, YDA_PCCO%P, YDVARS%GEOMETRY%RCOLON%P, YDVARS%GEOMETRY%RSILON%P, YDVARS%GEOMETRY%GECLO&
&%P, YDVARS%GEOMETRY%GEMU%P, YDVARS%GEOMETRY%GESLO%P, YDVARS%GEOMETRY%GSQM2%P)
CALL LACONE (YDGEOMETRY%YRDIM%NPROMA, YDCPG_BNDS%KIDIA, YDCPG_BNDS%KFDIA, YDGEOMETRY%YRDIMV&
&%NFLEVG, YDGEOMETRY%YRGEM%NSTTYP, YDCPG_OPTS%N2DINI, YDMODEL%YRML_DYN%YRDYN%YRSWE, YDMODEL&
&%YRCST%ROMEGA, PDSTRET, YDGEOMETRY%YRGEM%RC2M1, YDGEOMETRY%YRGEM%RC2P1, YDGEOMETRY%YRGEM%RMUCEN&
&, YDMODEL%YRCST%RA, YDMODEL%YRCST%RPI, YDA_PCCO%P (:,:, YDMODEL%YRML_DYN%YYTCCO%M_RLON), YDA_PCCO&
&%P (:,:, YDMODEL%YRML_DYN%YYTCCO%M_RLAT), YDCPG_GMV9%U%P, YDCPG_GMV9%V%P)
IF (LHOOK) CALL DR_HOOK ('LAPINEB_LARCHE', 1, ZHOOK_HANDLE)
ENDSUBROUTINE
