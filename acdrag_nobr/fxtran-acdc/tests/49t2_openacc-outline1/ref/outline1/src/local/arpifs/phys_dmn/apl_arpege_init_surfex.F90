SUBROUTINE APL_ARPEGE_INIT_SURFEX (YDMF_PHYS_BASE_STATE, YDGEOMETRY, YDCPG_BNDS&
&, YDCPG_OPTS, YDCPG_MISC, YDCPG_GPAR, YDMF_PHYS, YDVARS, YDMODEL, PALBD&
&, PALBP, PFLU_EMIS, PSGROUPEL, PSRAIN, PSSNOW, PTSN)
USE YOMHOOK,ONLY:LHOOK, DR_HOOK, JPHOOK
USE PARKIND1,ONLY:JPIM, JPRB
USE MF_PHYS_BASE_STATE_TYPE_MOD,ONLY:MF_PHYS_BASE_STATE_TYPE
USE GEOMETRY_MOD,ONLY:GEOMETRY
USE CPG_OPTS_TYPE_MOD,ONLY:CPG_BNDS_TYPE, CPG_OPTS_TYPE
USE CPG_TYPE_MOD,ONLY:CPG_MISC_TYPE, CPG_GPAR_TYPE
USE MF_PHYS_TYPE_MOD,ONLY:MF_PHYS_TYPE
USE FIELD_VARIABLES_MOD,ONLY:FIELD_VARIABLES
USE TYPE_MODEL,ONLY:MODEL

IMPLICIT NONE


TYPE (MF_PHYS_BASE_STATE_TYPE), INTENT (IN)::YDMF_PHYS_BASE_STATE
TYPE (GEOMETRY), INTENT (IN)::YDGEOMETRY
TYPE (CPG_BNDS_TYPE), INTENT (IN)::YDCPG_BNDS
TYPE (CPG_OPTS_TYPE), INTENT (IN)::YDCPG_OPTS
TYPE (CPG_MISC_TYPE), INTENT (INOUT)::YDCPG_MISC
TYPE (CPG_GPAR_TYPE), INTENT (INOUT)::YDCPG_GPAR
TYPE (MF_PHYS_TYPE), INTENT (INOUT)::YDMF_PHYS
TYPE (FIELD_VARIABLES), INTENT (INOUT)::YDVARS
TYPE (MODEL), INTENT (IN)::YDMODEL
REAL (KIND=JPRB), INTENT (OUT)::PALBD (YDCPG_OPTS%KLON, YDCPG_OPTS%KSW)
REAL (KIND=JPRB), INTENT (OUT)::PALBP (YDCPG_OPTS%KLON, YDCPG_OPTS%KSW)
REAL (KIND=JPRB), INTENT (OUT)::PFLU_EMIS (YDCPG_OPTS%KLON)
REAL (KIND=JPRB), INTENT (OUT)::PSGROUPEL (YDCPG_OPTS%KLON)
REAL (KIND=JPRB), INTENT (OUT)::PSRAIN (YDCPG_OPTS%KLON)
REAL (KIND=JPRB), INTENT (OUT)::PSSNOW (YDCPG_OPTS%KLON)
REAL (KIND=JPRB), INTENT (OUT)::PTSN (YDCPG_OPTS%KLON)
#include "abor1.intfb.h"
#include "apl_arpege_init_surfex_outline_002.intfb.h"
#include "apl_arpege_init_surfex_outline_001.intfb.h"
#include "apl_arpege_init_surfex_outline_000.intfb.h"



REAL (KIND=JPHOOK)::ZHOOK_HANDLE
IF (LHOOK) CALL DR_HOOK ('APL_ARPEGE_INIT_SURFEX', 0, ZHOOK_HANDLE)



IF (YDMODEL%YRML_PHY_MF%YRARPHY%LMSE) THEN
  

  IF (YDMODEL%YRML_PHY_MF%YRPARAR%NSWB_MNH/=YDMODEL%YRML_PHY_RAD%YRERAD%NSW) THEN
    CALL ABOR1 ('APL_ARPEGE_INIT_SURFEX: NSWB_MNH /= NSW')
  ENDIF

  
  CALL APL_ARPEGE_INIT_SURFEX_OUTLINE_000 (YDCPG_BNDS, YDCPG_GPAR, YDCPG_MISC, YDCPG_OPTS&
  &, YDMF_PHYS, YDMF_PHYS_BASE_STATE, PFLU_EMIS, PSGROUPEL, PSRAIN, PSSNOW, PTSN)
  CALL APL_ARPEGE_INIT_SURFEX_OUTLINE_001 (YDCPG_BNDS, YDCPG_GPAR, YDCPG_OPTS&
  &, YDMF_PHYS, YDMF_PHYS_BASE_STATE, YDMODEL, PALBD, PALBP, PFLU_EMIS)

  IF (YDCPG_OPTS%LCONFX) THEN
    CALL APL_ARPEGE_INIT_SURFEX_OUTLINE_002 (YDCPG_BNDS, YDGEOMETRY, YDMF_PHYS, YDVARS)
  ENDIF

ENDIF


IF (LHOOK) CALL DR_HOOK ('APL_ARPEGE_INIT_SURFEX', 1, ZHOOK_HANDLE)
ENDSUBROUTINE APL_ARPEGE_INIT_SURFEX
