MODULE YOMSPHYHIST

!$ACDC methods 


USE PARKIND1  ,ONLY : JPRB

IMPLICIT NONE

SAVE

! ------ INTER TIMESTEP MEMORY FOR TRAJECTORIES AND PERTURBATIONS IN TL/AD PHYSICS
!   The intention is to keep all such variables in one module 
!   It is also aimed to be done in a style similar with the other trajectories
!   passed between different models (NL/TL/AD)


TYPE SPHYS_HIST_TYPE
  REAL (KIND=JPRB), DIMENSION (:,:), POINTER :: YA5=>NULL(), YL5=>NULL(), YI5=>NULL() ! cloud variables for trajectory in TL
  REAL (KIND=JPRB), DIMENSION (:,:), POINTER :: RADLWM=>NULL(), RADLWM5=>NULL() ! downward longwave emmissivity (0:KLEV)
  REAL (KIND=JPRB), DIMENSION (:,:), POINTER :: TGWDWMSU=>NULL(), TGWDWMSU5=>NULL() ! u-tendencies from non-orographic GWD
  REAL (KIND=JPRB), DIMENSION (:,:), POINTER :: TGWDWMSV=>NULL(), TGWDWMSV5=>NULL() ! v-tendencies from non-orographic GWD
CONTAINS
PROCEDURE :: ACDC_COPY => ACDC_COPY_SPHYS_HIST_TYPE
PROCEDURE :: ACDC_CRC64 => ACDC_CRC64_SPHYS_HIST_TYPE
PROCEDURE :: ACDC_HOST => ACDC_HOST_SPHYS_HIST_TYPE
PROCEDURE :: ACDC_LOAD => ACDC_LOAD_SPHYS_HIST_TYPE
PROCEDURE :: ACDC_SAVE => ACDC_SAVE_SPHYS_HIST_TYPE
PROCEDURE :: ACDC_SIZE => ACDC_SIZE_SPHYS_HIST_TYPE
PROCEDURE :: ACDC_WIPE => ACDC_WIPE_SPHYS_HIST_TYPE
END TYPE SPHYS_HIST_TYPE

!!TYPE(SPHYS_HIST_TYPE), POINTER :: GPHIST(:) => NULL()


!     ------------------------------------------------------------------

INTERFACE

MODULE SUBROUTINE ACDC_COPY_SPHYS_HIST_TYPE (SELF, LDCREATED, LDFIELDAPI)

IMPLICIT NONE
CLASS (SPHYS_HIST_TYPE), INTENT (IN), TARGET :: SELF
LOGICAL, OPTIONAL, INTENT (IN) :: LDCREATED, LDFIELDAPI
END SUBROUTINE

MODULE SUBROUTINE ACDC_CRC64_SPHYS_HIST_TYPE (SELF, KLUN, CDPATH)
USE FXTRAN_ACDC_CRC64_INTRINSIC_MOD, ONLY : FCRC64 => FXTRAN_ACDC_CRC64
IMPLICIT NONE
CLASS (SPHYS_HIST_TYPE), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
CHARACTER(LEN=*), INTENT (IN) :: CDPATH
END SUBROUTINE

MODULE SUBROUTINE ACDC_HOST_SPHYS_HIST_TYPE (SELF)

IMPLICIT NONE
CLASS (SPHYS_HIST_TYPE), TARGET :: SELF
END SUBROUTINE

MODULE SUBROUTINE ACDC_LOAD_SPHYS_HIST_TYPE (SELF, KLUN)
USE PARKIND1, ONLY : JPRD
IMPLICIT NONE
CLASS (SPHYS_HIST_TYPE), INTENT (OUT), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
END SUBROUTINE

MODULE SUBROUTINE ACDC_SAVE_SPHYS_HIST_TYPE (SELF, KLUN)

IMPLICIT NONE
CLASS (SPHYS_HIST_TYPE), INTENT (IN), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
END SUBROUTINE

MODULE FUNCTION ACDC_SIZE_SPHYS_HIST_TYPE (SELF, CDPATH, LDPRINT) RESULT (KSIZE)

IMPLICIT NONE
CLASS (SPHYS_HIST_TYPE),     INTENT (IN), TARGET :: SELF
CHARACTER(LEN=*), INTENT (IN), OPTIONAL :: CDPATH
LOGICAL,          INTENT (IN), OPTIONAL :: LDPRINT
INTEGER*8 :: KSIZE
END FUNCTION

MODULE SUBROUTINE ACDC_WIPE_SPHYS_HIST_TYPE (SELF, LDDELETED, LDFIELDAPI)

IMPLICIT NONE
CLASS (SPHYS_HIST_TYPE), INTENT (IN), TARGET :: SELF
LOGICAL, OPTIONAL, INTENT (IN) :: LDDELETED, LDFIELDAPI
END SUBROUTINE


END INTERFACE

END MODULE YOMSPHYHIST
