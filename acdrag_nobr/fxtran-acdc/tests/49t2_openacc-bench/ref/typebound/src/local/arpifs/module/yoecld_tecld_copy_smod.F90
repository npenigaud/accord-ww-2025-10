SUBMODULE (YOECLD) YOECLD_TECLD_COPY_SMOD

IMPLICIT NONE

CONTAINS

MODULE SUBROUTINE ACDC_COPY_TECLD (SELF, LDCREATED, LDFIELDAPI)

IMPLICIT NONE
CLASS (TECLD), INTENT (IN), TARGET :: SELF
LOGICAL, OPTIONAL, INTENT (IN) :: LDCREATED, LDFIELDAPI
LOGICAL :: LLCREATED
LOGICAL :: LLFIELDAPI
LOGICAL :: LCETA
LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF
LLCREATED = .FALSE.
IF (PRESENT (LDCREATED)) THEN
  LLCREATED = LDCREATED
ENDIF
IF (.NOT. LLCREATED) THEN
  !$ACC ENTER DATA CREATE (SELF)
  !$ACC UPDATE DEVICE (SELF)
ENDIF
LCETA = ALLOCATED (SELF%CETA)
IF (LCETA) THEN
  !$ACC ENTER DATA CREATE (SELF%CETA)
  !$ACC UPDATE DEVICE (SELF%CETA)
  !$ACC ENTER DATA ATTACH (SELF%CETA)
ENDIF

END SUBROUTINE

END SUBMODULE
