SUBMODULE (YOMCSGEOM) YOMCSGEOM_TCSGEOM_LOAD_SMOD

IMPLICIT NONE

CONTAINS

MODULE SUBROUTINE ACDC_LOAD_TCSGEOM (SELF, KLUN)
USE PARKIND1, ONLY : JPRD
IMPLICIT NONE
CLASS (TCSGEOM), INTENT (OUT), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
INTEGER :: IL1(1), IU1(1)
LOGICAL :: LRATATH, LRATATX, LRCOLON, LRINDX, LRINDY, LRSILON
REAL(KIND=JPRD), ALLOCATABLE :: ZTMP1 (:)
READ (KLUN) LRCOLON
IF (LRCOLON) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (SELF%RCOLON (IL1(1):IU1(1)))
  READ (KLUN) SELF%RCOLON
ELSE
  NULLIFY (SELF%RCOLON)
ENDIF
READ (KLUN) LRSILON
IF (LRSILON) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (SELF%RSILON (IL1(1):IU1(1)))
  READ (KLUN) SELF%RSILON
ELSE
  NULLIFY (SELF%RSILON)
ENDIF
READ (KLUN) LRINDX
IF (LRINDX) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (SELF%RINDX (IL1(1):IU1(1)))
  ALLOCATE (ZTMP1(LBOUND(SELF%RINDX,1):UBOUND(SELF%RINDX,1)))
  READ (KLUN) ZTMP1
  SELF%RINDX = ZTMP1
  DEALLOCATE (ZTMP1)
ELSE
  NULLIFY (SELF%RINDX)
ENDIF
READ (KLUN) LRINDY
IF (LRINDY) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (SELF%RINDY (IL1(1):IU1(1)))
  ALLOCATE (ZTMP1(LBOUND(SELF%RINDY,1):UBOUND(SELF%RINDY,1)))
  READ (KLUN) ZTMP1
  SELF%RINDY = ZTMP1
  DEALLOCATE (ZTMP1)
ELSE
  NULLIFY (SELF%RINDY)
ENDIF
READ (KLUN) LRATATH
IF (LRATATH) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (SELF%RATATH (IL1(1):IU1(1)))
  ALLOCATE (ZTMP1(LBOUND(SELF%RATATH,1):UBOUND(SELF%RATATH,1)))
  READ (KLUN) ZTMP1
  SELF%RATATH = ZTMP1
  DEALLOCATE (ZTMP1)
ELSE
  NULLIFY (SELF%RATATH)
ENDIF
READ (KLUN) LRATATX
IF (LRATATX) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (SELF%RATATX (IL1(1):IU1(1)))
  ALLOCATE (ZTMP1(LBOUND(SELF%RATATX,1):UBOUND(SELF%RATATX,1)))
  READ (KLUN) ZTMP1
  SELF%RATATX = ZTMP1
  DEALLOCATE (ZTMP1)
ELSE
  NULLIFY (SELF%RATATX)
ENDIF
END SUBROUTINE

END SUBMODULE
