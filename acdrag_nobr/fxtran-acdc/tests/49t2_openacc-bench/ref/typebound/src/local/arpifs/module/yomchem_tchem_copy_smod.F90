MODULE SUBROUTINE ACDC_COPY_TCHEM (SELF, LDCREATED, LDFIELDAPI)

IMPLICIT NONE
CLASS (TCHEM), INTENT (IN), TARGET :: SELF
LOGICAL, OPTIONAL, INTENT (IN) :: LDCREATED, LDFIELDAPI
LOGICAL :: LLCREATED
LOGICAL :: LLFIELDAPI
LOGICAL :: LTCO2DG, LTCOTOP
LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF
LLCREATED = .FALSE.
IF (PRESENT (LDCREATED)) THEN
  LLCREATED = LDCREATED
ENDIF
IF (.NOT. LLCREATED) THEN
  !$ACC ENTER DATA CREATE (SELF)
  !$ACC UPDATE DEVICE (SELF)
ENDIF

LTCO2DG = ALLOCATED (SELF%TCO2DG)
IF (LTCO2DG) THEN
  !$ACC ENTER DATA CREATE (SELF%TCO2DG)
  !$ACC UPDATE DEVICE (SELF%TCO2DG)
  !$ACC ENTER DATA ATTACH (SELF%TCO2DG)
ENDIF

LTCOTOP = ALLOCATED (SELF%TCOTOP)
IF (LTCOTOP) THEN
  !$ACC ENTER DATA CREATE (SELF%TCOTOP)
  !$ACC UPDATE DEVICE (SELF%TCOTOP)
  !$ACC ENTER DATA ATTACH (SELF%TCOTOP)
ENDIF

END SUBROUTINE

END SUBMODULE
