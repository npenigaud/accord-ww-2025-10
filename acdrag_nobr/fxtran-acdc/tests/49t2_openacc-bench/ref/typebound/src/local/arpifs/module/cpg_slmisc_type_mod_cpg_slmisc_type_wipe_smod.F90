CONTAINS

MODULE SUBROUTINE ACDC_WIPE_CPG_SLMISC_TYPE (SELF, LDDELETED, LDFIELDAPI)
USE FIELD_UTIL_MODULE
IMPLICIT NONE
CLASS (CPG_SLMISC_TYPE), INTENT (IN), TARGET :: SELF
LOGICAL, OPTIONAL, INTENT (IN) :: LDDELETED, LDFIELDAPI
LOGICAL :: LLDELETED
LOGICAL :: LLFIELDAPI
LOGICAL :: LF_ISETTLOFF, LF_SLHDA, LF_SLHDD0
LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF
LF_SLHDA = ASSOCIATED (SELF%F_SLHDA)
IF (LF_SLHDA .AND. LLFIELDAPI) THEN
  CALL WIPE_FIELD_2RB (SELF%F_SLHDA)
ENDIF

LF_SLHDD0 = ASSOCIATED (SELF%F_SLHDD0)
IF (LF_SLHDD0 .AND. LLFIELDAPI) THEN
  CALL WIPE_FIELD_2RB (SELF%F_SLHDD0)
ENDIF

LF_ISETTLOFF = ASSOCIATED (SELF%F_ISETTLOFF)
IF (LF_ISETTLOFF .AND. LLFIELDAPI) THEN
  CALL WIPE_FIELD_3IM (SELF%F_ISETTLOFF)
ENDIF

LLDELETED = .FALSE.
IF (PRESENT (LDDELETED)) THEN
  LLDELETED = LDDELETED
ENDIF
IF (.NOT. LLDELETED) THEN
  !$ACC EXIT DATA DELETE (SELF)
ENDIF
END SUBROUTINE

END SUBMODULE
