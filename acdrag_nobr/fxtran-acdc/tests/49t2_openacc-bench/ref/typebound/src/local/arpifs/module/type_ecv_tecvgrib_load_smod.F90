SUBMODULE (TYPE_ECV) TYPE_ECV_TECVGRIB_LOAD_SMOD

IMPLICIT NONE

CONTAINS

MODULE SUBROUTINE ACDC_LOAD_TECVGRIB (SELF, KLUN)

IMPLICIT NONE
CLASS (TECVGRIB), INTENT (OUT), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
INTEGER :: IL1(1), IU1(1)
LOGICAL :: LMECVGRB_1D, LMECVGRB_2D, LMECVGRB_3D, LMECVGRB_ALL
READ (KLUN) LMECVGRB_1D
IF (LMECVGRB_1D) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (SELF%MECVGRB_1D (IL1(1):IU1(1)))
  READ (KLUN) SELF%MECVGRB_1D
ENDIF
READ (KLUN) LMECVGRB_2D
IF (LMECVGRB_2D) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (SELF%MECVGRB_2D (IL1(1):IU1(1)))
  READ (KLUN) SELF%MECVGRB_2D
ENDIF
READ (KLUN) LMECVGRB_3D
IF (LMECVGRB_3D) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (SELF%MECVGRB_3D (IL1(1):IU1(1)))
  READ (KLUN) SELF%MECVGRB_3D
ENDIF
READ (KLUN) LMECVGRB_ALL
IF (LMECVGRB_ALL) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (SELF%MECVGRB_ALL (IL1(1):IU1(1)))
  READ (KLUN) SELF%MECVGRB_ALL
ENDIF
END SUBROUTINE

END SUBMODULE
