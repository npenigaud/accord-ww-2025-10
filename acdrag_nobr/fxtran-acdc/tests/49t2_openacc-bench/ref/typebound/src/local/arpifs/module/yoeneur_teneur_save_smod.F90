IF (LENTREEMOY) THEN
  WRITE (KLUN) LBOUND (SELF%ENTREEMOY)
  WRITE (KLUN) UBOUND (SELF%ENTREEMOY)
  WRITE (KLUN) SELF%ENTREEMOY
