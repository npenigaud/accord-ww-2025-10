MODULE SUBROUTINE ACDC_HOST_MODEL_PHYSICS_SIMPLINEAR_TYPE (SELF)

IMPLICIT NONE
CLASS (MODEL_PHYSICS_SIMPLINEAR_TYPE), TARGET :: SELF
INTEGER :: J1
LOGICAL :: LGPHIST
CALL SELF%YREPHLI%ACDC_HOST ()

CALL SELF%YRCUMFS%ACDC_HOST ()

CALL SELF%YREGWDWMS%ACDC_HOST ()

CALL SELF%YRECUMF2%ACDC_HOST ()

CALL SELF%YRPHLC%ACDC_HOST ()

CALL SELF%YRPHNC%ACDC_HOST ()

CALL SELF%YRNCL%ACDC_HOST ()

CALL SELF%YRSRFTLAD%ACDC_HOST ()

LGPHIST = ASSOCIATED (SELF%GPHIST)
IF (LGPHIST) THEN
  DO J1 = LBOUND (SELF%GPHIST, 1), UBOUND (SELF%GPHIST, 1)
    CALL SELF%GPHIST (J1)%ACDC_HOST ()
  ENDDO
ENDIF

END SUBROUTINE

