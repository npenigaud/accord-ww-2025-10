SUBMODULE (TYPE_ECV) TYPE_ECV_TECVGRIB_SIZE_SMOD

IMPLICIT NONE

CONTAINS

MODULE FUNCTION ACDC_SIZE_TECVGRIB (SELF, CDPATH, LDPRINT) RESULT (KSIZE)

IMPLICIT NONE
CLASS (TECVGRIB),     INTENT (IN), TARGET :: SELF
CHARACTER(LEN=*), INTENT (IN), OPTIONAL :: CDPATH
LOGICAL,          INTENT (IN), OPTIONAL :: LDPRINT
INTEGER*8 :: KSIZE
INTEGER*8 :: ISIZE, JSIZE
LOGICAL :: LLPRINT
CHARACTER(LEN=128) :: CLPATH
LOGICAL :: LMECVGRB_1D, LMECVGRB_2D, LMECVGRB_3D, LMECVGRB_ALL
LLPRINT = .FALSE.
IF (PRESENT (LDPRINT)) THEN
  LLPRINT = LDPRINT
ENDIF
CLPATH=''
IF (PRESENT (CDPATH)) THEN
  CLPATH = CDPATH
ENDIF
KSIZE = 0
LMECVGRB_1D = ALLOCATED (SELF%MECVGRB_1D)
IF (LMECVGRB_1D) THEN
  ISIZE = KIND (SELF%MECVGRB_1D) * SIZE (SELF%MECVGRB_1D)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%MECVGRB_1D'
  ENDIF
  KSIZE = KSIZE + ISIZE
ENDIF
LMECVGRB_2D = ALLOCATED (SELF%MECVGRB_2D)
IF (LMECVGRB_2D) THEN
  ISIZE = KIND (SELF%MECVGRB_2D) * SIZE (SELF%MECVGRB_2D)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%MECVGRB_2D'
  ENDIF
  KSIZE = KSIZE + ISIZE
ENDIF
LMECVGRB_3D = ALLOCATED (SELF%MECVGRB_3D)
IF (LMECVGRB_3D) THEN
  ISIZE = KIND (SELF%MECVGRB_3D) * SIZE (SELF%MECVGRB_3D)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%MECVGRB_3D'
  ENDIF
  KSIZE = KSIZE + ISIZE
ENDIF
LMECVGRB_ALL = ALLOCATED (SELF%MECVGRB_ALL)
IF (LMECVGRB_ALL) THEN
  ISIZE = KIND (SELF%MECVGRB_ALL) * SIZE (SELF%MECVGRB_ALL)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%MECVGRB_ALL'
  ENDIF
  KSIZE = KSIZE + ISIZE
ENDIF
END FUNCTION

END SUBMODULE
