SUBMODULE (YOMCDDH) YOMCDDH_TCDDH_LOAD_SMOD

IMPLICIT NONE

CONTAINS

MODULE SUBROUTINE ACDC_LOAD_TCDDH (SELF, KLUN)

IMPLICIT NONE
CLASS (TCDDH), INTENT (OUT), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
INTEGER :: IL1(1), IU1(1)
LOGICAL :: LCADHFSS, LCADHIIS, LCADHSSS, LCADHTIS, LCADHTLS, LCADHTSS, LCADHTTS, LCADHWLS
READ (KLUN) LCADHTLS
IF (LCADHTLS) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (SELF%CADHTLS (IL1(1):IU1(1)))
  READ (KLUN) SELF%CADHTLS
ENDIF
READ (KLUN) LCADHTSS
IF (LCADHTSS) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (SELF%CADHTSS (IL1(1):IU1(1)))
  READ (KLUN) SELF%CADHTSS
ENDIF
READ (KLUN) LCADHTTS
IF (LCADHTTS) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (SELF%CADHTTS (IL1(1):IU1(1)))
  READ (KLUN) SELF%CADHTTS
ENDIF
READ (KLUN) LCADHTIS
IF (LCADHTIS) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (SELF%CADHTIS (IL1(1):IU1(1)))
  READ (KLUN) SELF%CADHTIS
ENDIF
READ (KLUN) LCADHSSS
IF (LCADHSSS) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (SELF%CADHSSS (IL1(1):IU1(1)))
  READ (KLUN) SELF%CADHSSS
ENDIF
READ (KLUN) LCADHIIS
IF (LCADHIIS) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (SELF%CADHIIS (IL1(1):IU1(1)))
  READ (KLUN) SELF%CADHIIS
ENDIF
READ (KLUN) LCADHWLS
IF (LCADHWLS) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (SELF%CADHWLS (IL1(1):IU1(1)))
  READ (KLUN) SELF%CADHWLS
ENDIF
READ (KLUN) LCADHFSS
IF (LCADHFSS) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (SELF%CADHFSS (IL1(1):IU1(1)))
  READ (KLUN) SELF%CADHFSS
ENDIF
END SUBROUTINE

END SUBMODULE
