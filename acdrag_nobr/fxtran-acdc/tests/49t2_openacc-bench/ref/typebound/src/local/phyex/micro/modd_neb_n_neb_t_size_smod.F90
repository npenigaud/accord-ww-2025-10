SUBMODULE (MODD_NEB_N) MODD_NEB_N_NEB_T_SIZE_SMOD

IMPLICIT NONE

CONTAINS

MODULE FUNCTION ACDC_SIZE_NEB_T (SELF, CDPATH, LDPRINT) RESULT (KSIZE)

IMPLICIT NONE
CLASS (NEB_T),     INTENT (IN), TARGET :: SELF
CHARACTER(LEN=*), INTENT (IN), OPTIONAL :: CDPATH
LOGICAL,          INTENT (IN), OPTIONAL :: LDPRINT
INTEGER*8 :: KSIZE
INTEGER*8 :: ISIZE, JSIZE
LOGICAL :: LLPRINT
CHARACTER(LEN=128) :: CLPATH
LLPRINT = .FALSE.
IF (PRESENT (LDPRINT)) THEN
  LLPRINT = LDPRINT
ENDIF
CLPATH=''
IF (PRESENT (CDPATH)) THEN
  CLPATH = CDPATH
ENDIF
KSIZE = 0
ISIZE = KIND (SELF%XTMINMIX)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%XTMINMIX'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%XTMAXMIX)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%XTMAXMIX'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%LHGT_QS)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%LHGT_QS'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%CFRAC_ICE_ADJUST) * LEN (SELF%CFRAC_ICE_ADJUST)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%CFRAC_ICE_ADJUST'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%CFRAC_ICE_SHALLOW_MF) * LEN (SELF%CFRAC_ICE_SHALLOW_MF)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%CFRAC_ICE_SHALLOW_MF'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%VSIGQSAT)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%VSIGQSAT'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%CCONDENS) * LEN (SELF%CCONDENS)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%CCONDENS'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%CLAMBDA3) * LEN (SELF%CLAMBDA3)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%CLAMBDA3'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%LSTATNW)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%LSTATNW'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%LSIGMAS)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%LSIGMAS'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%LSUBG_COND)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%LSUBG_COND'
ENDIF
KSIZE = KSIZE + ISIZE
END FUNCTION

END SUBMODULE
