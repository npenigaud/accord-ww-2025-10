MODULE SUBROUTINE ACDC_LOAD_TLEP (SELF, KLUN)
USE PARKIND1, ONLY : JPRD
IMPLICIT NONE
CLASS (TLEP), INTENT (OUT), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
INTEGER :: IL1(1), IU1(1)
LOGICAL :: LMVALUE, LNCPL2M, LNCPL2N, LNCPL4M, LNCPL4N, LNCPLM, LNCPLN, LNESM0, LNESM0G, LNESPZERO
LOGICAL :: LNPME, LNPNE, LRLEPDIM, LRLEPDIN, LRLEPINM, LRLEPINN
REAL(KIND=JPRD), ALLOCATABLE :: ZTMP1 (:)
READ (KLUN) LNCPL2M
IF (LNCPL2M) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (SELF%NCPL2M (IL1(1):IU1(1)))
  READ (KLUN) SELF%NCPL2M
ENDIF
READ (KLUN) LNCPL4M
IF (LNCPL4M) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (SELF%NCPL4M (IL1(1):IU1(1)))
  READ (KLUN) SELF%NCPL4M
ENDIF
READ (KLUN) LNCPLM
IF (LNCPLM) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (SELF%NCPLM (IL1(1):IU1(1)))
  READ (KLUN) SELF%NCPLM
ENDIF
READ (KLUN) LNCPL2N
IF (LNCPL2N) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (SELF%NCPL2N (IL1(1):IU1(1)))
  READ (KLUN) SELF%NCPL2N
ENDIF
READ (KLUN) LNCPL4N
IF (LNCPL4N) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (SELF%NCPL4N (IL1(1):IU1(1)))
  READ (KLUN) SELF%NCPL4N
ENDIF
READ (KLUN) LNCPLN
IF (LNCPLN) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (SELF%NCPLN (IL1(1):IU1(1)))
  READ (KLUN) SELF%NCPLN
ENDIF
READ (KLUN) LRLEPDIN
IF (LRLEPDIN) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (SELF%RLEPDIN (IL1(1):IU1(1)))
  ALLOCATE (ZTMP1(LBOUND(SELF%RLEPDIN,1):UBOUND(SELF%RLEPDIN,1)))
  READ (KLUN) ZTMP1
  SELF%RLEPDIN = ZTMP1
  DEALLOCATE (ZTMP1)
ENDIF
READ (KLUN) LRLEPINN
IF (LRLEPINN) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (SELF%RLEPINN (IL1(1):IU1(1)))
  ALLOCATE (ZTMP1(LBOUND(SELF%RLEPINN,1):UBOUND(SELF%RLEPINN,1)))
  READ (KLUN) ZTMP1
  SELF%RLEPINN = ZTMP1
  DEALLOCATE (ZTMP1)
ENDIF
READ (KLUN) LRLEPDIM
IF (LRLEPDIM) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (SELF%RLEPDIM (IL1(1):IU1(1)))
  ALLOCATE (ZTMP1(LBOUND(SELF%RLEPDIM,1):UBOUND(SELF%RLEPDIM,1)))
  READ (KLUN) ZTMP1
  SELF%RLEPDIM = ZTMP1
  DEALLOCATE (ZTMP1)
ENDIF
READ (KLUN) LRLEPINM
IF (LRLEPINM) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (SELF%RLEPINM (IL1(1):IU1(1)))
  ALLOCATE (ZTMP1(LBOUND(SELF%RLEPINM,1):UBOUND(SELF%RLEPINM,1)))
  READ (KLUN) ZTMP1
  SELF%RLEPINM = ZTMP1
  DEALLOCATE (ZTMP1)
ENDIF
READ (KLUN) LNESM0
IF (LNESM0) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (SELF%NESM0 (IL1(1):IU1(1)))
  READ (KLUN) SELF%NESM0
ENDIF
READ (KLUN) LNESPZERO
IF (LNESPZERO) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (SELF%NESPZERO (IL1(1):IU1(1)))
  READ (KLUN) SELF%NESPZERO
ENDIF
READ (KLUN) LNESM0G
IF (LNESM0G) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (SELF%NESM0G (IL1(1):IU1(1)))
  READ (KLUN) SELF%NESM0G
ENDIF
READ (KLUN) LNPME
IF (LNPME) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (SELF%NPME (IL1(1):IU1(1)))
  READ (KLUN) SELF%NPME
ENDIF
READ (KLUN) LNPNE
IF (LNPNE) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (SELF%NPNE (IL1(1):IU1(1)))
  READ (KLUN) SELF%NPNE
ENDIF
READ (KLUN) LMVALUE
IF (LMVALUE) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (SELF%MVALUE (IL1(1):IU1(1)))
  READ (KLUN) SELF%MVALUE
ENDIF
END SUBROUTINE

END SUBMODULE
