MODULE SUBROUTINE ACDC_COPY_TSPGEOM (SELF, LDCREATED, LDFIELDAPI)

IMPLICIT NONE
CLASS (TSPGEOM), INTENT (IN), TARGET :: SELF
LOGICAL, OPTIONAL, INTENT (IN) :: LDCREATED, LDFIELDAPI
LOGICAL :: LLCREATED
LOGICAL :: LLFIELDAPI
LOGICAL :: LGMR, LSCGMAP
LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF
LLCREATED = .FALSE.
IF (PRESENT (LDCREATED)) THEN
  LLCREATED = LDCREATED
ENDIF
IF (.NOT. LLCREATED) THEN
  !$ACC ENTER DATA CREATE (SELF)
  !$ACC UPDATE DEVICE (SELF)
ENDIF
LGMR = ALLOCATED (SELF%GMR)
IF (LGMR) THEN
  !$ACC ENTER DATA CREATE (SELF%GMR)
  !$ACC UPDATE DEVICE (SELF%GMR)
  !$ACC ENTER DATA ATTACH (SELF%GMR)
ENDIF

LSCGMAP = ALLOCATED (SELF%SCGMAP)
IF (LSCGMAP) THEN
  !$ACC ENTER DATA CREATE (SELF%SCGMAP)
  !$ACC UPDATE DEVICE (SELF%SCGMAP)
  !$ACC ENTER DATA ATTACH (SELF%SCGMAP)
ENDIF

END SUBROUTINE

END SUBMODULE
