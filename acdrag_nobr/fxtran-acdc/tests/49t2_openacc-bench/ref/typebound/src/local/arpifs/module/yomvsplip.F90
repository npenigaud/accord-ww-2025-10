MODULE YOMVSPLIP

!$ACDC methods 


USE PARKIND1 , ONLY : JPRB
IMPLICIT NONE

SAVE

! * Variables related to use of spline cubic vertical interpolations.
! RVSPTRI,RVSPC      : are used to re-profile the field to be interpolated
!                      in routine VSPLTRANS.
! RFVV: is used in the computation of the vertical weights.

TYPE TVSPLIP
REAL(KIND=JPRB),ALLOCATABLE :: RVSPTRI(:,:)
REAL(KIND=JPRB),ALLOCATABLE :: RVSPC(:)
REAL(KIND=JPRB),ALLOCATABLE :: RFVV(:,:,:)
CONTAINS
PROCEDURE :: ACDC_COPY => ACDC_COPY_TVSPLIP
PROCEDURE :: ACDC_CRC64 => ACDC_CRC64_TVSPLIP
PROCEDURE :: ACDC_HOST => ACDC_HOST_TVSPLIP
PROCEDURE :: ACDC_LOAD => ACDC_LOAD_TVSPLIP
PROCEDURE :: ACDC_SAVE => ACDC_SAVE_TVSPLIP
PROCEDURE :: ACDC_SIZE => ACDC_SIZE_TVSPLIP
PROCEDURE :: ACDC_WIPE => ACDC_WIPE_TVSPLIP
END TYPE TVSPLIP

INTERFACE

MODULE SUBROUTINE ACDC_COPY_TVSPLIP (SELF, LDCREATED, LDFIELDAPI)

IMPLICIT NONE
CLASS (TVSPLIP), INTENT (IN), TARGET :: SELF
LOGICAL, OPTIONAL, INTENT (IN) :: LDCREATED, LDFIELDAPI
END SUBROUTINE

MODULE SUBROUTINE ACDC_CRC64_TVSPLIP (SELF, KLUN, CDPATH)
USE FXTRAN_ACDC_CRC64_INTRINSIC_MOD, ONLY : FCRC64 => FXTRAN_ACDC_CRC64
IMPLICIT NONE
CLASS (TVSPLIP), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
CHARACTER(LEN=*), INTENT (IN) :: CDPATH
END SUBROUTINE

MODULE SUBROUTINE ACDC_HOST_TVSPLIP (SELF)

IMPLICIT NONE
CLASS (TVSPLIP), TARGET :: SELF
END SUBROUTINE

MODULE SUBROUTINE ACDC_LOAD_TVSPLIP (SELF, KLUN)
USE PARKIND1, ONLY : JPRD
IMPLICIT NONE
CLASS (TVSPLIP), INTENT (OUT), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
END SUBROUTINE

MODULE SUBROUTINE ACDC_SAVE_TVSPLIP (SELF, KLUN)

IMPLICIT NONE
CLASS (TVSPLIP), INTENT (IN), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
END SUBROUTINE

MODULE FUNCTION ACDC_SIZE_TVSPLIP (SELF, CDPATH, LDPRINT) RESULT (KSIZE)

IMPLICIT NONE
CLASS (TVSPLIP),     INTENT (IN), TARGET :: SELF
CHARACTER(LEN=*), INTENT (IN), OPTIONAL :: CDPATH
LOGICAL,          INTENT (IN), OPTIONAL :: LDPRINT
INTEGER*8 :: KSIZE
END FUNCTION

MODULE SUBROUTINE ACDC_WIPE_TVSPLIP (SELF, LDDELETED, LDFIELDAPI)

IMPLICIT NONE
CLASS (TVSPLIP), INTENT (IN), TARGET :: SELF
LOGICAL, OPTIONAL, INTENT (IN) :: LDDELETED, LDFIELDAPI
END SUBROUTINE


END INTERFACE

END MODULE YOMVSPLIP
