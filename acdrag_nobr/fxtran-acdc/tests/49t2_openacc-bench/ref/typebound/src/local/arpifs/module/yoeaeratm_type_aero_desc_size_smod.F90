SUBMODULE (YOEAERATM) YOEAERATM_TYPE_AERO_DESC_SIZE_SMOD

IMPLICIT NONE

CONTAINS

MODULE FUNCTION ACDC_SIZE_TYPE_AERO_DESC (SELF, CDPATH, LDPRINT) RESULT (KSIZE)

IMPLICIT NONE
CLASS (TYPE_AERO_DESC),     INTENT (IN), TARGET :: SELF
CHARACTER(LEN=*), INTENT (IN), OPTIONAL :: CDPATH
LOGICAL,          INTENT (IN), OPTIONAL :: LDPRINT
INTEGER*8 :: KSIZE
INTEGER*8 :: ISIZE, JSIZE
LOGICAL :: LLPRINT
CHARACTER(LEN=128) :: CLPATH
LLPRINT = .FALSE.
IF (PRESENT (LDPRINT)) THEN
  LLPRINT = LDPRINT
ENDIF
CLPATH=''
IF (PRESENT (CDPATH)) THEN
  CLPATH = CDPATH
ENDIF
KSIZE = 0
ISIZE = KIND (SELF%CNAME) * LEN (SELF%CNAME)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%CNAME'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%IGRBCODE)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%IGRBCODE'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%NTYP)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%NTYP'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%NBIN)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%NBIN'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%IGRIBDIAG) * SIZE (SELF%IGRIBDIAG)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%IGRIBDIAG'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%RDDEPVSEA)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%RDDEPVSEA'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%RDDEPVLIC)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%RDDEPVLIC'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%RSEDIMV)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%RSEDIMV'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%RSCAVIN)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%RSCAVIN'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%RSCAVBCR)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%RSCAVBCR'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%RSCAVBCS)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%RSCAVBCS'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%COPTCLASS) * LEN (SELF%COPTCLASS)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%COPTCLASS'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%CHYGCLASS) * LEN (SELF%CHYGCLASS)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%CHYGCLASS'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%IAEROCV)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%IAEROCV'
ENDIF
