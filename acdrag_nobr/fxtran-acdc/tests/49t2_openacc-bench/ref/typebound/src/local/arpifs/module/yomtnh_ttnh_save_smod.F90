SUBMODULE (YOMTNH) YOMTNH_TTNH_SAVE_SMOD

IMPLICIT NONE

CONTAINS

MODULE SUBROUTINE ACDC_SAVE_TTNH (SELF, KLUN)

IMPLICIT NONE
CLASS (TTNH), INTENT (IN), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
LOGICAL :: LTRAJNH
WRITE (KLUN) SELF%NLENNH95B
LTRAJNH = ALLOCATED (SELF%TRAJNH)
WRITE (KLUN) LTRAJNH
IF (LTRAJNH) THEN
  WRITE (KLUN) LBOUND (SELF%TRAJNH)
  WRITE (KLUN) UBOUND (SELF%TRAJNH)
  WRITE (KLUN) SELF%TRAJNH
ENDIF
END SUBROUTINE

END SUBMODULE
