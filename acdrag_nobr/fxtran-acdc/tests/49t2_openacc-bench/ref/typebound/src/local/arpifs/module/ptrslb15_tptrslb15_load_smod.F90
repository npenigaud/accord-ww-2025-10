MODULE SUBROUTINE ACDC_LOAD_TPTRSLB15 (SELF, KLUN)
USE PARKIND1, ONLY : JPRD
IMPLICIT NONE
CLASS (TPTRSLB15), INTENT (OUT), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
INTEGER :: IL1(1), IU1(1)
LOGICAL :: LRPARSL15
REAL(KIND=JPRD), ALLOCATABLE :: ZTMP1 (:)
READ (KLUN) SELF%NFLDSLB15
READ (KLUN) LRPARSL15
IF (LRPARSL15) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (SELF%RPARSL15 (IL1(1):IU1(1)))
  ALLOCATE (ZTMP1(LBOUND(SELF%RPARSL15,1):UBOUND(SELF%RPARSL15,1)))
  READ (KLUN) ZTMP1
  SELF%RPARSL15 = ZTMP1
  DEALLOCATE (ZTMP1)
ENDIF
READ (KLUN) SELF%MSLBUF15
READ (KLUN) SELF%MSLB1UR05
READ (KLUN) SELF%MSLB1VR05
READ (KLUN) SELF%MSLB1ZR05
READ (KLUN) SELF%MSLB1WR05
READ (KLUN) SELF%MSLB1UR005
READ (KLUN) SELF%MSLB1VR005
READ (KLUN) SELF%MSLB1ZR005
READ (KLUN) SELF%MSLB1WR005
READ (KLUN) SELF%MSLB1UR95
READ (KLUN) SELF%MSLB1VR95
READ (KLUN) SELF%MSLB1ZR95
READ (KLUN) SELF%MSLB1U05
READ (KLUN) SELF%MSLB1V05
READ (KLUN) SELF%MSLB1Z05
READ (KLUN) SELF%MSLB1T05
READ (KLUN) SELF%MSLB1C05
READ (KLUN) SELF%MSLB1SP05
READ (KLUN) SELF%MSLB1U95
READ (KLUN) SELF%MSLB1V95
READ (KLUN) SELF%MSLB1Z95
READ (KLUN) SELF%MSLB1T95
READ (KLUN) SELF%MSLB1GFL95
READ (KLUN) SELF%MSLB1C95
READ (KLUN) SELF%MSLB1UP95
READ (KLUN) SELF%MSLB1VP95
READ (KLUN) SELF%MSLB1ZP95
READ (KLUN) SELF%MSLB1TP95
READ (KLUN) SELF%MSLB1GFLP95
READ (KLUN) SELF%MSLB1SP95
END SUBROUTINE

