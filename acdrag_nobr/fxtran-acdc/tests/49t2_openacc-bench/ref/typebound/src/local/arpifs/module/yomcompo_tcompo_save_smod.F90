SUBMODULE (YOMCOMPO) YOMCOMPO_TCOMPO_SAVE_SMOD

IMPLICIT NONE

CONTAINS

MODULE SUBROUTINE ACDC_SAVE_TCOMPO (SELF, KLUN)

IMPLICIT NONE
CLASS (TCOMPO), INTENT (IN), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
INTEGER :: J1
LOGICAL :: LYEMIS2DAUX_DESC, LYEMIS2D_DESC, LYEMIS3D_DESC
WRITE (KLUN) SELF%LCHEM_DIA
WRITE (KLUN) SELF%RCHEM_DIA_PERIOD
WRITE (KLUN) SELF%LCHEM_DDFLX
WRITE (KLUN) SELF%LCOMPO_DDFLX_DIR
WRITE (KLUN) SELF%LCHEM_TROPO
WRITE (KLUN) SELF%LAEREQSAM4CLIM
WRITE (KLUN) SELF%LAEROSFC
WRITE (KLUN) SELF%LCOMPO_DCDD
WRITE (KLUN) SELF%LAERNITRATE
WRITE (KLUN) SELF%LAERRESUSPENSION
WRITE (KLUN) SELF%LAERSOA
WRITE (KLUN) SELF%LAERSOA_COUPLED
WRITE (KLUN) SELF%KGHG_CHEMTEND_CH4
WRITE (KLUN) SELF%LAERNUCL
WRITE (KLUN) SELF%AERO_SCHEME
WRITE (KLUN) SELF%NEMIS2D_DESC
LYEMIS2D_DESC = ALLOCATED (SELF%YEMIS2D_DESC)
WRITE (KLUN) LYEMIS2D_DESC
IF (LYEMIS2D_DESC) THEN
  WRITE (KLUN) LBOUND (SELF%YEMIS2D_DESC)
  WRITE (KLUN) UBOUND (SELF%YEMIS2D_DESC)
  DO J1 = LBOUND (SELF%YEMIS2D_DESC, 1), UBOUND (SELF%YEMIS2D_DESC, 1)
    CALL SELF%YEMIS2D_DESC (J1)%ACDC_SAVE (KLUN)
  ENDDO
ENDIF
WRITE (KLUN) SELF%NEMIS3D_DESC
LYEMIS3D_DESC = ALLOCATED (SELF%YEMIS3D_DESC)
WRITE (KLUN) LYEMIS3D_DESC
IF (LYEMIS3D_DESC) THEN
  WRITE (KLUN) LBOUND (SELF%YEMIS3D_DESC)
  WRITE (KLUN) UBOUND (SELF%YEMIS3D_DESC)
  DO J1 = LBOUND (SELF%YEMIS3D_DESC, 1), UBOUND (SELF%YEMIS3D_DESC, 1)
    CALL SELF%YEMIS3D_DESC (J1)%ACDC_SAVE (KLUN)
  ENDDO
ENDIF
LYEMIS2DAUX_DESC = ALLOCATED (SELF%YEMIS2DAUX_DESC)
WRITE (KLUN) LYEMIS2DAUX_DESC
IF (LYEMIS2DAUX_DESC) THEN
  WRITE (KLUN) LBOUND (SELF%YEMIS2DAUX_DESC)
  WRITE (KLUN) UBOUND (SELF%YEMIS2DAUX_DESC)
  DO J1 = LBOUND (SELF%YEMIS2DAUX_DESC, 1), UBOUND (SELF%YEMIS2DAUX_DESC, 1)
    CALL SELF%YEMIS2DAUX_DESC (J1)%ACDC_SAVE (KLUN)
  ENDDO
ENDIF
END SUBROUTINE

END SUBMODULE
