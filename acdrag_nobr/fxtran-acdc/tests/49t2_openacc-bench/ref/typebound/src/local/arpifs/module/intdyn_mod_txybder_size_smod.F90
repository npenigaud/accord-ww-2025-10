SUBMODULE (INTDYN_MOD) INTDYN_MOD_TXYBDER_SIZE_SMOD

IMPLICIT NONE

CONTAINS

MODULE FUNCTION ACDC_SIZE_TXYBDER (SELF, CDPATH, LDPRINT) RESULT (KSIZE)

IMPLICIT NONE
CLASS (TXYBDER),     INTENT (IN), TARGET :: SELF
CHARACTER(LEN=*), INTENT (IN), OPTIONAL :: CDPATH
LOGICAL,          INTENT (IN), OPTIONAL :: LDPRINT
INTEGER*8 :: KSIZE
INTEGER*8 :: ISIZE, JSIZE
LOGICAL :: LLPRINT
CHARACTER(LEN=128) :: CLPATH
LLPRINT = .FALSE.
IF (PRESENT (LDPRINT)) THEN
  LLPRINT = LDPRINT
ENDIF
CLPATH=''
IF (PRESENT (CDPATH)) THEN
  CLPATH = CDPATH
ENDIF
KSIZE = 0
ISIZE = KIND (SELF%M_LNPRL)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%M_LNPRL'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%M_LNPRM)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%M_LNPRM'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%M_ALPHL)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%M_ALPHL'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%M_ALPHM)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%M_ALPHM'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%M_ALPHPLL)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%M_ALPHPLL'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%M_ALPHPLM)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%M_ALPHPLM'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%M_COEFD)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%M_COEFD'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%M_COEFA)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%M_COEFA'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%M_COEFAPL)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%M_COEFAPL'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%NDIM)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%NDIM'
ENDIF
KSIZE = KSIZE + ISIZE
END FUNCTION

END SUBMODULE
