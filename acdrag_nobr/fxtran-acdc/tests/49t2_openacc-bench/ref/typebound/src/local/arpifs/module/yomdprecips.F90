MODULE YOMDPRECIPS

!$ACDC methods 


USE PARKIND1  ,ONLY : JPIM     ,JPRB

IMPLICIT NONE

SAVE

TYPE TDPRECIPS
!*
!     ------------------------------------------------------------------
!     CONSTANTES POUR LA DETERMINATION DU TYPE DE PRECIPITATIONS
!     
!       HDPRECIPS   : HAUTEUR DE LA DETERMINATION ( EN GENERAL 2M ).
!                   : HEIGHT ( IN GENERAL 2M ).
!       HDCLWC      : HAUTEUR DU SEUIL DE CLWC.
!                   : HEIGHT OF CLWC THRESHOLD.
!       RDHAIL1     : PREMIER SEUIL POUR LA GRELE / HAIL 
!                   : FIRST THRESHOLD 
!       RDHAIL2     : DEUXIEME SEUIL POUR LA GRELE / HAIL 
!                   : SECOND THRESHOLD 
!       RDSEUIL1    : PREMIER SEUIL POUR LE RATIO PLUIE/PRECIPS
!                   : FIRST THRESHOLD 
!       RDSEUIL2    : DEUXIEME SEUIL POUR LE RATIO PLUIE/PRECIPS
!                   : SECOND THRESHOLD 
!       RDSEUIL3    : TROISIEME SEUIL POUR LE RATIO PLUIE/PRECIPS
!                   : THIRD THRESHOLD 
!       RDSEUIL4    : QUATRIEME SEUIL POUR LE RATIO PLUIE/PRECIPS
!                   : FOURTH THRESHOLD 
!       RDSEUIL5    : CINQUIEME SEUIL POUR LE RATIO PLUIE/PRECIPS
!                   : FIFTH THRESHOLD 
!       RDCLWC      : SEUIL POUR LE CLWC
!                   : THRESHOLD OF CLWC 
!       RPRECSEUIL  : SEUIL DE PRESENCE DE PRECIPS
!                   : THRESHOLD  OF PRECIPITATION
!       RHTOP       : SEUIL DE HAUTEUR AU DESSUS DU SOL
!                   : H THRESHOLD ABOGE GROUND
!       RTPW        : T'W THRESHOLD
!       RAWARM      : AREA WITH T > 0 IN VERTICAL PROFILES USED FOR FREEZING
!       RAWARM2       RAIN 
!
!       RACOLD      : AREA WITH T < 0 IN VERTICAL PROFILES USED FOR FREEZING
!       RACOLD2       RAIN 
!
!       NDTPREC     : NB OF TIME STEPS USED FOR DPRECIPS


REAL(KIND=JPRB) :: HDPRECIPS
REAL(KIND=JPRB) :: HDCLWC
REAL(KIND=JPRB) :: RDHAIL1
REAL(KIND=JPRB) :: RDHAIL2
REAL(KIND=JPRB) :: RDSEUIL1
REAL(KIND=JPRB) :: RDSEUIL2
REAL(KIND=JPRB) :: RDSEUIL3
REAL(KIND=JPRB) :: RDSEUIL4
REAL(KIND=JPRB) :: RDSEUIL5
REAL(KIND=JPRB) :: RDCLWC
REAL(KIND=JPRB) :: RPRECSEUIL
REAL(KIND=JPRB) :: RHTOP
REAL(KIND=JPRB) :: RTPW
REAL(KIND=JPRB) :: RAWARM
REAL(KIND=JPRB) :: RAWARM2
REAL(KIND=JPRB) :: RACOLD
REAL(KIND=JPRB) :: RACOLD2
INTEGER(KIND=JPIM) :: NDTPREC  = 0
INTEGER(KIND=JPIM) :: NDTPREC2 = 0

CONTAINS
PROCEDURE :: ACDC_COPY => ACDC_COPY_TDPRECIPS
PROCEDURE :: ACDC_CRC64 => ACDC_CRC64_TDPRECIPS
PROCEDURE :: ACDC_HOST => ACDC_HOST_TDPRECIPS
PROCEDURE :: ACDC_LOAD => ACDC_LOAD_TDPRECIPS
PROCEDURE :: ACDC_SAVE => ACDC_SAVE_TDPRECIPS
PROCEDURE :: ACDC_SIZE => ACDC_SIZE_TDPRECIPS
PROCEDURE :: ACDC_WIPE => ACDC_WIPE_TDPRECIPS
END TYPE TDPRECIPS

!     ------------------------------------------------------------------
INTERFACE

MODULE SUBROUTINE ACDC_COPY_TDPRECIPS (SELF, LDCREATED, LDFIELDAPI)

IMPLICIT NONE
CLASS (TDPRECIPS), INTENT (IN), TARGET :: SELF
LOGICAL, OPTIONAL, INTENT (IN) :: LDCREATED, LDFIELDAPI
END SUBROUTINE

MODULE SUBROUTINE ACDC_CRC64_TDPRECIPS (SELF, KLUN, CDPATH)
USE FXTRAN_ACDC_CRC64_INTRINSIC_MOD, ONLY : FCRC64 => FXTRAN_ACDC_CRC64
IMPLICIT NONE
CLASS (TDPRECIPS), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
CHARACTER(LEN=*), INTENT (IN) :: CDPATH
END SUBROUTINE

MODULE SUBROUTINE ACDC_HOST_TDPRECIPS (SELF)

IMPLICIT NONE
CLASS (TDPRECIPS), TARGET :: SELF
END SUBROUTINE

MODULE SUBROUTINE ACDC_LOAD_TDPRECIPS (SELF, KLUN)
USE PARKIND1, ONLY : JPRD
IMPLICIT NONE
CLASS (TDPRECIPS), INTENT (OUT), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
END SUBROUTINE

MODULE SUBROUTINE ACDC_SAVE_TDPRECIPS (SELF, KLUN)

IMPLICIT NONE
CLASS (TDPRECIPS), INTENT (IN), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
END SUBROUTINE

MODULE FUNCTION ACDC_SIZE_TDPRECIPS (SELF, CDPATH, LDPRINT) RESULT (KSIZE)

IMPLICIT NONE
CLASS (TDPRECIPS),     INTENT (IN), TARGET :: SELF
CHARACTER(LEN=*), INTENT (IN), OPTIONAL :: CDPATH
LOGICAL,          INTENT (IN), OPTIONAL :: LDPRINT
INTEGER*8 :: KSIZE
END FUNCTION

MODULE SUBROUTINE ACDC_WIPE_TDPRECIPS (SELF, LDDELETED, LDFIELDAPI)

IMPLICIT NONE
CLASS (TDPRECIPS), INTENT (IN), TARGET :: SELF
LOGICAL, OPTIONAL, INTENT (IN) :: LDDELETED, LDFIELDAPI
END SUBROUTINE


END INTERFACE

END MODULE YOMDPRECIPS
