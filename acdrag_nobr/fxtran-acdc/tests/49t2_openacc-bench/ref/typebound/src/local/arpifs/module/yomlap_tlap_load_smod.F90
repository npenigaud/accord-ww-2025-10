SUBMODULE (YOMLAP) YOMLAP_TLAP_LOAD_SMOD

IMPLICIT NONE

CONTAINS

MODULE SUBROUTINE ACDC_LOAD_TLAP (SELF, KLUN)
USE PARKIND1, ONLY : JPRD
IMPLICIT NONE
CLASS (TLAP), INTENT (OUT), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
INTEGER :: IL1(1), IU1(1)
LOGICAL :: LMYMS, LNASM0, LNASM0G, LNASN0, LNSE0L, LNSPZERO, LNVALUE, LRLAPDI, LRLAPIN
REAL(KIND=JPRD), ALLOCATABLE :: ZTMP1 (:)
READ (KLUN) LNASN0
IF (LNASN0) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (SELF%NASN0 (IL1(1):IU1(1)))
  READ (KLUN) SELF%NASN0
ENDIF
READ (KLUN) LNASM0
IF (LNASM0) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (SELF%NASM0 (IL1(1):IU1(1)))
  READ (KLUN) SELF%NASM0
ENDIF
READ (KLUN) LNASM0G
IF (LNASM0G) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (SELF%NASM0G (IL1(1):IU1(1)))
  READ (KLUN) SELF%NASM0G
ENDIF
READ (KLUN) LNVALUE
IF (LNVALUE) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (SELF%NVALUE (IL1(1):IU1(1)))
  READ (KLUN) SELF%NVALUE
ENDIF
READ (KLUN) LMYMS
IF (LMYMS) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (SELF%MYMS (IL1(1):IU1(1)))
  READ (KLUN) SELF%MYMS
ENDIF
READ (KLUN) LNSPZERO
IF (LNSPZERO) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (SELF%NSPZERO (IL1(1):IU1(1)))
  READ (KLUN) SELF%NSPZERO
ENDIF
READ (KLUN) LNSE0L
IF (LNSE0L) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (SELF%NSE0L (IL1(1):IU1(1)))
  READ (KLUN) SELF%NSE0L
ENDIF
READ (KLUN) LRLAPDI
IF (LRLAPDI) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (SELF%RLAPDI (IL1(1):IU1(1)))
  ALLOCATE (ZTMP1(LBOUND(SELF%RLAPDI,1):UBOUND(SELF%RLAPDI,1)))
  READ (KLUN) ZTMP1
  SELF%RLAPDI = ZTMP1
  DEALLOCATE (ZTMP1)
ENDIF
READ (KLUN) LRLAPIN
IF (LRLAPIN) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (SELF%RLAPIN (IL1(1):IU1(1)))
  ALLOCATE (ZTMP1(LBOUND(SELF%RLAPIN,1):UBOUND(SELF%RLAPIN,1)))
  READ (KLUN) ZTMP1
  SELF%RLAPIN = ZTMP1
  DEALLOCATE (ZTMP1)
ENDIF
END SUBROUTINE

END SUBMODULE
