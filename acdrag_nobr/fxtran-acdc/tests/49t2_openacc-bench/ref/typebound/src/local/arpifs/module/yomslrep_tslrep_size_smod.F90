SUBMODULE (YOMSLREP) YOMSLREP_TSLREP_SIZE_SMOD

IMPLICIT NONE

CONTAINS

MODULE FUNCTION ACDC_SIZE_TSLREP (SELF, CDPATH, LDPRINT) RESULT (KSIZE)

IMPLICIT NONE
CLASS (TSLREP),     INTENT (IN), TARGET :: SELF
CHARACTER(LEN=*), INTENT (IN), OPTIONAL :: CDPATH
LOGICAL,          INTENT (IN), OPTIONAL :: LDPRINT
INTEGER*8 :: KSIZE
INTEGER*8 :: ISIZE, JSIZE
LOGICAL :: LLPRINT
CHARACTER(LEN=128) :: CLPATH
LOGICAL :: LLADCORE, LNADCORE, LNADMAP, LRSASIGN
LLPRINT = .FALSE.
IF (PRESENT (LDPRINT)) THEN
  LLPRINT = LDPRINT
ENDIF
CLPATH=''
IF (PRESENT (CDPATH)) THEN
  CLPATH = CDPATH
ENDIF
KSIZE = 0
ISIZE = KIND (SELF%NGPTOTAD)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%NGPTOTAD'
ENDIF
KSIZE = KSIZE + ISIZE
LNADMAP = ALLOCATED (SELF%NADMAP)
IF (LNADMAP) THEN
  ISIZE = KIND (SELF%NADMAP) * SIZE (SELF%NADMAP)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%NADMAP'
  ENDIF
  KSIZE = KSIZE + ISIZE
ENDIF
LNADCORE = ALLOCATED (SELF%NADCORE)
IF (LNADCORE) THEN
  ISIZE = KIND (SELF%NADCORE) * SIZE (SELF%NADCORE)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%NADCORE'
  ENDIF
  KSIZE = KSIZE + ISIZE
ENDIF
LLADCORE = ALLOCATED (SELF%LADCORE)
IF (LLADCORE) THEN
  ISIZE = KIND (SELF%LADCORE) * SIZE (SELF%LADCORE)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%LADCORE'
  ENDIF
  KSIZE = KSIZE + ISIZE
ENDIF
LRSASIGN = ALLOCATED (SELF%RSASIGN)
IF (LRSASIGN) THEN
  ISIZE = KIND (SELF%RSASIGN) * SIZE (SELF%RSASIGN)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%RSASIGN'
  ENDIF
  KSIZE = KSIZE + ISIZE
ENDIF
END FUNCTION

END SUBMODULE
