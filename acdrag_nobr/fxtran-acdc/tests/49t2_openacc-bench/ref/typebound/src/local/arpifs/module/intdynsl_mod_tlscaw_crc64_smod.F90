SUBMODULE (INTDYNSL_MOD) INTDYNSL_MOD_TLSCAW_CRC64_SMOD

IMPLICIT NONE

CONTAINS

MODULE SUBROUTINE ACDC_CRC64_TLSCAW (SELF, KLUN, CDPATH)
USE FXTRAN_ACDC_CRC64_INTRINSIC_MOD, ONLY : FCRC64 => FXTRAN_ACDC_CRC64
IMPLICIT NONE
CLASS (TLSCAW), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
CHARACTER(LEN=*), INTENT (IN) :: CDPATH
CHARACTER(LEN=128) :: CLIND
WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (SELF%M_WDLO), CDPATH//'%M_WDLO'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (SELF%M_WDLAT), CDPATH//'%M_WDLAT'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (SELF%M_WDVER), CDPATH//'%M_WDVER'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (SELF%M_WDLOMAD), CDPATH//'%M_WDLOMAD'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (SELF%M_WDLAMAD), CDPATH//'%M_WDLAMAD'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (SELF%M_WDVERMAD), CDPATH//'%M_WDVERMAD'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (SELF%NDIM), CDPATH//'%NDIM'

END SUBROUTINE

END SUBMODULE
