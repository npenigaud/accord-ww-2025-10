SUBMODULE (YOMHSLMER) YOMHSLMER_THSLMER_SAVE_SMOD

IMPLICIT NONE

CONTAINS

MODULE SUBROUTINE ACDC_SAVE_THSLMER (SELF, KLUN)

IMPLICIT NONE
CLASS (THSLMER), INTENT (IN), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
LOGICAL :: LR3DTW, LRIPI, LRSLD, LRSLDW
LRIPI = ALLOCATED (SELF%RIPI)
WRITE (KLUN) LRIPI
IF (LRIPI) THEN
  WRITE (KLUN) LBOUND (SELF%RIPI)
  WRITE (KLUN) UBOUND (SELF%RIPI)
  WRITE (KLUN) SELF%RIPI
ENDIF
LRSLD = ALLOCATED (SELF%RSLD)
WRITE (KLUN) LRSLD
IF (LRSLD) THEN
  WRITE (KLUN) LBOUND (SELF%RSLD)
  WRITE (KLUN) UBOUND (SELF%RSLD)
  WRITE (KLUN) SELF%RSLD
ENDIF
LRSLDW = ALLOCATED (SELF%RSLDW)
WRITE (KLUN) LRSLDW
IF (LRSLDW) THEN
  WRITE (KLUN) LBOUND (SELF%RSLDW)
  WRITE (KLUN) UBOUND (SELF%RSLDW)
  WRITE (KLUN) SELF%RSLDW
ENDIF
LR3DTW = ALLOCATED (SELF%R3DTW)
WRITE (KLUN) LR3DTW
IF (LR3DTW) THEN
  WRITE (KLUN) LBOUND (SELF%R3DTW)
  WRITE (KLUN) UBOUND (SELF%R3DTW)
  WRITE (KLUN) SELF%R3DTW
ENDIF
END SUBROUTINE

END SUBMODULE
