SUBMODULE (MODEL_PHYSICS_SIMPLINEAR_MOD) MODEL_PHYSICS_SIMPLINEAR_MOD_MODEL_PHYSICS_SIMPLINEAR_TYPE_LOAD_SMOD

IMPLICIT NONE

CONTAINS

MODULE SUBROUTINE ACDC_LOAD_MODEL_PHYSICS_SIMPLINEAR_TYPE (SELF, KLUN)

IMPLICIT NONE
CLASS (MODEL_PHYSICS_SIMPLINEAR_TYPE), INTENT (OUT), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
INTEGER :: J1
INTEGER :: IL1(1), IU1(1)
LOGICAL :: LGPHIST
CALL SELF%YREPHLI%ACDC_LOAD (KLUN)
CALL SELF%YRCUMFS%ACDC_LOAD (KLUN)
CALL SELF%YREGWDWMS%ACDC_LOAD (KLUN)
CALL SELF%YRECUMF2%ACDC_LOAD (KLUN)
CALL SELF%YRPHLC%ACDC_LOAD (KLUN)
CALL SELF%YRPHNC%ACDC_LOAD (KLUN)
CALL SELF%YRNCL%ACDC_LOAD (KLUN)
CALL SELF%YRSRFTLAD%ACDC_LOAD (KLUN)
READ (KLUN) LGPHIST
IF (LGPHIST) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (SELF%GPHIST (IL1(1):IU1(1)))
  DO J1 = LBOUND (SELF%GPHIST, 1), UBOUND (SELF%GPHIST, 1)
    CALL SELF%GPHIST (J1)%ACDC_LOAD (KLUN)
  ENDDO
ELSE
  NULLIFY (SELF%GPHIST)
ENDIF
END SUBROUTINE

END SUBMODULE
