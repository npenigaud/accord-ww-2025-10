SUBMODULE (YOMTDDH) YOMTDDH_TTDDH_SIZE_SMOD

IMPLICIT NONE

CONTAINS

MODULE FUNCTION ACDC_SIZE_TTDDH (SELF, CDPATH, LDPRINT) RESULT (KSIZE)

IMPLICIT NONE
CLASS (TTDDH),     INTENT (IN), TARGET :: SELF
CHARACTER(LEN=*), INTENT (IN), OPTIONAL :: CDPATH
LOGICAL,          INTENT (IN), OPTIONAL :: LDPRINT
INTEGER*8 :: KSIZE
INTEGER*8 :: ISIZE, JSIZE
LOGICAL :: LLPRINT
CHARACTER(LEN=128) :: CLPATH
LOGICAL :: LCFLDNAMES2D, LCFLDNAMES3D, LCFLDTYPES2D, LCFLDTYPES3D, LHDCS0, LHDCS1, LHDCVB0, LHDCVB1, LPDDHFSVI
LLPRINT = .FALSE.
IF (PRESENT (LDPRINT)) THEN
  LLPRINT = LDPRINT
ENDIF
CLPATH=''
IF (PRESENT (CDPATH)) THEN
  CLPATH = CDPATH
ENDIF
KSIZE = 0
LHDCVB0 = ALLOCATED (SELF%HDCVB0)
IF (LHDCVB0) THEN
  ISIZE = KIND (SELF%HDCVB0) * SIZE (SELF%HDCVB0)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%HDCVB0'
  ENDIF
  KSIZE = KSIZE + ISIZE
ENDIF
LHDCVB1 = ALLOCATED (SELF%HDCVB1)
IF (LHDCVB1) THEN
  ISIZE = KIND (SELF%HDCVB1) * SIZE (SELF%HDCVB1)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%HDCVB1'
  ENDIF
  KSIZE = KSIZE + ISIZE
ENDIF
LHDCS0 = ALLOCATED (SELF%HDCS0)
IF (LHDCS0) THEN
  ISIZE = KIND (SELF%HDCS0) * SIZE (SELF%HDCS0)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%HDCS0'
  ENDIF
  KSIZE = KSIZE + ISIZE
ENDIF
LHDCS1 = ALLOCATED (SELF%HDCS1)
IF (LHDCS1) THEN
  ISIZE = KIND (SELF%HDCS1) * SIZE (SELF%HDCS1)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%HDCS1'
  ENDIF
  KSIZE = KSIZE + ISIZE
ENDIF
LPDDHFSVI = ALLOCATED (SELF%PDDHFSVI)
IF (LPDDHFSVI) THEN
  ISIZE = KIND (SELF%PDDHFSVI) * SIZE (SELF%PDDHFSVI)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%PDDHFSVI'
  ENDIF
  KSIZE = KSIZE + ISIZE
ENDIF
LCFLDNAMES3D = ALLOCATED (SELF%CFLDNAMES3D)
IF (LCFLDNAMES3D) THEN
  ISIZE = KIND (SELF%CFLDNAMES3D) * SIZE (SELF%CFLDNAMES3D) * LEN (SELF%CFLDNAMES3D)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%CFLDNAMES3D'
  ENDIF
  KSIZE = KSIZE + ISIZE
ENDIF
LCFLDTYPES3D = ALLOCATED (SELF%CFLDTYPES3D)
IF (LCFLDTYPES3D) THEN
  ISIZE = KIND (SELF%CFLDTYPES3D) * SIZE (SELF%CFLDTYPES3D) * LEN (SELF%CFLDTYPES3D)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%CFLDTYPES3D'
  ENDIF
  KSIZE = KSIZE + ISIZE
ENDIF
LCFLDNAMES2D = ALLOCATED (SELF%CFLDNAMES2D)
IF (LCFLDNAMES2D) THEN
  ISIZE = KIND (SELF%CFLDNAMES2D) * SIZE (SELF%CFLDNAMES2D) * LEN (SELF%CFLDNAMES2D)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%CFLDNAMES2D'
  ENDIF
  KSIZE = KSIZE + ISIZE
ENDIF
LCFLDTYPES2D = ALLOCATED (SELF%CFLDTYPES2D)
IF (LCFLDTYPES2D) THEN
  ISIZE = KIND (SELF%CFLDTYPES2D) * SIZE (SELF%CFLDTYPES2D) * LEN (SELF%CFLDTYPES2D)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%CFLDTYPES2D'
  ENDIF
  KSIZE = KSIZE + ISIZE
ENDIF
END FUNCTION

END SUBMODULE
