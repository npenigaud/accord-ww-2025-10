SUBMODULE (YOMDIMF) YOMDIMF_TDIMF_COPY_SMOD

IMPLICIT NONE

CONTAINS

MODULE SUBROUTINE ACDC_COPY_TDIMF (SELF, LDCREATED, LDFIELDAPI)

IMPLICIT NONE
CLASS (TDIMF), INTENT (IN), TARGET :: SELF
LOGICAL, OPTIONAL, INTENT (IN) :: LDCREATED, LDFIELDAPI
LOGICAL :: LLCREATED
LOGICAL :: LLFIELDAPI
LOGICAL :: LNGRBSP2, LNGRBSP3
LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF
LLCREATED = .FALSE.
IF (PRESENT (LDCREATED)) THEN
  LLCREATED = LDCREATED
ENDIF
IF (.NOT. LLCREATED) THEN
  !$ACC ENTER DATA CREATE (SELF)
  !$ACC UPDATE DEVICE (SELF)
ENDIF

LNGRBSP3 = ALLOCATED (SELF%NGRBSP3)
IF (LNGRBSP3) THEN
  !$ACC ENTER DATA CREATE (SELF%NGRBSP3)
  !$ACC UPDATE DEVICE (SELF%NGRBSP3)
  !$ACC ENTER DATA ATTACH (SELF%NGRBSP3)
ENDIF

LNGRBSP2 = ALLOCATED (SELF%NGRBSP2)
IF (LNGRBSP2) THEN
  !$ACC ENTER DATA CREATE (SELF%NGRBSP2)
  !$ACC UPDATE DEVICE (SELF%NGRBSP2)
  !$ACC ENTER DATA ATTACH (SELF%NGRBSP2)
ENDIF

END SUBROUTINE

END SUBMODULE
