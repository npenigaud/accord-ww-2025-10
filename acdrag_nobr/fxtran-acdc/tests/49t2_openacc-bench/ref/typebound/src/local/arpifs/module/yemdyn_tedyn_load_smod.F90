IF (LREFILD) THEN
  READ (KLUN) IL2
  READ (KLUN) IU2
  ALLOCATE (SELF%REFILD (IL2(1):IU2(1), IL2(2):IU2(2)))
  ALLOCATE (ZTMP2(LBOUND(SELF%REFILD,1):UBOUND(SELF%REFILD,1), LBOUND(SELF%REFILD,2):UBOUND(SELF%REFILD,2)))
  READ (KLUN) ZTMP2
  SELF%REFILD = ZTMP2
  DEALLOCATE (ZTMP2)
