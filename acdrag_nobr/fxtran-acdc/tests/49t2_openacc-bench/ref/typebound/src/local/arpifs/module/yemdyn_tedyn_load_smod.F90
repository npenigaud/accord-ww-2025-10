  READ (KLUN) IL2
  READ (KLUN) IU2
  ALLOCATE (SELF%REFILV (IL2(1):IU2(1), IL2(2):IU2(2)))
  ALLOCATE (ZTMP2(LBOUND(SELF%REFILV,1):UBOUND(SELF%REFILV,1), LBOUND(SELF%REFILV,2):UBOUND(SELF%REFILV,2)))
  READ (KLUN) ZTMP2
  SELF%REFILV = ZTMP2
  DEALLOCATE (ZTMP2)
ENDIF
