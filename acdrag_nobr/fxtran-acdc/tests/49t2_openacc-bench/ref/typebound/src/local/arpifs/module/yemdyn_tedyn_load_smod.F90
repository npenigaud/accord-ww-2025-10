  READ (KLUN) IL2
  READ (KLUN) IU2
  ALLOCATE (SELF%RDSVDE (IL2(1):IU2(1), IL2(2):IU2(2)))
  ALLOCATE (ZTMP2(LBOUND(SELF%RDSVDE,1):UBOUND(SELF%RDSVDE,1), LBOUND(SELF%RDSVDE,2):UBOUND(SELF%RDSVDE,2)))
  READ (KLUN) ZTMP2
  SELF%RDSVDE = ZTMP2
  DEALLOCATE (ZTMP2)
