READ (KLUN) SELF%LESIDG
READ (KLUN) ZTMP0
SELF%RTHRESIDG = ZTMP0
READ (KLUN) ZTMP0
SELF%XMALD = ZTMP0
READ (KLUN) ZTMP0
SELF%TCDIS = ZTMP0
END SUBROUTINE

