IF (LRDIDIVE) THEN
  READ (KLUN) IL2
  READ (KLUN) IU2
  ALLOCATE (SELF%RDIDIVE (IL2(1):IU2(1), IL2(2):IU2(2)))
  ALLOCATE (ZTMP2(LBOUND(SELF%RDIDIVE,1):UBOUND(SELF%RDIDIVE,1), LBOUND(SELF%RDIDIVE,2):UBOUND(SELF%RDIDIVE,2)))
