MODULE YOMCOU

!$ACDC methods 


USE PARKIND1  ,ONLY : JPIM

IMPLICIT NONE

SAVE

!     ------------------------------------------------------------

!*    INFORMATION FOR USE WITH OASIS COUPLER

! NPIAT      PROCESS IDENTIFIER OF THE ATMOSPHERE RUN
! NCULMR     C UNIT IDENTIFIER FOR OASIS MODEL READ PIPE
! NCULMW     C UNIT IDENTIFIER FOR OASIS MODEL WRITE PIPE
! NCULF(20)  C UNIT IDENTIFIERS FOR OASIS MODEL FIELD PIPES

TYPE :: TCOU
INTEGER(KIND=JPIM) :: NCULF(0:20)
INTEGER(KIND=JPIM) :: NPIAT
INTEGER(KIND=JPIM) :: NCULMR
INTEGER(KIND=JPIM) :: NCULMW
 !---------------------------------------------------------------------
CONTAINS
  PROCEDURE, PASS :: PRINT => PRINT_CONFIGURATION 
PROCEDURE :: ACDC_COPY => ACDC_COPY_TCOU
PROCEDURE :: ACDC_CRC64 => ACDC_CRC64_TCOU
PROCEDURE :: ACDC_HOST => ACDC_HOST_TCOU
PROCEDURE :: ACDC_LOAD => ACDC_LOAD_TCOU
PROCEDURE :: ACDC_SAVE => ACDC_SAVE_TCOU
PROCEDURE :: ACDC_SIZE => ACDC_SIZE_TCOU
PROCEDURE :: ACDC_WIPE => ACDC_WIPE_TCOU
END TYPE  TCOU
!======================================================================

!!TYPE(TCOU), POINTER :: YRCOU => NULL()

INTERFACE

MODULE SUBROUTINE ACDC_COPY_TCOU (SELF, LDCREATED, LDFIELDAPI)

IMPLICIT NONE
CLASS (TCOU), INTENT (IN), TARGET :: SELF
LOGICAL, OPTIONAL, INTENT (IN) :: LDCREATED, LDFIELDAPI
END SUBROUTINE

MODULE SUBROUTINE ACDC_CRC64_TCOU (SELF, KLUN, CDPATH)
USE FXTRAN_ACDC_CRC64_INTRINSIC_MOD, ONLY : FCRC64 => FXTRAN_ACDC_CRC64
IMPLICIT NONE
CLASS (TCOU), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
CHARACTER(LEN=*), INTENT (IN) :: CDPATH
END SUBROUTINE

MODULE SUBROUTINE ACDC_HOST_TCOU (SELF)

IMPLICIT NONE
CLASS (TCOU), TARGET :: SELF
END SUBROUTINE

MODULE SUBROUTINE ACDC_LOAD_TCOU (SELF, KLUN)

IMPLICIT NONE
CLASS (TCOU), INTENT (OUT), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
END SUBROUTINE

MODULE SUBROUTINE ACDC_SAVE_TCOU (SELF, KLUN)

IMPLICIT NONE
CLASS (TCOU), INTENT (IN), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
END SUBROUTINE

MODULE FUNCTION ACDC_SIZE_TCOU (SELF, CDPATH, LDPRINT) RESULT (KSIZE)

IMPLICIT NONE
CLASS (TCOU),     INTENT (IN), TARGET :: SELF
CHARACTER(LEN=*), INTENT (IN), OPTIONAL :: CDPATH
LOGICAL,          INTENT (IN), OPTIONAL :: LDPRINT
INTEGER*8 :: KSIZE
END FUNCTION

MODULE SUBROUTINE ACDC_WIPE_TCOU (SELF, LDDELETED, LDFIELDAPI)

IMPLICIT NONE
CLASS (TCOU), INTENT (IN), TARGET :: SELF
LOGICAL, OPTIONAL, INTENT (IN) :: LDDELETED, LDFIELDAPI
END SUBROUTINE


END INTERFACE

CONTAINS 
  
SUBROUTINE PRINT_CONFIGURATION(SELF, KDEPTH, KOUTNO)
IMPLICIT NONE
CLASS(TCOU), INTENT(IN) :: SELF
INTEGER    , INTENT(IN) :: KDEPTH
INTEGER    , INTENT(IN) :: KOUTNO

INTEGER :: IDEPTHLOC 

IDEPTHLOC = KDEPTH + 2

WRITE(KOUTNO,*) REPEAT(' ',KDEPTH) // 'model%yrml_aoc%yrcou : '
WRITE(KOUTNO,*) REPEAT(' ',IDEPTHLOC) // 'NCULF sum = ', SUM(SELF%NCULF)
WRITE(KOUTNO,*) REPEAT(' ',IDEPTHLOC) // 'NPIAT = ', SELF%NPIAT
WRITE(KOUTNO,*) REPEAT(' ',IDEPTHLOC) // 'NCULMR = ', SELF%NCULMR
WRITE(KOUTNO,*) REPEAT(' ',IDEPTHLOC) // 'NCULMW = ', SELF%NCULMW

END SUBROUTINE PRINT_CONFIGURATION

END MODULE YOMCOU
