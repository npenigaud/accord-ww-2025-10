SUBMODULE (YOMSLREP) YOMSLREP_TSLREP_SAVE_SMOD

IMPLICIT NONE

CONTAINS

MODULE SUBROUTINE ACDC_SAVE_TSLREP (SELF, KLUN)

IMPLICIT NONE
CLASS (TSLREP), INTENT (IN), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
LOGICAL :: LLADCORE, LNADCORE, LNADMAP, LRSASIGN
WRITE (KLUN) SELF%NGPTOTAD
LNADMAP = ALLOCATED (SELF%NADMAP)
WRITE (KLUN) LNADMAP
IF (LNADMAP) THEN
  WRITE (KLUN) LBOUND (SELF%NADMAP)
  WRITE (KLUN) UBOUND (SELF%NADMAP)
  WRITE (KLUN) SELF%NADMAP
ENDIF
LNADCORE = ALLOCATED (SELF%NADCORE)
WRITE (KLUN) LNADCORE
IF (LNADCORE) THEN
  WRITE (KLUN) LBOUND (SELF%NADCORE)
  WRITE (KLUN) UBOUND (SELF%NADCORE)
  WRITE (KLUN) SELF%NADCORE
ENDIF
LLADCORE = ALLOCATED (SELF%LADCORE)
WRITE (KLUN) LLADCORE
IF (LLADCORE) THEN
  WRITE (KLUN) LBOUND (SELF%LADCORE)
  WRITE (KLUN) UBOUND (SELF%LADCORE)
  WRITE (KLUN) SELF%LADCORE
ENDIF
LRSASIGN = ALLOCATED (SELF%RSASIGN)
WRITE (KLUN) LRSASIGN
IF (LRSASIGN) THEN
  WRITE (KLUN) LBOUND (SELF%RSASIGN)
  WRITE (KLUN) UBOUND (SELF%RSASIGN)
  WRITE (KLUN) SELF%RSASIGN
ENDIF
END SUBROUTINE

END SUBMODULE
