SUBMODULE (YEMLBC_GEO) YEMLBC_GEO_TELBC_GEO_LOAD_SMOD

IMPLICIT NONE

CONTAINS

MODULE SUBROUTINE ACDC_LOAD_TELBC_GEO (SELF, KLUN)

IMPLICIT NONE
CLASS (TELBC_GEO), INTENT (OUT), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
INTEGER :: IL1(1), IU1(1), IL2(2), IU2(2)
LOGICAL :: LMPTRCPLBLK, LNIND_LEN, LNIND_LIST
READ (KLUN) LNIND_LIST
IF (LNIND_LIST) THEN
  READ (KLUN) IL2
  READ (KLUN) IU2
  ALLOCATE (SELF%NIND_LIST (IL2(1):IU2(1), IL2(2):IU2(2)))
  READ (KLUN) SELF%NIND_LIST
ENDIF
READ (KLUN) LNIND_LEN
IF (LNIND_LEN) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (SELF%NIND_LEN (IL1(1):IU1(1)))
  READ (KLUN) SELF%NIND_LEN
ENDIF
READ (KLUN) SELF%NCPLBLKS
READ (KLUN) LMPTRCPLBLK
IF (LMPTRCPLBLK) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (SELF%MPTRCPLBLK (IL1(1):IU1(1)))
  READ (KLUN) SELF%MPTRCPLBLK
ENDIF
END SUBROUTINE

END SUBMODULE
