MODULE YOMHSLMER

!$ACDC methods 


USE PARKIND1 , ONLY : JPRB, JPRD
IMPLICIT NONE

SAVE

! * Horizontal geometry-dependent intermediate quantities used to
!   compute horizontal weights in the interpolator.
! RIPI        : bi-cubic interpolation coefficients: RIPI(.,j)=RIPI[j-1](.)
! RSLD        : auxiliary quantity for SLHD interpolation weights: RSLD(.,j)=RSLD[j](.)
! RSLDW       : weights for SLHD  Laplacian smoother in latitude
! R3DTW       : weights for 3D turb. Laplacian smoother in latitude

TYPE THSLMER
REAL(KIND=JPRD),ALLOCATABLE:: RIPI(:,:)
REAL(KIND=JPRB),ALLOCATABLE:: RSLD(:,:)
REAL(KIND=JPRB),ALLOCATABLE:: RSLDW(:,:,:)
REAL(KIND=JPRB),ALLOCATABLE:: R3DTW(:,:,:)
CONTAINS
PROCEDURE :: ACDC_COPY => ACDC_COPY_THSLMER
PROCEDURE :: ACDC_CRC64 => ACDC_CRC64_THSLMER
PROCEDURE :: ACDC_HOST => ACDC_HOST_THSLMER
PROCEDURE :: ACDC_LOAD => ACDC_LOAD_THSLMER
PROCEDURE :: ACDC_SAVE => ACDC_SAVE_THSLMER
PROCEDURE :: ACDC_SIZE => ACDC_SIZE_THSLMER
PROCEDURE :: ACDC_WIPE => ACDC_WIPE_THSLMER
END TYPE THSLMER

!!TYPE(THSLMER), POINTER :: YRHSLMER => NULL()

INTERFACE

MODULE SUBROUTINE ACDC_COPY_THSLMER (SELF, LDCREATED, LDFIELDAPI)

IMPLICIT NONE
CLASS (THSLMER), INTENT (IN), TARGET :: SELF
LOGICAL, OPTIONAL, INTENT (IN) :: LDCREATED, LDFIELDAPI
END SUBROUTINE

MODULE SUBROUTINE ACDC_CRC64_THSLMER (SELF, KLUN, CDPATH)
USE FXTRAN_ACDC_CRC64_INTRINSIC_MOD, ONLY : FCRC64 => FXTRAN_ACDC_CRC64
IMPLICIT NONE
CLASS (THSLMER), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
CHARACTER(LEN=*), INTENT (IN) :: CDPATH
END SUBROUTINE

MODULE SUBROUTINE ACDC_HOST_THSLMER (SELF)

IMPLICIT NONE
CLASS (THSLMER), TARGET :: SELF
END SUBROUTINE

MODULE SUBROUTINE ACDC_LOAD_THSLMER (SELF, KLUN)
USE PARKIND1, ONLY : JPRD
IMPLICIT NONE
CLASS (THSLMER), INTENT (OUT), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
END SUBROUTINE

MODULE SUBROUTINE ACDC_SAVE_THSLMER (SELF, KLUN)

IMPLICIT NONE
CLASS (THSLMER), INTENT (IN), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
END SUBROUTINE

MODULE FUNCTION ACDC_SIZE_THSLMER (SELF, CDPATH, LDPRINT) RESULT (KSIZE)

IMPLICIT NONE
CLASS (THSLMER),     INTENT (IN), TARGET :: SELF
CHARACTER(LEN=*), INTENT (IN), OPTIONAL :: CDPATH
LOGICAL,          INTENT (IN), OPTIONAL :: LDPRINT
INTEGER*8 :: KSIZE
END FUNCTION

MODULE SUBROUTINE ACDC_WIPE_THSLMER (SELF, LDDELETED, LDFIELDAPI)

IMPLICIT NONE
CLASS (THSLMER), INTENT (IN), TARGET :: SELF
LOGICAL, OPTIONAL, INTENT (IN) :: LDDELETED, LDFIELDAPI
END SUBROUTINE


END INTERFACE

END MODULE YOMHSLMER
