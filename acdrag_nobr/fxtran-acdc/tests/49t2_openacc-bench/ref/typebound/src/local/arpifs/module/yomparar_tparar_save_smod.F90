WRITE (KLUN) SELF%LTOTPRECL
WRITE (KLUN) SELF%LDUALMF
WRITE (KLUN) SELF%LSCAWAREMF
WRITE (KLUN) SELF%XVMAXTHP
WRITE (KLUN) SELF%XVMAXTHM
WRITE (KLUN) SELF%LSQUALL
WRITE (KLUN) SELF%NREFROI1
WRITE (KLUN) SELF%NREFROI2
WRITE (KLUN) SELF%VSQUALL
