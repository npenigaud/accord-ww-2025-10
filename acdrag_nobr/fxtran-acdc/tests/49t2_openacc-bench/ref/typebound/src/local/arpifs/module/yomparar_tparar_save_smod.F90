CONTAINS

MODULE SUBROUTINE ACDC_SAVE_TPARAR (SELF, KLUN)

IMPLICIT NONE
CLASS (TPARAR), INTENT (IN), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
LOGICAL :: LXSW_BANDS
WRITE (KLUN) SELF%CMICRO
WRITE (KLUN) SELF%CTURB
CALL SELF%PHYEX%ACDC_SAVE (KLUN)
WRITE (KLUN) SELF%NRR
WRITE (KLUN) SELF%NRRL
WRITE (KLUN) SELF%NRRI
WRITE (KLUN) SELF%NSV
WRITE (KLUN) SELF%NSWB_MNH
WRITE (KLUN) SELF%NGPAR
WRITE (KLUN) SELF%MINPRR
WRITE (KLUN) SELF%MACPRR
WRITE (KLUN) SELF%MINPRS
WRITE (KLUN) SELF%MACPRS
WRITE (KLUN) SELF%MINPRG
WRITE (KLUN) SELF%MACPRG
WRITE (KLUN) SELF%MALBDIR
WRITE (KLUN) SELF%MALBSCA
WRITE (KLUN) SELF%MRAIN
WRITE (KLUN) SELF%MSNOW
WRITE (KLUN) SELF%MGZ0
WRITE (KLUN) SELF%MGZ0H
WRITE (KLUN) SELF%MVQS
WRITE (KLUN) SELF%MVTS
WRITE (KLUN) SELF%MVEMIS
WRITE (KLUN) SELF%MSWDIR
WRITE (KLUN) SELF%MSWDIF
WRITE (KLUN) SELF%MLSM
WRITE (KLUN) SELF%MCD
WRITE (KLUN) SELF%MCH
LXSW_BANDS = ALLOCATED (SELF%XSW_BANDS)
WRITE (KLUN) LXSW_BANDS
IF (LXSW_BANDS) THEN
  WRITE (KLUN) LBOUND (SELF%XSW_BANDS)
  WRITE (KLUN) UBOUND (SELF%XSW_BANDS)
  WRITE (KLUN) SELF%XSW_BANDS
