SUBMODULE (FIELD_5RD_ARRAY_MODULE) FIELD_5RD_ARRAY_MODULE_FIELD_5RD_ARRAY_LOAD_SMOD

IMPLICIT NONE

CONTAINS

MODULE SUBROUTINE ACDC_LOAD_FIELD_5RD_ARRAY (SELF, KLUN)
USE FIELD_UTIL_MODULE
IMPLICIT NONE
CLASS (FIELD_5RD_ARRAY), INTENT (OUT), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
INTEGER :: IL5(5), IU5(5)
LOGICAL :: LF_P, LP_FIELD
READ (KLUN) LF_P
IF (LF_P) THEN
  CALL LOAD_FIELD_5RD (KLUN, SELF%F_P)
ELSE
  NULLIFY (SELF%F_P)
ENDIF
READ (KLUN) LP_FIELD
IF (LP_FIELD) THEN
  READ (KLUN) IL5
  READ (KLUN) IU5
  ALLOCATE (SELF%P_FIELD (IL5(1):IU5(1), IL5(2):IU5(2), IL5(3):IU5(3), IL5(4):IU5(4), IL5(5):IU5(5)))
  READ (KLUN) SELF%P_FIELD
ELSE
  NULLIFY (SELF%P_FIELD)
ENDIF
NULLIFY (SELF%P)
END SUBROUTINE

END SUBMODULE
