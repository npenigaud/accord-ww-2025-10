INTERFACE ACDC_COPY
MODULE PROCEDURE ACDC_COPY_SURFACE_VARIABLE_GROUP_VCLIH
END INTERFACE

INTERFACE ACDC_HOST
MODULE PROCEDURE ACDC_HOST_SURFACE_VARIABLE_GROUP_VCLIH
END INTERFACE



INTERFACE ACDC_WIPE
MODULE PROCEDURE ACDC_WIPE_SURFACE_VARIABLE_GROUP_VCLIH
END INTERFACE


CONTAINS





SUBROUTINE ACDC_COPY_SURFACE_VARIABLE_GROUP_VCLIH (SELF, LDCREATED, LDFIELDAPI)
USE FIELD_UTIL_MODULE
USE UTIL_VARIABLE_2RB_MOD
IMPLICIT NONE
TYPE (SURFACE_VARIABLE_GROUP_VCLIH), INTENT (IN), TARGET :: SELF
LOGICAL, OPTIONAL, INTENT (IN) :: LDCREATED, LDFIELDAPI
LOGICAL :: LLCREATED
LOGICAL :: LLFIELDAPI
LOGICAL :: LF_GROUP
LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF
LLCREATED = .FALSE.
IF (PRESENT (LDCREATED)) THEN
  LLCREATED = LDCREATED
ENDIF
IF (.NOT. LLCREATED) THEN
  !$ACC ENTER DATA CREATE (SELF)
  !$ACC UPDATE DEVICE (SELF)
ENDIF
CALL ACDC_COPY_VARIABLE_2RB (SELF%VTCCH, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL ACDC_COPY_VARIABLE_2RB (SELF%VSCCH, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL ACDC_COPY_VARIABLE_2RB (SELF%VBCCH, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL ACDC_COPY_VARIABLE_2RB (SELF%VPBLH, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL ACDC_COPY_VARIABLE_2RB (SELF%VSPSH, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL ACDC_COPY_VARIABLE_2RB (SELF%VQSH, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL ACDC_COPY_VARIABLE_2RB (SELF%VPCL, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL ACDC_COPY_VARIABLE_2RB (SELF%VPSL, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL ACDC_COPY_VARIABLE_2RB (SELF%VPCN, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL ACDC_COPY_VARIABLE_2RB (SELF%VPSN, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL ACDC_COPY_VARIABLE_2RB (SELF%VEVA, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

LF_GROUP = ASSOCIATED (SELF%F_GROUP)
IF (LF_GROUP .AND. LLFIELDAPI) THEN
  CALL COPY_FIELD_3RB (SELF%F_GROUP)
  !$ACC ENTER DATA ATTACH (SELF%F_GROUP)
ENDIF

END SUBROUTINE

SUBROUTINE ACDC_HOST_SURFACE_VARIABLE_GROUP_VCLIH (SELF)
USE FIELD_UTIL_MODULE
USE UTIL_VARIABLE_2RB_MOD
IMPLICIT NONE
TYPE (SURFACE_VARIABLE_GROUP_VCLIH), TARGET :: SELF
LOGICAL :: LF_GROUP
CALL ACDC_HOST_VARIABLE_2RB (SELF%VTCCH)

CALL ACDC_HOST_VARIABLE_2RB (SELF%VSCCH)

CALL ACDC_HOST_VARIABLE_2RB (SELF%VBCCH)

CALL ACDC_HOST_VARIABLE_2RB (SELF%VPBLH)

CALL ACDC_HOST_VARIABLE_2RB (SELF%VSPSH)

CALL ACDC_HOST_VARIABLE_2RB (SELF%VQSH)

CALL ACDC_HOST_VARIABLE_2RB (SELF%VPCL)

CALL ACDC_HOST_VARIABLE_2RB (SELF%VPSL)

CALL ACDC_HOST_VARIABLE_2RB (SELF%VPCN)

CALL ACDC_HOST_VARIABLE_2RB (SELF%VPSN)

CALL ACDC_HOST_VARIABLE_2RB (SELF%VEVA)

LF_GROUP = ASSOCIATED (SELF%F_GROUP)
IF (LF_GROUP) THEN
  CALL HOST_FIELD_3RB (SELF%F_GROUP)
ENDIF

END SUBROUTINE





SUBROUTINE ACDC_WIPE_SURFACE_VARIABLE_GROUP_VCLIH (SELF, LDDELETED, LDFIELDAPI)
USE FIELD_UTIL_MODULE
USE UTIL_VARIABLE_2RB_MOD
IMPLICIT NONE
TYPE (SURFACE_VARIABLE_GROUP_VCLIH), INTENT (IN), TARGET :: SELF
LOGICAL, OPTIONAL, INTENT (IN) :: LDDELETED, LDFIELDAPI
LOGICAL :: LLDELETED
LOGICAL :: LLFIELDAPI
LOGICAL :: LF_GROUP
LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF
CALL ACDC_WIPE_VARIABLE_2RB (SELF%VTCCH, LDDELETED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL ACDC_WIPE_VARIABLE_2RB (SELF%VSCCH, LDDELETED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL ACDC_WIPE_VARIABLE_2RB (SELF%VBCCH, LDDELETED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL ACDC_WIPE_VARIABLE_2RB (SELF%VPBLH, LDDELETED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL ACDC_WIPE_VARIABLE_2RB (SELF%VSPSH, LDDELETED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL ACDC_WIPE_VARIABLE_2RB (SELF%VQSH, LDDELETED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL ACDC_WIPE_VARIABLE_2RB (SELF%VPCL, LDDELETED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL ACDC_WIPE_VARIABLE_2RB (SELF%VPSL, LDDELETED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL ACDC_WIPE_VARIABLE_2RB (SELF%VPCN, LDDELETED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL ACDC_WIPE_VARIABLE_2RB (SELF%VPSN, LDDELETED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL ACDC_WIPE_VARIABLE_2RB (SELF%VEVA, LDDELETED=.TRUE., LDFIELDAPI=LDFIELDAPI)

LF_GROUP = ASSOCIATED (SELF%F_GROUP)
IF (LF_GROUP .AND. LLFIELDAPI) THEN
  CALL WIPE_FIELD_3RB (SELF%F_GROUP)
ENDIF

LLDELETED = .FALSE.
IF (PRESENT (LDDELETED)) THEN
  LLDELETED = LDDELETED
ENDIF
IF (.NOT. LLDELETED) THEN
  !$ACC EXIT DATA DELETE (SELF)
ENDIF
END SUBROUTINE



END MODULE
