JSIZE = JSIZE + ISIZE
KSIZE = KSIZE + ISIZE
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') JSIZE
  WRITE (*, *) TRIM (CLPATH)//'%YRMDDH'
ENDIF
JSIZE = 0
ISIZE = ACDC_SIZE_TSDDH (SELF%YRSDDH, CLPATH//'%YRSDDH', .FALSE.)
JSIZE = JSIZE + ISIZE
KSIZE = KSIZE + ISIZE
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') JSIZE
  WRITE (*, *) TRIM (CLPATH)//'%YRSDDH'
ENDIF
JSIZE = 0
ISIZE = ACDC_SIZE_TTDDH (SELF%YRTDDH, CLPATH//'%YRTDDH', .FALSE.)
JSIZE = JSIZE + ISIZE
KSIZE = KSIZE + ISIZE
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') JSIZE
  WRITE (*, *) TRIM (CLPATH)//'%YRTDDH'
ENDIF
JSIZE = 0
ISIZE = ACDC_SIZE_TGPDDH (SELF%YRGPDDH, CLPATH//'%YRGPDDH', .FALSE.)
