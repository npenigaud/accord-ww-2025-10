FUNCTION ACDC_SIZE_MODEL_DIAGNOSTICS_TYPE (SELF, CDPATH, LDPRINT) RESULT (KSIZE)
USE UTIL_TCDDH_MOD
USE UTIL_TGPDDH_MOD
USE UTIL_TLDDH_MOD
USE UTIL_TMDDH_MOD
USE UTIL_TPADDH_MOD
USE UTIL_TSDDH_MOD
USE UTIL_TSPDDH_MOD
USE UTIL_TTDDH_MOD
IMPLICIT NONE
TYPE (MODEL_DIAGNOSTICS_TYPE),     INTENT (IN), TARGET :: SELF
CHARACTER(LEN=*), INTENT (IN), OPTIONAL :: CDPATH
LOGICAL,          INTENT (IN), OPTIONAL :: LDPRINT
INTEGER*8 :: KSIZE
INTEGER*8 :: ISIZE, JSIZE
LOGICAL :: LLPRINT
CHARACTER(LEN=128) :: CLPATH
LLPRINT = .FALSE.
IF (PRESENT (LDPRINT)) THEN
  LLPRINT = LDPRINT
ENDIF
CLPATH=''
IF (PRESENT (CDPATH)) THEN
  CLPATH = CDPATH
ENDIF
KSIZE = 0
JSIZE = 0
ISIZE = ACDC_SIZE_TCDDH (SELF%YRCDDH, CLPATH//'%YRCDDH', .FALSE.)
