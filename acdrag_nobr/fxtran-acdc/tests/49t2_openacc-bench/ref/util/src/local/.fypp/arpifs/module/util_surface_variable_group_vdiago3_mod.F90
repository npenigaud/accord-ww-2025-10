CALL ACDC_COPY_VARIABLE_3RB (SELF%VDO, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL ACDC_COPY_VARIABLE_3RB (SELF%VHO_INV, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

