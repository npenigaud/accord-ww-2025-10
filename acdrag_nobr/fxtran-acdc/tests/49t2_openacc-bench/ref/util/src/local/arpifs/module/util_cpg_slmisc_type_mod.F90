MODULE UTIL_CPG_SLMISC_TYPE_MOD

USE CPG_SLMISC_TYPE_MOD, ONLY : CPG_SLMISC_TYPE



INTERFACE ACDC_COPY
MODULE PROCEDURE ACDC_COPY_CPG_SLMISC_TYPE
END INTERFACE

INTERFACE ACDC_HOST
MODULE PROCEDURE ACDC_HOST_CPG_SLMISC_TYPE
END INTERFACE



INTERFACE ACDC_WIPE
MODULE PROCEDURE ACDC_WIPE_CPG_SLMISC_TYPE
END INTERFACE


CONTAINS





SUBROUTINE ACDC_COPY_CPG_SLMISC_TYPE (SELF, LDCREATED, LDFIELDAPI)
USE FIELD_UTIL_MODULE
IMPLICIT NONE
TYPE (CPG_SLMISC_TYPE), INTENT (IN), TARGET :: SELF
LOGICAL, OPTIONAL, INTENT (IN) :: LDCREATED, LDFIELDAPI
LOGICAL :: LLCREATED
LOGICAL :: LLFIELDAPI
LOGICAL :: LF_ISETTLOFF, LF_SLHDA, LF_SLHDD0
LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF
LLCREATED = .FALSE.
IF (PRESENT (LDCREATED)) THEN
  LLCREATED = LDCREATED
ENDIF
IF (.NOT. LLCREATED) THEN
  !$ACC ENTER DATA CREATE (SELF)
  !$ACC UPDATE DEVICE (SELF)
ENDIF
LF_SLHDA = ASSOCIATED (SELF%F_SLHDA)
IF (LF_SLHDA .AND. LLFIELDAPI) THEN
  CALL COPY_FIELD_2RB (SELF%F_SLHDA)
  !$ACC ENTER DATA ATTACH (SELF%F_SLHDA)
ENDIF

LF_SLHDD0 = ASSOCIATED (SELF%F_SLHDD0)
IF (LF_SLHDD0 .AND. LLFIELDAPI) THEN
  CALL COPY_FIELD_2RB (SELF%F_SLHDD0)
  !$ACC ENTER DATA ATTACH (SELF%F_SLHDD0)
ENDIF

LF_ISETTLOFF = ASSOCIATED (SELF%F_ISETTLOFF)
IF (LF_ISETTLOFF .AND. LLFIELDAPI) THEN
  CALL COPY_FIELD_3IM (SELF%F_ISETTLOFF)
  !$ACC ENTER DATA ATTACH (SELF%F_ISETTLOFF)
ENDIF

END SUBROUTINE

SUBROUTINE ACDC_HOST_CPG_SLMISC_TYPE (SELF)
USE FIELD_UTIL_MODULE
IMPLICIT NONE
TYPE (CPG_SLMISC_TYPE), TARGET :: SELF
LOGICAL :: LF_ISETTLOFF, LF_SLHDA, LF_SLHDD0
LF_SLHDA = ASSOCIATED (SELF%F_SLHDA)
IF (LF_SLHDA) THEN
  CALL HOST_FIELD_2RB (SELF%F_SLHDA)
ENDIF

LF_SLHDD0 = ASSOCIATED (SELF%F_SLHDD0)
IF (LF_SLHDD0) THEN
  CALL HOST_FIELD_2RB (SELF%F_SLHDD0)
ENDIF

LF_ISETTLOFF = ASSOCIATED (SELF%F_ISETTLOFF)
IF (LF_ISETTLOFF) THEN
  CALL HOST_FIELD_3IM (SELF%F_ISETTLOFF)
ENDIF

END SUBROUTINE





SUBROUTINE ACDC_WIPE_CPG_SLMISC_TYPE (SELF, LDDELETED, LDFIELDAPI)
USE FIELD_UTIL_MODULE
IMPLICIT NONE
TYPE (CPG_SLMISC_TYPE), INTENT (IN), TARGET :: SELF
LOGICAL, OPTIONAL, INTENT (IN) :: LDDELETED, LDFIELDAPI
LOGICAL :: LLDELETED
LOGICAL :: LLFIELDAPI
LOGICAL :: LF_ISETTLOFF, LF_SLHDA, LF_SLHDD0
LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF
LF_SLHDA = ASSOCIATED (SELF%F_SLHDA)
IF (LF_SLHDA .AND. LLFIELDAPI) THEN
  CALL WIPE_FIELD_2RB (SELF%F_SLHDA)
ENDIF

LF_SLHDD0 = ASSOCIATED (SELF%F_SLHDD0)
IF (LF_SLHDD0 .AND. LLFIELDAPI) THEN
  CALL WIPE_FIELD_2RB (SELF%F_SLHDD0)
ENDIF

LF_ISETTLOFF = ASSOCIATED (SELF%F_ISETTLOFF)
IF (LF_ISETTLOFF .AND. LLFIELDAPI) THEN
  CALL WIPE_FIELD_3IM (SELF%F_ISETTLOFF)
ENDIF

LLDELETED = .FALSE.
IF (PRESENT (LDDELETED)) THEN
  LLDELETED = LDDELETED
ENDIF
IF (.NOT. LLDELETED) THEN
  !$ACC EXIT DATA DELETE (SELF)
ENDIF
END SUBROUTINE



END MODULE
