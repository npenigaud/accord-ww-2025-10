USE UTIL_TVSPLIP_MOD
USE FXTRAN_ACDC_CRC64_INTRINSIC_MOD, ONLY : FCRC64 => FXTRAN_ACDC_CRC64
IMPLICIT NONE
TYPE (TSLINT), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
CHARACTER(LEN=*), INTENT (IN) :: CDPATH
CHARACTER(LEN=128) :: CLIND
CLIND = ''
CALL ACDC_CRC64_TVSPLIP (SELF%YRVSPLIP, KLUN, CDPATH//'%YRVSPLIP'//TRIM(CLIND))

CLIND = ''
CALL ACDC_CRC64_TVSLETA (SELF%YRVSLETA, KLUN, CDPATH//'%YRVSLETA'//TRIM(CLIND))

CLIND = ''
CALL ACDC_CRC64_THSLMER (SELF%YRHSLMER, KLUN, CDPATH//'%YRHSLMER'//TRIM(CLIND))

END SUBROUTINE

SUBROUTINE ACDC_WIPE_TSLINT (SELF, LDDELETED, LDFIELDAPI)
USE UTIL_THSLMER_MOD
USE UTIL_TVSLETA_MOD
USE UTIL_TVSPLIP_MOD
IMPLICIT NONE
TYPE (TSLINT), INTENT (IN), TARGET :: SELF
LOGICAL, OPTIONAL, INTENT (IN) :: LDDELETED, LDFIELDAPI
LOGICAL :: LLDELETED
LOGICAL :: LLFIELDAPI
LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF
CALL ACDC_WIPE_TVSPLIP (SELF%YRVSPLIP, LDDELETED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL ACDC_WIPE_TVSLETA (SELF%YRVSLETA, LDDELETED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL ACDC_WIPE_THSLMER (SELF%YRHSLMER, LDDELETED=.TRUE., LDFIELDAPI=LDFIELDAPI)

LLDELETED = .FALSE.
IF (PRESENT (LDDELETED)) THEN
  LLDELETED = LDDELETED
ENDIF
IF (.NOT. LLDELETED) THEN
  !$ACC EXIT DATA DELETE (SELF)
ENDIF
END SUBROUTINE

FUNCTION ACDC_SIZE_TSLINT (SELF, CDPATH, LDPRINT) RESULT (KSIZE)
USE UTIL_THSLMER_MOD
USE UTIL_TVSLETA_MOD
USE UTIL_TVSPLIP_MOD
IMPLICIT NONE
TYPE (TSLINT),     INTENT (IN), TARGET :: SELF
CHARACTER(LEN=*), INTENT (IN), OPTIONAL :: CDPATH
LOGICAL,          INTENT (IN), OPTIONAL :: LDPRINT
INTEGER*8 :: KSIZE
INTEGER*8 :: ISIZE, JSIZE
LOGICAL :: LLPRINT
CHARACTER(LEN=128) :: CLPATH
LLPRINT = .FALSE.
IF (PRESENT (LDPRINT)) THEN
  LLPRINT = LDPRINT
ENDIF
CLPATH=''
IF (PRESENT (CDPATH)) THEN
  CLPATH = CDPATH
ENDIF
KSIZE = 0
JSIZE = 0
ISIZE = ACDC_SIZE_TVSPLIP (SELF%YRVSPLIP, CLPATH//'%YRVSPLIP', .FALSE.)
JSIZE = JSIZE + ISIZE
KSIZE = KSIZE + ISIZE
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') JSIZE
  WRITE (*, *) TRIM (CLPATH)//'%YRVSPLIP'
ENDIF
JSIZE = 0
ISIZE = ACDC_SIZE_TVSLETA (SELF%YRVSLETA, CLPATH//'%YRVSLETA', .FALSE.)
JSIZE = JSIZE + ISIZE
KSIZE = KSIZE + ISIZE
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') JSIZE
  WRITE (*, *) TRIM (CLPATH)//'%YRVSLETA'
ENDIF
JSIZE = 0
ISIZE = ACDC_SIZE_THSLMER (SELF%YRHSLMER, CLPATH//'%YRHSLMER', .FALSE.)
JSIZE = JSIZE + ISIZE
KSIZE = KSIZE + ISIZE
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') JSIZE
  WRITE (*, *) TRIM (CLPATH)//'%YRHSLMER'
ENDIF
END FUNCTION

