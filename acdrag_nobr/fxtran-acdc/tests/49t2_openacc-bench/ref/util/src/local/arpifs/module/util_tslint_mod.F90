MODULE PROCEDURE ACDC_SIZE_TSLINT
END INTERFACE

CONTAINS

SUBROUTINE ACDC_SAVE_TSLINT (SELF, KLUN)
USE UTIL_THSLMER_MOD
USE UTIL_TVSLETA_MOD
USE UTIL_TVSPLIP_MOD
IMPLICIT NONE
TYPE (TSLINT), INTENT (IN), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN

CALL ACDC_SAVE_TVSPLIP (SELF%YRVSPLIP, KLUN)
CALL ACDC_SAVE_TVSLETA (SELF%YRVSLETA, KLUN)
CALL ACDC_SAVE_THSLMER (SELF%YRHSLMER, KLUN)
END SUBROUTINE

SUBROUTINE ACDC_LOAD_TSLINT (SELF, KLUN)
USE UTIL_THSLMER_MOD
USE UTIL_TVSLETA_MOD
USE UTIL_TVSPLIP_MOD
IMPLICIT NONE
TYPE (TSLINT), INTENT (OUT), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN

CALL ACDC_LOAD_TVSPLIP (SELF%YRVSPLIP, KLUN)
CALL ACDC_LOAD_TVSLETA (SELF%YRVSLETA, KLUN)
CALL ACDC_LOAD_THSLMER (SELF%YRHSLMER, KLUN)
END SUBROUTINE

SUBROUTINE ACDC_COPY_TSLINT (SELF, LDCREATED, LDFIELDAPI)
USE UTIL_THSLMER_MOD
USE UTIL_TVSLETA_MOD
USE UTIL_TVSPLIP_MOD
IMPLICIT NONE
TYPE (TSLINT), INTENT (IN), TARGET :: SELF
LOGICAL, OPTIONAL, INTENT (IN) :: LDCREATED, LDFIELDAPI
LOGICAL :: LLCREATED
LOGICAL :: LLFIELDAPI
LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF
LLCREATED = .FALSE.
IF (PRESENT (LDCREATED)) THEN
  LLCREATED = LDCREATED
ENDIF
IF (.NOT. LLCREATED) THEN
  !$ACC ENTER DATA CREATE (SELF)
  !$ACC UPDATE DEVICE (SELF)
ENDIF
CALL ACDC_COPY_TVSPLIP (SELF%YRVSPLIP, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL ACDC_COPY_TVSLETA (SELF%YRVSLETA, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL ACDC_COPY_THSLMER (SELF%YRHSLMER, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

END SUBROUTINE

