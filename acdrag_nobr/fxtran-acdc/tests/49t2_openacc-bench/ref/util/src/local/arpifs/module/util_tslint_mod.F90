CALL ACDC_HOST_TVSPLIP (SELF%YRVSPLIP)

CALL ACDC_HOST_TVSLETA (SELF%YRVSLETA)

CALL ACDC_HOST_THSLMER (SELF%YRHSLMER)

END SUBROUTINE



SUBROUTINE ACDC_CRC64_TSLINT (SELF, KLUN, CDPATH)
USE UTIL_THSLMER_MOD
