INTERFACE ACDC_COPY
MODULE PROCEDURE ACDC_COPY_CPG_SL1B_3D_TYPE
END INTERFACE

INTERFACE ACDC_HOST
MODULE PROCEDURE ACDC_HOST_CPG_SL1B_3D_TYPE
END INTERFACE



INTERFACE ACDC_WIPE
MODULE PROCEDURE ACDC_WIPE_CPG_SL1B_3D_TYPE
END INTERFACE


CONTAINS





SUBROUTINE ACDC_COPY_CPG_SL1B_3D_TYPE (SELF, LDCREATED, LDFIELDAPI)
USE FIELD_UTIL_MODULE
USE UTIL_TYPE_GFL_COMP_MOD
IMPLICIT NONE
TYPE (CPG_SL1B_3D_TYPE), INTENT (IN), TARGET :: SELF
LOGICAL, OPTIONAL, INTENT (IN) :: LDCREATED, LDFIELDAPI
LOGICAL :: LLCREATED
LOGICAL :: LLFIELDAPI
LOGICAL :: LF_P, LF_P_F, LF_P_P, LF_P_SP, LF_P_SPF
LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF
LLCREATED = .FALSE.
IF (PRESENT (LDCREATED)) THEN
  LLCREATED = LDCREATED
ENDIF
IF (.NOT. LLCREATED) THEN
  !$ACC ENTER DATA CREATE (SELF)
  !$ACC UPDATE DEVICE (SELF)
ENDIF
LF_P = ASSOCIATED (SELF%F_P)
IF (LF_P .AND. LLFIELDAPI) THEN
  CALL COPY_FIELD_3RB (SELF%F_P)
  !$ACC ENTER DATA ATTACH (SELF%F_P)
ENDIF

LF_P_F = ASSOCIATED (SELF%F_P_F)
IF (LF_P_F .AND. LLFIELDAPI) THEN
  CALL COPY_FIELD_3RB (SELF%F_P_F)
  !$ACC ENTER DATA ATTACH (SELF%F_P_F)
ENDIF

LF_P_SPF = ASSOCIATED (SELF%F_P_SPF)
IF (LF_P_SPF .AND. LLFIELDAPI) THEN
  CALL COPY_FIELD_3RB (SELF%F_P_SPF)
  !$ACC ENTER DATA ATTACH (SELF%F_P_SPF)
ENDIF

LF_P_SP = ASSOCIATED (SELF%F_P_SP)
IF (LF_P_SP .AND. LLFIELDAPI) THEN
  CALL COPY_FIELD_3RB (SELF%F_P_SP)
  !$ACC ENTER DATA ATTACH (SELF%F_P_SP)
ENDIF

LF_P_P = ASSOCIATED (SELF%F_P_P)
IF (LF_P_P .AND. LLFIELDAPI) THEN
  CALL COPY_FIELD_3RB (SELF%F_P_P)
  !$ACC ENTER DATA ATTACH (SELF%F_P_P)
ENDIF

CALL ACDC_COPY_TYPE_GFL_COMP (SELF%YCOMP, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

END SUBROUTINE

SUBROUTINE ACDC_HOST_CPG_SL1B_3D_TYPE (SELF)
USE FIELD_UTIL_MODULE
USE UTIL_TYPE_GFL_COMP_MOD
IMPLICIT NONE
TYPE (CPG_SL1B_3D_TYPE), TARGET :: SELF
LOGICAL :: LF_P, LF_P_F, LF_P_P, LF_P_SP, LF_P_SPF
LF_P = ASSOCIATED (SELF%F_P)
IF (LF_P) THEN
  CALL HOST_FIELD_3RB (SELF%F_P)
ENDIF

LF_P_F = ASSOCIATED (SELF%F_P_F)
IF (LF_P_F) THEN
  CALL HOST_FIELD_3RB (SELF%F_P_F)
ENDIF

LF_P_SPF = ASSOCIATED (SELF%F_P_SPF)
IF (LF_P_SPF) THEN
  CALL HOST_FIELD_3RB (SELF%F_P_SPF)
ENDIF

LF_P_SP = ASSOCIATED (SELF%F_P_SP)
IF (LF_P_SP) THEN
  CALL HOST_FIELD_3RB (SELF%F_P_SP)
ENDIF

LF_P_P = ASSOCIATED (SELF%F_P_P)
IF (LF_P_P) THEN
  CALL HOST_FIELD_3RB (SELF%F_P_P)
ENDIF

CALL ACDC_HOST_TYPE_GFL_COMP (SELF%YCOMP)

END SUBROUTINE





SUBROUTINE ACDC_WIPE_CPG_SL1B_3D_TYPE (SELF, LDDELETED, LDFIELDAPI)
USE FIELD_UTIL_MODULE
USE UTIL_TYPE_GFL_COMP_MOD
IMPLICIT NONE
TYPE (CPG_SL1B_3D_TYPE), INTENT (IN), TARGET :: SELF
LOGICAL, OPTIONAL, INTENT (IN) :: LDDELETED, LDFIELDAPI
LOGICAL :: LLDELETED
LOGICAL :: LLFIELDAPI
LOGICAL :: LF_P, LF_P_F, LF_P_P, LF_P_SP, LF_P_SPF
LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF
LF_P = ASSOCIATED (SELF%F_P)
IF (LF_P .AND. LLFIELDAPI) THEN
  CALL WIPE_FIELD_3RB (SELF%F_P)
ENDIF

LF_P_F = ASSOCIATED (SELF%F_P_F)
IF (LF_P_F .AND. LLFIELDAPI) THEN
  CALL WIPE_FIELD_3RB (SELF%F_P_F)
ENDIF

LF_P_SPF = ASSOCIATED (SELF%F_P_SPF)
IF (LF_P_SPF .AND. LLFIELDAPI) THEN
  CALL WIPE_FIELD_3RB (SELF%F_P_SPF)
ENDIF

LF_P_SP = ASSOCIATED (SELF%F_P_SP)
IF (LF_P_SP .AND. LLFIELDAPI) THEN
  CALL WIPE_FIELD_3RB (SELF%F_P_SP)
ENDIF

LF_P_P = ASSOCIATED (SELF%F_P_P)
IF (LF_P_P .AND. LLFIELDAPI) THEN
  CALL WIPE_FIELD_3RB (SELF%F_P_P)
ENDIF

CALL ACDC_WIPE_TYPE_GFL_COMP (SELF%YCOMP, LDDELETED=.TRUE., LDFIELDAPI=LDFIELDAPI)

LLDELETED = .FALSE.
IF (PRESENT (LDDELETED)) THEN
  LLDELETED = LDDELETED
ENDIF
IF (.NOT. LLDELETED) THEN
  !$ACC EXIT DATA DELETE (SELF)
ENDIF
END SUBROUTINE



