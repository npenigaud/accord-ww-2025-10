SUBROUTINE ACDC_SAVE_TEAERMAP (SELF, KLUN)

IMPLICIT NONE
TYPE (TEAERMAP), INTENT (IN), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN

WRITE (KLUN) SELF%NDUSRCP
WRITE (KLUN) SELF%RDDUAER
WRITE (KLUN) SELF%RDUSRCP
END SUBROUTINE

SUBROUTINE ACDC_LOAD_TEAERMAP (SELF, KLUN)
USE PARKIND1, ONLY : JPRD
IMPLICIT NONE
TYPE (TEAERMAP), INTENT (OUT), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
REAL(KIND=JPRD), ALLOCATABLE :: ZTMP1 (:)
REAL(KIND=JPRD), ALLOCATABLE :: ZTMP2 (:,:)
READ (KLUN) SELF%NDUSRCP
ALLOCATE (ZTMP1(LBOUND(SELF%RDDUAER,1):UBOUND(SELF%RDDUAER,1)))
READ (KLUN) ZTMP1
SELF%RDDUAER = ZTMP1
DEALLOCATE (ZTMP1)
ALLOCATE (ZTMP2(LBOUND(SELF%RDUSRCP,1):UBOUND(SELF%RDUSRCP,1), LBOUND(SELF%RDUSRCP,2):UBOUND(SELF%RDUSRCP,2)))
READ (KLUN) ZTMP2
SELF%RDUSRCP = ZTMP2
DEALLOCATE (ZTMP2)
END SUBROUTINE

