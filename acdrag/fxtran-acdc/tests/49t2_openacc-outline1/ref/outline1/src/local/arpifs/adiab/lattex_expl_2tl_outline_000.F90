SUBROUTINE LATTEX_EXPL_2TL_OUTLINE_000 (JGFL, YDCPG_BNDS, YDCPG_SL1, YDGEOMETRY, YDVARS)
USE CPG_OPTS_TYPE_MOD,ONLY:CPG_BNDS_TYPE, CPG_OPTS_TYPE
USE CPG_SL1_TYPE_MOD,ONLY:CPG_SL1B_TYPE
USE FIELD_VARIABLES_MOD,ONLY:FIELD_VARIABLES
USE GEOMETRY_MOD,ONLY:GEOMETRY
USE PARKIND1,ONLY:JPIM, JPRB
USE YOMHOOK,ONLY:LHOOK, JPHOOK, DR_HOOK

IMPLICIT NONE

INTEGER (KIND=JPIM), INTENT (INOUT)::JGFL
TYPE (CPG_BNDS_TYPE), INTENT (IN)::YDCPG_BNDS
TYPE (CPG_SL1B_TYPE), INTENT (INOUT)::YDCPG_SL1
TYPE (GEOMETRY), INTENT (IN)::YDGEOMETRY
TYPE (FIELD_VARIABLES), INTENT (INOUT)::YDVARS
INTEGER (KIND=JPIM)::JLEV
INTEGER (KIND=JPIM)::JROF
REAL (KIND=JPHOOK)::ZHOOK_HANDLE
IF (LHOOK) CALL DR_HOOK ('LATTEX_EXPL_2TL_OUTLINE_000', 0, ZHOOK_HANDLE)

DO JLEV=1, YDGEOMETRY%YRDIMV%NFLEVG

  DO JROF=YDCPG_BNDS%KIDIA, YDCPG_BNDS%KFDIA
    YDCPG_SL1%GFL (JGFL)%P (JROF, JLEV)=YDCPG_SL1%GFL (JGFL)%P (JROF, JLEV)+YDCPG_SL1&
    &%GFL (JGFL)%P_F (JROF, JLEV)+YDVARS%GFL_PTR (JGFL)%T9 (JROF, JLEV)
  ENDDO

ENDDO

IF (LHOOK) CALL DR_HOOK ('LATTEX_EXPL_2TL_OUTLINE_000', 1, ZHOOK_HANDLE)
ENDSUBROUTINE
