SUBROUTINE APL_ARPEGE_DEEP_CONVECTION_INIT (YDCPG_BNDS, YDCPG_DYN0, YDCPG_OPTS, YDGEOMETRY&
&, YDMF_PHYS_BASE_STATE, YDMF_PHYS_SURF, YDVARS, LDLAND, LDSHCV, PACPR, PCUCONVCA,&
& PDXTDK, PFCQLF, PFCQLI, PGEOM1, PGEOMH, PLCRIT_AER, PLISUM, PVERVEL)
USE CPG_OPTS_TYPE_MOD,ONLY:CPG_BNDS_TYPE, CPG_OPTS_TYPE
USE CPG_TYPE_MOD,ONLY:CPG_DYN_TYPE
USE FIELD_VARIABLES_MOD,ONLY:FIELD_VARIABLES
USE GEOMETRY_MOD,ONLY:GEOMETRY
USE PARKIND1,ONLY:JPIM, JPRB
USE MF_PHYS_BASE_STATE_TYPE_MOD,ONLY:MF_PHYS_BASE_STATE_TYPE
USE MF_PHYS_SURFACE_TYPE_MOD,ONLY:MF_PHYS_SURF_TYPE
USE YOMHOOK,ONLY:LHOOK, JPHOOK, DR_HOOK

IMPLICIT NONE

TYPE (CPG_BNDS_TYPE), INTENT (IN)::YDCPG_BNDS
TYPE (CPG_DYN_TYPE), INTENT (IN)::YDCPG_DYN0
TYPE (CPG_OPTS_TYPE), INTENT (IN)::YDCPG_OPTS
TYPE (GEOMETRY), INTENT (IN)::YDGEOMETRY
TYPE (MF_PHYS_BASE_STATE_TYPE), INTENT (IN)::YDMF_PHYS_BASE_STATE
TYPE (MF_PHYS_SURF_TYPE), INTENT (INOUT)::YDMF_PHYS_SURF
TYPE (FIELD_VARIABLES), INTENT (INOUT)::YDVARS
LOGICAL, INTENT (INOUT)::LDLAND (YDCPG_OPTS%KLON)
LOGICAL, INTENT (INOUT)::LDSHCV (YDCPG_OPTS%KLON)
REAL (KIND=JPRB), INTENT (INOUT)::PACPR (YDCPG_OPTS%KLON)
REAL (KIND=JPRB), INTENT (INOUT)::PCUCONVCA (YDCPG_OPTS%KLON)
REAL (KIND=JPRB), INTENT (INOUT)::PDXTDK (YDCPG_OPTS%KLON)
REAL (KIND=JPRB), INTENT (INOUT)::PFCQLF (YDCPG_OPTS%KLON, 0:YDCPG_OPTS%KFLEVG)
REAL (KIND=JPRB), INTENT (INOUT)::PFCQLI (YDCPG_OPTS%KLON, 0:YDCPG_OPTS%KFLEVG)
REAL (KIND=JPRB), INTENT (INOUT)::PGEOM1 (YDCPG_OPTS%KLON, YDCPG_OPTS%KFLEVG)
REAL (KIND=JPRB), INTENT (INOUT)::PGEOMH (YDCPG_OPTS%KLON, 0:YDCPG_OPTS%KFLEVG)
REAL (KIND=JPRB), INTENT (INOUT)::PLCRIT_AER (YDCPG_OPTS%KLON, YDCPG_OPTS%KFLEVG)
REAL (KIND=JPRB), INTENT (INOUT)::PLISUM (YDCPG_OPTS%KLON, YDCPG_OPTS%KFLEVG)
REAL (KIND=JPRB), INTENT (INOUT)::PVERVEL (YDCPG_OPTS%KLON, YDCPG_OPTS%KFLEVG)
INTEGER (KIND=JPIM)::JLEV
INTEGER (KIND=JPIM)::JLON
REAL (KIND=JPHOOK)::ZHOOK_HANDLE
IF (LHOOK) CALL DR_HOOK ('APL_ARPEGE_DEEP_CONVECTION_INIT', 0, ZHOOK_HANDLE)

DO JLEV=1, YDCPG_OPTS%KFLEVG

  DO JLON=YDCPG_BNDS%KIDIA, YDCPG_BNDS%KFDIA
    PGEOM1 (JLON, JLEV)=YDMF_PHYS_BASE_STATE%YCPG_DYN%PHIF (JLON, JLEV&
    &)-YDMF_PHYS_BASE_STATE%YCPG_DYN%PHI (JLON, YDCPG_OPTS%KFLEVG)
    PVERVEL (JLON, JLEV)=YDCPG_DYN0%CTY%VVEL (JLON, JLEV)*YDMF_PHYS_BASE_STATE&
    &%YCPG_PHY%PREHYDF (JLON, JLEV)
    PLISUM (JLON, JLEV)=0.0_JPRB
    PLCRIT_AER (JLON, JLEV)=5.E-4_JPRB
  ENDDO

ENDDO


DO JLEV=0, YDCPG_OPTS%KFLEVG

  DO JLON=YDCPG_BNDS%KIDIA, YDCPG_BNDS%KFDIA
    PGEOMH (JLON, JLEV)=YDMF_PHYS_BASE_STATE%YCPG_DYN%PHI (JLON, JLEV&
    &)-YDMF_PHYS_BASE_STATE%YCPG_DYN%PHI (JLON, YDCPG_OPTS%KFLEVG)
    PFCQLF (JLON, JLEV)=0.0_JPRB
    PFCQLI (JLON, JLEV)=0.0_JPRB
  ENDDO

ENDDO


DO JLON=YDCPG_BNDS%KIDIA, YDCPG_BNDS%KFDIA
  LDLAND (JLON)=(YDMF_PHYS_SURF%GSD_VF%PLSM (JLON)>0.5_JPRB)
  LDSHCV (JLON)=.FALSE.
  PCUCONVCA (JLON)=0.0_JPRB
  PACPR (JLON)=0.0_JPRB
  PDXTDK (JLON)=YDGEOMETRY%YRGEM%RDELXN/YDVARS%GEOMETRY%GM%T0 (JLON)
ENDDO

IF (LHOOK) CALL DR_HOOK ('APL_ARPEGE_DEEP_CONVECTION_INIT', 1, ZHOOK_HANDLE)
ENDSUBROUTINE
