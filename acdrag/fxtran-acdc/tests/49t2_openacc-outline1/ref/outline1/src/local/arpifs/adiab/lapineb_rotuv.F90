SUBROUTINE LAPINEB_ROTUV (LDSLPHY, YDA_PUP9, YDA_PVP9, YDCPG_BNDS, YDCPG_GMV9,&
& YDCPG_GMV9_SI, YDCPG_SL2, YDGEOMETRY, YDMODEL, YDVARS, PP, PQ, PUP9, PVP9)
USE CPG_OPTS_TYPE_MOD,ONLY:CPG_OPTS_TYPE, CPG_BNDS_TYPE
USE CPG_GMV_TYPE_MOD,ONLY:CPG_GMV_TYPE
USE CPG_SL2_TYPE_MOD,ONLY:CPG_SL2_TYPE
USE FIELD_ARRAY_MODULE,ONLY:FIELD_3IM_ARRAY, FIELD_4IM_ARRAY, FIELD_3RB_ARRAY, FIELD_4RB_ARRAY
USE FIELD_VARIABLES_MOD,ONLY:FIELD_VARIABLES
USE GEOMETRY_MOD,ONLY:GEOMETRY
USE PARKIND1,ONLY:JPIM, JPRB
USE TYPE_MODEL,ONLY:MODEL
USE YOMHOOK,ONLY:LHOOK, JPHOOK, DR_HOOK

IMPLICIT NONE

LOGICAL, INTENT (IN)::LDSLPHY
TYPE (FIELD_3RB_ARRAY), INTENT (IN)::YDA_PUP9
TYPE (FIELD_3RB_ARRAY), INTENT (IN)::YDA_PVP9
TYPE (CPG_BNDS_TYPE), INTENT (IN)::YDCPG_BNDS
TYPE (CPG_GMV_TYPE), INTENT (INOUT)::YDCPG_GMV9
TYPE (CPG_GMV_TYPE), INTENT (INOUT)::YDCPG_GMV9_SI
TYPE (CPG_SL2_TYPE), INTENT (IN)::YDCPG_SL2
TYPE (GEOMETRY), INTENT (IN)::YDGEOMETRY
TYPE (MODEL), INTENT (IN)::YDMODEL
TYPE (FIELD_VARIABLES), INTENT (INOUT)::YDVARS
REAL (KIND=JPRB), INTENT (INOUT)::PP (YDGEOMETRY%YRDIM%NPROMA, YDGEOMETRY%YRDIMV%NFLEVG)
REAL (KIND=JPRB), INTENT (INOUT)::PQ (YDGEOMETRY%YRDIM%NPROMA, YDGEOMETRY%YRDIMV%NFLEVG)
REAL (KIND=JPRB), INTENT (INOUT)::PUP9 (YDGEOMETRY%YRDIM%NPROMA, YDGEOMETRY%YRDIMV%NFLEVG)
REAL (KIND=JPRB), INTENT (INOUT)::PVP9 (YDGEOMETRY%YRDIM%NPROMA, YDGEOMETRY%YRDIMV%NFLEVG)
#include "rotuv.intfb.h"
REAL (KIND=JPHOOK)::ZHOOK_HANDLE
IF (LHOOK) CALL DR_HOOK ('LAPINEB_ROTUV', 0, ZHOOK_HANDLE)
CALL ROTUV (YDGEOMETRY%YRDIMV%NFLEVG, YDGEOMETRY%YRDIM%NPROMA, YDCPG_BNDS%KIDIA, YDCPG_BNDS%KFDIA,&
& YDCPG_GMV9%U%P, YDCPG_GMV9%V%P, PP, PQ, YDVARS%U%T1, YDVARS%V%T1, LDIR=.FALSE., LDADD=.TRUE.)

IF (LDSLPHY) THEN
  CALL ROTUV (YDGEOMETRY%YRDIMV%NFLEVG, YDGEOMETRY%YRDIM%NPROMA, YDCPG_BNDS%KIDIA,&
  & YDCPG_BNDS%KFDIA, PUP9, PVP9, PP, PQ, YDA_PUP9%P, YDA_PVP9%P, LDIR=.FALSE.)
ENDIF


IF (YDMODEL%YRML_DYN%YRDYNA%LSLINL.AND.YDMODEL%YRML_DYN%YRDYNA%NGWADVSI&
  &==1.AND.YDMODEL%YRML_DYN%YRDYN%NCURRENT_ITER==0) THEN
  CALL ROTUV (YDGEOMETRY%YRDIMV%NFLEVG, YDGEOMETRY%YRDIM%NPROMA, YDCPG_BNDS&
  &%KIDIA, YDCPG_BNDS%KFDIA, YDCPG_GMV9_SI%U%P, YDCPG_GMV9_SI%V%P, PP, PQ&
  &, YDCPG_SL2%USI, YDCPG_SL2%VSI, LDIR=.FALSE., LDADD=.TRUE.)
ENDIF

IF (LHOOK) CALL DR_HOOK ('LAPINEB_ROTUV', 1, ZHOOK_HANDLE)
ENDSUBROUTINE
