SUBROUTINE MF_PHYS_INIT (YDCPG_BNDS, YDCPG_OPTS, YDMF_PHYS, YDCPG_MISC)
USE PARKIND1,ONLY:JPIM, JPRB
USE YOMHOOK,ONLY:LHOOK, DR_HOOK, JPHOOK
USE CPG_OPTS_TYPE_MOD,ONLY:CPG_BNDS_TYPE, CPG_OPTS_TYPE
USE CPG_TYPE_MOD,ONLY:CPG_MISC_TYPE
USE MF_PHYS_TYPE_MOD,ONLY:MF_PHYS_TYPE

IMPLICIT NONE


TYPE (CPG_BNDS_TYPE), INTENT (IN)::YDCPG_BNDS
TYPE (CPG_OPTS_TYPE), INTENT (IN)::YDCPG_OPTS
TYPE (MF_PHYS_TYPE), INTENT (INOUT)::YDMF_PHYS
TYPE (CPG_MISC_TYPE), INTENT (INOUT)::YDCPG_MISC


REAL (KIND=JPRB)::ZEPS0
REAL (KIND=JPHOOK)::ZHOOK_HANDLE
#include "mf_phys_init_zero.intfb.h"
IF (LHOOK) CALL DR_HOOK ('MF_PHYS_INIT', 0, ZHOOK_HANDLE)
ZEPS0=1.E-12_JPRB
CALL MF_PHYS_INIT_ZERO (YDCPG_MISC, YDCPG_OPTS, YDMF_PHYS, ZEPS0)
IF (LHOOK) CALL DR_HOOK ('MF_PHYS_INIT', 1, ZHOOK_HANDLE)
ENDSUBROUTINE MF_PHYS_INIT
