SUBROUTINE MF_PHYS_FPL_PART1_PART1 (YDCPG_BNDS, YDCPG_OPTS&
&, YDMODEL, PCPF_T0, PPFL_FPLCH, PPFL_FPLSH, PSPF_T0)
USE CPG_OPTS_TYPE_MOD,ONLY:CPG_BNDS_TYPE, CPG_OPTS_TYPE
USE PARKIND1,ONLY:JPIM, JPRB
USE TYPE_MODEL,ONLY:MODEL
USE YOMHOOK,ONLY:LHOOK, JPHOOK, DR_HOOK

IMPLICIT NONE

TYPE (CPG_BNDS_TYPE), INTENT (IN)::YDCPG_BNDS
TYPE (CPG_OPTS_TYPE), INTENT (IN)::YDCPG_OPTS
TYPE (MODEL), INTENT (IN)::YDMODEL
REAL (KIND=JPRB), INTENT (IN)::PCPF_T0 (YDCPG_OPTS%KLON, YDCPG_OPTS%KFLEVG)
REAL (KIND=JPRB), INTENT (OUT)::PPFL_FPLCH (YDCPG_OPTS%KLON, 0:YDCPG_OPTS%KFLEVG)
REAL (KIND=JPRB), INTENT (OUT)::PPFL_FPLSH (YDCPG_OPTS%KLON, 0:YDCPG_OPTS%KFLEVG)
REAL (KIND=JPRB), INTENT (IN)::PSPF_T0 (YDCPG_OPTS%KLON, YDCPG_OPTS%KFLEVG)
INTEGER (KIND=JPIM)::JLEV
INTEGER (KIND=JPIM)::JLON
REAL (KIND=JPHOOK)::ZHOOK_HANDLE
IF (LHOOK) CALL DR_HOOK ('MF_PHYS_FPL_PART1_PART1', 0, ZHOOK_HANDLE)

DO JLON=YDCPG_BNDS%KIDIA, YDCPG_BNDS%KFDIA
  PPFL_FPLCH (JLON, 0)=0.0_JPRB
  PPFL_FPLSH (JLON, 0)=0.0_JPRB
ENDDO


IF (YDMODEL%YRML_PHY_MF%YRPHY%LNEBN.OR.YDMODEL%YRML_PHY_MF&
  &%YRPHY%LNEBR.OR.YDMODEL%YRML_PHY_MF%YRPHY%LRRGUST) THEN

  DO JLEV=1, YDCPG_OPTS%KFLEVG

    DO JLON=YDCPG_BNDS%KIDIA, YDCPG_BNDS%KFDIA
      PPFL_FPLCH (JLON, JLEV)=PCPF_T0 (JLON, JLEV)
    ENDDO

  ENDDO

ENDIF


IF (YDMODEL%YRML_PHY_MF%YRPHY%LRRGUST) THEN

  DO JLEV=1, YDCPG_OPTS%KFLEVG

    DO JLON=YDCPG_BNDS%KIDIA, YDCPG_BNDS%KFDIA
      PPFL_FPLSH (JLON, JLEV)=PSPF_T0 (JLON, JLEV)
    ENDDO

  ENDDO

ENDIF

IF (LHOOK) CALL DR_HOOK ('MF_PHYS_FPL_PART1_PART1', 1, ZHOOK_HANDLE)
ENDSUBROUTINE
