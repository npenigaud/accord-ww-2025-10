SUBROUTINE RECMWF_FINISH (YDCPG_BNDS, YDCPG_OPTS, YDMODEL, PAER, PALB, PALBD, PALBP, PAPRS, PAPRSF,&
& PCEMTR, PCTRSO, PDELP, PEMTD, PEMTU, PFSDNN, PFSDNV, PLWFC, PLWFT, PMU0LU, PMU0M, PNEB, PQ, PQICE&
&, PQLIQ, PQO3, PSFSWDIF, PSFSWDIR, PSWDIF, PSWDIR, PSWFC, PSWFT, PT, PTRSO, PTRSOD, PTRSODIF, PTRSODIR&
&, PVRMOON, PFCS, PFCT, PFRSOD, PPFDIF, PPFDIR, PRCO2, PRMU0, PSWDIFFUSEBAND, PSWDIRECTBAND)
USE CPG_OPTS_TYPE_MOD,ONLY:CPG_BNDS_TYPE, CPG_OPTS_TYPE
USE PARKIND1,ONLY:JPIM, JPRB
USE TYPE_MODEL,ONLY:MODEL
USE YOMHOOK,ONLY:LHOOK, JPHOOK, DR_HOOK

IMPLICIT NONE

TYPE (CPG_BNDS_TYPE), INTENT (IN)::YDCPG_BNDS
TYPE (CPG_OPTS_TYPE), INTENT (IN)::YDCPG_OPTS
TYPE (MODEL), INTENT (IN)::YDMODEL
REAL (KIND=JPRB), INTENT (IN)::PAER (YDCPG_OPTS%KLON, YDCPG_OPTS%KFLEVG, 6)
REAL (KIND=JPRB), INTENT (INOUT)::PALB (YDCPG_OPTS%KLON)
REAL (KIND=JPRB), INTENT (IN)::PALBD (YDCPG_OPTS%KLON, YDMODEL%YRML_PHY_RAD%YRERAD%NSW)
REAL (KIND=JPRB), INTENT (IN)::PALBP (YDCPG_OPTS%KLON, YDMODEL%YRML_PHY_RAD%YRERAD%NSW)
REAL (KIND=JPRB), INTENT (IN)::PAPRS (YDCPG_OPTS%KLON, 0:YDCPG_OPTS%KFLEVG)
REAL (KIND=JPRB), INTENT (IN)::PAPRSF (YDCPG_OPTS%KLON, YDCPG_OPTS%KFLEVG)
REAL (KIND=JPRB), INTENT (OUT)::PCEMTR (YDCPG_OPTS%KLON, 0:1)
REAL (KIND=JPRB), INTENT (OUT)::PCTRSO (YDCPG_OPTS%KLON, 0:1)
REAL (KIND=JPRB), INTENT (IN)::PDELP (YDCPG_OPTS%KLON, YDCPG_OPTS%KFLEVG)
REAL (KIND=JPRB), INTENT (OUT)::PEMTD (YDCPG_OPTS%KLON, 0:YDCPG_OPTS%KFLEVG)
REAL (KIND=JPRB), INTENT (OUT)::PEMTU (YDCPG_OPTS%KLON, 0:YDCPG_OPTS%KFLEVG)
REAL (KIND=JPRB), INTENT (OUT)::PFSDNN (YDCPG_OPTS%KLON)
REAL (KIND=JPRB), INTENT (OUT)::PFSDNV (YDCPG_OPTS%KLON)
REAL (KIND=JPRB), INTENT (OUT)::PLWFC (YDCPG_OPTS%KLON, 0:1)
REAL (KIND=JPRB), INTENT (OUT)::PLWFT (YDCPG_OPTS%KLON, 0:YDCPG_OPTS%KFLEVG, 1:YDCPG_OPTS%KTSSG+1)
REAL (KIND=JPRB), INTENT (IN)::PMU0LU (YDCPG_OPTS%KLON)
REAL (KIND=JPRB), INTENT (IN)::PMU0M (YDCPG_OPTS%KLON)
REAL (KIND=JPRB), INTENT (IN)::PNEB (YDCPG_OPTS%KLON, YDCPG_OPTS%KFLEVG)
REAL (KIND=JPRB), INTENT (IN)::PQ (YDCPG_OPTS%KLON, YDCPG_OPTS%KFLEVG)
REAL (KIND=JPRB), INTENT (IN)::PQICE (YDCPG_OPTS%KLON, YDCPG_OPTS%KFLEVG)
REAL (KIND=JPRB), INTENT (IN)::PQLIQ (YDCPG_OPTS%KLON, YDCPG_OPTS%KFLEVG)
REAL (KIND=JPRB), INTENT (IN)::PQO3 (YDCPG_OPTS%KLON, 0:YDCPG_OPTS%KFLEVG)
REAL (KIND=JPRB), INTENT (OUT)::PSFSWDIF (YDCPG_OPTS%KLON, YDMODEL%YRML_PHY_RAD%YRERAD%NSW)
REAL (KIND=JPRB), INTENT (OUT)::PSFSWDIR (YDCPG_OPTS%KLON, YDMODEL%YRML_PHY_RAD%YRERAD%NSW)
REAL (KIND=JPRB), INTENT (INOUT)::PSWDIF (YDCPG_OPTS%KLON, YDCPG_OPTS%KSW)
REAL (KIND=JPRB), INTENT (INOUT)::PSWDIR (YDCPG_OPTS%KLON, YDCPG_OPTS%KSW)
REAL (KIND=JPRB), INTENT (OUT)::PSWFC (YDCPG_OPTS%KLON, 0:1)
REAL (KIND=JPRB), INTENT (OUT)::PSWFT (YDCPG_OPTS%KLON, 0:YDCPG_OPTS%KFLEVG, 1:YDCPG_OPTS%KTSSG+1)
REAL (KIND=JPRB), INTENT (IN)::PT (YDCPG_OPTS%KLON, YDCPG_OPTS%KFLEVG)
REAL (KIND=JPRB), INTENT (OUT)::PTRSO (YDCPG_OPTS%KLON, 0:YDCPG_OPTS%KFLEVG)
REAL (KIND=JPRB), INTENT (OUT)::PTRSOD (YDCPG_OPTS%KLON)
REAL (KIND=JPRB), INTENT (OUT)::PTRSODIF (YDCPG_OPTS%KLON, YDMODEL%YRML_PHY_RAD%YRERAD%NSW)
REAL (KIND=JPRB), INTENT (OUT)::PTRSODIR (YDCPG_OPTS%KLON, YDMODEL%YRML_PHY_RAD%YRERAD%NSW)
REAL (KIND=JPRB), INTENT (INOUT)::PVRMOON (YDCPG_OPTS%KLON)
REAL (KIND=JPRB), INTENT (INOUT)::PFCS (YDCPG_OPTS%KLON, 0:YDCPG_OPTS%KFLEVG)
REAL (KIND=JPRB), INTENT (INOUT)::PFCT (YDCPG_OPTS%KLON, 0:YDCPG_OPTS%KFLEVG)
REAL (KIND=JPRB), INTENT (INOUT)::PFRSOD (YDCPG_OPTS%KLON)
REAL (KIND=JPRB), INTENT (INOUT)::PPFDIF (YDCPG_OPTS%KLON)
REAL (KIND=JPRB), INTENT (INOUT)::PPFDIR (YDCPG_OPTS%KLON)
REAL (KIND=JPRB), INTENT (INOUT)::PRCO2 (YDCPG_OPTS%KLON, YDCPG_OPTS%KFLEVG)
REAL (KIND=JPRB), INTENT (INOUT)::PRMU0 (YDCPG_OPTS%KLON)
REAL (KIND=JPRB), INTENT (INOUT)::PSWDIFFUSEBAND (YDCPG_OPTS%KLON, YDMODEL%YRML_PHY_RAD%YRERAD%NSW)
REAL (KIND=JPRB), INTENT (INOUT)::PSWDIRECTBAND (YDCPG_OPTS%KLON, YDMODEL%YRML_PHY_RAD%YRERAD%NSW)
#include "acralu.intfb.h"
INTEGER (KIND=JPIM)::JLEV
INTEGER (KIND=JPIM)::JLON
INTEGER (KIND=JPIM)::JSW
REAL (KIND=JPRB)::ZSW
REAL (KIND=JPHOOK)::ZHOOK_HANDLE
IF (LHOOK) CALL DR_HOOK ('RECMWF_FINISH', 0, ZHOOK_HANDLE)

DO JLON=YDCPG_BNDS%KIDIA, YDCPG_BNDS%KFDIA
  PSWFC (JLON, 0)=PFCS (JLON, 0)
  PSWFC (JLON, 1)=PFCS (JLON, YDCPG_OPTS%KFLEVG+0)
  PLWFC (JLON, 0)=PFCT (JLON, 0)
  PLWFC (JLON, 1)=PFCT (JLON, YDCPG_OPTS%KFLEVG+0)
ENDDO

PEMTD=PLWFT (:,:, 1)
PEMTU=PLWFT (:,:, 1)
PCEMTR=PLWFC

IF (YDMODEL%YRML_PHY_RAD%YRERAD%LSRTM.AND..NOT.YDMODEL%YRML_PHY_RAD%YRERAD%LUSEPRE2017RAD) THEN

  DO JSW=1, YDMODEL%YRML_PHY_RAD%YRERAD%NSW

    DO JLON=YDCPG_BNDS%KIDIA, YDCPG_BNDS%KFDIA
      PTRSODIR (JLON, JSW)=PSWDIRECTBAND (JLON, JSW)*(1.-PALBP (JLON&
      &, JSW))/(YDMODEL%YRML_PHY_MF%YRPHY3%RII0*PRMU0 (JLON))
      PTRSODIF (JLON, JSW)=PSWDIFFUSEBAND (JLON, JSW)*(1.-PALBD (JLON&
      &, JSW))/(YDMODEL%YRML_PHY_MF%YRPHY3%RII0*PRMU0 (JLON))
    ENDDO

  ENDDO

ELSEIF (YDMODEL%YRML_PHY_RAD%YRERAD%LSRTM.AND.YDMODEL%YRML_PHY_RAD%YRERAD%LUSEPRE2017RAD) THEN
  ZSW=REAL (YDMODEL%YRML_PHY_RAD%YRERAD%NSW, JPRB)

  DO JSW=1, YDMODEL%YRML_PHY_RAD%YRERAD%NSW

    DO JLON=YDCPG_BNDS%KIDIA, YDCPG_BNDS%KFDIA
      PTRSODIR (JLON, JSW)=PPFDIR (JLON)*(1.-PALBP (JLON, JSW)&
      &)/ZSW/(YDMODEL%YRML_PHY_MF%YRPHY3%RII0*PRMU0 (JLON))
      PTRSODIF (JLON, JSW)=PPFDIF (JLON)*(1.-PALBD (JLON, JSW)&
      &)/ZSW/(YDMODEL%YRML_PHY_MF%YRPHY3%RII0*PRMU0 (JLON))
    ENDDO

  ENDDO

ELSE

  DO JSW=1, YDMODEL%YRML_PHY_RAD%YRERAD%NSW

    DO JLON=YDCPG_BNDS%KIDIA, YDCPG_BNDS%KFDIA
      PTRSODIR (JLON, JSW)=PSFSWDIR (JLON, JSW)*(1.-PALBP (JLON&
      &, JSW))/(YDMODEL%YRML_PHY_MF%YRPHY3%RII0*PRMU0 (JLON))
      PTRSODIF (JLON, JSW)=PSFSWDIF (JLON, JSW)*(1.-PALBD (JLON&
      &, JSW))/(YDMODEL%YRML_PHY_MF%YRPHY3%RII0*PRMU0 (JLON))
    ENDDO

  ENDDO

ENDIF


DO JLEV=0, YDCPG_OPTS%KFLEVG

  DO JLON=YDCPG_BNDS%KIDIA, YDCPG_BNDS%KFDIA
    PTRSO (JLON, JLEV)=PSWFT (JLON, JLEV, 1)/(YDMODEL%YRML_PHY_MF%YRPHY3%RII0*PRMU0 (JLON))
  ENDDO

ENDDO


DO JLEV=0, 1

  DO JLON=YDCPG_BNDS%KIDIA, YDCPG_BNDS%KFDIA
    PCTRSO (JLON, JLEV)=PSWFC (JLON, JLEV)/(YDMODEL%YRML_PHY_MF%YRPHY3%RII0*PRMU0 (JLON))
  ENDDO

ENDDO


DO JLON=YDCPG_BNDS%KIDIA, YDCPG_BNDS%KFDIA
  PTRSOD (JLON)=MAX (0.0_JPRB, MIN (1.0_JPRB, PFRSOD (JLON&
  &)/(YDMODEL%YRML_PHY_MF%YRPHY3%RII0*PRMU0 (JLON))))
ENDDO


DO JLON=YDCPG_BNDS%KIDIA, YDCPG_BNDS%KFDIA

  IF (PMU0M (JLON)<1.E-10_JPRB) THEN
    PRMU0 (JLON)=0.0_JPRB
    PSFSWDIR (JLON,:)=0.0_JPRB
    PSFSWDIF (JLON,:)=0.0_JPRB
  ENDIF

ENDDO


IF (YDMODEL%YRML_PHY_MF%YRARPHY%LMSE) THEN

  IF (YDMODEL%YRML_PHY_RAD%YRERAD%NSW==6) THEN

    DO JSW=1, YDMODEL%YRML_PHY_RAD%YRERAD%NSW
      PSWDIR (YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA, JSW)=PTRSODIR (YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA, JSW)
      PSWDIF (YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA, JSW)=PTRSODIF (YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA, JSW)
    ENDDO

  ELSEIF (YDMODEL%YRML_PHY_RAD%YRERAD%NSW==1) THEN
    PSWDIR (YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA, 1)=PFSDNN (YDCPG_BNDS%KIDIA&
    &:YDCPG_BNDS%KFDIA)+PFSDNV (YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA)
    PSWDIF (YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA, 1)=0.0_JPRB
  ENDIF

ENDIF


IF (YDMODEL%YRML_PHY_MF%YRPHY%LRAYLU) THEN
  CALL ACRALU (YDMODEL%YRML_GCONF%YRRIP, YDMODEL%YRML_PHY_MF%YRPHY3, YDCPG_BNDS%KIDIA, YDCPG_BNDS&
  &%KFDIA, YDCPG_OPTS%KLON, YDMODEL%YRML_PHY_MF%YRTOPH%NTRADI, YDCPG_OPTS%KFLEVG, PAPRS, PAPRSF&
  &, PDELP, PNEB, PQ, PRCO2, PQICE, PQLIQ, PQO3, PT, PALB, PMU0LU, PVRMOON, PAER)
ENDIF

IF (LHOOK) CALL DR_HOOK ('RECMWF_FINISH', 1, ZHOOK_HANDLE)
ENDSUBROUTINE
