SUBROUTINE APL_ARPEGE_ZDPHIV (YDCPG_BNDS, YDCPG_OPTS, YDMODEL, PDPHIT, PDPHIV)
USE CPG_OPTS_TYPE_MOD,ONLY:CPG_BNDS_TYPE, CPG_OPTS_TYPE
USE PARKIND1,ONLY:JPIM, JPRB
USE TYPE_MODEL,ONLY:MODEL
USE YOMHOOK,ONLY:LHOOK, JPHOOK, DR_HOOK

IMPLICIT NONE

TYPE (CPG_BNDS_TYPE), INTENT (IN)::YDCPG_BNDS
TYPE (CPG_OPTS_TYPE), INTENT (IN)::YDCPG_OPTS
TYPE (MODEL), INTENT (IN)::YDMODEL
REAL (KIND=JPRB), INTENT (INOUT)::PDPHIT (YDCPG_OPTS%KLON)
REAL (KIND=JPRB), INTENT (INOUT)::PDPHIV (YDCPG_OPTS%KLON)
INTEGER (KIND=JPIM)::JLON
REAL (KIND=JPHOOK)::ZHOOK_HANDLE
IF (LHOOK) CALL DR_HOOK ('APL_ARPEGE_ZDPHIV', 0, ZHOOK_HANDLE)

DO JLON=YDCPG_BNDS%KIDIA, YDCPG_BNDS%KFDIA
  PDPHIV (JLON)=YDMODEL%YRCST%RG*YDMODEL%YRML_PHY_MF%YRPHY2%HVCLS
  PDPHIT (JLON)=YDMODEL%YRCST%RG*YDMODEL%YRML_PHY_MF%YRPHY2%HTCLS
ENDDO

IF (LHOOK) CALL DR_HOOK ('APL_ARPEGE_ZDPHIV', 1, ZHOOK_HANDLE)
ENDSUBROUTINE
