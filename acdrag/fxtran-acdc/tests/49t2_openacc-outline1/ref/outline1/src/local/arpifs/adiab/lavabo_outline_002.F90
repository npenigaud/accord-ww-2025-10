SUBROUTINE LAVABO_OUTLINE_002 (JLEV1, JLEV2, YDCPG_BNDS, YDCPG_SL1, YDMODEL)
USE CPG_OPTS_TYPE_MOD,ONLY:CPG_OPTS_TYPE, CPG_BNDS_TYPE
USE CPG_SL1_TYPE_MOD,ONLY:CPG_SL1B_TYPE
USE PARKIND1,ONLY:JPRB, JPIM
USE TYPE_MODEL,ONLY:MODEL
USE YOMHOOK,ONLY:LHOOK, JPHOOK, DR_HOOK

IMPLICIT NONE

INTEGER (KIND=JPIM), INTENT (INOUT)::JLEV1
INTEGER (KIND=JPIM), INTENT (INOUT)::JLEV2
TYPE (CPG_BNDS_TYPE), INTENT (IN)::YDCPG_BNDS
TYPE (CPG_SL1B_TYPE), INTENT (INOUT)::YDCPG_SL1
TYPE (MODEL), INTENT (IN)::YDMODEL
REAL (KIND=JPHOOK)::ZHOOK_HANDLE
IF (LHOOK) CALL DR_HOOK ('LAVABO_OUTLINE_002', 0, ZHOOK_HANDLE)

IF (YDMODEL%YRML_DYN%YRDYN%NWLAG/=2) THEN
  YDCPG_SL1%U0%P (YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA, JLEV1)=0.0_JPRB
  YDCPG_SL1%U0%P (YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA, JLEV2)=0.0_JPRB
  YDCPG_SL1%V0%P (YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA, JLEV1)=0.0_JPRB
  YDCPG_SL1%V0%P (YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA, JLEV2)=0.0_JPRB

  IF (YDMODEL%YRML_DYN%YRDYN%LRHS_CURV) THEN
    YDCPG_SL1%Z0%P (YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA, JLEV1)=0.0_JPRB
    YDCPG_SL1%Z0%P (YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA, JLEV2)=0.0_JPRB
  ENDIF

ENDIF


IF (YDMODEL%YRML_DYN%YRDYN%NTLAG/=2) THEN
  YDCPG_SL1%T0%P (YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA, JLEV1)=0.0_JPRB
  YDCPG_SL1%T0%P (YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA, JLEV2)=0.0_JPRB
ENDIF


IF (YDMODEL%YRML_DYN%YRDYNA%LNHDYN) THEN
  YDCPG_SL1%PD9%P (YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA, JLEV1)=0.0_JPRB
  YDCPG_SL1%PD9%P (YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA, JLEV2)=0.0_JPRB

  IF (YDMODEL%YRML_DYN%YRDYN%NSPDLAG/=2) THEN
    YDCPG_SL1%PD0%P (YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA, JLEV1)=0.0_JPRB
    YDCPG_SL1%PD0%P (YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA, JLEV2)=0.0_JPRB
  ENDIF

ENDIF


IF (YDMODEL%YRML_DYN%YRDYNA%LNHDYN) THEN
  YDCPG_SL1%VD9%P (YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA, JLEV1)=0.0_JPRB
  YDCPG_SL1%VD9%P (YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA, JLEV2)=0.0_JPRB

  IF (YDMODEL%YRML_DYN%YRDYN%NSPLTHOI/=0) THEN
    YDCPG_SL1%VDF9%P (YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA, JLEV1)=0.0_JPRB
    YDCPG_SL1%VDF9%P (YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA, JLEV2)=0.0_JPRB
  ENDIF


  IF (YDMODEL%YRML_DYN%YRDYNA%NVDVAR==4.OR.YDMODEL%YRML_DYN%YRDYNA%NVDVAR==5) THEN
    YDCPG_SL1%NHX9%P (YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA, JLEV1)=0.0_JPRB
    YDCPG_SL1%NHX9%P (YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA, JLEV2)=0.0_JPRB
  ENDIF


  IF (YDMODEL%YRML_DYN%YRDYN%NSVDLAG/=2) THEN
    YDCPG_SL1%VD0%P (YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA, JLEV1)=0.0_JPRB
    YDCPG_SL1%VD0%P (YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA, JLEV2)=0.0_JPRB
  ENDIF

ENDIF


IF (YDMODEL%YRML_DYN%YRDYN%NVLAG==2.OR.YDMODEL%YRML_DYN%YRDYN%NVLAG==3) THEN
  YDCPG_SL1%C9%P (YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA, JLEV1)=0.0_JPRB
  YDCPG_SL1%C9%P (YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA, JLEV2)=0.0_JPRB

  IF (YDMODEL%YRML_DYN%YRDYNA%LSLINLC2) THEN
    YDCPG_SL1%C9_SI%P (YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA, JLEV1)=0.0_JPRB
  ENDIF


  IF (YDMODEL%YRML_DYN%YRDYNA%LSLINLC2) THEN
    YDCPG_SL1%C9_SI%P (YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA, JLEV2)=0.0_JPRB
  ENDIF

ENDIF

IF (LHOOK) CALL DR_HOOK ('LAVABO_OUTLINE_002', 1, ZHOOK_HANDLE)
ENDSUBROUTINE
