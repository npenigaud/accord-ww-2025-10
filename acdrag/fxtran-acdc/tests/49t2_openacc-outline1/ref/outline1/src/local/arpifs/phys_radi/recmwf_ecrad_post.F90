SUBROUTINE RECMWF_ECRAD_POST (YDCPG_BNDS, YDCPG_OPTS, PFRSOD, PPFDIF, PPFDIR)
USE CPG_OPTS_TYPE_MOD,ONLY:CPG_BNDS_TYPE, CPG_OPTS_TYPE
USE PARKIND1,ONLY:JPIM, JPRB
USE YOMHOOK,ONLY:LHOOK, JPHOOK, DR_HOOK

IMPLICIT NONE

TYPE (CPG_BNDS_TYPE), INTENT (IN)::YDCPG_BNDS
TYPE (CPG_OPTS_TYPE), INTENT (IN)::YDCPG_OPTS
REAL (KIND=JPRB), INTENT (INOUT)::PFRSOD (YDCPG_OPTS%KLON)
REAL (KIND=JPRB), INTENT (INOUT)::PPFDIF (YDCPG_OPTS%KLON)
REAL (KIND=JPRB), INTENT (INOUT)::PPFDIR (YDCPG_OPTS%KLON)
REAL (KIND=JPHOOK)::ZHOOK_HANDLE
IF (LHOOK) CALL DR_HOOK ('RECMWF_ECRAD_POST', 0, ZHOOK_HANDLE)
PPFDIF (YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA)=PFRSOD (YDCPG_BNDS%KIDIA&
&:YDCPG_BNDS%KFDIA)-PPFDIR (YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA)
IF (LHOOK) CALL DR_HOOK ('RECMWF_ECRAD_POST', 1, ZHOOK_HANDLE)
ENDSUBROUTINE
