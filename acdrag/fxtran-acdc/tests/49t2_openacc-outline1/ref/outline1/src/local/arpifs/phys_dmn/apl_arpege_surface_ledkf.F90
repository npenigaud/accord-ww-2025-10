SUBROUTINE APL_ARPEGE_SURFACE_LEDKF (YDCPG_BNDS, YDCPG_OPTS, YDMF_PHYS, YDMF_PHYS_SURF)
USE CPG_OPTS_TYPE_MOD,ONLY:CPG_BNDS_TYPE, CPG_OPTS_TYPE
USE PARKIND1,ONLY:JPIM, JPRB
USE MF_PHYS_SURFACE_TYPE_MOD,ONLY:MF_PHYS_SURF_TYPE
USE MF_PHYS_TYPE_MOD,ONLY:MF_PHYS_TYPE
USE YOMHOOK,ONLY:LHOOK, JPHOOK, DR_HOOK

IMPLICIT NONE

TYPE (CPG_BNDS_TYPE), INTENT (IN)::YDCPG_BNDS
TYPE (CPG_OPTS_TYPE), INTENT (IN)::YDCPG_OPTS
TYPE (MF_PHYS_TYPE), INTENT (INOUT)::YDMF_PHYS
TYPE (MF_PHYS_SURF_TYPE), INTENT (INOUT)::YDMF_PHYS_SURF
INTEGER (KIND=JPIM)::JLON
REAL (KIND=JPHOOK)::ZHOOK_HANDLE
IF (LHOOK) CALL DR_HOOK ('APL_ARPEGE_SURFACE_LEDKF', 0, ZHOOK_HANDLE)

DO JLON=YDCPG_BNDS%KIDIA, YDCPG_BNDS%KFDIA
  YDMF_PHYS_SURF%GSD_SFL%PGROUP (JLON, 1)=YDMF_PHYS%OUT%DIFTS (JLON, YDCPG_OPTS%KFLEVG)
  YDMF_PHYS_SURF%GSD_SFL%PGROUP (JLON, 2)=YDMF_PHYS%OUT%DIFTQ (JLON, YDCPG_OPTS%KFLEVG)
ENDDO

IF (LHOOK) CALL DR_HOOK ('APL_ARPEGE_SURFACE_LEDKF', 1, ZHOOK_HANDLE)
ENDSUBROUTINE
