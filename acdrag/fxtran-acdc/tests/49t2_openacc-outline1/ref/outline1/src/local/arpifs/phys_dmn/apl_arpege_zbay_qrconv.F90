SUBROUTINE APL_ARPEGE_ZBAY_QRCONV (YDCPG_BNDS, YDCPG_OPTS&
&, YDMF_PHYS, YDMODEL, PBAY_QRCONV, PBAY_QSCONV)
USE CPG_OPTS_TYPE_MOD,ONLY:CPG_BNDS_TYPE, CPG_OPTS_TYPE
USE PARKIND1,ONLY:JPIM, JPRB
USE MF_PHYS_TYPE_MOD,ONLY:MF_PHYS_TYPE
USE TYPE_MODEL,ONLY:MODEL
USE YOMHOOK,ONLY:LHOOK, JPHOOK, DR_HOOK

IMPLICIT NONE

TYPE (CPG_BNDS_TYPE), INTENT (IN)::YDCPG_BNDS
TYPE (CPG_OPTS_TYPE), INTENT (IN)::YDCPG_OPTS
TYPE (MF_PHYS_TYPE), INTENT (INOUT)::YDMF_PHYS
TYPE (MODEL), INTENT (IN)::YDMODEL
REAL (KIND=JPRB), INTENT (INOUT)::PBAY_QRCONV (YDCPG_OPTS%KLON, 1:YDCPG_OPTS%KFLEVG)
REAL (KIND=JPRB), INTENT (INOUT)::PBAY_QSCONV (YDCPG_OPTS%KLON, 1:YDCPG_OPTS%KFLEVG)
INTEGER (KIND=JPIM)::JLEV
INTEGER (KIND=JPIM)::JLON
REAL (KIND=JPHOOK)::ZHOOK_HANDLE
IF (LHOOK) CALL DR_HOOK ('APL_ARPEGE_ZBAY_QRCONV', 0, ZHOOK_HANDLE)

DO JLEV=1, YDCPG_OPTS%KFLEVG

  DO JLON=YDCPG_BNDS%KIDIA, YDCPG_BNDS%KFDIA
    PBAY_QRCONV (JLON, JLEV)=0.001_JPRB*((ABS (YDMF_PHYS%OUT%FPLCL (JLON, JLEV))*YDMODEL&
    &%YRML_PHY_MF%YRPHY0%RCOEFRAIN (1))**YDMODEL%YRML_PHY_MF%YRPHY0%RCOEFRAIN (2))
    PBAY_QSCONV (JLON, JLEV)=0.001_JPRB*((ABS (YDMF_PHYS%OUT%FPLCN (JLON, JLEV))*YDMODEL&
    &%YRML_PHY_MF%YRPHY0%RCOEFSNOW (1))**YDMODEL%YRML_PHY_MF%YRPHY0%RCOEFSNOW (2))
  ENDDO

ENDDO

IF (LHOOK) CALL DR_HOOK ('APL_ARPEGE_ZBAY_QRCONV', 1, ZHOOK_HANDLE)
ENDSUBROUTINE
