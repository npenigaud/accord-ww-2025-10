&%KFDIA, YDCPG_OPTS%KLON, YDCPG_OPTS%KFLEVG, YDCPG_OPTS%KTDIA, YDMODEL%YRML_PHY_G&
&%YRDPHY%NTSSG, YDVARS%GEOMETRY%GEMU%T0, PRDG_MU0, YDMF_PHYS_BASE_STATE%YCPG_DYN&
&%PHIF, YDMF_PHYS_BASE_STATE%YCPG_PHY%XYB%DELP, YDMF_PHYS%OUT%FRSO)
