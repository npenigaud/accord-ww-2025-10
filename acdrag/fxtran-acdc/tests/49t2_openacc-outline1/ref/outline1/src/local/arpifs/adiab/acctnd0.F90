SUBROUTINE ACCTND0 (YDCST, LDT1, YDCPG_OPTS, YDCPG_BNDS, PRCP, PACC, PTND)
USE PARKIND1,ONLY:JPIM, JPRB
USE YOMHOOK,ONLY:LHOOK, DR_HOOK, JPHOOK
USE CPG_OPTS_TYPE_MOD,ONLY:CPG_OPTS_TYPE, CPG_BNDS_TYPE
USE YOMCST,ONLY:TCST

IMPLICIT NONE


TYPE (TCST), INTENT (IN)::YDCST
LOGICAL, INTENT (IN)::LDT1
TYPE (CPG_OPTS_TYPE), INTENT (IN)::YDCPG_OPTS
TYPE (CPG_BNDS_TYPE), INTENT (IN)::YDCPG_BNDS
REAL (KIND=JPRB), INTENT (IN)::PRCP
REAL (KIND=JPRB), INTENT (INOUT)::PACC (YDCPG_OPTS%KLON, YDCPG_OPTS%KFLEVG)
REAL (KIND=JPRB), INTENT (IN)::PTND (YDCPG_OPTS%KLON, YDCPG_OPTS%KFLEVG)


REAL (KIND=JPHOOK)::ZHOOK_HANDLE
#include "acctnd0_pacc.intfb.h"
IF (LHOOK) CALL DR_HOOK ('ACCTND0', 0, ZHOOK_HANDLE)

IF (LDT1) THEN
  CALL ACCTND0_PACC (PRCP, YDCPG_BNDS, YDCPG_OPTS, YDCST, PACC, PTND)
ENDIF

IF (LHOOK) CALL DR_HOOK ('ACCTND0', 1, ZHOOK_HANDLE)
ENDSUBROUTINE ACCTND0
