SUBROUTINE APL_ARPEGE_SURFACE_UPDATE_LMSE_T (LDCONFX, YDCPG_BNDS, YDCPG_GPAR, YDMF_PHYS_SURF)
USE CPG_OPTS_TYPE_MOD,ONLY:CPG_BNDS_TYPE, CPG_OPTS_TYPE
USE CPG_TYPE_MOD,ONLY:CPG_GPAR_TYPE
USE PARKIND1,ONLY:JPIM, JPRB
USE MF_PHYS_SURFACE_TYPE_MOD,ONLY:MF_PHYS_SURF_TYPE
USE YOMHOOK,ONLY:LHOOK, JPHOOK, DR_HOOK

IMPLICIT NONE

LOGICAL, INTENT (IN)::LDCONFX
TYPE (CPG_BNDS_TYPE), INTENT (IN)::YDCPG_BNDS
TYPE (CPG_GPAR_TYPE), INTENT (INOUT)::YDCPG_GPAR
TYPE (MF_PHYS_SURF_TYPE), INTENT (INOUT)::YDMF_PHYS_SURF
INTEGER (KIND=JPIM)::JLON
REAL (KIND=JPHOOK)::ZHOOK_HANDLE
IF (LHOOK) CALL DR_HOOK ('APL_ARPEGE_SURFACE_UPDATE_LMSE_T', 0, ZHOOK_HANDLE)

IF (LDCONFX) THEN

  DO JLON=YDCPG_BNDS%KIDIA, YDCPG_BNDS%KFDIA
    YDMF_PHYS_SURF%GSP_RR%PT_T0 (JLON)=YDCPG_GPAR%VTS (JLON)
  ENDDO

ELSE

  DO JLON=YDCPG_BNDS%KIDIA, YDCPG_BNDS%KFDIA
    YDMF_PHYS_SURF%GSP_RR%PT_T1 (JLON)=YDCPG_GPAR%VTS (JLON)
  ENDDO

ENDIF

IF (LHOOK) CALL DR_HOOK ('APL_ARPEGE_SURFACE_UPDATE_LMSE_T', 1, ZHOOK_HANDLE)
ENDSUBROUTINE
