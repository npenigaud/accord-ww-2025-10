SUBROUTINE LAPINEA_LARCINA (LDINTV, KHVI, KROT, KTIP, YDA_KL0, YDA_KLH0, YDA_KNOWENO&
&, YDA_KVSEPC, YDA_KVSEPL, YDA_PCCO, YDA_PLSCAW, YDA_PRSCAW, YDA_PSCO, YDCPG_BNDS, YDCPG_OPTS&
&, YDCPG_SL2, YDGEOMETRY, YDMASK_SL2, YDMODEL, YDSL, YDVARS, KLEV, PLEV)
USE CPG_OPTS_TYPE_MOD,ONLY:CPG_OPTS_TYPE, CPG_BNDS_TYPE
USE CPG_SL2_TYPE_MOD,ONLY:CPG_SL2_TYPE
USE CPG_SL_MASK_TYPE_MOD,ONLY:CPG_SL_MASK_TYPE
USE FIELD_ARRAY_MODULE,ONLY:FIELD_3RB_ARRAY, FIELD_2IM_ARRAY&
&, FIELD_4RB_ARRAY, FIELD_4IM_ARRAY, FIELD_3IM_ARRAY
USE FIELD_VARIABLES_MOD,ONLY:FIELD_VARIABLES
USE GEOMETRY_MOD,ONLY:GEOMETRY
USE PARKIND1,ONLY:JPIM, JPRB
USE TYPE_MODEL,ONLY:MODEL
USE EINT_MOD,ONLY:SL_STRUCT
USE YOMHOOK,ONLY:LHOOK, JPHOOK, DR_HOOK

IMPLICIT NONE

LOGICAL, INTENT (INOUT)::LDINTV
INTEGER (KIND=JPIM), INTENT (INOUT)::KHVI
INTEGER (KIND=JPIM), INTENT (INOUT)::KROT
INTEGER (KIND=JPIM), INTENT (INOUT)::KTIP
TYPE (FIELD_4IM_ARRAY), INTENT (IN)::YDA_KL0
TYPE (FIELD_4IM_ARRAY), INTENT (IN)::YDA_KLH0
TYPE (FIELD_3IM_ARRAY), INTENT (IN)::YDA_KNOWENO
TYPE (FIELD_2IM_ARRAY), INTENT (INOUT)::YDA_KVSEPC
TYPE (FIELD_2IM_ARRAY), INTENT (INOUT)::YDA_KVSEPL
TYPE (FIELD_4RB_ARRAY), INTENT (IN)::YDA_PCCO
TYPE (FIELD_4RB_ARRAY), INTENT (IN)::YDA_PLSCAW
TYPE (FIELD_4RB_ARRAY), INTENT (IN)::YDA_PRSCAW
TYPE (FIELD_4RB_ARRAY), INTENT (IN)::YDA_PSCO
TYPE (CPG_BNDS_TYPE), INTENT (IN)::YDCPG_BNDS
TYPE (CPG_OPTS_TYPE), INTENT (IN)::YDCPG_OPTS
TYPE (CPG_SL2_TYPE), INTENT (IN)::YDCPG_SL2
TYPE (GEOMETRY), INTENT (IN)::YDGEOMETRY
TYPE (CPG_SL_MASK_TYPE), INTENT (INOUT)::YDMASK_SL2
TYPE (MODEL), INTENT (IN)::YDMODEL
TYPE (SL_STRUCT), INTENT (IN)::YDSL
TYPE (FIELD_VARIABLES), INTENT (INOUT)::YDVARS
INTEGER (KIND=JPIM), INTENT (INOUT)::KLEV (YDGEOMETRY%YRDIM%NPROMA, YDGEOMETRY%YRDIMV%NFLEVG)
REAL (KIND=JPRB), INTENT (INOUT)::PLEV (YDGEOMETRY%YRDIM%NPROMA, YDGEOMETRY%YRDIMV%NFLEVG)
#include "larcina.intfb.h"
REAL (KIND=JPRB)::ZUF0 (1)
REAL (KIND=JPRB)::ZURL0 (1)
REAL (KIND=JPRB)::ZVF0 (1)
REAL (KIND=JPRB)::ZVRL0 (1)
REAL (KIND=JPRB)::ZWF0 (1)
REAL (KIND=JPRB)::ZWFSM (1)
REAL (KIND=JPRB)::ZWRL0 (1)
REAL (KIND=JPRB)::ZZF0 (1)
REAL (KIND=JPRB)::ZZRL0 (1)
INTEGER (KIND=JPIM)::IDEP (YDGEOMETRY%YRDIM%NPROMA, YDGEOMETRY%YRDIMV%NFLEVG)
REAL (KIND=JPHOOK)::ZHOOK_HANDLE
IF (LHOOK) CALL DR_HOOK ('LAPINEA_LARCINA', 0, ZHOOK_HANDLE)
CALL LARCINA (YDGEOMETRY, YDMODEL%YRCST, YDMODEL%YRML_DYN, YDCPG_BNDS%KIDIA, YDCPG_BNDS%KFDIA&
&, YDSL, KHVI, YDMODEL%YRML_DYN%YRDYN%LFINDVSEP, YDMODEL%YRML_DYN%YRDYNA%LSLHD, YDMODEL%YRML_DYN&
&%YRDYNA%LSLHDQUAD, LDINTV, KTIP, KROT, YDCPG_OPTS%LRPLANE, YDA_PSCO%P, PLEV, YDCPG_SL2%KAPPA&
&, YDCPG_SL2%KAPPAT, YDCPG_SL2%KAPPAM, YDCPG_SL2%KAPPAH, YDCPG_SL2%STDDISU, YDCPG_SL2%STDDISV&
&, YDCPG_SL2%STDDISW, ZURL0, ZVRL0, ZZRL0, ZWRL0, YDMASK_SL2%P, YDA_KVSEPC%P, YDA_KVSEPL%P, YDA_PCCO&
&%P, ZUF0, ZVF0, ZZF0, ZWF0, ZWFSM, YDA_KL0%P, YDA_KLH0%P, KLEV, YDA_PLSCAW%P, YDA_PRSCAW%P, IDEP&
&, YDA_KNOWENO%P, YDVARS%GEOMETRY%RCOLON%P, YDVARS%GEOMETRY%RSILON%P, YDVARS%GEOMETRY%GECLO%P&
&, YDVARS%GEOMETRY%GEMU%P, YDVARS%GEOMETRY%GESLO%P, YDVARS%GEOMETRY%GSQM2%P)
IF (LHOOK) CALL DR_HOOK ('LAPINEA_LARCINA', 1, ZHOOK_HANDLE)
ENDSUBROUTINE
