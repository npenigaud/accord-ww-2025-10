SUBROUTINE APL_ARPEGE_SURFACE_UPDATE (YDCPG_BNDS, YDCPG_OPTS, YDCPG_GPAR, YDMF_PHYS,&
& YDMF_PHYS_SURF, YDMODEL, LDCONFX, PDTPHY, PDSA_C1, PDSA_C2, PFLU_FEVI, PFLU_VEG)
USE YOMHOOK,ONLY:LHOOK, DR_HOOK, JPHOOK
USE PARKIND1,ONLY:JPIM, JPRB
USE CPG_OPTS_TYPE_MOD,ONLY:CPG_BNDS_TYPE, CPG_OPTS_TYPE
USE CPG_TYPE_MOD,ONLY:CPG_GPAR_TYPE
USE MF_PHYS_TYPE_MOD,ONLY:MF_PHYS_TYPE
USE MF_PHYS_SURFACE_TYPE_MOD,ONLY:MF_PHYS_SURF_TYPE
USE TYPE_MODEL,ONLY:MODEL

IMPLICIT NONE


TYPE (CPG_BNDS_TYPE), INTENT (IN)::YDCPG_BNDS
TYPE (CPG_OPTS_TYPE), INTENT (IN)::YDCPG_OPTS
TYPE (CPG_GPAR_TYPE), INTENT (INOUT)::YDCPG_GPAR
TYPE (MF_PHYS_TYPE), INTENT (INOUT)::YDMF_PHYS
TYPE (MF_PHYS_SURF_TYPE), INTENT (INOUT)::YDMF_PHYS_SURF
TYPE (MODEL), INTENT (IN)::YDMODEL
LOGICAL, INTENT (IN)::LDCONFX
REAL (KIND=JPRB), INTENT (IN)::PDTPHY
REAL (KIND=JPRB), INTENT (IN)::PDSA_C1 (YDCPG_OPTS%KLON)
REAL (KIND=JPRB), INTENT (IN)::PDSA_C2 (YDCPG_OPTS%KLON)
REAL (KIND=JPRB), INTENT (IN)::PFLU_FEVI (YDCPG_OPTS%KLON, YDCPG_OPTS%KTSSG+1)
REAL (KIND=JPRB), INTENT (IN)::PFLU_VEG (YDCPG_OPTS%KLON)















REAL (KIND=JPHOOK)::ZHOOK_HANDLE
#include "apl_arpege_surface_update_lmse_f.intfb.h"
#include "apl_arpege_surface_update_lmse_t.intfb.h"
IF (LHOOK) CALL DR_HOOK ('APL_ARPEGE_SURFACE_UPDATE', 0, ZHOOK_HANDLE)



IF (.NOT.YDMODEL%YRML_PHY_MF%YRARPHY%LMSE) THEN
  CALL APL_ARPEGE_SURFACE_UPDATE_LMSE_F (PDTPHY, YDCPG_BNDS, YDCPG_OPTS, YDMF_PHYS&
  &, YDMF_PHYS_SURF, YDMODEL, PDSA_C1, PDSA_C2, PFLU_FEVI, PFLU_VEG)
ELSE
  CALL APL_ARPEGE_SURFACE_UPDATE_LMSE_T (LDCONFX, YDCPG_BNDS, YDCPG_GPAR, YDMF_PHYS_SURF)
ENDIF


IF (LHOOK) CALL DR_HOOK ('APL_ARPEGE_SURFACE_UPDATE', 1, ZHOOK_HANDLE)
ENDSUBROUTINE APL_ARPEGE_SURFACE_UPDATE
