SUBROUTINE ARP_SHALLOW_MF_ZERO (YDMF_PHYS_SURF)
USE MF_PHYS_SURFACE_TYPE_MOD,ONLY:MF_PHYS_SURF_TYPE
USE YOMHOOK,ONLY:LHOOK, JPHOOK, DR_HOOK

IMPLICIT NONE

TYPE (MF_PHYS_SURF_TYPE), INTENT (IN)::YDMF_PHYS_SURF
REAL (KIND=JPHOOK)::ZHOOK_HANDLE
IF (LHOOK) CALL DR_HOOK ('ARP_SHALLOW_MF_ZERO', 0, ZHOOK_HANDLE)
YDMF_PHYS_SURF%GSD_SFL%PGROUP (:,:)=0.0_JPRB
IF (LHOOK) CALL DR_HOOK ('ARP_SHALLOW_MF_ZERO', 1, ZHOOK_HANDLE)
ENDSUBROUTINE
