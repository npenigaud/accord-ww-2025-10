SUBROUTINE LAVABO_EXPL_LAITVSPCQM_PART1 (YDGEOMETRY, YDCPG_BNDS, YDCPG_OPTS, YDCPG_SL1)

!$ACDC outline1    


USE PARKIND1         , ONLY : JPIM, JPRB
USE YOMHOOK          , ONLY : LHOOK, JPHOOK, DR_HOOK
USE GEOMETRY_MOD     , ONLY : GEOMETRY
USE CPG_SL1_TYPE_MOD , ONLY : CPG_SL1B_TYPE
USE CPG_OPTS_TYPE_MOD, ONLY : CPG_OPTS_TYPE, CPG_BNDS_TYPE

IMPLICIT NONE

TYPE(GEOMETRY)      ,INTENT(IN)    :: YDGEOMETRY
TYPE(CPG_BNDS_TYPE) ,INTENT(IN)    :: YDCPG_BNDS
TYPE(CPG_OPTS_TYPE) ,INTENT(IN)    :: YDCPG_OPTS
TYPE(CPG_SL1B_TYPE) ,INTENT(INOUT) :: YDCPG_SL1

INTEGER (KIND=JPIM) :: JGFL

REAL(KIND=JPHOOK) :: ZHOOK_HANDLE

IF (LHOOK) CALL DR_HOOK('LAVABO_EXPL_LAITVSPCQM_PART1',0,ZHOOK_HANDLE)

DO JGFL = 1, SIZE (YDCPG_SL1%GFL)
  IF ((YDCPG_SL1%GFL (JGFL)%YCOMP%CSLINT /= 'LAITVSPCQM  ') .AND. YDCPG_SL1%GFL (JGFL)%YCOMP%LADV) THEN

!$ACDC PARALLEL, TARGET=OpenMP/OpenMPSingleColumn/OpenACCSingleColumn {

    YDCPG_SL1%GFL (JGFL)%P(YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA,0)=0.0_JPRB
    YDCPG_SL1%GFL (JGFL)%P(YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA,YDGEOMETRY%YRDIMV%NFLEVG+1)=0.0_JPRB

!$ACDC }

  ENDIF
ENDDO

IF (LHOOK) CALL DR_HOOK('LAVABO_EXPL_LAITVSPCQM_PART1',1,ZHOOK_HANDLE)

END SUBROUTINE LAVABO_EXPL_LAITVSPCQM_PART1
