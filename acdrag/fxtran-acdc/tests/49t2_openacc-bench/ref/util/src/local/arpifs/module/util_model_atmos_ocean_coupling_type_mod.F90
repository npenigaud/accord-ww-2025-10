FUNCTION ACDC_SIZE_MODEL_ATMOS_OCEAN_COUPLING_TYPE (SELF, CDPATH, LDPRINT) RESULT (KSIZE)
USE UTIL_TCOM_MOD
USE UTIL_TCOU_MOD
USE UTIL_TMCC_MOD
IMPLICIT NONE
TYPE (MODEL_ATMOS_OCEAN_COUPLING_TYPE),     INTENT (IN), TARGET :: SELF
CHARACTER(LEN=*), INTENT (IN), OPTIONAL :: CDPATH
LOGICAL,          INTENT (IN), OPTIONAL :: LDPRINT
INTEGER*8 :: KSIZE
INTEGER*8 :: ISIZE, JSIZE
LOGICAL :: LLPRINT
CHARACTER(LEN=128) :: CLPATH
LLPRINT = .FALSE.
IF (PRESENT (LDPRINT)) THEN
  LLPRINT = LDPRINT
ENDIF
CLPATH=''
IF (PRESENT (CDPATH)) THEN
  CLPATH = CDPATH
ENDIF
KSIZE = 0
JSIZE = 0
ISIZE = ACDC_SIZE_TMCC (SELF%YRMCC, CLPATH//'%YRMCC', .FALSE.)
JSIZE = JSIZE + ISIZE
KSIZE = KSIZE + ISIZE
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') JSIZE
  WRITE (*, *) TRIM (CLPATH)//'%YRMCC'
ENDIF
JSIZE = 0
ISIZE = ACDC_SIZE_TCOM (SELF%YRCOM, CLPATH//'%YRCOM', .FALSE.)
JSIZE = JSIZE + ISIZE
KSIZE = KSIZE + ISIZE
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') JSIZE
  WRITE (*, *) TRIM (CLPATH)//'%YRCOM'
ENDIF
JSIZE = 0
ISIZE = ACDC_SIZE_TCOU (SELF%YRCOU, CLPATH//'%YRCOU', .FALSE.)
JSIZE = JSIZE + ISIZE
KSIZE = KSIZE + ISIZE
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') JSIZE
  WRITE (*, *) TRIM (CLPATH)//'%YRCOU'
ENDIF
END FUNCTION

