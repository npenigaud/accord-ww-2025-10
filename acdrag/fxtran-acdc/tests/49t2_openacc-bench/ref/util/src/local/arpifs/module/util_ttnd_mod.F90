WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (SELF%M_TNDV), CDPATH//'%M_TNDV'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (SELF%M_TNDU_NOC), CDPATH//'%M_TNDU_NOC'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (SELF%M_TNDV_NOC), CDPATH//'%M_TNDV_NOC'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (SELF%M_TNDT), CDPATH//'%M_TNDT'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (SELF%M_TNDPD), CDPATH//'%M_TNDPD'

