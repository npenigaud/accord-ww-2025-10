FUNCTION ACDC_SIZE_TECV_CONFIG_BASE (SELF, CDPATH, LDPRINT) RESULT (KSIZE)

IMPLICIT NONE
TYPE (TECV_CONFIG_BASE),     INTENT (IN), TARGET :: SELF
CHARACTER(LEN=*), INTENT (IN), OPTIONAL :: CDPATH
LOGICAL,          INTENT (IN), OPTIONAL :: LDPRINT
INTEGER*8 :: KSIZE
INTEGER*8 :: ISIZE, JSIZE
LOGICAL :: LLPRINT
CHARACTER(LEN=128) :: CLPATH
LLPRINT = .FALSE.
IF (PRESENT (LDPRINT)) THEN
  LLPRINT = LDPRINT
ENDIF
CLPATH=''
IF (PRESENT (CDPATH)) THEN
  CLPATH = CDPATH
ENDIF
KSIZE = 0
ISIZE = KIND (SELF%L_IN_1D)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%L_IN_1D'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%L_IN_2D_SP)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%L_IN_2D_SP'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%L_IN_2D_GP)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%L_IN_2D_GP'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%L_IN_3D_SP)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%L_IN_3D_SP'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%L_IN_3D_GP)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%L_IN_3D_GP'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%CSETDESC) * LEN (SELF%CSETDESC)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%CSETDESC'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%CWNAME) * LEN (SELF%CWNAME)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%CWNAME'
ENDIF
KSIZE = KSIZE + ISIZE
END FUNCTION

