MODULE UTIL_MF_PHYS_STATE_GSP_RR_TYPE_MOD

USE MF_PHYS_BASE_STATE_TYPE_MOD, ONLY : MF_PHYS_STATE_GSP_RR_TYPE



INTERFACE ACDC_COPY
MODULE PROCEDURE ACDC_COPY_MF_PHYS_STATE_GSP_RR_TYPE
END INTERFACE




INTERFACE ACDC_WIPE
MODULE PROCEDURE ACDC_WIPE_MF_PHYS_STATE_GSP_RR_TYPE
END INTERFACE


CONTAINS





SUBROUTINE ACDC_COPY_MF_PHYS_STATE_GSP_RR_TYPE (SELF, LDCREATED, LDFIELDAPI)
USE FIELD_UTIL_MODULE
IMPLICIT NONE
TYPE (MF_PHYS_STATE_GSP_RR_TYPE), INTENT (IN), TARGET :: SELF
LOGICAL, OPTIONAL, INTENT (IN) :: LDCREATED, LDFIELDAPI
LOGICAL :: LLCREATED
LOGICAL :: LLFIELDAPI
LOGICAL :: LF_FC, LF_IC, LF_T, LF_W
LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF
LLCREATED = .FALSE.
IF (PRESENT (LDCREATED)) THEN
  LLCREATED = LDCREATED
ENDIF
IF (.NOT. LLCREATED) THEN
  !$ACC ENTER DATA CREATE (SELF)
  !$ACC UPDATE DEVICE (SELF)
ENDIF
LF_T = ASSOCIATED (SELF%F_T)
IF (LF_T .AND. LLFIELDAPI) THEN
  CALL COPY_FIELD_2RB (SELF%F_T)
  !$ACC ENTER DATA ATTACH (SELF%F_T)
ENDIF

LF_FC = ASSOCIATED (SELF%F_FC)
IF (LF_FC .AND. LLFIELDAPI) THEN
  CALL COPY_FIELD_2RB (SELF%F_FC)
  !$ACC ENTER DATA ATTACH (SELF%F_FC)
ENDIF

LF_W = ASSOCIATED (SELF%F_W)
IF (LF_W .AND. LLFIELDAPI) THEN
  CALL COPY_FIELD_2RB (SELF%F_W)
  !$ACC ENTER DATA ATTACH (SELF%F_W)
ENDIF

LF_IC = ASSOCIATED (SELF%F_IC)
IF (LF_IC .AND. LLFIELDAPI) THEN
  CALL COPY_FIELD_2RB (SELF%F_IC)
  !$ACC ENTER DATA ATTACH (SELF%F_IC)
ENDIF

END SUBROUTINE







SUBROUTINE ACDC_WIPE_MF_PHYS_STATE_GSP_RR_TYPE (SELF, LDDELETED, LDFIELDAPI)
USE FIELD_UTIL_MODULE
IMPLICIT NONE
TYPE (MF_PHYS_STATE_GSP_RR_TYPE), INTENT (IN), TARGET :: SELF
LOGICAL, OPTIONAL, INTENT (IN) :: LDDELETED, LDFIELDAPI
LOGICAL :: LLDELETED
LOGICAL :: LLFIELDAPI
LOGICAL :: LF_FC, LF_IC, LF_T, LF_W
LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF
LF_T = ASSOCIATED (SELF%F_T)
IF (LF_T .AND. LLFIELDAPI) THEN
  CALL WIPE_FIELD_2RB (SELF%F_T)
ENDIF

LF_FC = ASSOCIATED (SELF%F_FC)
IF (LF_FC .AND. LLFIELDAPI) THEN
  CALL WIPE_FIELD_2RB (SELF%F_FC)
ENDIF

LF_W = ASSOCIATED (SELF%F_W)
IF (LF_W .AND. LLFIELDAPI) THEN
  CALL WIPE_FIELD_2RB (SELF%F_W)
ENDIF

LF_IC = ASSOCIATED (SELF%F_IC)
IF (LF_IC .AND. LLFIELDAPI) THEN
  CALL WIPE_FIELD_2RB (SELF%F_IC)
ENDIF

LLDELETED = .FALSE.
IF (PRESENT (LDDELETED)) THEN
  LLDELETED = LDDELETED
ENDIF
IF (.NOT. LLDELETED) THEN
  !$ACC EXIT DATA DELETE (SELF)
ENDIF
END SUBROUTINE



END MODULE
