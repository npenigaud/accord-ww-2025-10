MODULE PROCEDURE ACDC_WIPE_CPG_DDH_TYPE
END INTERFACE


CONTAINS





SUBROUTINE ACDC_COPY_CPG_DDH_TYPE (SELF, LDCREATED, LDFIELDAPI)
USE FIELD_UTIL_MODULE
IMPLICIT NONE
TYPE (CPG_DDH_TYPE), INTENT (IN), TARGET :: SELF
LOGICAL, OPTIONAL, INTENT (IN) :: LDCREATED, LDFIELDAPI
LOGICAL :: LLCREATED
LOGICAL :: LLFIELDAPI
LOGICAL :: LF_AUX2D, LF_AUX3D, LF_AUXSM, LF_DDHI, LF_DHCV
LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF
LLCREATED = .FALSE.
IF (PRESENT (LDCREATED)) THEN
  LLCREATED = LDCREATED
ENDIF
IF (.NOT. LLCREATED) THEN
  !$ACC ENTER DATA CREATE (SELF)
  !$ACC UPDATE DEVICE (SELF)
ENDIF
LF_AUX3D = ASSOCIATED (SELF%F_AUX3D)
IF (LF_AUX3D .AND. LLFIELDAPI) THEN
  CALL COPY_FIELD_4RB (SELF%F_AUX3D)
  !$ACC ENTER DATA ATTACH (SELF%F_AUX3D)
ENDIF

LF_AUX2D = ASSOCIATED (SELF%F_AUX2D)
IF (LF_AUX2D .AND. LLFIELDAPI) THEN
  CALL COPY_FIELD_3RB (SELF%F_AUX2D)
  !$ACC ENTER DATA ATTACH (SELF%F_AUX2D)
ENDIF

LF_DHCV = ASSOCIATED (SELF%F_DHCV)
IF (LF_DHCV .AND. LLFIELDAPI) THEN
  CALL COPY_FIELD_4RB (SELF%F_DHCV)
  !$ACC ENTER DATA ATTACH (SELF%F_DHCV)
ENDIF

LF_AUXSM = ASSOCIATED (SELF%F_AUXSM)
IF (LF_AUXSM .AND. LLFIELDAPI) THEN
  CALL COPY_FIELD_5RB (SELF%F_AUXSM)
  !$ACC ENTER DATA ATTACH (SELF%F_AUXSM)
ENDIF

LF_DDHI = ASSOCIATED (SELF%F_DDHI)
IF (LF_DDHI .AND. LLFIELDAPI) THEN
  CALL COPY_FIELD_2IM (SELF%F_DDHI)
  !$ACC ENTER DATA ATTACH (SELF%F_DDHI)
ENDIF

END SUBROUTINE

