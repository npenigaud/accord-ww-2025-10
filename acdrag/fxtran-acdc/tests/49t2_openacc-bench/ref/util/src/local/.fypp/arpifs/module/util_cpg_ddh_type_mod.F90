  CALL HOST_FIELD_4RB (SELF%F_DHCV)
ENDIF

LF_AUXSM = ASSOCIATED (SELF%F_AUXSM)
IF (LF_AUXSM) THEN
  CALL HOST_FIELD_5RB (SELF%F_AUXSM)
ENDIF

LF_DDHI = ASSOCIATED (SELF%F_DDHI)
IF (LF_DDHI) THEN
  CALL HOST_FIELD_2IM (SELF%F_DDHI)
ENDIF

END SUBROUTINE





