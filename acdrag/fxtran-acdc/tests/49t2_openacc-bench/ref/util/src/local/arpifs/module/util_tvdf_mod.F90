SUBROUTINE ACDC_CRC64_TVDF (SELF, KLUN, CDPATH)
USE FXTRAN_ACDC_CRC64_INTRINSIC_MOD, ONLY : FCRC64 => FXTRAN_ACDC_CRC64
IMPLICIT NONE
TYPE (TVDF), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
CHARACTER(LEN=*), INTENT (IN) :: CDPATH
CHARACTER(LEN=128) :: CLIND
WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (SELF%RLAM), CDPATH//'%RLAM'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (SELF%RVDIFTS), CDPATH//'%RVDIFTS'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (SELF%LWDS), CDPATH//'%LWDS'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (SELF%REPS1WDS), CDPATH//'%REPS1WDS'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (SELF%REPS2WDS), CDPATH//'%REPS2WDS'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (SELF%RETAWDS), CDPATH//'%RETAWDS'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (SELF%RTOFDALPHA), CDPATH//'%RTOFDALPHA'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (SELF%REISTHSC), CDPATH//'%REISTHSC'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (SELF%NSUBST), CDPATH//'%NSUBST'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (SELF%RFAC_TWO_COEF), CDPATH//'%RFAC_TWO_COEF'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (SELF%RZC_H), CDPATH//'%RZC_H'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (SELF%RZL_INF), CDPATH//'%RZL_INF'

END SUBROUTINE

