FUNCTION ACDC_SIZE_TVDF (SELF, CDPATH, LDPRINT) RESULT (KSIZE)

IMPLICIT NONE
TYPE (TVDF),     INTENT (IN), TARGET :: SELF
CHARACTER(LEN=*), INTENT (IN), OPTIONAL :: CDPATH
LOGICAL,          INTENT (IN), OPTIONAL :: LDPRINT
INTEGER*8 :: KSIZE
INTEGER*8 :: ISIZE, JSIZE
LOGICAL :: LLPRINT
CHARACTER(LEN=128) :: CLPATH
LLPRINT = .FALSE.
IF (PRESENT (LDPRINT)) THEN
  LLPRINT = LDPRINT
ENDIF
CLPATH=''
IF (PRESENT (CDPATH)) THEN
  CLPATH = CDPATH
ENDIF
KSIZE = 0
ISIZE = KIND (SELF%RLAM)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%RLAM'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%RVDIFTS)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%RVDIFTS'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%LWDS)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%LWDS'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%REPS1WDS)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%REPS1WDS'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%REPS2WDS)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%REPS2WDS'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%RETAWDS)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%RETAWDS'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%RTOFDALPHA)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%RTOFDALPHA'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%REISTHSC)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%REISTHSC'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%NSUBST)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%NSUBST'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%RFAC_TWO_COEF)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%RFAC_TWO_COEF'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%RZC_H)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%RZC_H'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%RZL_INF)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%RZL_INF'
ENDIF
KSIZE = KSIZE + ISIZE
END FUNCTION

