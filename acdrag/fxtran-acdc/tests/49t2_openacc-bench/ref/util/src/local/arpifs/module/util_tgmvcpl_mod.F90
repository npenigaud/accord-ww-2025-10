IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%MSPD'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%MSVD)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%MSVD'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%MNHX)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%MNHX'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%MDIV)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%MDIV'
ENDIF
