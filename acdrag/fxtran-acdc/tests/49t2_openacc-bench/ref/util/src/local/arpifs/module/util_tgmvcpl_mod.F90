ISIZE = KIND (SELF%MTL)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%MTL'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%MTM)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%MTM'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%MSPDL)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%MSPDL'
