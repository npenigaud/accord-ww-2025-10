SUBROUTINE ACDC_CRC64_TMSE (SELF, KLUN, CDPATH)
USE FXTRAN_ACDC_CRC64_INTRINSIC_MOD, ONLY : FCRC64 => FXTRAN_ACDC_CRC64
IMPLICIT NONE
TYPE (TMSE), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
CHARACTER(LEN=*), INTENT (IN) :: CDPATH
CHARACTER(LEN=128) :: CLIND
WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (SELF%LPGDFWR), CDPATH//'%LPGDFWR'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (SELF%LHISFWR), CDPATH//'%LHISFWR'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (SELF%LFTZERO), CDPATH//'%LFTZERO'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (SELF%NSURFEXCTLMAX), CDPATH//'%NSURFEXCTLMAX'

