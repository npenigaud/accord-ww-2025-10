FUNCTION ACDC_SIZE_TMSE (SELF, CDPATH, LDPRINT) RESULT (KSIZE)

IMPLICIT NONE
TYPE (TMSE),     INTENT (IN), TARGET :: SELF
CHARACTER(LEN=*), INTENT (IN), OPTIONAL :: CDPATH
LOGICAL,          INTENT (IN), OPTIONAL :: LDPRINT
INTEGER*8 :: KSIZE
INTEGER*8 :: ISIZE, JSIZE
LOGICAL :: LLPRINT
CHARACTER(LEN=128) :: CLPATH
LLPRINT = .FALSE.
IF (PRESENT (LDPRINT)) THEN
  LLPRINT = LDPRINT
ENDIF
CLPATH=''
IF (PRESENT (CDPATH)) THEN
  CLPATH = CDPATH
ENDIF
KSIZE = 0
ISIZE = KIND (SELF%LPGDFWR)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%LPGDFWR'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%LHISFWR)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%LHISFWR'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%LFTZERO)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%LFTZERO'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%NSURFEXCTLMAX)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%NSURFEXCTLMAX'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%NSURFEXCTL)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%NSURFEXCTL'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%NSTEP_DUMP_STATE) * SIZE (SELF%NSTEP_DUMP_STATE)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%NSTEP_DUMP_STATE'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%CPREFIX_SFX1) * LEN (SELF%CPREFIX_SFX1)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%CPREFIX_SFX1'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%CPREFIX_SFX2) * LEN (SELF%CPREFIX_SFX2)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%CPREFIX_SFX2'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%XZSEPS)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%XZSEPS'
ENDIF
KSIZE = KSIZE + ISIZE
END FUNCTION

