SUBROUTINE ACDC_CRC64_TPHLC (SELF, KLUN, CDPATH)
USE FXTRAN_ACDC_CRC64_INTRINSIC_MOD, ONLY : FCRC64 => FXTRAN_ACDC_CRC64
IMPLICIT NONE
TYPE (TPHLC), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
CHARACTER(LEN=*), INTENT (IN) :: CDPATH
CHARACTER(LEN=128) :: CLIND
WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (SELF%ALPHA), CDPATH//'%ALPHA'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (SELF%AH0), CDPATH//'%AH0'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (SELF%USTARL), CDPATH//'%USTARL'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (SELF%USTARS), CDPATH//'%USTARS'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (SELF%ALANDZ0), CDPATH//'%ALANDZ0'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (SELF%ASEAZ0), CDPATH//'%ASEAZ0'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (SELF%LSPHLC), CDPATH//'%LSPHLC'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (SELF%LVDFLC), CDPATH//'%LVDFLC'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (SELF%LSDRLC), CDPATH//'%LSDRLC'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (SELF%LZMCON), CDPATH//'%LZMCON'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (SELF%LKEXP), CDPATH//'%LKEXP'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (SELF%LVDFDS), CDPATH//'%LVDFDS'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (SELF%LSDRDS), CDPATH//'%LSDRDS'

END SUBROUTINE

