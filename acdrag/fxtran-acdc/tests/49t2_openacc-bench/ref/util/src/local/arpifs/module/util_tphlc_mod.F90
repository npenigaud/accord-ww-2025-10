IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%USTARL'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%USTARS)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%USTARS'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%ALANDZ0)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%ALANDZ0'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%ASEAZ0)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%ASEAZ0'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%LSPHLC)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%LSPHLC'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%LVDFLC)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%LVDFLC'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%LSDRLC)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%LSDRLC'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%LZMCON)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%LZMCON'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%LKEXP)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%LKEXP'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%LVDFDS)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%LVDFDS'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%LSDRDS)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%LSDRDS'
ENDIF
KSIZE = KSIZE + ISIZE
END FUNCTION

