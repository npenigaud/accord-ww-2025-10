FUNCTION ACDC_SIZE_ECPHYS_VARIABLES (SELF, CDPATH, LDPRINT) RESULT (KSIZE)
USE UTIL_VARIABLE_3RB_MOD
IMPLICIT NONE
TYPE (ECPHYS_VARIABLES),     INTENT (IN), TARGET :: SELF
CHARACTER(LEN=*), INTENT (IN), OPTIONAL :: CDPATH
LOGICAL,          INTENT (IN), OPTIONAL :: LDPRINT
INTEGER*8 :: KSIZE
INTEGER*8 :: ISIZE, JSIZE
LOGICAL :: LLPRINT
CHARACTER(LEN=128) :: CLPATH
LLPRINT = .FALSE.
IF (PRESENT (LDPRINT)) THEN
  LLPRINT = LDPRINT
ENDIF
CLPATH=''
IF (PRESENT (CDPATH)) THEN
  CLPATH = CDPATH
ENDIF
KSIZE = 0
JSIZE = 0
ISIZE = ACDC_SIZE_VARIABLE_3RB (SELF%USTRTI, CLPATH//'%USTRTI', .FALSE.)
JSIZE = JSIZE + ISIZE
KSIZE = KSIZE + ISIZE
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') JSIZE
  WRITE (*, *) TRIM (CLPATH)//'%USTRTI'
ENDIF
JSIZE = 0
ISIZE = ACDC_SIZE_VARIABLE_3RB (SELF%VSTRTI, CLPATH//'%VSTRTI', .FALSE.)
JSIZE = JSIZE + ISIZE
KSIZE = KSIZE + ISIZE
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') JSIZE
  WRITE (*, *) TRIM (CLPATH)//'%VSTRTI'
ENDIF
JSIZE = 0
ISIZE = ACDC_SIZE_VARIABLE_3RB (SELF%AHFSTI, CLPATH//'%AHFSTI', .FALSE.)
JSIZE = JSIZE + ISIZE
KSIZE = KSIZE + ISIZE
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') JSIZE
  WRITE (*, *) TRIM (CLPATH)//'%AHFSTI'
ENDIF
JSIZE = 0
ISIZE = ACDC_SIZE_VARIABLE_3RB (SELF%EVAPTI, CLPATH//'%EVAPTI', .FALSE.)
JSIZE = JSIZE + ISIZE
KSIZE = KSIZE + ISIZE
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') JSIZE
  WRITE (*, *) TRIM (CLPATH)//'%EVAPTI'
ENDIF
JSIZE = 0
ISIZE = ACDC_SIZE_VARIABLE_3RB (SELF%TSKTI, CLPATH//'%TSKTI', .FALSE.)
JSIZE = JSIZE + ISIZE
KSIZE = KSIZE + ISIZE
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') JSIZE
  WRITE (*, *) TRIM (CLPATH)//'%TSKTI'
ENDIF
END FUNCTION

