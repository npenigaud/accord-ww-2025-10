MODULE UTIL_CPG_DDH_TND_TYPE_MOD

USE CPG_DDH_TND_TYPE_MOD, ONLY : CPG_DDH_TND_TYPE



INTERFACE ACDC_COPY
MODULE PROCEDURE ACDC_COPY_CPG_DDH_TND_TYPE
END INTERFACE

INTERFACE ACDC_HOST
MODULE PROCEDURE ACDC_HOST_CPG_DDH_TND_TYPE
END INTERFACE



INTERFACE ACDC_WIPE
MODULE PROCEDURE ACDC_WIPE_CPG_DDH_TND_TYPE
END INTERFACE


CONTAINS





SUBROUTINE ACDC_COPY_CPG_DDH_TND_TYPE (SELF, LDCREATED, LDFIELDAPI)
USE UTIL_CPG_TNDHD_DDH_TYPE_MOD
USE UTIL_CPG_TNDSI_DDH_TYPE_MOD
USE UTIL_CPG_TNDSL_DDH_TYPE_MOD
IMPLICIT NONE
TYPE (CPG_DDH_TND_TYPE), INTENT (IN), TARGET :: SELF
LOGICAL, OPTIONAL, INTENT (IN) :: LDCREATED, LDFIELDAPI
LOGICAL :: LLCREATED
LOGICAL :: LLFIELDAPI
LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF
LLCREATED = .FALSE.
IF (PRESENT (LDCREATED)) THEN
  LLCREATED = LDCREATED
ENDIF
IF (.NOT. LLCREATED) THEN
  !$ACC ENTER DATA CREATE (SELF)
  !$ACC UPDATE DEVICE (SELF)
ENDIF
CALL ACDC_COPY_CPG_TNDSI_DDH_TYPE (SELF%YSI, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL ACDC_COPY_CPG_TNDHD_DDH_TYPE (SELF%YHD, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL ACDC_COPY_CPG_TNDSL_DDH_TYPE (SELF%YSL, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

END SUBROUTINE

SUBROUTINE ACDC_HOST_CPG_DDH_TND_TYPE (SELF)
USE UTIL_CPG_TNDHD_DDH_TYPE_MOD
USE UTIL_CPG_TNDSI_DDH_TYPE_MOD
USE UTIL_CPG_TNDSL_DDH_TYPE_MOD
IMPLICIT NONE
TYPE (CPG_DDH_TND_TYPE), TARGET :: SELF

CALL ACDC_HOST_CPG_TNDSI_DDH_TYPE (SELF%YSI)

CALL ACDC_HOST_CPG_TNDHD_DDH_TYPE (SELF%YHD)

CALL ACDC_HOST_CPG_TNDSL_DDH_TYPE (SELF%YSL)

END SUBROUTINE





SUBROUTINE ACDC_WIPE_CPG_DDH_TND_TYPE (SELF, LDDELETED, LDFIELDAPI)
USE UTIL_CPG_TNDHD_DDH_TYPE_MOD
USE UTIL_CPG_TNDSI_DDH_TYPE_MOD
USE UTIL_CPG_TNDSL_DDH_TYPE_MOD
IMPLICIT NONE
TYPE (CPG_DDH_TND_TYPE), INTENT (IN), TARGET :: SELF
LOGICAL, OPTIONAL, INTENT (IN) :: LDDELETED, LDFIELDAPI
LOGICAL :: LLDELETED
LOGICAL :: LLFIELDAPI
LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF
CALL ACDC_WIPE_CPG_TNDSI_DDH_TYPE (SELF%YSI, LDDELETED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL ACDC_WIPE_CPG_TNDHD_DDH_TYPE (SELF%YHD, LDDELETED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL ACDC_WIPE_CPG_TNDSL_DDH_TYPE (SELF%YSL, LDDELETED=.TRUE., LDFIELDAPI=LDFIELDAPI)

LLDELETED = .FALSE.
IF (PRESENT (LDDELETED)) THEN
  LLDELETED = LDDELETED
ENDIF
IF (.NOT. LLDELETED) THEN
  !$ACC EXIT DATA DELETE (SELF)
ENDIF
END SUBROUTINE



END MODULE
