!     *CADHTLS*   CHARACTER  NAMES OF DDH VARIABLES AND FLUXES
!                            IN TILE ARRAY 
!     *CADHTSS*   CHARACTER  NAMES OF DDH VARIABLES AND FLUXES
!                            IN SNOW ENERGY BUDGET ARRAY 
!     *CADHTTS*   CHARACTER  NAMES OF DDH VARIABLES AND FLUXES 
!                            IN SOIL ENERGY BUDGET ARRAY 
!     *CADHTIS*   CHARACTER  NAMES OF DDH VARIABLES AND FLUXES 
!                            IN SEA-ICE ENERGY BUDGET ARRAY 
!     *CADHSSS*   CHARACTER  NAMES OF DDH VARIABLES AND FLUXES 
!                            IN SNOW WATER BUDGET ARRAY 
!     *CADHIIS*   CHARACTER  NAMES OF DDH VARIABLES AND FLUXES 
!                            IN INTERCEPTION LAYER WATER BUDGET ARRAY 
!     *CADHWLS*   CHARACTER  NAMES OF DDH VARIABLES AND FLUXES 
!                            IN SOIL WATER BUDGET ARRAY 
!     *CADHFSS*   CHARACTER  NAMES OF DDH VARIABLES AND FLUXES 
!                            IN FREE-STYLE SINGLE LEVEL ARRAY 

