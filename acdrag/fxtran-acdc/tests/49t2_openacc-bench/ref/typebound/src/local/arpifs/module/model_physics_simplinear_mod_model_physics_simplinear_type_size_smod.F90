SUBMODULE (MODEL_PHYSICS_SIMPLINEAR_MOD) MODEL_PHYSICS_SIMPLINEAR_MOD_MODEL_PHYSICS_SIMPLINEAR_TYPE_SIZE_SMOD

IMPLICIT NONE

CONTAINS

MODULE FUNCTION ACDC_SIZE_MODEL_PHYSICS_SIMPLINEAR_TYPE (SELF, CDPATH, LDPRINT) RESULT (KSIZE)

IMPLICIT NONE
CLASS (MODEL_PHYSICS_SIMPLINEAR_TYPE),     INTENT (IN), TARGET :: SELF
CHARACTER(LEN=*), INTENT (IN), OPTIONAL :: CDPATH
LOGICAL,          INTENT (IN), OPTIONAL :: LDPRINT
INTEGER*8 :: KSIZE
INTEGER*8 :: ISIZE, JSIZE
LOGICAL :: LLPRINT
CHARACTER(LEN=128) :: CLPATH
INTEGER :: J1
LOGICAL :: LGPHIST
LLPRINT = .FALSE.
IF (PRESENT (LDPRINT)) THEN
  LLPRINT = LDPRINT
ENDIF
CLPATH=''
IF (PRESENT (CDPATH)) THEN
  CLPATH = CDPATH
ENDIF
KSIZE = 0
JSIZE = 0
ISIZE = SELF%YREPHLI%ACDC_SIZE (CLPATH//'%YREPHLI', .FALSE.)
JSIZE = JSIZE + ISIZE
KSIZE = KSIZE + ISIZE
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') JSIZE
  WRITE (*, *) TRIM (CLPATH)//'%YREPHLI'
ENDIF
JSIZE = 0
ISIZE = SELF%YRCUMFS%ACDC_SIZE (CLPATH//'%YRCUMFS', .FALSE.)
JSIZE = JSIZE + ISIZE
KSIZE = KSIZE + ISIZE
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') JSIZE
  WRITE (*, *) TRIM (CLPATH)//'%YRCUMFS'
ENDIF
JSIZE = 0
ISIZE = SELF%YREGWDWMS%ACDC_SIZE (CLPATH//'%YREGWDWMS', .FALSE.)
JSIZE = JSIZE + ISIZE
KSIZE = KSIZE + ISIZE
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') JSIZE
  WRITE (*, *) TRIM (CLPATH)//'%YREGWDWMS'
ENDIF
JSIZE = 0
ISIZE = SELF%YRECUMF2%ACDC_SIZE (CLPATH//'%YRECUMF2', .FALSE.)
JSIZE = JSIZE + ISIZE
KSIZE = KSIZE + ISIZE
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') JSIZE
  WRITE (*, *) TRIM (CLPATH)//'%YRECUMF2'
ENDIF
JSIZE = 0
ISIZE = SELF%YRPHLC%ACDC_SIZE (CLPATH//'%YRPHLC', .FALSE.)
JSIZE = JSIZE + ISIZE
KSIZE = KSIZE + ISIZE
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') JSIZE
  WRITE (*, *) TRIM (CLPATH)//'%YRPHLC'
ENDIF
JSIZE = 0
ISIZE = SELF%YRPHNC%ACDC_SIZE (CLPATH//'%YRPHNC', .FALSE.)
JSIZE = JSIZE + ISIZE
KSIZE = KSIZE + ISIZE
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') JSIZE
  WRITE (*, *) TRIM (CLPATH)//'%YRPHNC'
ENDIF
JSIZE = 0
ISIZE = SELF%YRNCL%ACDC_SIZE (CLPATH//'%YRNCL', .FALSE.)
JSIZE = JSIZE + ISIZE
KSIZE = KSIZE + ISIZE
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') JSIZE
  WRITE (*, *) TRIM (CLPATH)//'%YRNCL'
ENDIF
JSIZE = 0
ISIZE = SELF%YRSRFTLAD%ACDC_SIZE (CLPATH//'%YRSRFTLAD', .FALSE.)
JSIZE = JSIZE + ISIZE
KSIZE = KSIZE + ISIZE
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') JSIZE
  WRITE (*, *) TRIM (CLPATH)//'%YRSRFTLAD'
ENDIF
LGPHIST = ASSOCIATED (SELF%GPHIST)
IF (LGPHIST) THEN
  JSIZE = 0
  DO J1 = LBOUND (SELF%GPHIST, 1), UBOUND (SELF%GPHIST, 1)
    ISIZE = SELF%GPHIST (J1)%ACDC_SIZE (CLPATH//'%GPHIST', .FALSE.)
    JSIZE = JSIZE + ISIZE
    KSIZE = KSIZE + ISIZE
  ENDDO
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') JSIZE
    WRITE (*, *) TRIM (CLPATH)//'%GPHIST'
  ENDIF
ENDIF
END FUNCTION

END SUBMODULE
