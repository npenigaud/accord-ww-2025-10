IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%XVALIDMAX'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%CDIR) * LEN (SELF%CDIR)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%CDIR'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%CLBTYPE) * LEN (SELF%CLBTYPE)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%CLBTYPE'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%LTIMEDEP)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%LTIMEDEP'
ENDIF
