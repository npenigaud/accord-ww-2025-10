SUBMODULE (MODD_FIELD) MODD_FIELD_TFIELDMETADATA_SIZE_SMOD

IMPLICIT NONE

CONTAINS

MODULE FUNCTION ACDC_SIZE_TFIELDMETADATA (SELF, CDPATH, LDPRINT) RESULT (KSIZE)

IMPLICIT NONE
CLASS (TFIELDMETADATA),     INTENT (IN), TARGET :: SELF
CHARACTER(LEN=*), INTENT (IN), OPTIONAL :: CDPATH
LOGICAL,          INTENT (IN), OPTIONAL :: LDPRINT
INTEGER*8 :: KSIZE
INTEGER*8 :: ISIZE, JSIZE
LOGICAL :: LLPRINT
CHARACTER(LEN=128) :: CLPATH
LLPRINT = .FALSE.
IF (PRESENT (LDPRINT)) THEN
  LLPRINT = LDPRINT
ENDIF
CLPATH=''
IF (PRESENT (CDPATH)) THEN
  CLPATH = CDPATH
ENDIF
KSIZE = 0
ISIZE = KIND (SELF%CMNHNAME) * LEN (SELF%CMNHNAME)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%CMNHNAME'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%CSTDNAME) * LEN (SELF%CSTDNAME)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%CSTDNAME'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%CLONGNAME) * LEN (SELF%CLONGNAME)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%CLONGNAME'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%CUNITS) * LEN (SELF%CUNITS)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%CUNITS'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%CCOMMENT) * LEN (SELF%CCOMMENT)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%CCOMMENT'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%NGRID)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%NGRID'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%NTYPE)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%NTYPE'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%NDIMS)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%NDIMS'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%NDIMLIST) * SIZE (SELF%NDIMLIST)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%NDIMLIST'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%NFILLVALUE)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%NFILLVALUE'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%XFILLVALUE)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%XFILLVALUE'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%NVALIDMIN)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%NVALIDMIN'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%NVALIDMAX)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%NVALIDMAX'
ENDIF
