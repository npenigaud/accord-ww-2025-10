SUBMODULE (MODEL_PHYSICS_SIMPLINEAR_MOD) MODEL_PHYSICS_SIMPLINEAR_MOD_MODEL_PHYSICS_SIMPLINEAR_TYPE_SAVE_SMOD

IMPLICIT NONE

CONTAINS

MODULE SUBROUTINE ACDC_SAVE_MODEL_PHYSICS_SIMPLINEAR_TYPE (SELF, KLUN)

IMPLICIT NONE
CLASS (MODEL_PHYSICS_SIMPLINEAR_TYPE), INTENT (IN), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
INTEGER :: J1
LOGICAL :: LGPHIST
CALL SELF%YREPHLI%ACDC_SAVE (KLUN)
CALL SELF%YRCUMFS%ACDC_SAVE (KLUN)
CALL SELF%YREGWDWMS%ACDC_SAVE (KLUN)
CALL SELF%YRECUMF2%ACDC_SAVE (KLUN)
CALL SELF%YRPHLC%ACDC_SAVE (KLUN)
CALL SELF%YRPHNC%ACDC_SAVE (KLUN)
CALL SELF%YRNCL%ACDC_SAVE (KLUN)
CALL SELF%YRSRFTLAD%ACDC_SAVE (KLUN)
LGPHIST = ASSOCIATED (SELF%GPHIST)
WRITE (KLUN) LGPHIST
IF (LGPHIST) THEN
  WRITE (KLUN) LBOUND (SELF%GPHIST)
  WRITE (KLUN) UBOUND (SELF%GPHIST)
  DO J1 = LBOUND (SELF%GPHIST, 1), UBOUND (SELF%GPHIST, 1)
    CALL SELF%GPHIST (J1)%ACDC_SAVE (KLUN)
  ENDDO
ENDIF
END SUBROUTINE

END SUBMODULE
