MODULE SUBROUTINE ACDC_COPY_CPLNG_FLD_TYPE (SELF, LDCREATED, LDFIELDAPI)

IMPLICIT NONE
CLASS (CPLNG_FLD_TYPE), INTENT (IN), TARGET :: SELF
LOGICAL, OPTIONAL, INTENT (IN) :: LDCREATED, LDFIELDAPI
LOGICAL :: LLCREATED
LOGICAL :: LLFIELDAPI
LOGICAL :: LD, LID
LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF
LLCREATED = .FALSE.
IF (PRESENT (LDCREATED)) THEN
  LLCREATED = LDCREATED
ENDIF
IF (.NOT. LLCREATED) THEN
  !$ACC ENTER DATA CREATE (SELF)
  !$ACC UPDATE DEVICE (SELF)
ENDIF
LID = ALLOCATED (SELF%ID)
IF (LID) THEN
  !$ACC ENTER DATA CREATE (SELF%ID)
  !$ACC UPDATE DEVICE (SELF%ID)
  !$ACC ENTER DATA ATTACH (SELF%ID)
ENDIF

LD = ALLOCATED (SELF%D)
IF (LD) THEN
  !$ACC ENTER DATA CREATE (SELF%D)
  !$ACC UPDATE DEVICE (SELF%D)
  !$ACC ENTER DATA ATTACH (SELF%D)
ENDIF

END SUBROUTINE

END SUBMODULE
