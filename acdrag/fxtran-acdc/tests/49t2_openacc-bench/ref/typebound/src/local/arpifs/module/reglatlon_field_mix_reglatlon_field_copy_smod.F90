  !$ACC ENTER DATA CREATE (SELF%PFLD)
  !$ACC UPDATE DEVICE (SELF%PFLD)
  !$ACC ENTER DATA ATTACH (SELF%PFLD)
ENDIF

LPLAT = ASSOCIATED (SELF%PLAT)
IF (LPLAT) THEN
  !$ACC ENTER DATA CREATE (SELF%PLAT)
  !$ACC UPDATE DEVICE (SELF%PLAT)
  !$ACC ENTER DATA ATTACH (SELF%PLAT)
ENDIF

LPSIN = ASSOCIATED (SELF%PSIN)
IF (LPSIN) THEN
  !$ACC ENTER DATA CREATE (SELF%PSIN)
  !$ACC UPDATE DEVICE (SELF%PSIN)
  !$ACC ENTER DATA ATTACH (SELF%PSIN)
ENDIF

LPLON = ASSOCIATED (SELF%PLON)
IF (LPLON) THEN
  !$ACC ENTER DATA CREATE (SELF%PLON)
  !$ACC UPDATE DEVICE (SELF%PLON)
  !$ACC ENTER DATA ATTACH (SELF%PLON)
ENDIF

END SUBROUTINE

