SUBMODULE (MODD_NSV) MODD_NSV_NSV_T_HOST_SMOD

IMPLICIT NONE

CONTAINS

MODULE SUBROUTINE ACDC_HOST_NSV_T (SELF)

IMPLICIT NONE
CLASS (NSV_T), TARGET :: SELF
INTEGER :: J1, J2
LOGICAL :: LCSV, LCSV_A, LCSV_CHEM_LIST, LCSV_CHEM_LIST_A, LTSVLIST, LTSVLIST_A

LTSVLIST_A = ALLOCATED (SELF%TSVLIST_A)
IF (LTSVLIST_A) THEN
  DO J2 = LBOUND (SELF%TSVLIST_A, 2), UBOUND (SELF%TSVLIST_A, 2)
    DO J1 = LBOUND (SELF%TSVLIST_A, 1), UBOUND (SELF%TSVLIST_A, 1)
      CALL SELF%TSVLIST_A (J1, J2)%ACDC_HOST ()
    ENDDO
  ENDDO
ENDIF

LTSVLIST = ASSOCIATED (SELF%TSVLIST)
IF (LTSVLIST) THEN
  DO J1 = LBOUND (SELF%TSVLIST, 1), UBOUND (SELF%TSVLIST, 1)
    CALL SELF%TSVLIST (J1)%ACDC_HOST ()
  ENDDO
ENDIF

END SUBROUTINE

END SUBMODULE
