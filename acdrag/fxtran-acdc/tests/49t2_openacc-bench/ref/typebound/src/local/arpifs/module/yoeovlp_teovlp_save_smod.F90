SUBMODULE (YOEOVLP) YOEOVLP_TEOVLP_SAVE_SMOD

IMPLICIT NONE

CONTAINS

MODULE SUBROUTINE ACDC_SAVE_TEOVLP (SELF, KLUN)

IMPLICIT NONE
CLASS (TEOVLP), INTENT (IN), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
LOGICAL :: LRA1OVLP
LRA1OVLP = ALLOCATED (SELF%RA1OVLP)
WRITE (KLUN) LRA1OVLP
IF (LRA1OVLP) THEN
  WRITE (KLUN) LBOUND (SELF%RA1OVLP)
  WRITE (KLUN) UBOUND (SELF%RA1OVLP)
  WRITE (KLUN) SELF%RA1OVLP
ENDIF
END SUBROUTINE

END SUBMODULE
