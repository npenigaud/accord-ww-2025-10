IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF
LLCREATED = .FALSE.
IF (PRESENT (LDCREATED)) THEN
  LLCREATED = LDCREATED
ENDIF
IF (.NOT. LLCREATED) THEN
  !$ACC ENTER DATA CREATE (SELF)
  !$ACC UPDATE DEVICE (SELF)
ENDIF
LRA1OVLP = ALLOCATED (SELF%RA1OVLP)
IF (LRA1OVLP) THEN
  !$ACC ENTER DATA CREATE (SELF%RA1OVLP)
  !$ACC UPDATE DEVICE (SELF%RA1OVLP)
