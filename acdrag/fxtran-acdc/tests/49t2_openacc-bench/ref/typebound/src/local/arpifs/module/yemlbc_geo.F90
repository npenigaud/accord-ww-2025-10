PROCEDURE :: ACDC_COPY => ACDC_COPY_TELBC_GEO
PROCEDURE :: ACDC_CRC64 => ACDC_CRC64_TELBC_GEO
PROCEDURE :: ACDC_HOST => ACDC_HOST_TELBC_GEO
PROCEDURE :: ACDC_LOAD => ACDC_LOAD_TELBC_GEO
PROCEDURE :: ACDC_SAVE => ACDC_SAVE_TELBC_GEO
PROCEDURE :: ACDC_SIZE => ACDC_SIZE_TELBC_GEO
PROCEDURE :: ACDC_WIPE => ACDC_WIPE_TELBC_GEO
END TYPE TELBC_GEO

