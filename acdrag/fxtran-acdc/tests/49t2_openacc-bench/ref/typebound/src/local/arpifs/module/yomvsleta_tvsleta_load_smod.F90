MODULE SUBROUTINE ACDC_LOAD_TVSLETA (SELF, KLUN)
USE PARKIND1, ONLY : JPRD
IMPLICIT NONE
CLASS (TVSLETA), INTENT (OUT), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
INTEGER :: IL1(1), IU1(1), IL2(2), IU2(2), IL3(3), IU3(3)
LOGICAL :: LGAMMA_WENO, LNVAUTF, LNVAUTH, LVCUICO, LVCUICOH, LVRDETAR, LVSLD, LVSLDH, LVSLDW, LVSLDWH
LOGICAL :: LVSLVF, LVSLVFH
REAL(KIND=JPRD) :: ZTMP0
REAL(KIND=JPRD), ALLOCATABLE :: ZTMP1 (:)
REAL(KIND=JPRD), ALLOCATABLE :: ZTMP2 (:,:)
REAL(KIND=JPRD), ALLOCATABLE :: ZTMP3 (:,:,:)
READ (KLUN) LVCUICO
IF (LVCUICO) THEN
  READ (KLUN) IL2
  READ (KLUN) IU2
  ALLOCATE (SELF%VCUICO (IL2(1):IU2(1), IL2(2):IU2(2)))
  ALLOCATE (ZTMP2(LBOUND(SELF%VCUICO,1):UBOUND(SELF%VCUICO,1), LBOUND(SELF%VCUICO,2):UBOUND(SELF%VCUICO,2)))
  READ (KLUN) ZTMP2
  SELF%VCUICO = ZTMP2
  DEALLOCATE (ZTMP2)
ENDIF
READ (KLUN) LVCUICOH
IF (LVCUICOH) THEN
  READ (KLUN) IL2
  READ (KLUN) IU2
  ALLOCATE (SELF%VCUICOH (IL2(1):IU2(1), IL2(2):IU2(2)))
  ALLOCATE (ZTMP2(LBOUND(SELF%VCUICOH,1):UBOUND(SELF%VCUICOH,1), LBOUND(SELF%VCUICOH,2):UBOUND(SELF%VCUICOH,2)))
  READ (KLUN) ZTMP2
  SELF%VCUICOH = ZTMP2
  DEALLOCATE (ZTMP2)
ENDIF
READ (KLUN) LVSLD
IF (LVSLD) THEN
  READ (KLUN) IL2
  READ (KLUN) IU2
  ALLOCATE (SELF%VSLD (IL2(1):IU2(1), IL2(2):IU2(2)))
  ALLOCATE (ZTMP2(LBOUND(SELF%VSLD,1):UBOUND(SELF%VSLD,1), LBOUND(SELF%VSLD,2):UBOUND(SELF%VSLD,2)))
  READ (KLUN) ZTMP2
  SELF%VSLD = ZTMP2
  DEALLOCATE (ZTMP2)
ENDIF
READ (KLUN) LVSLDH
IF (LVSLDH) THEN
  READ (KLUN) IL2
  READ (KLUN) IU2
  ALLOCATE (SELF%VSLDH (IL2(1):IU2(1), IL2(2):IU2(2)))
  ALLOCATE (ZTMP2(LBOUND(SELF%VSLDH,1):UBOUND(SELF%VSLDH,1), LBOUND(SELF%VSLDH,2):UBOUND(SELF%VSLDH,2)))
  READ (KLUN) ZTMP2
  SELF%VSLDH = ZTMP2
  DEALLOCATE (ZTMP2)
ENDIF
READ (KLUN) LVSLDW
IF (LVSLDW) THEN
  READ (KLUN) IL3
  READ (KLUN) IU3
  ALLOCATE (SELF%VSLDW (IL3(1):IU3(1), IL3(2):IU3(2), IL3(3):IU3(3)))
  ALLOCATE (ZTMP3(LBOUND(SELF%VSLDW,1):UBOUND(SELF%VSLDW,1), LBOUND(SELF%VSLDW,2):UBOUND(SELF%VSLDW,2), LBOUND(SELF%VSLDW,3):UBOUND(SELF%VSLDW,3)))
  READ (KLUN) ZTMP3
  SELF%VSLDW = ZTMP3
  DEALLOCATE (ZTMP3)
ENDIF
READ (KLUN) LVSLDWH
IF (LVSLDWH) THEN
  READ (KLUN) IL3
  READ (KLUN) IU3
  ALLOCATE (SELF%VSLDWH (IL3(1):IU3(1), IL3(2):IU3(2), IL3(3):IU3(3)))
  ALLOCATE (ZTMP3(LBOUND(SELF%VSLDWH,1):UBOUND(SELF%VSLDWH,1), LBOUND(SELF%VSLDWH,2):UBOUND(SELF%VSLDWH,2), LBOUND(SELF%VSLDWH,3):UBOUND(SELF%VSLDWH,3)))
  READ (KLUN) ZTMP3
  SELF%VSLDWH = ZTMP3
  DEALLOCATE (ZTMP3)
ENDIF
READ (KLUN) LVSLVF
IF (LVSLVF) THEN
  READ (KLUN) IL3
  READ (KLUN) IU3
  ALLOCATE (SELF%VSLVF (IL3(1):IU3(1), IL3(2):IU3(2), IL3(3):IU3(3)))
  ALLOCATE (ZTMP3(LBOUND(SELF%VSLVF,1):UBOUND(SELF%VSLVF,1), LBOUND(SELF%VSLVF,2):UBOUND(SELF%VSLVF,2), LBOUND(SELF%VSLVF,3):UBOUND(SELF%VSLVF,3)))
  READ (KLUN) ZTMP3
  SELF%VSLVF = ZTMP3
  DEALLOCATE (ZTMP3)
ENDIF
READ (KLUN) LVSLVFH
IF (LVSLVFH) THEN
  READ (KLUN) IL3
  READ (KLUN) IU3
  ALLOCATE (SELF%VSLVFH (IL3(1):IU3(1), IL3(2):IU3(2), IL3(3):IU3(3)))
  ALLOCATE (ZTMP3(LBOUND(SELF%VSLVFH,1):UBOUND(SELF%VSLVFH,1), LBOUND(SELF%VSLVFH,2):UBOUND(SELF%VSLVFH,2), LBOUND(SELF%VSLVFH,3):UBOUND(SELF%VSLVFH,3)))
  READ (KLUN) ZTMP3
  SELF%VSLVFH = ZTMP3
  DEALLOCATE (ZTMP3)
ENDIF
READ (KLUN) LGAMMA_WENO
IF (LGAMMA_WENO) THEN
  READ (KLUN) IL2
  READ (KLUN) IU2
  ALLOCATE (SELF%GAMMA_WENO (IL2(1):IU2(1), IL2(2):IU2(2)))
  ALLOCATE (ZTMP2(LBOUND(SELF%GAMMA_WENO,1):UBOUND(SELF%GAMMA_WENO,1), LBOUND(SELF%GAMMA_WENO,2):UBOUND(SELF%GAMMA_WENO,2)))
  READ (KLUN) ZTMP2
  SELF%GAMMA_WENO = ZTMP2
  DEALLOCATE (ZTMP2)
ENDIF
READ (KLUN) LVRDETAR
IF (LVRDETAR) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (SELF%VRDETAR (IL1(1):IU1(1)))
  ALLOCATE (ZTMP1(LBOUND(SELF%VRDETAR,1):UBOUND(SELF%VRDETAR,1)))
  READ (KLUN) ZTMP1
  SELF%VRDETAR = ZTMP1
  DEALLOCATE (ZTMP1)
ENDIF
READ (KLUN) SELF%NRLEVX
READ (KLUN) ZTMP0
SELF%VRLEVX = ZTMP0
READ (KLUN) LNVAUTF
IF (LNVAUTF) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (SELF%NVAUTF (IL1(1):IU1(1)))
  READ (KLUN) SELF%NVAUTF
ENDIF
READ (KLUN) LNVAUTH
IF (LNVAUTH) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (SELF%NVAUTH (IL1(1):IU1(1)))
  READ (KLUN) SELF%NVAUTH
ENDIF
END SUBROUTINE

END SUBMODULE
