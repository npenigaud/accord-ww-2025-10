SUBMODULE (CPLNG_TYPES_MOD) CPLNG_TYPES_MOD_CPLNG_FLD_TYPE_LOAD_SMOD

IMPLICIT NONE

CONTAINS

MODULE SUBROUTINE ACDC_LOAD_CPLNG_FLD_TYPE (SELF, KLUN)

IMPLICIT NONE
CLASS (CPLNG_FLD_TYPE), INTENT (OUT), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
INTEGER :: IL2(2), IU2(2), IL3(3), IU3(3)
LOGICAL :: LD, LID
READ (KLUN) LID
IF (LID) THEN
  READ (KLUN) IL2
  READ (KLUN) IU2
  ALLOCATE (SELF%ID (IL2(1):IU2(1), IL2(2):IU2(2)))
  READ (KLUN) SELF%ID
ENDIF
READ (KLUN) SELF%NAME
READ (KLUN) SELF%TYPE
READ (KLUN) SELF%INOUT
READ (KLUN) SELF%STAGE
READ (KLUN) SELF%NUM_LVL
READ (KLUN) SELF%NUM_CAT
READ (KLUN) LD
IF (LD) THEN
  READ (KLUN) IL3
  READ (KLUN) IU3
  ALLOCATE (SELF%D (IL3(1):IU3(1), IL3(2):IU3(2), IL3(3):IU3(3)))
  READ (KLUN) SELF%D
ENDIF
END SUBROUTINE

END SUBMODULE
