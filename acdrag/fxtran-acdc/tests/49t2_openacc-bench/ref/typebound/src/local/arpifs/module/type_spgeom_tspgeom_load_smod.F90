SUBMODULE (TYPE_SPGEOM) TYPE_SPGEOM_TSPGEOM_LOAD_SMOD

IMPLICIT NONE

CONTAINS

MODULE SUBROUTINE ACDC_LOAD_TSPGEOM (SELF, KLUN)
USE PARKIND1, ONLY : JPRD
IMPLICIT NONE
CLASS (TSPGEOM), INTENT (OUT), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
INTEGER :: IL2(2), IU2(2)
LOGICAL :: LGMR, LSCGMAP
REAL(KIND=JPRD), ALLOCATABLE :: ZTMP1 (:)
REAL(KIND=JPRD), ALLOCATABLE :: ZTMP2 (:,:)
READ (KLUN) LGMR
IF (LGMR) THEN
  READ (KLUN) IL2
  READ (KLUN) IU2
  ALLOCATE (SELF%GMR (IL2(1):IU2(1), IL2(2):IU2(2)))
  ALLOCATE (ZTMP2(LBOUND(SELF%GMR,1):UBOUND(SELF%GMR,1), LBOUND(SELF%GMR,2):UBOUND(SELF%GMR,2)))
  READ (KLUN) ZTMP2
  SELF%GMR = ZTMP2
  DEALLOCATE (ZTMP2)
ENDIF
READ (KLUN) LSCGMAP
IF (LSCGMAP) THEN
  READ (KLUN) IL2
  READ (KLUN) IU2
  ALLOCATE (SELF%SCGMAP (IL2(1):IU2(1), IL2(2):IU2(2)))
  ALLOCATE (ZTMP2(LBOUND(SELF%SCGMAP,1):UBOUND(SELF%SCGMAP,1), LBOUND(SELF%SCGMAP,2):UBOUND(SELF%SCGMAP,2)))
  READ (KLUN) ZTMP2
  SELF%SCGMAP = ZTMP2
  DEALLOCATE (ZTMP2)
ENDIF
ALLOCATE (ZTMP1(LBOUND(SELF%ESCGMAP,1):UBOUND(SELF%ESCGMAP,1)))
READ (KLUN) ZTMP1
SELF%ESCGMAP = ZTMP1
DEALLOCATE (ZTMP1)
END SUBROUTINE

END SUBMODULE
