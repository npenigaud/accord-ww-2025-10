MODULE SUBROUTINE ACDC_LOAD_REGLATLON_FIELD (SELF, KLUN)
USE PARKIND1, ONLY : JPRD
IMPLICIT NONE
CLASS (REGLATLON_FIELD), INTENT (OUT), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
INTEGER :: IL1(1), IU1(1), IL2(2), IU2(2)
LOGICAL :: LPFLD, LPLAT, LPLON, LPSIN
REAL(KIND=JPRD) :: ZTMP0
REAL(KIND=JPRD), ALLOCATABLE :: ZTMP1 (:)
REAL(KIND=JPRD), ALLOCATABLE :: ZTMP2 (:,:)
READ (KLUN) SELF%NLAT
READ (KLUN) SELF%NLON
READ (KLUN) ZTMP0
SELF%DLAT = ZTMP0
READ (KLUN) ZTMP0
SELF%DLON = ZTMP0
READ (KLUN) LPFLD
IF (LPFLD) THEN
  READ (KLUN) IL2
  READ (KLUN) IU2
  ALLOCATE (SELF%PFLD (IL2(1):IU2(1), IL2(2):IU2(2)))
  ALLOCATE (ZTMP2(LBOUND(SELF%PFLD,1):UBOUND(SELF%PFLD,1), LBOUND(SELF%PFLD,2):UBOUND(SELF%PFLD,2)))
  READ (KLUN) ZTMP2
  SELF%PFLD = ZTMP2
  DEALLOCATE (ZTMP2)
ELSE
  NULLIFY (SELF%PFLD)
ENDIF
READ (KLUN) LPLAT
IF (LPLAT) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (SELF%PLAT (IL1(1):IU1(1)))
  ALLOCATE (ZTMP1(LBOUND(SELF%PLAT,1):UBOUND(SELF%PLAT,1)))
  READ (KLUN) ZTMP1
  SELF%PLAT = ZTMP1
  DEALLOCATE (ZTMP1)
ELSE
  NULLIFY (SELF%PLAT)
ENDIF
READ (KLUN) LPSIN
IF (LPSIN) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (SELF%PSIN (IL1(1):IU1(1)))
  ALLOCATE (ZTMP1(LBOUND(SELF%PSIN,1):UBOUND(SELF%PSIN,1)))
  READ (KLUN) ZTMP1
  SELF%PSIN = ZTMP1
  DEALLOCATE (ZTMP1)
ELSE
  NULLIFY (SELF%PSIN)
ENDIF
READ (KLUN) LPLON
IF (LPLON) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (SELF%PLON (IL1(1):IU1(1)))
  ALLOCATE (ZTMP1(LBOUND(SELF%PLON,1):UBOUND(SELF%PLON,1)))
  READ (KLUN) ZTMP1
  SELF%PLON = ZTMP1
  DEALLOCATE (ZTMP1)
ELSE
  NULLIFY (SELF%PLON)
ENDIF
END SUBROUTINE

END SUBMODULE
