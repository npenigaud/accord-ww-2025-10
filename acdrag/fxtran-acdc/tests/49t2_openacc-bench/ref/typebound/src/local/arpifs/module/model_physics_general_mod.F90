MODULE MODEL_PHYSICS_GENERAL_MOD

!$ACDC methods 

  USE YOMDPHY , ONLY : TDPHY
  USE YOMSLPHY, ONLY : TSLPHY
  USE YOEVDF,   ONLY : TVDF
  IMPLICIT NONE
  
  TYPE MODEL_PHYSICS_GENERAL_TYPE

    TYPE(TDPHY)  :: YRDPHY   !! dimensions
    TYPE(TSLPHY) :: YRSLPHY  !! physics & dynamics interfacing
    TYPE(TVDF)   :: YRVDF
  
  CONTAINS
  
    PROCEDURE, PASS :: PRINT => PRINT_CONFIGURATION 

  PROCEDURE :: ACDC_COPY => ACDC_COPY_MODEL_PHYSICS_GENERAL_TYPE
PROCEDURE :: ACDC_CRC64 => ACDC_CRC64_MODEL_PHYSICS_GENERAL_TYPE
PROCEDURE :: ACDC_HOST => ACDC_HOST_MODEL_PHYSICS_GENERAL_TYPE
PROCEDURE :: ACDC_LOAD => ACDC_LOAD_MODEL_PHYSICS_GENERAL_TYPE
PROCEDURE :: ACDC_SAVE => ACDC_SAVE_MODEL_PHYSICS_GENERAL_TYPE
PROCEDURE :: ACDC_SIZE => ACDC_SIZE_MODEL_PHYSICS_GENERAL_TYPE
PROCEDURE :: ACDC_WIPE => ACDC_WIPE_MODEL_PHYSICS_GENERAL_TYPE
END TYPE MODEL_PHYSICS_GENERAL_TYPE

  !---------------------------------------------------------------------

  INTERFACE

MODULE SUBROUTINE ACDC_COPY_MODEL_PHYSICS_GENERAL_TYPE (SELF, LDCREATED, LDFIELDAPI)

IMPLICIT NONE
CLASS (MODEL_PHYSICS_GENERAL_TYPE), INTENT (IN), TARGET :: SELF
LOGICAL, OPTIONAL, INTENT (IN) :: LDCREATED, LDFIELDAPI
END SUBROUTINE

MODULE SUBROUTINE ACDC_CRC64_MODEL_PHYSICS_GENERAL_TYPE (SELF, KLUN, CDPATH)
USE FXTRAN_ACDC_CRC64_INTRINSIC_MOD, ONLY : FCRC64 => FXTRAN_ACDC_CRC64
IMPLICIT NONE
CLASS (MODEL_PHYSICS_GENERAL_TYPE), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
CHARACTER(LEN=*), INTENT (IN) :: CDPATH
END SUBROUTINE

MODULE SUBROUTINE ACDC_HOST_MODEL_PHYSICS_GENERAL_TYPE (SELF)

IMPLICIT NONE
CLASS (MODEL_PHYSICS_GENERAL_TYPE), TARGET :: SELF
END SUBROUTINE

MODULE SUBROUTINE ACDC_LOAD_MODEL_PHYSICS_GENERAL_TYPE (SELF, KLUN)

IMPLICIT NONE
CLASS (MODEL_PHYSICS_GENERAL_TYPE), INTENT (OUT), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
END SUBROUTINE

MODULE SUBROUTINE ACDC_SAVE_MODEL_PHYSICS_GENERAL_TYPE (SELF, KLUN)

IMPLICIT NONE
CLASS (MODEL_PHYSICS_GENERAL_TYPE), INTENT (IN), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
END SUBROUTINE

MODULE FUNCTION ACDC_SIZE_MODEL_PHYSICS_GENERAL_TYPE (SELF, CDPATH, LDPRINT) RESULT (KSIZE)

IMPLICIT NONE
CLASS (MODEL_PHYSICS_GENERAL_TYPE),     INTENT (IN), TARGET :: SELF
CHARACTER(LEN=*), INTENT (IN), OPTIONAL :: CDPATH
LOGICAL,          INTENT (IN), OPTIONAL :: LDPRINT
INTEGER*8 :: KSIZE
END FUNCTION

MODULE SUBROUTINE ACDC_WIPE_MODEL_PHYSICS_GENERAL_TYPE (SELF, LDDELETED, LDFIELDAPI)

IMPLICIT NONE
CLASS (MODEL_PHYSICS_GENERAL_TYPE), INTENT (IN), TARGET :: SELF
LOGICAL, OPTIONAL, INTENT (IN) :: LDDELETED, LDFIELDAPI
END SUBROUTINE


END INTERFACE

CONTAINS 

  SUBROUTINE PRINT_CONFIGURATION(SELF, KDEPTH, KOUTNO)
  IMPLICIT NONE
  CLASS(MODEL_PHYSICS_GENERAL_TYPE), INTENT(IN) :: SELF
  INTEGER                          , INTENT(IN) :: KDEPTH
  INTEGER                          , INTENT(IN) :: KOUTNO

  WRITE(KOUTNO,*) REPEAT(' ',KDEPTH) // 'model%yrml_phy_g : '
  CALL SELF%YRDPHY%PRINT(KDEPTH+2, KOUTNO)
  CALL SELF%YRSLPHY%PRINT(KDEPTH+2, KOUTNO)
  CALL SELF%YRVDF%PRINT(KDEPTH+2, KOUTNO)

  END SUBROUTINE PRINT_CONFIGURATION

END MODULE MODEL_PHYSICS_GENERAL_MOD
