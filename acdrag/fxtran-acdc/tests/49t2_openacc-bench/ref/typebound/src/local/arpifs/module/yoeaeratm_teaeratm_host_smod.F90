SUBMODULE (YOEAERATM) YOEAERATM_TEAERATM_HOST_SMOD

IMPLICIT NONE

CONTAINS

MODULE SUBROUTINE ACDC_HOST_TEAERATM (SELF)

IMPLICIT NONE
CLASS (TEAERATM), TARGET :: SELF
INTEGER :: J1
LOGICAL :: LYAERO_DESC

LYAERO_DESC = ASSOCIATED (SELF%YAERO_DESC)
IF (LYAERO_DESC) THEN
  DO J1 = LBOUND (SELF%YAERO_DESC, 1), UBOUND (SELF%YAERO_DESC, 1)
    CALL SELF%YAERO_DESC (J1)%ACDC_HOST ()
  ENDDO
ENDIF

END SUBROUTINE

END SUBMODULE
