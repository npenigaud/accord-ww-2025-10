SUBMODULE (TYPE_ECV) TYPE_ECV_ECV_CONTAINER_SAVE_SMOD

IMPLICIT NONE

CONTAINS

MODULE SUBROUTINE ACDC_SAVE_ECV_CONTAINER (SELF, KLUN)

IMPLICIT NONE
CLASS (ECV_CONTAINER), INTENT (IN), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
LOGICAL :: LRECV1D, LRGPECV2D, LRGPECV3D, LRLSM, LRSPECV2D, LRSPECV3D
LRECV1D = ALLOCATED (SELF%RECV1D)
WRITE (KLUN) LRECV1D
IF (LRECV1D) THEN
  WRITE (KLUN) LBOUND (SELF%RECV1D)
  WRITE (KLUN) UBOUND (SELF%RECV1D)
  WRITE (KLUN) SELF%RECV1D
ENDIF
LRSPECV2D = ALLOCATED (SELF%RSPECV2D)
WRITE (KLUN) LRSPECV2D
IF (LRSPECV2D) THEN
  WRITE (KLUN) LBOUND (SELF%RSPECV2D)
  WRITE (KLUN) UBOUND (SELF%RSPECV2D)
  WRITE (KLUN) SELF%RSPECV2D
ENDIF
LRGPECV2D = ALLOCATED (SELF%RGPECV2D)
WRITE (KLUN) LRGPECV2D
IF (LRGPECV2D) THEN
  WRITE (KLUN) LBOUND (SELF%RGPECV2D)
  WRITE (KLUN) UBOUND (SELF%RGPECV2D)
  WRITE (KLUN) SELF%RGPECV2D
ENDIF
LRSPECV3D = ALLOCATED (SELF%RSPECV3D)
WRITE (KLUN) LRSPECV3D
IF (LRSPECV3D) THEN
  WRITE (KLUN) LBOUND (SELF%RSPECV3D)
  WRITE (KLUN) UBOUND (SELF%RSPECV3D)
  WRITE (KLUN) SELF%RSPECV3D
ENDIF
LRGPECV3D = ALLOCATED (SELF%RGPECV3D)
WRITE (KLUN) LRGPECV3D
IF (LRGPECV3D) THEN
  WRITE (KLUN) LBOUND (SELF%RGPECV3D)
  WRITE (KLUN) UBOUND (SELF%RGPECV3D)
  WRITE (KLUN) SELF%RGPECV3D
ENDIF
LRLSM = ALLOCATED (SELF%RLSM)
WRITE (KLUN) LRLSM
IF (LRLSM) THEN
  WRITE (KLUN) LBOUND (SELF%RLSM)
  WRITE (KLUN) UBOUND (SELF%RLSM)
  WRITE (KLUN) SELF%RLSM
ENDIF
END SUBROUTINE

END SUBMODULE
