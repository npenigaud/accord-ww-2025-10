MODULE SUBROUTINE ACDC_COPY_TECVGRIB (SELF, LDCREATED, LDFIELDAPI)

IMPLICIT NONE
CLASS (TECVGRIB), INTENT (IN), TARGET :: SELF
LOGICAL, OPTIONAL, INTENT (IN) :: LDCREATED, LDFIELDAPI
LOGICAL :: LLCREATED
LOGICAL :: LLFIELDAPI
LOGICAL :: LMECVGRB_1D, LMECVGRB_2D, LMECVGRB_3D, LMECVGRB_ALL
LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF
LLCREATED = .FALSE.
IF (PRESENT (LDCREATED)) THEN
  LLCREATED = LDCREATED
ENDIF
IF (.NOT. LLCREATED) THEN
  !$ACC ENTER DATA CREATE (SELF)
  !$ACC UPDATE DEVICE (SELF)
ENDIF
LMECVGRB_1D = ALLOCATED (SELF%MECVGRB_1D)
IF (LMECVGRB_1D) THEN
  !$ACC ENTER DATA CREATE (SELF%MECVGRB_1D)
  !$ACC UPDATE DEVICE (SELF%MECVGRB_1D)
  !$ACC ENTER DATA ATTACH (SELF%MECVGRB_1D)
ENDIF

LMECVGRB_2D = ALLOCATED (SELF%MECVGRB_2D)
IF (LMECVGRB_2D) THEN
  !$ACC ENTER DATA CREATE (SELF%MECVGRB_2D)
  !$ACC UPDATE DEVICE (SELF%MECVGRB_2D)
  !$ACC ENTER DATA ATTACH (SELF%MECVGRB_2D)
ENDIF

LMECVGRB_3D = ALLOCATED (SELF%MECVGRB_3D)
IF (LMECVGRB_3D) THEN
  !$ACC ENTER DATA CREATE (SELF%MECVGRB_3D)
  !$ACC UPDATE DEVICE (SELF%MECVGRB_3D)
  !$ACC ENTER DATA ATTACH (SELF%MECVGRB_3D)
ENDIF

LMECVGRB_ALL = ALLOCATED (SELF%MECVGRB_ALL)
IF (LMECVGRB_ALL) THEN
  !$ACC ENTER DATA CREATE (SELF%MECVGRB_ALL)
  !$ACC UPDATE DEVICE (SELF%MECVGRB_ALL)
  !$ACC ENTER DATA ATTACH (SELF%MECVGRB_ALL)
ENDIF

END SUBROUTINE

END SUBMODULE
