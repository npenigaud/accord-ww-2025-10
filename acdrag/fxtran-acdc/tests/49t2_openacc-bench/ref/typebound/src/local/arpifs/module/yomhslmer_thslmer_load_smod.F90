SUBMODULE (YOMHSLMER) YOMHSLMER_THSLMER_LOAD_SMOD

IMPLICIT NONE

CONTAINS

MODULE SUBROUTINE ACDC_LOAD_THSLMER (SELF, KLUN)
USE PARKIND1, ONLY : JPRD
IMPLICIT NONE
CLASS (THSLMER), INTENT (OUT), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
INTEGER :: IL2(2), IU2(2), IL3(3), IU3(3)
LOGICAL :: LR3DTW, LRIPI, LRSLD, LRSLDW
REAL(KIND=JPRD), ALLOCATABLE :: ZTMP2 (:,:)
REAL(KIND=JPRD), ALLOCATABLE :: ZTMP3 (:,:,:)
READ (KLUN) LRIPI
IF (LRIPI) THEN
  READ (KLUN) IL2
  READ (KLUN) IU2
  ALLOCATE (SELF%RIPI (IL2(1):IU2(1), IL2(2):IU2(2)))
  READ (KLUN) SELF%RIPI
ENDIF
READ (KLUN) LRSLD
IF (LRSLD) THEN
  READ (KLUN) IL2
  READ (KLUN) IU2
  ALLOCATE (SELF%RSLD (IL2(1):IU2(1), IL2(2):IU2(2)))
  ALLOCATE (ZTMP2(LBOUND(SELF%RSLD,1):UBOUND(SELF%RSLD,1), LBOUND(SELF%RSLD,2):UBOUND(SELF%RSLD,2)))
  READ (KLUN) ZTMP2
  SELF%RSLD = ZTMP2
  DEALLOCATE (ZTMP2)
ENDIF
READ (KLUN) LRSLDW
IF (LRSLDW) THEN
  READ (KLUN) IL3
  READ (KLUN) IU3
  ALLOCATE (SELF%RSLDW (IL3(1):IU3(1), IL3(2):IU3(2), IL3(3):IU3(3)))
  ALLOCATE (ZTMP3(LBOUND(SELF%RSLDW,1):UBOUND(SELF%RSLDW,1), LBOUND(SELF%RSLDW,2):UBOUND(SELF%RSLDW,2), LBOUND(SELF%RSLDW,3):UBOUND(SELF%RSLDW,3)))
  READ (KLUN) ZTMP3
  SELF%RSLDW = ZTMP3
  DEALLOCATE (ZTMP3)
ENDIF
READ (KLUN) LR3DTW
IF (LR3DTW) THEN
  READ (KLUN) IL3
  READ (KLUN) IU3
  ALLOCATE (SELF%R3DTW (IL3(1):IU3(1), IL3(2):IU3(2), IL3(3):IU3(3)))
  ALLOCATE (ZTMP3(LBOUND(SELF%R3DTW,1):UBOUND(SELF%R3DTW,1), LBOUND(SELF%R3DTW,2):UBOUND(SELF%R3DTW,2), LBOUND(SELF%R3DTW,3):UBOUND(SELF%R3DTW,3)))
  READ (KLUN) ZTMP3
  SELF%R3DTW = ZTMP3
  DEALLOCATE (ZTMP3)
ENDIF
END SUBROUTINE

END SUBMODULE
