SUBMODULE (YOMCSGEOM) YOMCSGEOM_TCSGEOM_BLOCKED_LOAD_SMOD

IMPLICIT NONE

CONTAINS

MODULE SUBROUTINE ACDC_LOAD_TCSGEOM_BLOCKED (SELF, KLUN)
USE PARKIND1, ONLY : JPRD
IMPLICIT NONE
CLASS (TCSGEOM_BLOCKED), INTENT (OUT), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
INTEGER :: IL2(2), IU2(2)
LOGICAL :: LRATATH, LRATATX, LRCOLON, LRINDX, LRINDY, LRSILON
REAL(KIND=JPRD), ALLOCATABLE :: ZTMP2 (:,:)
READ (KLUN) LRCOLON
IF (LRCOLON) THEN
  READ (KLUN) IL2
  READ (KLUN) IU2
  ALLOCATE (SELF%RCOLON (IL2(1):IU2(1), IL2(2):IU2(2)))
  READ (KLUN) SELF%RCOLON
ELSE
  NULLIFY (SELF%RCOLON)
ENDIF
READ (KLUN) LRSILON
IF (LRSILON) THEN
  READ (KLUN) IL2
  READ (KLUN) IU2
  ALLOCATE (SELF%RSILON (IL2(1):IU2(1), IL2(2):IU2(2)))
  READ (KLUN) SELF%RSILON
ELSE
  NULLIFY (SELF%RSILON)
ENDIF
READ (KLUN) LRINDX
IF (LRINDX) THEN
  READ (KLUN) IL2
  READ (KLUN) IU2
  ALLOCATE (SELF%RINDX (IL2(1):IU2(1), IL2(2):IU2(2)))
  ALLOCATE (ZTMP2(LBOUND(SELF%RINDX,1):UBOUND(SELF%RINDX,1), LBOUND(SELF%RINDX,2):UBOUND(SELF%RINDX,2)))
  READ (KLUN) ZTMP2
  SELF%RINDX = ZTMP2
  DEALLOCATE (ZTMP2)
ELSE
  NULLIFY (SELF%RINDX)
ENDIF
READ (KLUN) LRINDY
IF (LRINDY) THEN
  READ (KLUN) IL2
  READ (KLUN) IU2
  ALLOCATE (SELF%RINDY (IL2(1):IU2(1), IL2(2):IU2(2)))
  ALLOCATE (ZTMP2(LBOUND(SELF%RINDY,1):UBOUND(SELF%RINDY,1), LBOUND(SELF%RINDY,2):UBOUND(SELF%RINDY,2)))
  READ (KLUN) ZTMP2
  SELF%RINDY = ZTMP2
  DEALLOCATE (ZTMP2)
ELSE
  NULLIFY (SELF%RINDY)
ENDIF
READ (KLUN) LRATATH
IF (LRATATH) THEN
  READ (KLUN) IL2
  READ (KLUN) IU2
  ALLOCATE (SELF%RATATH (IL2(1):IU2(1), IL2(2):IU2(2)))
  ALLOCATE (ZTMP2(LBOUND(SELF%RATATH,1):UBOUND(SELF%RATATH,1), LBOUND(SELF%RATATH,2):UBOUND(SELF%RATATH,2)))
  READ (KLUN) ZTMP2
  SELF%RATATH = ZTMP2
  DEALLOCATE (ZTMP2)
ELSE
  NULLIFY (SELF%RATATH)
ENDIF
READ (KLUN) LRATATX
IF (LRATATX) THEN
  READ (KLUN) IL2
  READ (KLUN) IU2
  ALLOCATE (SELF%RATATX (IL2(1):IU2(1), IL2(2):IU2(2)))
  ALLOCATE (ZTMP2(LBOUND(SELF%RATATX,1):UBOUND(SELF%RATATX,1), LBOUND(SELF%RATATX,2):UBOUND(SELF%RATATX,2)))
  READ (KLUN) ZTMP2
  SELF%RATATX = ZTMP2
  DEALLOCATE (ZTMP2)
ELSE
  NULLIFY (SELF%RATATX)
ENDIF
END SUBROUTINE

END SUBMODULE
