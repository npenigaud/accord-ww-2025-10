SUBMODULE (YOMOROG) YOMOROG_TOROG_SIZE_SMOD

IMPLICIT NONE

CONTAINS

MODULE FUNCTION ACDC_SIZE_TOROG (SELF, CDPATH, LDPRINT) RESULT (KSIZE)

IMPLICIT NONE
CLASS (TOROG),     INTENT (IN), TARGET :: SELF
CHARACTER(LEN=*), INTENT (IN), OPTIONAL :: CDPATH
LOGICAL,          INTENT (IN), OPTIONAL :: LDPRINT
INTEGER*8 :: KSIZE
INTEGER*8 :: ISIZE, JSIZE
LOGICAL :: LLPRINT
CHARACTER(LEN=128) :: CLPATH
LOGICAL :: LOROG, LOROGL, LOROGLL, LOROGLM, LOROGM, LOROGMM
LLPRINT = .FALSE.
IF (PRESENT (LDPRINT)) THEN
  LLPRINT = LDPRINT
ENDIF
CLPATH=''
IF (PRESENT (CDPATH)) THEN
  CLPATH = CDPATH
ENDIF
KSIZE = 0
LOROG = ALLOCATED (SELF%OROG)
IF (LOROG) THEN
  ISIZE = KIND (SELF%OROG) * SIZE (SELF%OROG)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%OROG'
  ENDIF
  KSIZE = KSIZE + ISIZE
ENDIF
LOROGL = ALLOCATED (SELF%OROGL)
IF (LOROGL) THEN
  ISIZE = KIND (SELF%OROGL) * SIZE (SELF%OROGL)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%OROGL'
  ENDIF
  KSIZE = KSIZE + ISIZE
ENDIF
LOROGM = ALLOCATED (SELF%OROGM)
IF (LOROGM) THEN
  ISIZE = KIND (SELF%OROGM) * SIZE (SELF%OROGM)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%OROGM'
  ENDIF
  KSIZE = KSIZE + ISIZE
ENDIF
LOROGLL = ALLOCATED (SELF%OROGLL)
IF (LOROGLL) THEN
  ISIZE = KIND (SELF%OROGLL) * SIZE (SELF%OROGLL)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%OROGLL'
  ENDIF
  KSIZE = KSIZE + ISIZE
ENDIF
LOROGMM = ALLOCATED (SELF%OROGMM)
IF (LOROGMM) THEN
  ISIZE = KIND (SELF%OROGMM) * SIZE (SELF%OROGMM)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%OROGMM'
  ENDIF
  KSIZE = KSIZE + ISIZE
ENDIF
LOROGLM = ALLOCATED (SELF%OROGLM)
IF (LOROGLM) THEN
  ISIZE = KIND (SELF%OROGLM) * SIZE (SELF%OROGLM)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%OROGLM'
  ENDIF
  KSIZE = KSIZE + ISIZE
ENDIF
END FUNCTION

END SUBMODULE
