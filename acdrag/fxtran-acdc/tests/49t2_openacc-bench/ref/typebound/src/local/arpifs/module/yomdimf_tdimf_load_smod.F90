SUBMODULE (YOMDIMF) YOMDIMF_TDIMF_LOAD_SMOD

IMPLICIT NONE

CONTAINS

MODULE SUBROUTINE ACDC_LOAD_TDIMF (SELF, KLUN)

IMPLICIT NONE
CLASS (TDIMF), INTENT (OUT), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
INTEGER :: IL1(1), IU1(1)
LOGICAL :: LNGRBSP2, LNGRBSP3
READ (KLUN) SELF%NFTHER
READ (KLUN) SELF%NF3D
READ (KLUN) SELF%NFD2D
READ (KLUN) SELF%NFC2D
READ (KLUN) SELF%NS3D
READ (KLUN) SELF%NS2D
READ (KLUN) SELF%NS1D
READ (KLUN) LNGRBSP3
IF (LNGRBSP3) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (SELF%NGRBSP3 (IL1(1):IU1(1)))
  READ (KLUN) SELF%NGRBSP3
ENDIF
READ (KLUN) LNGRBSP2
IF (LNGRBSP2) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (SELF%NGRBSP2 (IL1(1):IU1(1)))
  READ (KLUN) SELF%NGRBSP2
ENDIF
READ (KLUN) SELF%LVOR
READ (KLUN) SELF%LADER
READ (KLUN) SELF%LUVDER
READ (KLUN) SELF%LSPT
END SUBROUTINE

END SUBMODULE
