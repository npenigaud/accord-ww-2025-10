IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%NHDQLNFD'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%NHDQLNFP)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%NHDQLNFP'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%NHDQLNTP)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%NHDQLNTP'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%NHDPASVA)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%NHDPASVA'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%NHDPASTD)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%NHDPASTD'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%NHDPASFD)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%NHDPASFD'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%NHDPASFP)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%NHDPASFP'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%NHDPASTP)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%NHDPASTP'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%LHDQLN)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%LHDQLN'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%LHDPAS)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%LHDPAS'
ENDIF
KSIZE = KSIZE + ISIZE
END FUNCTION

END SUBMODULE
