SUBMODULE (YOEOZOC) YOEOZOC_TEOZOC_SAVE_SMOD

IMPLICIT NONE

CONTAINS

MODULE SUBROUTINE ACDC_SAVE_TEOZOC (SELF, KLUN)

IMPLICIT NONE
CLASS (TEOZOC), INTENT (IN), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
LOGICAL :: LZOZCL, LZOZCLAQUA
LZOZCL = ALLOCATED (SELF%ZOZCL)
WRITE (KLUN) LZOZCL
IF (LZOZCL) THEN
  WRITE (KLUN) LBOUND (SELF%ZOZCL)
  WRITE (KLUN) UBOUND (SELF%ZOZCL)
  WRITE (KLUN) SELF%ZOZCL
ENDIF
LZOZCLAQUA = ALLOCATED (SELF%ZOZCLAQUA)
WRITE (KLUN) LZOZCLAQUA
IF (LZOZCLAQUA) THEN
  WRITE (KLUN) LBOUND (SELF%ZOZCLAQUA)
  WRITE (KLUN) UBOUND (SELF%ZOZCLAQUA)
  WRITE (KLUN) SELF%ZOZCLAQUA
ENDIF
WRITE (KLUN) SELF%COZQC
WRITE (KLUN) SELF%COZQS
WRITE (KLUN) SELF%COZHC
WRITE (KLUN) SELF%COZHS
WRITE (KLUN) SELF%RSINC
WRITE (KLUN) SELF%ROZT
WRITE (KLUN) SELF%RPROC
END SUBROUTINE

END SUBMODULE
