SUBMODULE (CPG_TYPE_MOD) CPG_TYPE_MOD_CPG_PHY_TYPE_WIPE_SMOD

IMPLICIT NONE

CONTAINS

MODULE SUBROUTINE ACDC_WIPE_CPG_PHY_TYPE (SELF, LDDELETED, LDFIELDAPI)
USE FIELD_UTIL_MODULE
IMPLICIT NONE
CLASS (CPG_PHY_TYPE), INTENT (IN), TARGET :: SELF
LOGICAL, OPTIONAL, INTENT (IN) :: LDDELETED, LDFIELDAPI
LOGICAL :: LLDELETED
LOGICAL :: LLFIELDAPI
LOGICAL :: LF_PRE, LF_PREF, LF_PREHYD, LF_PREHYDF, LF_W, LF_WL, LF_WM
LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF
LF_PRE = ASSOCIATED (SELF%F_PRE)
IF (LF_PRE .AND. LLFIELDAPI) THEN
  CALL WIPE_FIELD_3RB (SELF%F_PRE)
ENDIF

LF_PREF = ASSOCIATED (SELF%F_PREF)
IF (LF_PREF .AND. LLFIELDAPI) THEN
  CALL WIPE_FIELD_3RB (SELF%F_PREF)
ENDIF

LF_PREHYD = ASSOCIATED (SELF%F_PREHYD)
IF (LF_PREHYD .AND. LLFIELDAPI) THEN
  CALL WIPE_FIELD_3RB (SELF%F_PREHYD)
ENDIF

LF_PREHYDF = ASSOCIATED (SELF%F_PREHYDF)
IF (LF_PREHYDF .AND. LLFIELDAPI) THEN
  CALL WIPE_FIELD_3RB (SELF%F_PREHYDF)
ENDIF

LF_WL = ASSOCIATED (SELF%F_WL)
IF (LF_WL .AND. LLFIELDAPI) THEN
  CALL WIPE_FIELD_3RB (SELF%F_WL)
ENDIF

LF_WM = ASSOCIATED (SELF%F_WM)
IF (LF_WM .AND. LLFIELDAPI) THEN
  CALL WIPE_FIELD_3RB (SELF%F_WM)
ENDIF

LF_W = ASSOCIATED (SELF%F_W)
IF (LF_W .AND. LLFIELDAPI) THEN
  CALL WIPE_FIELD_3RB (SELF%F_W)
ENDIF

CALL SELF%XYB%ACDC_WIPE (LDDELETED=.TRUE., LDFIELDAPI=LDFIELDAPI)

LLDELETED = .FALSE.
IF (PRESENT (LDDELETED)) THEN
  LLDELETED = LDDELETED
ENDIF
IF (.NOT. LLDELETED) THEN
  !$ACC EXIT DATA DELETE (SELF)
ENDIF
END SUBROUTINE

END SUBMODULE
