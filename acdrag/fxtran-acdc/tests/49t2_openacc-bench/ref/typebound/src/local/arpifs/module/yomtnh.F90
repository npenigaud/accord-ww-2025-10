MODULE YOMTNH

!$ACDC methods 


USE PARKIND1  ,ONLY : JPIM     ,JPRB

IMPLICIT NONE

SAVE

!   -----------------------------------------------------------------

!*   Buffer for trajectory array at time t-dt (nonhydrostatic dynamics)

!    NLENNH95B                    : length of buffer for 3D fields
!    TRAJNH(NLENNH95B,0:NSTOP)    : buffer for 3D fields

TYPE :: TTNH
INTEGER(KIND=JPIM) :: NLENNH95B
REAL(KIND=JPRB),ALLOCATABLE:: TRAJNH(:,:)
!--------------------------------------------------------------
CONTAINS
  
  PROCEDURE, PASS :: PRINT => PRINT_CONFIGURATION 

PROCEDURE :: ACDC_COPY => ACDC_COPY_TTNH
PROCEDURE :: ACDC_CRC64 => ACDC_CRC64_TTNH
PROCEDURE :: ACDC_HOST => ACDC_HOST_TTNH
PROCEDURE :: ACDC_LOAD => ACDC_LOAD_TTNH
PROCEDURE :: ACDC_SAVE => ACDC_SAVE_TTNH
PROCEDURE :: ACDC_SIZE => ACDC_SIZE_TTNH
PROCEDURE :: ACDC_WIPE => ACDC_WIPE_TTNH
END TYPE TTNH

!!TYPE(TTNH), POINTER :: YRTNH => NULL()
!==============================================================

!!TYPE(TPTRSLB1), POINTER :: YRPTRSLB1 => NULL()

INTERFACE

MODULE SUBROUTINE ACDC_COPY_TTNH (SELF, LDCREATED, LDFIELDAPI)

IMPLICIT NONE
CLASS (TTNH), INTENT (IN), TARGET :: SELF
LOGICAL, OPTIONAL, INTENT (IN) :: LDCREATED, LDFIELDAPI
END SUBROUTINE

MODULE SUBROUTINE ACDC_CRC64_TTNH (SELF, KLUN, CDPATH)
USE FXTRAN_ACDC_CRC64_INTRINSIC_MOD, ONLY : FCRC64 => FXTRAN_ACDC_CRC64
IMPLICIT NONE
CLASS (TTNH), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
CHARACTER(LEN=*), INTENT (IN) :: CDPATH
END SUBROUTINE

MODULE SUBROUTINE ACDC_HOST_TTNH (SELF)

IMPLICIT NONE
CLASS (TTNH), TARGET :: SELF
END SUBROUTINE

MODULE SUBROUTINE ACDC_LOAD_TTNH (SELF, KLUN)
USE PARKIND1, ONLY : JPRD
IMPLICIT NONE
CLASS (TTNH), INTENT (OUT), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
END SUBROUTINE

MODULE SUBROUTINE ACDC_SAVE_TTNH (SELF, KLUN)

IMPLICIT NONE
CLASS (TTNH), INTENT (IN), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
END SUBROUTINE

MODULE FUNCTION ACDC_SIZE_TTNH (SELF, CDPATH, LDPRINT) RESULT (KSIZE)

IMPLICIT NONE
CLASS (TTNH),     INTENT (IN), TARGET :: SELF
CHARACTER(LEN=*), INTENT (IN), OPTIONAL :: CDPATH
LOGICAL,          INTENT (IN), OPTIONAL :: LDPRINT
INTEGER*8 :: KSIZE
END FUNCTION

MODULE SUBROUTINE ACDC_WIPE_TTNH (SELF, LDDELETED, LDFIELDAPI)

IMPLICIT NONE
CLASS (TTNH), INTENT (IN), TARGET :: SELF
LOGICAL, OPTIONAL, INTENT (IN) :: LDDELETED, LDFIELDAPI
END SUBROUTINE


END INTERFACE

CONTAINS 
  
SUBROUTINE PRINT_CONFIGURATION(SELF, KDEPTH, KOUTNO)
  IMPLICIT NONE
  CLASS(TTNH), INTENT(IN) :: SELF
  INTEGER    , INTENT(IN) :: KDEPTH
  INTEGER    , INTENT(IN) :: KOUTNO

  INTEGER :: IDEPTHLOC

  IDEPTHLOC = KDEPTH + 2

  WRITE(KOUTNO,*) REPEAT(' ',KDEPTH   ) // 'model%yrml_gconf%yrtnh : '
  WRITE(KOUTNO,*) REPEAT(' ',IDEPTHLOC) // 'NLENNH95B = ', SELF%NLENNH95B
  IF (ALLOCATED(SELF%TRAJNH)) THEN
    WRITE(KOUTNO,*) REPEAT(' ',IDEPTHLOC) // 'TRAJNH allocated of shape ', SHAPE(SELF%TRAJNH),' sum ',SUM(SELF%TRAJNH)
  ELSE
    WRITE(KOUTNO,*) REPEAT(' ',IDEPTHLOC) // 'TRAJNH not allocated'
  ENDIF

END SUBROUTINE PRINT_CONFIGURATION
!   ----------------------------------------------------------------
END MODULE YOMTNH
