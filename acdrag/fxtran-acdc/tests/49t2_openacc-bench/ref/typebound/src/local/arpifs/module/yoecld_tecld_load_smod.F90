MODULE SUBROUTINE ACDC_LOAD_TECLD (SELF, KLUN)
USE PARKIND1, ONLY : JPRD
IMPLICIT NONE
CLASS (TECLD), INTENT (OUT), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
INTEGER :: IL1(1), IU1(1)
LOGICAL :: LCETA
REAL(KIND=JPRD) :: ZTMP0
REAL(KIND=JPRD), ALLOCATABLE :: ZTMP1 (:)
READ (KLUN) LCETA
IF (LCETA) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (SELF%CETA (IL1(1):IU1(1)))
  ALLOCATE (ZTMP1(LBOUND(SELF%CETA,1):UBOUND(SELF%CETA,1)))
  READ (KLUN) ZTMP1
  SELF%CETA = ZTMP1
  DEALLOCATE (ZTMP1)
ENDIF
READ (KLUN) ZTMP0
SELF%RANVA = ZTMP0
READ (KLUN) ZTMP0
SELF%RANVB = ZTMP0
READ (KLUN) ZTMP0
SELF%RANVH = ZTMP0
READ (KLUN) ZTMP0
SELF%RCCA = ZTMP0
READ (KLUN) ZTMP0
SELF%RCCB = ZTMP0
READ (KLUN) ZTMP0
SELF%RCCC = ZTMP0
READ (KLUN) ZTMP0
SELF%RCFCT = ZTMP0
READ (KLUN) ZTMP0
SELF%RCLWMR = ZTMP0
READ (KLUN) ZTMP0
SELF%RCSCAL = ZTMP0
READ (KLUN) ZTMP0
SELF%RDECORR_CF = ZTMP0
READ (KLUN) ZTMP0
SELF%RDECORR_CW = ZTMP0
READ (KLUN) ZTMP0
SELF%RETAHB = ZTMP0
READ (KLUN) ZTMP0
SELF%RETAMB = ZTMP0
READ (KLUN) ZTMP0
SELF%RLOIA = ZTMP0
