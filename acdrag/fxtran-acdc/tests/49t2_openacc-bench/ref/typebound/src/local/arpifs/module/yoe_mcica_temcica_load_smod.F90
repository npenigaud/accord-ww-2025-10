SUBMODULE (YOE_MCICA) YOE_MCICA_TEMCICA_LOAD_SMOD

IMPLICIT NONE

CONTAINS

MODULE SUBROUTINE ACDC_LOAD_TEMCICA (SELF, KLUN)
USE PARKIND1, ONLY : JPRD
IMPLICIT NONE
CLASS (TEMCICA), INTENT (OUT), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
REAL(KIND=JPRD), ALLOCATABLE :: ZTMP2 (:,:)
ALLOCATE (ZTMP2(LBOUND(SELF%XCW,1):UBOUND(SELF%XCW,1), LBOUND(SELF%XCW,2):UBOUND(SELF%XCW,2)))
READ (KLUN) ZTMP2
SELF%XCW = ZTMP2
DEALLOCATE (ZTMP2)
READ (KLUN) SELF%XCW_D
READ (KLUN) SELF%NMCI1
READ (KLUN) SELF%NMCI2
END SUBROUTINE

END SUBMODULE
