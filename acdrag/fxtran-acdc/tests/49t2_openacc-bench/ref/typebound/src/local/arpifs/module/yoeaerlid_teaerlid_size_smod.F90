SUBMODULE (YOEAERLID) YOEAERLID_TEAERLID_SIZE_SMOD

IMPLICIT NONE

CONTAINS

MODULE FUNCTION ACDC_SIZE_TEAERLID (SELF, CDPATH, LDPRINT) RESULT (KSIZE)

IMPLICIT NONE
CLASS (TEAERLID),     INTENT (IN), TARGET :: SELF
CHARACTER(LEN=*), INTENT (IN), OPTIONAL :: CDPATH
LOGICAL,          INTENT (IN), OPTIONAL :: LDPRINT
INTEGER*8 :: KSIZE
INTEGER*8 :: ISIZE, JSIZE
LOGICAL :: LLPRINT
CHARACTER(LEN=128) :: CLPATH
LLPRINT = .FALSE.
IF (PRESENT (LDPRINT)) THEN
  LLPRINT = LDPRINT
ENDIF
CLPATH=''
IF (PRESENT (CDPATH)) THEN
  CLPATH = CDPATH
ENDIF
KSIZE = 0
ISIZE = KIND (SELF%NAELID)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%NAELID'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%NWLID)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%NWLID'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%NAERLISI)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%NAERLISI'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%JWLID) * SIZE (SELF%JWLID)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%JWLID'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%RWLID) * SIZE (SELF%RWLID)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%RWLID'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%RLICO2) * SIZE (SELF%RLICO2)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%RLICO2'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%RLIO2) * SIZE (SELF%RLIO2)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%RLIO2'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%RLINO2) * SIZE (SELF%RLINO2)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%RLINO2'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%RLIO3) * SIZE (SELF%RLIO3)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%RLIO3'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%RLICLS)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%RLICLS'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%RLIDELT)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%RLIDELT'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%RLINS)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%RLINS'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%RLIPREF)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%RLIPREF'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%RLITREF)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%RLITREF'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%RLIT0)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%RLIT0'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%RLIDDZ)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%RLIDDZ'
ENDIF
KSIZE = KSIZE + ISIZE
END FUNCTION

END SUBMODULE
