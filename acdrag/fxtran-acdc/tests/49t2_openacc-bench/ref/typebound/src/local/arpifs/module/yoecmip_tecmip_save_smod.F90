MODULE SUBROUTINE ACDC_SAVE_TECMIP (SELF, KLUN)

IMPLICIT NONE
CLASS (TECMIP), INTENT (IN), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
LOGICAL :: LRLATCLI, LRLONCLI, LROZT1, LRPROC1, LRSINC1, LZOZCL
WRITE (KLUN) SELF%NRCP
WRITE (KLUN) SELF%NO3CMIP
WRITE (KLUN) SELF%NGHGCMIP
WRITE (KLUN) SELF%NCMIPFIXYR
WRITE (KLUN) SELF%NCURRYR
WRITE (KLUN) SELF%GHGDATADIR
WRITE (KLUN) SELF%CSOLARDATA
WRITE (KLUN) SELF%CO3DATADIR
WRITE (KLUN) SELF%CO3DATAFIL
LZOZCL = ALLOCATED (SELF%ZOZCL)
WRITE (KLUN) LZOZCL
IF (LZOZCL) THEN
  WRITE (KLUN) LBOUND (SELF%ZOZCL)
  WRITE (KLUN) UBOUND (SELF%ZOZCL)
  WRITE (KLUN) SELF%ZOZCL
ENDIF
LRSINC1 = ALLOCATED (SELF%RSINC1)
WRITE (KLUN) LRSINC1
IF (LRSINC1) THEN
  WRITE (KLUN) LBOUND (SELF%RSINC1)
  WRITE (KLUN) UBOUND (SELF%RSINC1)
  WRITE (KLUN) SELF%RSINC1
ENDIF
LRPROC1 = ALLOCATED (SELF%RPROC1)
WRITE (KLUN) LRPROC1
IF (LRPROC1) THEN
  WRITE (KLUN) LBOUND (SELF%RPROC1)
  WRITE (KLUN) UBOUND (SELF%RPROC1)
  WRITE (KLUN) SELF%RPROC1
ENDIF
LRLATCLI = ALLOCATED (SELF%RLATCLI)
WRITE (KLUN) LRLATCLI
IF (LRLATCLI) THEN
  WRITE (KLUN) LBOUND (SELF%RLATCLI)
  WRITE (KLUN) UBOUND (SELF%RLATCLI)
  WRITE (KLUN) SELF%RLATCLI
ENDIF
LRLONCLI = ALLOCATED (SELF%RLONCLI)
WRITE (KLUN) LRLONCLI
IF (LRLONCLI) THEN
  WRITE (KLUN) LBOUND (SELF%RLONCLI)
  WRITE (KLUN) UBOUND (SELF%RLONCLI)
  WRITE (KLUN) SELF%RLONCLI
ENDIF
LROZT1 = ALLOCATED (SELF%ROZT1)
WRITE (KLUN) LROZT1
IF (LROZT1) THEN
  WRITE (KLUN) LBOUND (SELF%ROZT1)
  WRITE (KLUN) UBOUND (SELF%ROZT1)
  WRITE (KLUN) SELF%ROZT1
ENDIF
END SUBROUTINE

END SUBMODULE
