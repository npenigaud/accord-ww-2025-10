MODULE SUBROUTINE ACDC_COPY_TMODERR (SELF, LDCREATED, LDFIELDAPI)

IMPLICIT NONE
CLASS (TMODERR), INTENT (IN), TARGET :: SELF
LOGICAL, OPTIONAL, INTENT (IN) :: LDCREATED, LDFIELDAPI
LOGICAL :: LLCREATED
LOGICAL :: LLFIELDAPI
LOGICAL :: LFSTEPERR, LMSTEPERR, LTSTEPERR
LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF
LLCREATED = .FALSE.
IF (PRESENT (LDCREATED)) THEN
  LLCREATED = LDCREATED
ENDIF
IF (.NOT. LLCREATED) THEN
  !$ACC ENTER DATA CREATE (SELF)
  !$ACC UPDATE DEVICE (SELF)
ENDIF

LMSTEPERR = ALLOCATED (SELF%MSTEPERR)
IF (LMSTEPERR) THEN
  !$ACC ENTER DATA CREATE (SELF%MSTEPERR)
  !$ACC UPDATE DEVICE (SELF%MSTEPERR)
  !$ACC ENTER DATA ATTACH (SELF%MSTEPERR)
ENDIF

LTSTEPERR = ALLOCATED (SELF%TSTEPERR)
IF (LTSTEPERR) THEN
  !$ACC ENTER DATA CREATE (SELF%TSTEPERR)
  !$ACC UPDATE DEVICE (SELF%TSTEPERR)
  !$ACC ENTER DATA ATTACH (SELF%TSTEPERR)
ENDIF

LFSTEPERR = ALLOCATED (SELF%FSTEPERR)
IF (LFSTEPERR) THEN
  !$ACC ENTER DATA CREATE (SELF%FSTEPERR)
  !$ACC UPDATE DEVICE (SELF%FSTEPERR)
  !$ACC ENTER DATA ATTACH (SELF%FSTEPERR)
ENDIF

END SUBROUTINE

END SUBMODULE
