SUBMODULE (CPG_TYPE_MOD) CPG_TYPE_MOD_CPG_PHY_TYPE_COPY_SMOD

IMPLICIT NONE

CONTAINS

MODULE SUBROUTINE ACDC_COPY_CPG_PHY_TYPE (SELF, LDCREATED, LDFIELDAPI)
USE FIELD_UTIL_MODULE
IMPLICIT NONE
CLASS (CPG_PHY_TYPE), INTENT (IN), TARGET :: SELF
LOGICAL, OPTIONAL, INTENT (IN) :: LDCREATED, LDFIELDAPI
LOGICAL :: LLCREATED
LOGICAL :: LLFIELDAPI
LOGICAL :: LF_PRE, LF_PREF, LF_PREHYD, LF_PREHYDF, LF_W, LF_WL, LF_WM
LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF
LLCREATED = .FALSE.
IF (PRESENT (LDCREATED)) THEN
  LLCREATED = LDCREATED
ENDIF
IF (.NOT. LLCREATED) THEN
  !$ACC ENTER DATA CREATE (SELF)
  !$ACC UPDATE DEVICE (SELF)
ENDIF
LF_PRE = ASSOCIATED (SELF%F_PRE)
IF (LF_PRE .AND. LLFIELDAPI) THEN
  CALL COPY_FIELD_3RB (SELF%F_PRE)
  !$ACC ENTER DATA ATTACH (SELF%F_PRE)
ENDIF

LF_PREF = ASSOCIATED (SELF%F_PREF)
IF (LF_PREF .AND. LLFIELDAPI) THEN
  CALL COPY_FIELD_3RB (SELF%F_PREF)
  !$ACC ENTER DATA ATTACH (SELF%F_PREF)
ENDIF

LF_PREHYD = ASSOCIATED (SELF%F_PREHYD)
IF (LF_PREHYD .AND. LLFIELDAPI) THEN
  CALL COPY_FIELD_3RB (SELF%F_PREHYD)
  !$ACC ENTER DATA ATTACH (SELF%F_PREHYD)
ENDIF

LF_PREHYDF = ASSOCIATED (SELF%F_PREHYDF)
IF (LF_PREHYDF .AND. LLFIELDAPI) THEN
  CALL COPY_FIELD_3RB (SELF%F_PREHYDF)
  !$ACC ENTER DATA ATTACH (SELF%F_PREHYDF)
ENDIF

LF_WL = ASSOCIATED (SELF%F_WL)
IF (LF_WL .AND. LLFIELDAPI) THEN
  CALL COPY_FIELD_3RB (SELF%F_WL)
  !$ACC ENTER DATA ATTACH (SELF%F_WL)
ENDIF

LF_WM = ASSOCIATED (SELF%F_WM)
IF (LF_WM .AND. LLFIELDAPI) THEN
  CALL COPY_FIELD_3RB (SELF%F_WM)
  !$ACC ENTER DATA ATTACH (SELF%F_WM)
ENDIF

LF_W = ASSOCIATED (SELF%F_W)
IF (LF_W .AND. LLFIELDAPI) THEN
  CALL COPY_FIELD_3RB (SELF%F_W)
  !$ACC ENTER DATA ATTACH (SELF%F_W)
ENDIF

CALL SELF%XYB%ACDC_COPY (LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

END SUBROUTINE

END SUBMODULE
