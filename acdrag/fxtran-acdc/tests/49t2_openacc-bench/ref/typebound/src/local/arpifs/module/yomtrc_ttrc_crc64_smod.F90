WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (SELF%GUEOTI), CDPATH//'%GUEOTI'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (SELF%GUEOTI2), CDPATH//'%GUEOTI2'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (SELF%GEOLT), CDPATH//'%GEOLT'

