WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (SELF%GDEOSI), CDPATH//'%GDEOSI'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (SELF%GUEOSI), CDPATH//'%GUEOSI'

