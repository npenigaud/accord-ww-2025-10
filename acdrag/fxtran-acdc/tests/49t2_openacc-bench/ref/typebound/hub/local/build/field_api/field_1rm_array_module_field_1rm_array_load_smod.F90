SUBMODULE (FIELD_1RM_ARRAY_MODULE) FIELD_1RM_ARRAY_MODULE_FIELD_1RM_ARRAY_LOAD_SMOD

IMPLICIT NONE

CONTAINS

MODULE SUBROUTINE ACDC_LOAD_FIELD_1RM_ARRAY (SELF, KLUN)
USE FIELD_UTIL_MODULE
IMPLICIT NONE
CLASS (FIELD_1RM_ARRAY), INTENT (OUT), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
INTEGER :: IL1(1), IU1(1)
LOGICAL :: LF_P, LP_FIELD
READ (KLUN) LF_P
IF (LF_P) THEN
  CALL LOAD_FIELD_1RM (KLUN, SELF%F_P)
ELSE
  NULLIFY (SELF%F_P)
ENDIF
READ (KLUN) LP_FIELD
IF (LP_FIELD) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (SELF%P_FIELD (IL1(1):IU1(1)))
  READ (KLUN) SELF%P_FIELD
ELSE
  NULLIFY (SELF%P_FIELD)
ENDIF
END SUBROUTINE

END SUBMODULE
