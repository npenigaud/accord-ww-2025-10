SUBMODULE (FIELD_4LM_ARRAY_MODULE) FIELD_4LM_ARRAY_MODULE_FIELD_4LM_ARRAY_COPY_SMOD

IMPLICIT NONE

CONTAINS

MODULE SUBROUTINE ACDC_COPY_FIELD_4LM_ARRAY (SELF, LDCREATED, LDFIELDAPI)
USE FIELD_UTIL_MODULE
IMPLICIT NONE
CLASS (FIELD_4LM_ARRAY), INTENT (IN), TARGET :: SELF
LOGICAL, OPTIONAL, INTENT (IN) :: LDCREATED, LDFIELDAPI
LOGICAL :: LLCREATED
LOGICAL :: LLFIELDAPI
LOGICAL :: LF_P, LP_FIELD
LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF
LLCREATED = .FALSE.
IF (PRESENT (LDCREATED)) THEN
  LLCREATED = LDCREATED
ENDIF
IF (.NOT. LLCREATED) THEN
  !$ACC ENTER DATA CREATE (SELF)
  !$ACC UPDATE DEVICE (SELF)
ENDIF
LF_P = ASSOCIATED (SELF%F_P)
IF (LF_P .AND. LLFIELDAPI) THEN
  CALL COPY_FIELD_4LM (SELF%F_P)
  !$ACC ENTER DATA ATTACH (SELF%F_P)
ENDIF

LP_FIELD = ASSOCIATED (SELF%P_FIELD)
IF (LP_FIELD) THEN
  !$ACC ENTER DATA CREATE (SELF%P_FIELD)
  !$ACC UPDATE DEVICE (SELF%P_FIELD)
  !$ACC ENTER DATA ATTACH (SELF%P_FIELD)
ENDIF

END SUBROUTINE

END SUBMODULE
