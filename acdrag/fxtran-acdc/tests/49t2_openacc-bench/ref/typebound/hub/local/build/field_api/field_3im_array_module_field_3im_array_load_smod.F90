SUBMODULE (FIELD_3IM_ARRAY_MODULE) FIELD_3IM_ARRAY_MODULE_FIELD_3IM_ARRAY_LOAD_SMOD

IMPLICIT NONE

CONTAINS

MODULE SUBROUTINE ACDC_LOAD_FIELD_3IM_ARRAY (SELF, KLUN)
USE FIELD_UTIL_MODULE
IMPLICIT NONE
CLASS (FIELD_3IM_ARRAY), INTENT (OUT), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
INTEGER :: IL3(3), IU3(3)
LOGICAL :: LF_P, LP_FIELD
READ (KLUN) LF_P
IF (LF_P) THEN
  CALL LOAD_FIELD_3IM (KLUN, SELF%F_P)
ELSE
  NULLIFY (SELF%F_P)
ENDIF
READ (KLUN) LP_FIELD
IF (LP_FIELD) THEN
  READ (KLUN) IL3
  READ (KLUN) IU3
  ALLOCATE (SELF%P_FIELD (IL3(1):IU3(1), IL3(2):IU3(2), IL3(3):IU3(3)))
  READ (KLUN) SELF%P_FIELD
ELSE
  NULLIFY (SELF%P_FIELD)
ENDIF
NULLIFY (SELF%P)
END SUBROUTINE

END SUBMODULE
